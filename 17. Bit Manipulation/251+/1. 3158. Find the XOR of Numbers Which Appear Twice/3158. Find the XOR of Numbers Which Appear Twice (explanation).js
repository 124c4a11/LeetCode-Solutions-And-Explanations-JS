/**
 * @param {number[]} nums
 * @return {number}
 *
 * duplicateNumbersXOR принимает массив nums, в котором каждое
 * число встречается либо один раз, либо дважды.
 *
 * Возвращает побитовое XOR всех чисел, которые встречаются в
 * массиве дважды.
 *
 * Если ни одно число не встречается дважды, возвращает 0.
 *
 *
 * Пример для:
 *   nums = [1,2,1,3]
 * Результат:
 *   1
 * Пояснение:
 *   Единственное число, которое встречается дважды в nums -
 *   это 1.
 *
 *
 * Пример для:
 *   nums = [1,2,3]
 * Результат:
 *   0
 * Пояснение:
 *   Ни одно число не встречается дважды в nums.
 */
function duplicateNumbersXOR(nums) {

  // Инициализация переменной duplicateXOR значением 0.
  //
  // 0 - нейтральный элемент для операции XOR: x ^ 0 === x.
  //
  // По мере обхода массива будем включать в этот аккумулятор
  // XOR всех уникальных чисел, которые встречаются дважды
  let duplicateXOR = 0;

  // Создание Int32Array длиной 51 для хранения счётчиков
  // частоты появления чисел.
  //
  // Использование Int32Array даёт фиксированный тип и размер
  // в памяти, что быстрее и экономнее, чем обычный объект или
  // Array для целочисленных счётчиков.
  //
  // Каждый элемент по умолчанию равен 0
  //
  // Длина 51 означает, что корректная область индексов 0..50
  // включительно.
  const numToFreq = new Int32Array(51);

  // Проходит по всем элементам коллекции nums
  for (const num of nums) {

    // Увеличиваем счётчик в массиве частот для текущего
    // значения num.
    numToFreq[num]++;

    // Проверяем, стала ли частота текущего числа равной 2.
    //
    // Включаем число в итоговый XOR
    if (numToFreq[num] == 2) duplicateXOR ^= num;
  }

  // Возвращаем накопленный результат XOR.
  //
  // Если в массиве не было чисел, встретившихся дважды,
  // вернётся 0.
  return duplicateXOR;
}
