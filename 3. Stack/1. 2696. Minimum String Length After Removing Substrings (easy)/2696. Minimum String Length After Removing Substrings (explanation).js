/**
 * @param {string} s
 * @return {number}
 *
 * minLength принимает строку s и возвращает число - длину строки
 * после последовательного удаления всех соседних пар символов
 * AB и CD
 *
 * Пример для:
 *   s = "ABFCACDB"
 * Результат:
 *   2
 * Пояснение:
 *   Можно выполнить следующие операции:
 *   - Удалить подстроку "ABFCACDB", тогда s = "FCACDB".
 *   - Удалить подстроку "FCACDB", тогда s = "FCAB".
 *   - Удалить подстроку "FCAB", тогда s = "FC".
 *
 *   В результате длина строки равна 2. Это минимальная длина,
 *   которую можно получить.
 */
function minLength(s) {

  // Создаём пустой массив, который будем использовать как стек:
  // добавляем элементы в конец (push) и удаляем последний (pop).
  //
  // Стек хранит символы, которые ещё не удалены и могут
  // образовать пары с последующими символами.
  const stack = [];

  // Проходим по каждому символу входной строки s в порядке
  // слева направо.
  for (const char of s) {

    // Получаем последний элемент стека без его удаления с
    // помощью stack.at(-1).
    const prevChar = stack.at(-1);

    // Проверяем, образует ли предыдущий символ в стеке и текущий
    // символ пару AB.
    const isAB = prevChar === 'A' && char === 'B';

    // Проверяем, образует ли предыдущий символ в стеке и текущий
    // символ пару CD.
    const isCD = prevChar === 'C' && char === 'D';

    // Если обнаружена одна из удаляемых пар (AB или CD), то
    // удаляем предыдущий символ из стека.
    //
    // Это моделирует удаление пары: предыдущий символ удаляется
    // из накопленного результата, а текущий символ не
    // добавляется в стек, то есть пара полностью исключается.
    if (isAB || isCD) stack.pop();

    // В противном случае текущий символ не образует с предыдущим
    // допустимой пары, поэтому мы помещаем его в стек как
    // потенциальную часть будущих пар.
    else stack.push(char);
  }

  // После обработки всех символов в стеке останутся только те
  // символы, которые не были удалены.
  //
  // Длина стека равна минимальной длине строки после всех
  // возможных последовательных удалений пар AB и CD.
  return stack.length;
}
