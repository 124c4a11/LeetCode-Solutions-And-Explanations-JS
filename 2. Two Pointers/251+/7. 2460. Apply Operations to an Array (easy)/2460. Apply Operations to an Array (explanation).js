/**
 * @param {number[]} nums
 * @return {number[]}
 *
 * applyOperations принимает массив nums длины n с индексами
 * от 0, состоящий из неотрицательных целых чисел.
 *
 * Выполняет n - 1 операций над этим массивом: в i‑й
 * операции (0‑индексация) выполняет действие над элементом
 * nums[i]:
 * - Если nums[i] == nums[i + 1], то умножает nums[i] на 2
 *   и присваивает nums[i + 1] значение 0. В противном
 *   случае операция пропускается.
 *
 * После выполнения всех операций сдвигает все нули в конец
 * массива, сохранив относительный порядок ненулевых
 * элементов.
 *
 * Пример: массив [1,0,2,0,0,1] после сдвига нулей в конец
 * превращается в [1,2,1,0,0,0].
 *
 * Возвращает получившийся массив.
 *
 * Важно: операции выполняются последовательно, а не
 * одновременно.
 *
 *
 * Пример для:
 *   nums = [1,2,2,1,1,0]
 * Результат:
 *   [1,4,2,0,0,0]
 * Пояснение:
 * Выполняем следующие операции:
 * - i = 0: nums[0] и nums[1] не равны, поэтому эту операцию
 *   пропускаем.
 *
 * - i = 1: nums[1] и nums[2] равны, умножаем nums[1] на 2 и
 *   меняем nums[2] на 0. Массив становится [1,4,0,1,1,0].
 *                                             ^ ^
 *
 * - i = 2: nums[2] и nums[3] не равны, поэтому эту операцию
 *   пропускаем.
 *
 * - i = 3: nums[3] и nums[4] равны, умножаем nums[3] на 2 и
 *   меняем nums[4] на 0. Массив становится [1,4,0,2,0,0].
 *                                                 ^ ^
 *
 * - i = 4: nums[4] и nums[5] равны, умножаем nums[4] на 2 и
 *   меняем nums[5] на 0. Массив остаётся [1,4,0,2,0,0].
 *                                                 ^ ^
 *
 * После этого сдвигаем все нули в конец, что даёт массив
 * [1,4,2,0,0,0].
 */
function applyOperations(nums) {
  const n = nums.length;

  // Цикл идёт от i = 0 до i = n - 2 включительно, так как
  // на каждой итерации мы рассматриваем пару соседних
  // элементов nums[i] и nums[i+1].
  //
  // Если nums[i] == nums[i + 1], то умножает nums[i] на 2
  // и присваивает nums[i + 1] значение 0. В противном
  // случае операция пропускается.
  for (let i = 0; i < n - 1; i++) {

    // Если текущий элемент не равен следующему - ничего не
    // делаем.
    //
    // Используем continue, чтобы перейти к следующему i.
    if (nums[i] !== nums[i + 1]) continue;

    // Если nums[i] == nums[i + 1], умножаем nums[i] на 2
    // и присваивает nums[i + 1] значение 0.
    nums[i] *= 2;
    nums[i + 1] = 0;
  }

  // После выполнения всех операций сдвигает все нули в
  // конец массива, сохранив относительный порядок
  // ненулевых элементов.

  // Инициализируем указатель l, который будет указывать на
  // позицию, куда нужно поместить следующий ненулевой
  // элемент.
  let l = 0;

  // Проходим правым указателем r по всему массиву, чтобы
  // собрать все ненулевые элементы в начале массива.
  //
  // Когда встречаем ненулевой элемент, перемещаем его на
  // позицию l.
  for (let r = 0; r < n; r++) {

    // Если текущий элемент равен 0, пропускаем его - он
    // должен оказаться в конце после всех обменов.
    if (nums[r] === 0) continue;

    // Сохраняем текущее значение nums[l] во временную
    // константу tmp, чтобы не потерять его при обмене.
    //
    // Для ускорения работы цикла при обмене элементов
    // местами не используем подход с деструктуризацией
    // массива. Вместо этого используем подход с
    // использованием временной переменной tmp.
    const tmp = nums[l];

    // Перемещаем найденный ненулевой элемент из позиции r в
    // позицию l.
    //
    // Это сдвигает ненулевые элементы влево по мере
    // продвижения r.
    nums[l] = nums[r];

    // В позицию r записываем прежнее значение nums[l] (tmp).
    //
    // Если tmp был 0, то в r окажется 0 - это обеспечивает
    // сдвиг нулей вправо.
    nums[r] = tmp;

    // Увеличиваем l, чтобы следующая ненулевая запись
    // попала в следующую свободную позицию слева.
    //
    // l всегда указывает на первую позицию, которая ещё не
    // занята ненулевым элементом после сдвига.
    //
    // После каждой такой операции все индексы < l содержат
    // ненулевые элементы в итоговом порядке.
    l++;
  }

  // Возвращаем модифицированный массив nums:
  //
  // Сначала идут все ненулевые элементы (после объединений),
  // затем все нули
  return nums;
}
