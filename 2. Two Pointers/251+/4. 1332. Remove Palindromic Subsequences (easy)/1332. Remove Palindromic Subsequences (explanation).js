/**
 * @param {string} s
 * @return {number}
 *
 * removePalindromeSub принимает строку s, состоящаю
 * только из букв 'a' и 'b'.
 *
 * За один шаг можно удалить из s одну палиндромную
 * подпоследовательность.
 *
 * Возвращает минимальное количество шагов, чтобы
 * сделать данную строку пустой.
 *
 * Подпоследовательность строки — это строка,
 * полученная удалением некоторых символов исходной
 * строки без изменения порядка оставшихся символов.
 *
 * Обратите внимание, что подпоследовательность не
 * обязательно должна быть непрерывной.
 *
 * Строка называется палиндромом, если она читается
 * одинаково слева направо и справа налево.
 *
 *
 * Пример для:
 *   s = "ababa"
 * Результат:
 *   1
 * Пояснение:
 * s уже является палиндромом, поэтому её можно удалить
 * целиком за один шаг.
 *
 *
 * Пример 2:
 *   s = "abb"
 * Результат:
 *   2
 * Пояснение:
 * "abb" → "bb" → "".
 *  ^        ^^
 * Удаляем палиндромную подпоследовательность "a",
 * затем "bb".
 *
 *
 * Пример 3:
 *   s = "baabb"
 * Результат:
 *   2
 * Пояснение:
 * "baabb" → "b" → "".
 *  ^^^ ^      ^
 * Удаляем палиндромную подпоследовательность "baab",
 * затем "b".
 */
function removePalindromeSub(s) {

  // Первое наблюдение: строка содержит только два
  // различных символа: 'a' и 'b'. Это ключевое
  // ограничение, которое значительно упрощает задачу.
  //
  // Подумайте, какие палиндромные
  // подпоследовательности можно составить:
  // - Любая последовательность одинаковых символов
  //   является палиндромом: "aaa...a" или "bbb...b".
  //
  // - Мы можем выбрать все символы 'a' из строки
  //   (независимо от их позиций) и получить
  //   палиндромную подпоследовательность.
  //
  // - Аналогично, мы можем выбрать все символы 'b' и
  //   получить другую палиндромную
  //   подпоследовательность.
  //
  // Это означает, что мы всегда можем опустошить
  // строку максимум за 2 шага:
  // - Удалить все 'a' как одну палиндромную
  //   подпоследовательность.
  //
  // - Удалить все 'b' как другую палиндромную
  //   подпоследовательность.
  //
  // Но можно ли сделать лучше? Да — если сама строка
  // уже является палиндромом. В этом случае можно
  // удалить всю строку за один шаг.
  //
  // Следовательно, задача сводится к следующему:
  // - Если строка пустая (крайний случай), вернуть 0.
  //
  // - Если вся строка является палиндромом, вернуть 1.
  //
  // - В противном случае вернуть 2.
  //
  // Поскольку в условии задачи гарантируется непустая
  // x строка, нам достаточно проверить, является ли
  // s палиндромом. Если да — нужен 1 шаг. Если нет —
  // нужено 2 шага.
  //
  // Элегантное решение return 1 if s[::-1] == s else 2
  // точно отражает эту логику — оно проверяет, равна
  // ли строка своей обратной (проверка на палиндром),
  // и возвращает соответствующее минимальное число
  // шагов.


  // Инициализация цикла с двумя указателями:
  // l — индекс слева,
  // r — индекс справа.
  //
  // l устанавливается в 0 (первый символ),
  // r устанавливается в s.length - 1 (последний
  // индекс строки).
  //
  // Условие продолжения цикла l < r гарантирует, что
  // сравниваются только пары символов до середины
  // строки.
  //
  // После каждой итерации l увеличивается на 1
  // (двигается вправо), r уменьшается на 1 (двигается
  // влево).
  for (let l = 0, r = s.length - 1; l < r; l++, r--) {

    // Сравниваем символы строки в позициях l и r.
    //
    // Если они не равны, это означает, что строка не
    // является палиндромом.
    //
    // Для непалиндромной строки минимальное число
    // подпоследовательностей, которые нужно удалить,
    // чтобы получить пустую строку, равно 2 (нужно
    // удалить все 'a' затем все 'b' и т.д.), поэтому
    // функция сразу возвращает 2.
    //
    // Ранний возврат экономит время, прекращая
    // дальнейшие ненужные сравнения.
    if (s[l] !== s[r]) return 2;
  }

  // Если цикл завершился без раннего возврата, это
  // означает, что для всех пар (l, r) символы
  // совпали и строка является палиндромом.
  //
  // Это покрывает случаи пустой строки и строки
  // длины 1 (цикл не выполнится) и все палиндромы.
  //
  // В таком случае достаточно удалить одну
  // подпоследовательность (всю строку, так как она
  // сама является палиндромом), поэтому возвращаем 1.
  return 1;
}
