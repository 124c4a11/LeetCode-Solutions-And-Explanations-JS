/**
 * @param {number[][]} image
 * @return {number[][]}
 *
 * flipAndInvertImage принимает двумерный массив image
 * (матрица) чисел, где каждая внутренняя структура - массив
 * чисел 0 или 1.
 *
 * Для каждого ряда (строки) входной матрицы image функция
 * выполняет два действия последовательно:
 * - flip horizontally (развернуть строку в обратном порядке)
 * - invert (заменить 0 на 1 и 1 на 0).
 *
 * Возвращает ту же матрицу image, но уже преобразованную.
 * Операция выполняется in‑place
 *
 *
 * Пример для:
 *   image = [[1,1,0],[1,0,1],[0,0,0]]
 * Результат:
 *   [[1,0,0],[0,1,0],[1,1,1]]
 * Пояснение:
 *   Сначала развернуть каждую строку по горизонтали:
 *   [[0,1,1],[1,0,1],[0,0,0]].
 *
 *   Затем инвертировать биты в image (0 → 1, 1 → 0):
 *   [[1,0,0],[0,1,0],[1,1,1]]
 */
function flipAndInvertImage(image) {

  // Подумайте, что происходит с каждым элементом при
  // развороте строки по горизонтали, а затем при инверсии.
  //
  // Рассмотрим любую строку и возьмём два элемента: один на
  // позиции l слева и другой на позиции r справа
  // (где r = n - 1 - i).
  //
  // После горизонтального разворота эти два элемента
  // меняются местами. Затем при инверсии оба значения
  // меняются с 0 на 1 или с 1 на 0.
  //
  // Ключевая идея: вместо явного обмена и затем инверсии
  // проанализируем, каким будет итоговый результат.
  //
  // Случай 1: когда row[l] == row[r]
  // - Допустим, оба равны 1. После разворота они поменяются
  //   местами, но останутся 1 в новых позициях. После
  //   инверсии оба станут 0.
  //
  // - Если оба были 0, после разворота они по-прежнему 0 в
  //   swapped позициях, а после инверсии оба станут 1.
  //
  // - В обоих сценариях в конечном итоге получаются
  //   противоположные значения в обеих позициях. Это
  //   эквивалентно операции XOR с 1: value ^ 1.
  //
  // Случай 2: когда row[i] != row[j]
  // - Если row[l] = 1 и row[r] = 0, после разворота они
  //   станут row[l] = 0 и row[r] = 1. После инверсии они
  //   снова станут row[l] = 1 и row[r] = 0.
  //
  // - Значения остаются точно такими же, как и были до
  //   преобразований - обмен и инверсия взаимно
  //   компенсируются.
  //
  // Из этого следует элегантное решение: используем два
  // указателя, сканирующие строку с обоих концов к центру.
  // Когда значения равны, инвертируем оба (через XOR с 1).
  // Когда значения различны, ничего не делаем.
  // Для центрального элемента (при нечётном n) он остаётся
  // на месте после разворота, поэтому его нужно только
  // инвертировать.
  //
  // Таким образом можно преобразовать матрицу на месте за
  // один проход по каждой строке, избегая явного выполнения
  // операции разворота.


  // Итерация по каждой строке (подмассиву) входной матрицы
  // image.
  //
  // Переменная row ссылается на текущую строку и позволяет
  // изменять её элементы на месте.
  for (const row of image) {

    // Инициализация левого указателя l, который будет
    // двигаться от начала строки к центру.
    let l = 0;

    // Инициализация правого указателя r, который будет
    // двигаться от конца строки к центру.
    let r = row.length - 1;

    // Внутренний цикл, который проходит по строке парами с
    // двух концов.
    //
    // Условие l < r гарантирует, что мы обрабатываем пары
    // разных индексов (левый и правый).
    //
    // В выражении for пропущена инициализация (она уже
    // сделана выше), а шаги l++ и r-- выполняются после
    // тела цикла.
    for (; l < r; l++, r--) {

      // Сравниваем элементы в симметричных позициях
      // относительно центра строки.
      //
      // Если они равны (оба 0 или оба 1), то после
      // зеркального отражения их значения останутся равными,
      // поэтому требуется инвертировать оба бита, чтобы
      // получить корректный результат "flip + invert".
      if (row[l] === row[r]) {

        // Инвертирование элементов в позициях l и r с
        // помощью побитового XOR с 1 для 0 даёт 1, для 1
        // даёт 0. Операция выполняется in-place.
        row[l] ^= 1;
        row[r] ^= 1;
      }
    }

    // После выхода из внутреннего цикла возможны два случая:
    // 1. l > r - строка обработана полностью (чётная длина),
    //    все пары обработаны.
    //
    // 2. l === r - длина строки нечётная, остался
    //    центральный элемент, который не был затронут парной
    //    обработкой.
    //
    // В случае l === r нужно инвертировать центральный
    // элемент, потому что при зеркальном перевороте он
    // остаётся на том же месте, и требуется только
    // инвертировать его значение.
    if (l === r) row[l] ^= 1;
  }

  // Возвращаем ссылку на тот же объект image, который был
  // модифицирован in-place.
  return image;
}
