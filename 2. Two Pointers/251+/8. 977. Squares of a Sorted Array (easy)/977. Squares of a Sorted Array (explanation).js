/**
 * @param {number[]} nums
 * @return {number[]}
 *
 * принимает отсортированный по неубыванию массив чисел nums
 * и возвращает новый массив, содержащий квадраты этих чисел,
 * также отсортированные по неубыванию.
 *
 *
 * Пример для:
 *   nums = [-4,-1,0,3,10]
 * Результат:
 *   [0,1,9,16,100]
 * Пояснение:
 *   После возведения в квадрат массив становится
 *   [16,1,0,9,100].
 *
 *   После сортировки он становится [0,1,9,16,100].
 */
function sortedSquares(nums) {

  // Когда мы возводим числа в квадрат, отрицательные
  // значения становятся положительными. Это создаёт
  // интересный рисунок в отсортированном массиве.
  // Рассмотрим массив [-4, -1, 0, 3, 10]. Квадраты равны
  // [16, 1, 0, 9, 100]. Обратите внимание, что квадраты
  // образуют "долину" - они убывают с левой стороны (от
  // отрицательных чисел) к середине, а затем растут к
  // правой стороне (положительные числа).
  //
  // Ключевая мысль в том, что в отсортированном массиве
  // наибольшие квадраты обязательно будут получены из
  // крайних элементов - либо из самых отрицательных слева,
  // либо из самых положительных справа. Наименьшие квадраты
  // окажутся где‑то посередине, рядом с нулём.
  //
  // Это наблюдение наводит на идею использовать два
  // указателя. Если поставить один указатель в начало, а
  // другой в конец массива, мы всегда сможем определить,
  // какой элемент даёт следующий по величине квадрат,
  // сравнив nums[left]^2 и nums[right]^2.
  //
  // Почему строить от большего к меньшему? Потому что так
  // мы детерминированно на каждом шаге находим наибольший
  // квадрат, сравнивая крайние значения. Найти наименьший
  // квадрат было бы сложнее - он может находиться
  // где‑угодно в середине массива, и его пришлось бы искать.
  //
  // Повторяя выбор большего квадрата между двумя краями и
  // сдвигая соответствующий указатель внутрь, мы постепенно
  // движемся к меньшим квадратам в середине. Это даёт нам
  // все квадраты в порядке убывания. Простая реверсия в
  // конце даёт требуемый порядок по возрастанию.
  //
  // Этот подход элегантно работает во всех случаях - для
  // массивов только с отрицательными числами, только с
  // положительными или со смешанными - потому что логика
  // "наибольшие квадраты приходят с краёв" всегда верна
  // для отсортированных массивов.

  const n = nums.length;

  // Создаём новый массив squares длины n, который будет
  // содержать квадраты чисел в отсортированном порядке.
  const squares = new Array(n);

  // Инициализируем индекс i для записи в массив squares.
  //
  // Инициализируем i последним индексом (n - 1), потому что
  // мы заполняем результирующий массив sqares от конца к
  // началу (от наибольших квадратов к наименьшим), что
  // позволяет избежать дополнительного реверса
  // результирующего массива
  let i = n - 1;

  // Указатель l (left) ставим в начало массива nums - будет
  // двигаться вправо к центру массива nums.
  let l = 0;

  // Указатель r (right) ставим в конец массива nums - будет
  // двигаться влево к центру массива nums.
  let r = n - 1;

  // Запускаем цикл, который продолжается, пока левый
  // указатель не превысит правый.
  //
  // Это гарантирует, что мы обработаем все элементы массива
  // ровно один раз.
  while (l <= r) {

    // Вычисляем квадрат текущего левого элемента.
    const lSquare = nums[l] ** 2;

    // Вычисляем квадрат текущего правого элемента.
    const rSquare = nums[r] ** 2;

    // Сравниваем квадраты крайних элементов, чтобы
    // определить, какой из них больше.
    if (lSquare > rSquare) {

      // Если квадрат левого элемента больше, записываем
      // его в текущую позицию i результирующего массива.
      squares[i] = lSquare;

      // Сдвигаем левый указатель вправо, потому что этот
      // элемент уже использован.
      l++;
    } else {

      // В противном случае записываем квадрат правого
      // элемента в позицию i.
      squares[i] = rSquare;

      // Сдвигаем правый указатель влево, потому что этот
      // элемент уже использован.
      r--;
    }

    // Переходим к следующей позиции в результирующем
    // массиве слева от текущей, то есть уменьшаем i, так
    // как мы заполняем массив от конца к началу.
    i--;
  }

  // Возвращаем массив squares, содержащий квадраты исходных
  // чисел nums в неубывающем порядке
  return squares;
}
