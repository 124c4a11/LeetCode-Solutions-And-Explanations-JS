/**
 * @param {number[]} nums
 * @return {number}
 *
 * findTheArrayConcVal принимает массив чисел nums и вычисляет
 * сумму специальных конкатенаций пар элементов массива,
 * взятых с начала и с конца массива. Для каждой пары берётся
 * элемент c начала массива и элемент с конца массива, их
 * строковые представления соединяются в порядке "левый затем
 * правый", полученная строка преобразуется в число и
 * добавляется к сумме. Если длина массива нечётная,
 * центральный элемент добавляется к сумме как обычное число.
 *
 * Пример для:
 *   nums = [7,52,2,4]
 * Результат:
 *   596
 * Объяснение:
 * До выполнения каких‑либо операций nums равен [7,52,2,4],
 * а значение конкатенации равно 0.
 * - В первой операции:
 *   Мы берём первый элемент 7 и последний элемент 4.
 *
 *   Их конкатенация даёт 74, и мы прибавляем это к значению
 *   конкатенации, поэтому оно становится равным 74.
 *
 *   Затем мы удаляем эти элементы из nums, поэтому nums
 *   становится равным [52,2].
 *
 * - Во второй операции:
 *   Мы берём первый элемент 52 и последний элемент 2.
 *
 *   Их конкатенация даёт 522, и мы прибавляем это к значению
 *   конкатенации, поэтому оно становится равным 596.
 *
 *   Затем мы удаляем эти элементы из nums, поэтому nums
 *   становится пустым.
 *
 * Поскольку значение конкатенации равно 596, ответ равен 596.
 */
function findTheArrayConcVal(nums) {

  // Инициализация аккумулятора concVal значением 0.
  //
  // Здесь будет накапливаться итоговая сумма.
  let concVal = 0;

  // Установка левого указателя l на начало массива (индекс 0).
  //
  // l будет двигаться вправо к центру по мере обработки пар
  // элементов массива.
  let l = 0;

  // Установка правого указателя r на последний индекс массива
  // (nums.length - 1).
  //
  // r будет двигаться влево к центру по мере обработки пар
  // элементов массива.
  let r = nums.length - 1;

  // Цикл while выполняется, пока левый указатель строго
  // меньше правого.
  //
  // Это гарантирует, что обрабатываются только
  // непересекающиеся пары (левый, правый).
  while (l < r) {

    // Создаём строковую конкатенацию значений nums[l] и
    // nums[r] в порядке "левый затем правый", затем
    // преобразуем полученную строку в число с помощью Number
    // и прибавляем к concVal.
    //
    // Такой подход сохраняет порядок цифр и корректно
    // работает для многозначных чисел.
    //
    // Пример:
    //   nums[l]=12, nums[r]=34
    //   → `${nums[l]}${nums[r]}` = "1234"
    //   → Number("1234") = 1234.
    concVal += Number(`${nums[l]}${nums[r]}`);

    // Увеличиваем левый указатель, чтобы перейти к следующему
    // элементу справа от текущего левого.
    //
    // Это сдвигает окно обработки к центру массива.
    l++;

    // Уменьшаем правый указатель, чтобы перейти к следующему
    // элементу слева от текущего правого.
    //
    // В сочетании с l++ это обеспечивает обработку следующей
    // пары с начала и конца массива.
    r--;
  }

  // После выхода из цикла возможны два состояния:
  // 1. l > r - все элементы обработаны (чётная длина массива).
  //
  // 2. l === r - остался один центральный элемент (нечётная
  //    длина массива).
  //
  // В случае совпадения указателей добавляем значение
  // центрального элемента к concVal.
  //
  // Здесь мы добавляем саму числовую величину nums[l], а не
  // её строковую конкатенацию, так как центральный элемент не
  // имеет пары.
  if (l === r) concVal += nums[l];

  // Возвращаем накопленную сумму concVal
  return concVal;
}
