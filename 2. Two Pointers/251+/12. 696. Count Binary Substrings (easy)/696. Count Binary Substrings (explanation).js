/**
 * @param {string} s
 * @return {number}
 *
 * countBinarySubstrings принимает параметр бинарную строку s,
 * состоящую только из символов '0' и '1' и подсчитывает
 * количество непустых подстрок этой строки, в которых подряд
 * идут сначала одинаковые символы одного типа, затем другого,
 * и их количества равны (например, "0011" или "10").
 *
 *
 * Пример для:
 *   s = "00110011"
 * Результат:
 *   6
 * Пояснение:
 *   Существует 6 подстрок, в которых равное количество подряд
 *   идущих 1 и 0: "0011", "01", "1100", "10", "0011", "01".
 *
 *   Обратите внимание, что некоторые из этих подстрок
 *   повторяются и учитываются столько раз, сколько они
 *   встречаются.
 *
 *   Также "00110011" не является допустимой подстрокой,
 *   потому что все нули (и единицы) не сгруппированы вместе.
 */
function countBinarySubstrings(s) {
  const n = s.length;

  // Инициализируем счётчик подходящих подстрок
  let substringsCnt = 0;

  // Индекс l указывает на начало текущей группы одинаковых
  // символов (например, начало блока '000' или '111').
  let l = 0;

  // prevLen хранит длину предыдущей группы одинаковых
  // символов, чтобы при переходе между группами можно было
  // определить, сколько новых валидных подстрок образуется
  // между предыдущей и текущей группой.
  //
  // Изначально предыдущей группы нет, поэтому prevLen = 0.
  let prevLen = 0;

  // Внешний цикл проходит по строке блоками одинаковых
  // символов, пока l не достигнет конца строки
  while (l < n) {

    // r будет указателем, который ищет конец текущей группы
    // одинаковых символов (начало следующей группы), начиная
    // со следующей позиции.
    //
    // Инициализируем r как l + 1, потому что группа как
    // минимум содержит символ в позиции l.
    //
    // Идея:
    // [l, r) - полуоткрытый интервал, содержащий текущий блок
    // одинаковых символов.
    let r = l + 1;

    // Внутренний цикл продвигает r вправо, пока символы на
    // позициях l и r одинаковы и r не достиг конца строки.
    //
    // После завершения этого цикла диапазон [l, r)
    // представляет собой непрерывную группу одинаковых
    // символов, в котором r указывает на первый индекс, где
    // символ отличается от s[l], либо на n.
    while (r < n && s[l] === s[r]) r++;

    // Вычисляем длину текущего блока одинаковых символов как
    // разницу между r и l.
    //
    // Например, если l указывает на первый '0' в "00011", а
    // r остановился на первом '1', то currLen будет 3.
    const currLen = r - l;

    // Количество новых подходящих бинарных подстрок, которые
    // можно составить из соседних блоков, равно минимальной
    // длине между предыдущим блоком и текущим блоком.
    //
    // Например, если предыдущий блок длины 2 ('00'), а
    // текущий длины 3 ('111'), то можно составить 2
    // подстроки: "0011" и "01" (в терминах границ блоков).
    //
    // Добавляем это число к общему счётчику.
    substringsCnt += Math.min(prevLen, currLen);

    // Обновляем prevLen:
    //
    // Текущий блок станет "предыдущим" для следующей
    // итерации, чтобы при переходе к следующему блоку можно
    // было сравнить их длины.
    prevLen = currLen;

    // Перемещаем l на r - то есть на начало следующего блока
    // символов (или на n, если мы дошли до конца строки s).
    l = r;
  }

  // После обработки всех блоков возвращаем накопленное
  // количество подходящих бинарных подстрок.
  return substringsCnt;
}
