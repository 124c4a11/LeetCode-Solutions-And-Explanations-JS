/**
 * @param {number[]} nums
 * @param {number} key
 * @param {number} k
 * @return {number[]}
 *
 * findKDistantIndices возвращает все индексы массива nums, которые
 * находятся на расстоянии не более k от хотя бы одного элемента,
 * равного key.
 *
 * Параметры:
 *   nums - массив чисел
 *   key - искомое значение
 *   k - допустимая дистанция в индексах.
 *
 *
 * Пример для:
 *   nums = [3,4,9,1,3,9,5],
 *   key = 9,
 *   k = 1
 * Вывод:
 *   [1,2,3,4,5,6]
 * Пояснение:
 * Здесь nums[2] == key и nums[5] == key.
 * - Для индекса 0, |0 - 2| > k и |0 - 5| > k,
 *   поэтому не существует j, для которого
 *   |0 - j| <= k и nums[j] == key.
 *   Следовательно, 0 не является k‑дистанционным индексом.
 *
 * - Для индекса 1, |1 - 2| <= k и nums[2] == key,
 *   поэтому 1 является k‑дистанционным индексом.
 *
 * - Для индекса 2, |2 - 2| <= k и nums[2] == key,
 *   поэтому 2 является k‑дистанционным индексом.
 *
 * - Для индекса 3, |3 - 2| <= k и nums[2] == key,
 *   поэтому 3 является k‑дистанционным индексом.
 *
 * - Для индекса 4, |4 - 5| <= k и nums[5] == key,
 *   поэтому 4 является k‑дистанционным индексом.
 *
 * - Для индекса 5, |5 - 5| <= k и nums[5] == key,
 *   поэтому 5 является k‑дистанционным индексом.
 *
 * - Для индекса 6, |6 - 5| <= k и nums[5] == key,
 *   поэтому 6 является k‑дистанционным индексом.
 *
 * Таким образом, возвращаем [1,2,3,4,5,6], отсортированный в
 * порядке возрастания.
 */
function findKDistantIndices(nums, key, k) {
  const n = nums.length;

  // Инициализируем пустой массив для накопления индексов, которые
  // находятся на расстоянии <= k от любого вхождения key.
  const kDistantIndices = [];

  // lastAddedNdx хранит последний индекс, добавленный в
  // kDistantIndices.
  //
  // Инициализируем -1, чтобы корректно работать с индексом 0 при
  // первом добавлении.
  let lastAddedNdx = -1;

  // Внешний цикл по всем индексам массива nums;
  //
  // j - текущая позиция, которую проверяем на совпадение с key
  for (let j = 0; j < n; j++) {

    // Если элемент в позиции j не равен key, пропускаем
    // дальнейшие действия и переходим к следующему j.
    if (nums[j] !== key) continue;

    // Вычисляем левую границу интервала индексов, которые
    // находятся на расстоянии не более k слева от j.
    //
    // Math.max гарантирует, что граница не уйдёт за начало
    // массива (не станет отрицательной).
    const start = Math.max(0, j - k);

    // Вычисляем правую границу интервала индексов, которые
    // находятся на расстоянии не более k справа от j.
    //
    // Math.min гарантирует, что граница не превысит последний
    // индекс массива (n - 1).
    const end = Math.min(n - 1, j + k);

    // Внутренний цикл добавляет индексы от start до end
    // включительно в результирующий массив.
    //
    // Начало цикла выбирается как максимум между start и
    // lastAddedNdx + 1, чтобы:
    // 1. не добавлять индексы, которые уже были добавлены ранее
    //    при обработке предыдущих вхождений key;
    //
    // 2. экономить время, пропуская уже обработанную часть
    //    интервала при перекрытии интервалов.
    for (let i = Math.max(start, lastAddedNdx + 1); i <= end; i++) {

      // Добавляем текущий индекс i в массив kDistantIndices.
      //
      // Порядок добавления сохраняет возрастание индексов по
      // мере обхода массива.
      kDistantIndices.push(i);

      // Обновляем lastAddedNdx до только что добавленного индекса i.
      //
      // Это ключевой шаг для предотвращения дублирования.
      //
      // Следующий внутренний цикл начнёт с i+1.
      lastAddedNdx = i;
    }
  }

  // После обработки всех вхождений key возвращаем массив
  // kDistantIndices, содержащий все уникальные индексы,
  // находящиеся на расстоянии <= k от хотя бы одного key.
  return kDistantIndices;
}
