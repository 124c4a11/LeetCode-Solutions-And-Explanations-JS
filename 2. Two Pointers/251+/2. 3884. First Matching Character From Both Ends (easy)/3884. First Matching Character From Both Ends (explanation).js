/**
 * @param {string} s
 * @return {number}
 *
 * firstMatchingIndex принимает строку s длины n, состоящую
 * из строчных английских букв.
 *
 * Возвращает наименьший индекс i такой, что
 * s[i] == s[n - i - 1].
 *
 * Если такого индекса не существует, возвращает -1.
 *
 *
 * Пример для:
 *   s = "abcacbd"
 *         ^   ^
 * Результат:
 *   1
 * Пояснение:
 *   Для индекса i = 1 символы s[1] и s[5] оба равны 'b'.
 *
 *   Нет меньшего индекса, удовлетворяющего условию,
 *   поэтому ответ равен 1.
 *
 *
 * Пример для:
 *   s = "abc"
 *         ^
 * Результат:
 *   1
 * Пояснение:
 *   Для индекса i = 1 сравниваемые позиции совпадают (оба
 *   указывают на один и тот же символ), поэтому оба
 *   символа равны 'b'.
 *
 *   Нет меньшего индекса, удовлетворяющего условию,
 *   поэтому ответ равен 1.
 */
function firstMatchingIndex(s) {

  // Инициализация переменной l как левого указателя.
  //
  // Начинает с нулевого индекса (первый элемент).
  //
  // Будет использоваться для итерации от начала к центру.
  let l = 0;

  // Инициализация переменной r как правого указателя.
  //
  // Устанавливается на последний индекс строкистроки s.
  //
  // Если s пустая последовательность, r станет -1, что
  // корректно завершит цикл без доступа к s[-1].
  //
  // Будет использоваться для итерации с конца к центру.
  let r = s.length - 1;

  // Цикл while выполняется до тех пор, пока левый
  // указатель не превысит правый.
  //
  // Условие l <= r гарантирует, что каждая пара (l, r)
  // проверяется ровно один раз и что при нечётной длине
  // центральный элемент тоже будет проверен (l === r).
  while (l <= r) {

    // Сравниваем символы на текущих позициях l и r.
    //
    // Если найдено совпадение, функция немедленно
    // возвращает индекс l - первый (слева) индекс, у
    // которого элемент совпал с соответствующим справа.
    if (s[l] === s[r]) return l;

    // Сдвигаем левый указатель на один вправо, чтобы
    // перейти к следующей внутренней позиции.
    //
    // Это эквивалентно продвижению по строке от начала к
    // центру.
    l++;

    // Сдвигаем правый указатель на один влево, чтобы
    // перейти к следующей внутренней позиции с конца.
    //
    // Это эквивалентно продвижению по строке от конца к
    // центру.
    r--;
  }

  // Если цикл завершился без раннего возврата, значит ни
  // одна проверенная пара (l, r) не содержала равных
  // элементов.
  //
  // Возвращаем -1 как индикатор отсутствия подходящей пары.
  return -1;
}
