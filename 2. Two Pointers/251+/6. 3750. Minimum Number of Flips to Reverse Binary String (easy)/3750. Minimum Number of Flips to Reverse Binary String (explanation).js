/**
 * @param {number} n
 * @return {number}
 *
 * minimumFlips принимает одно число и возвращает минимальное
 * количество одиночных инверсий битов (флипов), необходимых,
 * чтобы двоичное представление этого числа стало равно
 * своему обратному (реверсу). Функция работает с двоичной
 * строкой без ведущих нулей, полученной из входного числа n.
 *
 *
 * Пример для:
 *   n = 10
 * Результат:
 *   4
 * Пояснение:
 * Двоичное представление числа 10 - "1010".
 * Обратная (реверсная) строка - "0101".
 *
 * Чтобы сделать строку равной своей реверсной версии,
 * необходимо инвертировать все четыре бита.
 *
 * Следовательно, минимальное количество флипсов равно 4.
 */
function minimumFlips(n) {

  // Преобразуем число n в строку, представляющую его в
  // двоичной системе счисления.
  //
  // Метод toString(2) возвращает строку без ведущих нулей,
  // например 10 → "1010".
  const s = n.toString(2);

  // Инициализация счётчика flipsCnt, который будет
  // аккумулировать количество инверсий битов, необходимых
  // для превращения строки s в её реверс.
  let flipsCnt = 0;

  // Запускаем цикл с двумя указателями:
  // l (левый) и r (правый).
  //
  // l начинается с 0 (первый символ строки), r - с индекса
  // последнего символа (s.length - 1).
  //
  // Условие l < r гарантирует, что мы сравниваем только
  // пары без повторного сравнения центрального бита в
  // нечётной длине.
  //
  // После каждой итерации l увеличивается на 1, r
  // уменьшается на 1 - указатели сходятся к центру.
  for (let l = 0, r = s.length - 1; l < r; l++, r--) {

    // Для текущей пары позиций сравниваем символы s[l] и
    // s[r].
    //
    // Если символы различаются (один '0', другой '1'), то
    // чтобы строка стала равна своей реверсной версии,
    // необходимо изменить оба бита в этой паре (по одной
    // инверсии на каждый бит), поэтому добавляем 2.
    if (s[l] !== s[r]) flipsCnt += 2;
  }

  // Возвращаем минимальное количество инверсий, необходимых,
  // чтобы полученная строка s стала равна реверсной версии
  // исходной строки.
  return flipsCnt;
}
