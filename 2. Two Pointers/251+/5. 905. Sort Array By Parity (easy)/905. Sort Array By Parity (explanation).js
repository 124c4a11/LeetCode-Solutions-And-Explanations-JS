/**
 * @param {number[]} nums
 * @return {number[]}
 *
 * функция принимет массив чисел nums, переставляет элементы
 * массива in‑place так, чтобы все чётные числа оказались
 * слева, а нечётные справа, и возвращает тот же массив.
 *
 *
 * Пример для:
 *   nums = [3,1,2,4]
 * Результат:
 *   [2,4,3,1]
 * Пояснение:
 *   Также будут приняты варианты [4,2,3,1], [2,4,1,3] и
 *   [4,2,1,3]
 */
function sortArrayByParity(nums) {

  // Инициализация левого указателя l, который будет
  // двигаться вправо и указывать на текущую позицию для
  // проверки/размещения чётных элементов.
  let l = 0;

  // Инициализация правого указателя r, который будет
  // двигаться влево и указывать на позицию, куда можно
  // поместить найденный нечётный элемент.
  let r = nums.length - 1;

  // Цикл выполняется пока указатели не пересеклись.
  //
  // Гарантирует, что каждая пара позиций рассматривается
  // не более одного раза, что обеспечивает линейную
  // сложность по времени.
  //
  // Инвариант:
  // Все индексы < l уже содержат чётные числа.
  // Все индексы > r уже содержат нечётные числа.
  while (l < r) {

    // Проверка нечётности элемента в позиции l.
    //
    // Операция (nums[l] & 1) использует побитовое И с 1:
    // результат 1 для нечётных, 0 для чётных.
    //
    // Побитовое сравнение быстрее арифметического остатка
    // и корректно работает для целых чисел.
    if ((nums[l] & 1) === 1) {

      // Если nums[l] нечётное, его нужно переместить
      // вправо.
      //
      // Сохраняем текущее значение nums[l] во временную
      // константу tmp, чтобы не потерять его при обмене.
      //
      // Для ускорения работы цикла при обмене элементов
      // местами не используем подход с деструктуризацией
      // массива. Вместо этого используем подход с
      // использованием временной переменной tmp.
      const tmp = nums[l];

      // Выполняем обмен значений между позициями l и r.
      //
      // После этих двух присваиваний значение из tmp
      // (исходно nums[l]) окажется в nums[r], а значение,
      // которое было в nums[r], переместится в nums[l].
      nums[l] = nums[r];
      nums[r] = tmp;

      // Поскольку на позицию r мы поместили нечётное
      // значение (из tmp), правый указатель можно безопасно
      // сдвинуть влево.
      //
      // Это уменьшает область поиска и предотвращает
      // повторную обработку уже корректно размещённых
      // нечётных элементов.
      //
      // Мы не увеличиваем l здесь, потому что на позицию l
      // пришло новое значение (из nums[r]) и его нужно
      // проверить - оно может быть как чётным, так и
      // нечётным. Если сдвинуть l, можно пропустить
      // некорректно размещённый элемент.
      r--;
    } else {

      // Если nums[l] чётный, он уже находится в правильной
      // (левой) части массива, поэтому просто сдвигаем
      // левый указатель вправо, чтобы проверить следующий
      // элемент.
      l++;
    }
  }

  // После завершения цикла все чётные элементы
  // гарантированно находятся в индексах < l (или <= l-1),
  // а все нечётные - в индексах > r (или >= r+1).
  //
  // Массив модифицирован на месте.
  //
  // Возвращаем тот же массив nums, модифицированный
  // функцией.
  return nums;
}
