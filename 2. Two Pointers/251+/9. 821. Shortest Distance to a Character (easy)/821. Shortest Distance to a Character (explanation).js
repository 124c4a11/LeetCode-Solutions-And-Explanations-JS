/**
 * @param {string} s
 * @param {character} c
 * @return {number[]}
 *
 * shortestToChar принимает строку "s" и символ "c" и для каждого
 * индекса строки возвращает минимальное расстояние до ближайшего
 * вхождения символа "c".
 *
 *
 * Пример для:
 *   s = "loveleetcode",
 *   c = "e"
 * Результат:
 *   [3,2,1,0,1,0,0,1,2,2,1,0]
 * Пояснение:
 *   Символ 'e' встречается на индексах 3, 5, 6 и 11 (нумерация с
 *   нуля).
 *
 *   Ближайшее вхождение 'e' для индекса 0 находится на индексе 3,
 *   поэтому расстояние равно abs(0 - 3) = 3.
 *
 *   Ближайшее вхождение 'e' для индекса 1 находится на индексе 3,
 *   поэтому расстояние равно abs(1 - 3) = 2.
 *
 *   Для индекса 4 есть равенство между 'e' на индексе 3 и 'e' на
 *   индексе 5, но расстояние остаётся одинаковым:
 *   abs(4 - 3) == abs(4 - 5) = 1.
 *
 *   Ближайшее вхождение 'e' для индекса 8 находится на индексе 6,
 *   поэтому расстояние равно abs(8 - 6) = 2.
 */
function shortestToChar(s, c) {

  // Ключевая идея в том, что для любой позиции в строке ближайший
  // символ "c: может находиться только в одном из двух
  // направлений: либо слева, либо справа. Это означает, что нам
  // не нужно проверять каждую возможную позицию - достаточно
  // знать ближайший "c" с каждой стороны.
  //
  // Представьте, что вы стоите на улице и ищете ближайшую
  // автобусную остановку. Нужно проверить только два направления
  // - влево и вправо - и выбрать ближайшую. Нет смысла проверять
  // каждую точку на улице.
  //
  // Это наблюдение приводит к стратегии с двумя проходами:
  // - Почему два прохода? При одном проходе слева направо мы
  //   знаем только о тех "c", которые уже встречались (то есть
  //   слева). У нас нет информации о возможных c впереди.
  //   Аналогично, один проход справа налево даёт информацию
  //   только о "c" справа.
  //
  // - Отслеживание позиций "с" бесконечностью:
  //   Инициализируем pre = -infinity для прохода слева направо,
  //   потому что изначально мы не видели ни одного "c".
  //   Использование отрицательной бесконечности гарантирует, что
  //   i - pre даст очень большое расстояние, что уместно,
  //   поскольку слева нет "c". Аналогично, suf = infinity для
  //   прохода справа налево означает, что изначально нет
  //   известного "c" справа.
  //
  // - Операция взятия минимума:
  //   После обоих проходов каждая позиция оценена относительно
  //   ближайшего слева "c" и ближайшего справа "c". Взятие
  //   минимума гарантирует, что мы получим абсолютное наименьшее
  //   расстояние. Если "c" есть только с одной стороны, то эта
  //   сторона естественно даст меньшее расстояние.
  //
  // - Почему это работает:
  //   Каждая позиция между двумя символами "c" получит правильное
  //   расстояние, потому что учитываются оба соседа. Позиции на
  //   краях (до первого "c" или после последнего "c") также
  //   получат корректные расстояния, потому что у них "c" только
  //   с одной стороны, а бесконечность на другой стороне
  //   обеспечивает выбор правильной стороны.
  //
  // Красота этого подхода в его простоте - вместо сложных поисков
  // для каждой позиции мы используем линейную структуру строки и
  // решаем задачу всего двумя простыми проходами.

  const n = s.length;

  // Создаём массив distances длины n и заполняем его значением
  // Infinity.
  //
  // Infinity здесь служит как "бесконечное" начальное расстояние
  // - удобно для последующего взятия минимума.
  const distances = new Array(n).fill(Infinity);

  // Инициализируем переменную prevCharPos значением -Infinity,
  // чтобы при первом проходе слева направо выражение
  // i - prevCharPos давало Infinity для позиций до первой
  // найденной c.
  //
  // Это позволяет корректно использовать Math.min позже без
  // дополнительных проверок "нашли ли мы c".
  let prevCharPos = -Infinity;

  // Проходим по строке слева направо
  //
  // Цель этого прохода - вычислить расстояние до ближайшего "c"
  // слева (включая текущую позицию).
  //
  // Пример для:
  //   s = "loveleetcode",
  //   c = "e"
  // После выполнения цикла (слева направо) массив distances
  // будет:
  //   [Infinity, Infinity, Infinity, 0, 1, 0, 0, 1, 2, 3, 4, 0];
  for (let i = 0; i < n; i++) {

    // Если символ в текущей позиции равен "c", обновляем
    // prevCharPos на текущий индекс.
    //
    // Это фиксирует последнее встреченное вхождение "c" при
    // движении слева направо.
    if (s[i] === c) prevCharPos = i;

    // Записываем в distances[i] расстояние от текущей позиции i
    // до последнего встреченного c слева.
    //
    // Если c ещё не встречался, prevCharPos == -Infinity и
    // результат будет +Infinity, что корректно отражает
    // отсутствие "c" слева.
    distances[i] = i - prevCharPos;
  }

  // Инициализируем переменную nextCharPos значением +Infinity.
  //
  // Она будет хранить ближайшую позицию символа "c", найденную
  // при проходе справа налево.
  let nextCharPos = Infinity;

  // Начинаем проход по строке справа налево, индекс i пробегает
  // от n-1 до 0.
  //
  // Цель этого прохода - учесть расстояния до ближайшего "c"
  // справа и взять минимум с уже вычисленным расстоянием слева.
  //
  // Пример для:
  //   s = "loveleetcode",
  //   c = "e"
  // После выполнения цикла (cправа налево) массив distances
  // будет:
  //   [3, 2, 1, 0, 1, 0, 0, 1, 2, 2, 1, 0];
  for (let i = n - 1; i >= 0; i--) {

    // Если символ в текущей позиции равен "c", обновляем
    // nextCharPos на текущий индекс.
    //
    // Это фиксирует ближайшее вхождение c при движении справа
    // налево.
    if (s[i] === c) nextCharPos = i;

    // Обновляем distances[i] минимальным значением между текущим
    // (расстоянием до "c" слева) и расстоянием до ближайшего "c"
    // справа (nextCharPos - i).
    //
    // Таким образом для каждой позиции сохраняется минимальное
    // расстояние до любого вхождения c.
    distances[i] = Math.min(distances[i], nextCharPos - i);
  }

  // Возвращаем массив distances, где для каждой позиции i
  // хранится минимальное целочисленное расстояние до ближайшего
  // символа "c".
  return distances;
}
