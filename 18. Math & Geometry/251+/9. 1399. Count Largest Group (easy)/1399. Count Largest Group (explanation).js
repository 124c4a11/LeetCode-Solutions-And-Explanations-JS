/**
 * @param {number} n
 * @return {number}
 *
 * countLargestGroup принимает целое число n и группирует числа
 * от 1 до n по сумме их цифр и возвращает количество групп,
 * имеющих максимальный размер (сколько разных сумм цифр
 * встречаются у наибольшего числа элементов).
 *
 *
 * Пример для:
 *   n = 13
 * Результат:
 *   4
 * Пояснение:
 *   Всего 9 групп, они образованы по сумме цифр чисел
 *   от 1 до 13:
 *   [1,10], [2,11], [3,12], [4,13], [5], [6], [7], [8], [9].
 *
 *   Четыре группы имеют наибольший размер.
 */
function countLargestGroup(n) {

  // Ключевая идея в том, что нужно отслеживать два параметра:
  // размер каждой группы и какая величина размера является
  // максимальной.
  //
  // Поскольку мы группируем числа по сумме цифр, можно
  // использовать сумму цифр как ключ, чтобы подсчитывать,
  // сколько чисел попадает в каждую группу. Для любого числа
  // от 1 до n сумму цифр можно вычислить, последовательно беря
  // остаток от деления на 10 (чтобы получить последнюю цифру)
  // и затем деля на 10 (чтобы отбросить последнюю цифру).
  //
  // Обрабатывая каждое число от 1 до n, мы вычисляем его сумму
  // цифр и увеличиваем счётчик для соответствующей суммы. Так
  // естественным образом формируются наши группы.
  //
  // Хитрость в том, чтобы в процессе сразу же отслеживать
  // ответ. Вместо того чтобы сначала построить все группы, а
  // затем найти максимальный размер и посчитать, сколько групп
  // имеют этот размер, можно сделать это за один проход:
  // - Если при обновлении счётчика какая‑то группа становится
  //   больше текущего максимального размера, то теперь это
  //   единственная группа с максимальным размером, поэтому мы
  //   сбрасываем ответ в 1.
  //
  // - Если при обновлении счётчика группа становится равной
  //   текущему максимальному размеру, значит мы нашли ещё одну
  //   группу с максимальным размером, и увеличиваем ответ на 1.
  //
  // Таким образом, к моменту обработки всех чисел от 1 до n у
  // нас уже есть ответ, и второй проход по данным не требуется.
  //
  // Ограничение n ≤ 10^4 говорит о том, что максимальная
  // возможная сумма цифр равна 9 + 9 + 9 + 9 = 36 (для 9999),
  // поэтому групп будет не более 36, что делает этот подход
  // очень эффективным.


  // Инициализация переменной для хранения количества групп,
  // которые в текущий момент имеют максимальный размер.
  let largestGroupsCnt = 0;

  // Переменная для хранения текущего максимального размера
  // группы - максимального количества чисел, имеющих
  // одинаковую сумму цифр.
  let maxGroupSize = 0;

  // Создание массива фиксированной длины для подсчёта
  // количества чисел по сумме цифр.
  //
  // Используется Int32Array для экономии памяти и более
  // быстрой работы с целыми числами.
  //
  // Длина 40 выбрана с запасом, потому что при n ≤ 10^4
  // максимальная сумма цифр ≤ 36.
  const digitSumToCnt = new Int32Array(40);

  // Цикл по всем числам от 1 до n включительно.
  //
  // Для каждого числа мы вычисляем сумму его цифр и обновляем
  // соответствующую группу.
  for (let num = 1; num <= n; num++) {

    // Переменная для накопления суммы цифр текущего числа num.
    //
    // Сбрасывается для каждого нового числа.
    let digitSum = 0;

    // Временная переменная, содержащая копию текущего числа,
    // которую мы будем изменять при извлечении цифр.
    let tmpNum = num;

    // Пока в tmpNum остались цифры (tmpNum > 0), извлекаем
    // последнюю цифру и добавляем её к digitSum.
    //
    // Этот цикл выполняет стандартную операцию разложения
    // числа на цифры.
    while (tmpNum > 0) {

      // Добавляем к сумме последнюю цифру: остаток от деления
      // tmpNum на 10.
      //
      // Для положительных целых чисел tmpNum % 10 даёт
      // последнюю цифру (0..9).
      digitSum += tmpNum % 10;

      // Удаление последней "цифры" из tmpNum путём деления
      // на 10 и округления вниз с помощью Math.floor.
      //
      // Пример:
      // Math.floor(123/10) === 12).
      tmpNum = Math.floor(tmpNum / 10);
    }

    // Увеличиваем счётчик для группы с ключом digitSum на 1 -
    // это отражает добавление числа num в соответствующую
    // группу.
    digitSumToCnt[digitSum]++;

    // Если после увеличения счётчика текущая группа стала
    // больше, чем предыдущий максимум, то обновляем
    // maxGroupSize и сбрасываем largestGroupsCnt в 1, потому
    // что теперь только эта группа является наибольшей.
    if (digitSumToCnt[digitSum] > maxGroupSize) {
      maxGroupSize = digitSumToCnt[digitSum];
      largestGroupsCnt = 1;

      // Если размер текущей группы теперь равен текущему
      // максимуму, значит мы обнаружили ещё одну группу,
      // имеющую максимальный размер, и увеличиваем счётчик
      // таких групп.
    } else if (digitSumToCnt[digitSum] === maxGroupSize) {
      largestGroupsCnt++;
    }
  }

  // Возвращаем количество групп, которые имеют максимальный
  // размер после обработки всех чисел от 1 до n.
  return largestGroupsCnt;
}
