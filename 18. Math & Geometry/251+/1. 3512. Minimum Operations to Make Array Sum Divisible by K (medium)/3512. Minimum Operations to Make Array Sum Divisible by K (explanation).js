/**
 * @param {number[]} nums
 * @param {number} k
 * @return {number}
 *
 * minOperations принимает целочисленный массив
 * nums и целое число k.
 *
 * Возвращает минимальное количество операций,
 * необходимых, чтобы сумма массива стала делиться
 * на k.
 *
 * Разрешено выполнять следующую операцию любое
 * количество раз:
 * - Выберите индекс i и замените nums[i] на
 *   nums[i] - 1.
 *
 *
 * Пример для:
 *   nums = [3,9,7],
 *   k = 5
 * Результат:
 *   4
 * Пояснение:
 *   Выполните 4 операции над nums[1] = 9, уменьшив
 *   его на 4.
 *
 *   Теперь nums = [3,5,7].
 *
 *   Сумма равна 15, которая делится на 5.
 *
 *
 * Пример для:
 *  nums = [4,1,3],
 *   k = 4
 * Результат:
 *   0
 * Пояснение:
 *   Сумма равна 8, которая уже делится на 4,
 *   поэтому операции не требуются.
 */
function minOperations(nums, k) {

  // Идея:
  //
  // Каждая операция уменьшает один элемент на 1,
  // а значит каждая операция уменьшает общую
  // сумму массива ровно на 1.
  //
  // Если текущая сумма массива равна S, и мы
  // хотим сделать её делящейся на k, нам нужно
  // найти наименьшее неотрицательное целое x
  // такое, что (S - x) делится на k.
  //
  // Когда число делится на k? Когда при делении
  // на k оно даёт остаток 0. В настоящий момент
  // наша сумма S даёт некоторый остаток при
  // делении на k. Обозначим этот остаток как r, то
  // есть S % k = r.
  //
  // Чтобы сделать сумму делящейся на k, нам нужно
  // уменьшить её ровно на r. Почему?
  // - Если S = q * k + r (где q - частное,
  //   а r - остаток),
  //
  // - то после r операций:
  //   S - r = q * k + r - r = q * k.
  //
  // - Теперь (S - r) % k = 0, то есть сумма
  //   делится на k.
  //
  // Мы не можем сделать это меньшим числом
  // операций, потому что:
  // - Мы можем только уменьшать сумму (операции
  //   только вычитают 1).
  //
  // - Следующее меньшее число, делящееся на k,
  //   равно (q - 1) * k, для достижения которого
  //   потребовалось бы k + r операций.
  //
  // Поскольку нам нужно минимальное число
  // операций, выбираем r вместо k + r.
  //
  // Следовательно, минимальное число операций
  // просто равно sum(nums) % k.

  // Вычисляем сумму всех элементов массива
  const totalSum = nums.reduce((a, b) => a + b, 0);

  // Возвращаем остаток от деления totalSum на k,
  // что является минимальным числом операций,
  // необходимых, чтобы сумма массива стала
  // делиться на k.
  return totalSum % k;
}
