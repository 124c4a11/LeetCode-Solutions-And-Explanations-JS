/**
 * @param {number[]} digits
 * @return {number[]}
 *
 * findEvenNumbers принимает массив цифр digits (числа 0–9) и
 * возвращает массив всех трёхзначных чётных чисел (от 100 до
 * 998), которые можно составить из этих цифр, при условии,
 * что каждая цифра может быть использована не более того
 * количества раз, в котором она встречается в digits.
 *
 *
 * Пример для:
 *   digits = [2,1,3,0]
 * Результат:
 *   [102,120,130,132,210,230,302,310,312,320]
 * Пояснение:
 *   В выходном массиве перечислены все возможные целые числа,
 *   которые соответствуют требованиям.
 * Обратите внимание:
 *   В массиве нет нечётных чисел и чисел с ведущими нулями.
 */
function findEvenNumbers(digits) {

  // Создаём массив частот для цифр 0–9 с помощью Int32Array
  // длиной 10.
  //
  // Int32Array хранит 32‑битные целые, инициализируется
  // нулями.
  //
  // Индексы соответствуют самим цифрам, значение
  // соответствует частоте вхождения текущей цифры в массиве
  // digits.
  const digitToFreq = new Int32Array(10);

  // Проходим по входному массиву digits и увеличиваем счётчик
  // для каждой встреченной цифры.
  for (let digit of digits) digitToFreq[digit]++;

  // Инициализируем пустой массив для накопления найденных
  // трёхзначных чётных чисел.
  const evenNums = [];

  // Перебираем все трёхзначные чётные числа.
  //
  // Начинаем с 100 (минимальное трёхзначное число), идём до
  // 998 включительно (999 не включается в условии цикла), шаг
  // 2 гарантирует, что рассматриваются только чётные числа
  // (последняя цифра 0,2,4,6,8).
  //
  // На каждой итерации проверяем возможно ли составить
  // текущее трёхзначное чётное число num из имеющихся цифр в
  // digits
  for (let num = 100; num < 999; num += 2) {

    // Вычисляем цифру сотен: целая часть от деления на 100.
    //
    // Для трёхзначного числа это даст значение от 1 до 9.
    const hundred = Math.floor(num / 100);

    // Вычисляем цифру десятков: сначала берём остаток от
    // деления на 100 (две последние цифры), затем делим на 10
    // и берём целую часть, чтобы получить значение от 0 до 9.
    const ten = Math.floor((num % 100) / 10);

    // Вычисляем цифру единиц: остаток от деления на 10, даёт
    // значение от 0 до 9.
    const unit = num % 10;

    // Флаг, указывающий, удалось ли составить текущее число
    // из доступных цифр.
    //
    // Изначально предполагаем, что составить можно, и будем
    // опровергать это при нехватке какой-либо цифры.
    let canFormNum = true;

    // Пытаемся "использовать" одну единицу цифры сотен:
    // уменьшаем соответствующий счётчик.
    //
    // Оператор префиксного декремента (--digitToFreq[hundred])
    // сначала уменьшает значение, затем возвращает новое
    // значение.
    //
    // Оператор префиксного декремента изменяет digitToFreq
    // на месте, поэтому важно восстановить значение позже.
    //
    // Если после уменьшения значение стало отрицательным,
    // значит исходных цифр сотен было недостаточно, и число
    // составить нельзя - устанавливаем флаг в false.
    if (--digitToFreq[hundred] < 0) canFormNum = false;

    // Аналогично для цифры десятков: уменьшаем счётчик и
    // проверяем, не ушло ли значение ниже нуля.
    if (--digitToFreq[ten] < 0) canFormNum = false;

    // Аналогично для цифры единиц: уменьшаем счётчик и
    // проверяем на отрицательное значение.
    if (--digitToFreq[unit] < 0) canFormNum = false;

    // Если все три уменьшения прошли без перехода в
    // отрицательное значение (флаг остался true), значит у
    // нас достаточно каждой из трёх цифр, и текущее число
    // можно составить - добавляем его в результат.
    if (canFormNum) evenNums.push(num);

    // Восстанавливаем частоты: возвращаем использованные
    // цифры обратно, чтобы следующая итерация начиналась с
    // исходных частот.
    //
    // Это важно, потому что мы проверяем каждое число
    // независимо, не потребляя цифры навсегда.
    digitToFreq[hundred]++;
    digitToFreq[ten]++;
    digitToFreq[unit]++;
  }

  // Возвращаем массив всех найденных трёхзначных чётных чисел,
  // которые можно составить из исходного набора цифр.
  return evenNums;
}
