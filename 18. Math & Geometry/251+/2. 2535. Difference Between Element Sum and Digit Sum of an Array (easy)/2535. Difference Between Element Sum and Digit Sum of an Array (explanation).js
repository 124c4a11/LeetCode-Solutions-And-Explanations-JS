/**
 * @param {number[]} nums
 * @return {number}
 *
 * differenceOfSum принимает массив положительных целых
 * чисел nums.
 *
 * - Сумма элементов - это сумма всех элементов массива
 *   nums.
 *
 * - Сумма цифр - это сумма всех цифр (не обязательно
 *   различных), которые встречаются в числах массива
 *   nums. Например для числа 193 сумма его цифр:
 *   1 + 9 + 3 = 13
 *
 * Возвращает абсолютную разницу между суммой элементов
 * и суммой цифр массива nums.
 *
 * Абсолютная разница между двумя целыми числами x и y
 * определяется как |x - y|.
 *
 *
 * Пример для:
 *   nums = [1,15,6,3]
 * Результат:
 *   9
 * Пояснение:
 *   Сумма элементов nums: 1 + 15 + 6 + 3 = 25.
 *   Сумма цифр nums: 1 + 1 + 5 + 6 + 3 = 16.
 *   Абсолютная разница между суммой элементов и суммой
 *   цифр: |25 - 16| = 9.
 */
function differenceOfSum(nums) {

  // Объявление и инициализация переменной elementSum
  // значением 0.
  //
  // Будет использоваться для накопления суммы
  // элементов массива nums.
  let elementSum = 0;

  // Объявление и инициализация переменной digitSum
  // значением 0.
  //
  // Будет использоваться для накопления суммы всех
  // цифр каждого числа из nums.
  let digitSum = 0;

  // Перебирает каждый элемент массива nums по значению.
  for (let num of nums) {

    // Добавляем текущее число num к общей сумме
    // элементов elementSum.
    elementSum += num;

    // Разбираем текущее число num на отдельные цифры
    // и добавляем их к накопителю digitSum.
    //
    // Последовательно извлекаем последнюю цифру числа
    // num, добавляем её к накопителю digitSum, затем
    // удаляем эту цифру из num делением на 10 с
    // округлением вниз, так как она уже добавлена в
    // digitSum и не должна использоваться в следующих
    // итерациях.
    //
    // Повторяем, пока num больше нуля.
    while (num > 0) {

      // Добавляем последнюю цифру текущего num к
      // digitSum.
      //
      // Операция num % 10 возвращает остаток от
      // деления на 10 - последнюю (младшую) цифру в
      // десятичной записи.
      digitSum += num % 10;

      // Math.floor(num / 10) отбрасывает дробную
      // часть после деления на 10, фактически удаляя
      // младшую цифру из числа.
      num = Math.floor(num / 10);

      // Пример работы цикла для num = 193
      //
      // Исходные значения перед входом в цикл
      // - num = 193
      // - digitSum = 0
      //
      // ---
      //
      // Итерация 1
      // - Вычисляем последнюю цифру:
      //   digit = num % 10 = 193 % 10 = 3
      //
      // - Обновляем сумму цифр:
      //   digitSum = 0 + 3 = 3
      //
      // - Удаляем последнюю цифру из num:
      //   num = Math.floor(193 / 10) = 19
      //
      // - Состояние после итерации:
      //   num = 19,
      //   digitSum = 3
      //
      // ---
      //
      // Итерация 2
      // - Вычисляем последнюю цифру:
      //   digit = num % 10 = 19 % 10 = 9
      //
      // - Обновляем сумму цифр:
      //   digitSum = 3 + 9 = 12
      //
      // - Удаляем последнюю цифру из num:
      //   num = Math.floor(19 / 10) = 1
      //
      // - Состояние после итерации:
      //   num = 1,
      //   digitSum = 12
      //
      // ---
      //
      // Итерация 3
      // - Вычисляем последнюю цифру:
      //   digit = num % 10 = 1 % 10 = 1
      //
      // - Обновляем сумму цифр:
      //   digitSum = 12 + 1 = 13
      //
      // - Удаляем последнюю цифру из num:
      //   num = Math.floor(1 / 10) = 0
      //
      // - Состояние после итерации:
      //   num = 0,
      //   digitSum = 13
      //
      // ---
      //
      // Остановка цикла
      // - Условие while (num > 0) теперь ложно, потому
      //   что num = 0, поэтому цикл завершается.
      //
      // ---
      //
      // Итог
      // - Конечное значение digitSum = 13, что равно
      //   сумме цифр числа 193 (1 + 9 + 3).
      //
      // - Переменная num в теле цикла стала 0.
      //   Так как num была локальной копией элемента
      //   массива, исходный элемент массива не
      //   изменился.
    }
  }

  // Возвращаем абсолютное значение разницы между
  // суммой элементов и суммой цифр.
  return Math.abs(elementSum - digitSum);
}
