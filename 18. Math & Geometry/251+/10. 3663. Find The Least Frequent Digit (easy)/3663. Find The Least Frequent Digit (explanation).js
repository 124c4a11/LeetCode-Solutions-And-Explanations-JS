/**
 * @param {number} n
 * @return {number}
 *
 * getLeastFrequentDigit принимает целое число и возвращает
 * одну цифру от 0 до 9, которая в десятичном представлении
 * этого числа встречается реже всего при условии, что эта
 * цифра встречается хотя бы один раз.
 *
 *
 * Пример для:
 *   n = 723344511
 * Результат:
 *   2
 * Пояснение:
 *   Наименее частые цифры в n - 7, 2 и 5.
 *
 *   Каждая встречается ровно один раз.
 *
 *   Наименьшая из них 2
 */
function getLeastFrequentDigit(n) {

  // Создаём массив фиксированной длины 10 для подсчёта
  // частоты каждой цифры от 0 до 9.
  //
  // Int32Array используется для экономии памяти и гарантии
  // целочисленного хранения.
  //
  // Все элементы инициализируются нулём.
  const digitToFreq = new Int32Array(10);

  // Цикл извлекает цифры числа n по одной, начиная с
  // младших разрядов.
  while (n > 0) {

    // n % 10 даёт последнюю (младшую) цифру текущего
    // значения n.
    //
    // Увеличиваем соответствующий счётчик в массиве
    // digitToFreq.
    digitToFreq[n % 10]++;

    // Удаляем последнюю цифру из n:
    //
    // Math.floor обеспечивает целочисленное деление вниз.
    //
    // Для положительных n это эквивалентно целочисленному
    // делению на 10.
    n = Math.floor(n / 10);
  }

  // Инициализируем переменную для хранения цифры с
  // минимальной положительной частотой.
  //
  // По умолчанию ставим 0. Это значение будет заменено
  // при нахождении первой подходящей цифры.
  let digitWithMinFreq = 0;

  // minFreq хранит текущую минимальную найденную частоту.
  //
  // Начинаем с Infinity, чтобы любое реальное значение
  // было меньше.
  let minFreq = Infinity;

  // Перебираем все возможные цифры в порядке возрастания
  // от 0 до 9.
  //
  // Порядок важен:
  // При равных частотах (например, если несколько цифр
  // встречаются одинаково редко) будет выбран наименьший
  // по значению цифровой символ, потому что обновление
  // происходит только при строгом уменьшении minFreq.
  for (let digit = 0; digit < 10; digit++) {

    // Считываем ранее накопленную частоту для текущей
    // цифры.
    const digitFreq = digitToFreq[digit];

    // Пропускаем текущую цифру, если:
    // 1. digitFreq === 0 - цифра не встречалась в числе n.
    //
    // 2. digitFreq >= minFreq - текущая частота не меньше
    //    уже найденного минимума. Это условие предотвращает
    //    замену при равных частотах, сохраняя меньшую по
    //    значению цифру, поскольку мы перебираем цифры в
    //    порядке возрастания.
    //
    // Оператор continue пропускает оставшуюся часть тела
    // цикла и переходит к следующей цифре.
    if (
      digitFreq === 0
      || digitFreq >= minFreq
    ) continue;

    // Если мы попали сюда, цифра присутствует в числе n, и
    // частота ее вхождения в n строго меньше текущего
    // minFreq.
    //
    // Обновляем digitWithMinFreq на текущую цифру - это
    // новый кандидат с наименьшей частотой.
    digitWithMinFreq = digit;

    // Обновляем минимальную частоту, чтобы последующие
    // проверки сравнивались с новым минимальным значением.
    minFreq = digitFreq;
  }

  // Возвращаем цифру, которая была определена как имеющая
  // наименьшую положительную частоту.
  return digitWithMinFreq;
}
