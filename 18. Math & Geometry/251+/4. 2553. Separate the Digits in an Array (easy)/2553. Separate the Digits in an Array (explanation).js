/**
 * @param {number[]} nums
 * @return {number[]}
 *
 * separateDigits извлекает все отдельные цифры из каждого
 * числа входного массива и возвращает единый массив этих
 * цифр в том же порядке, в котором цифры встречаются в
 * исходных числах.
 *
 *
 * Пример для:
 *   nums = [13,25,83,77]
 * Результат:
 *   [1,3,2,5,8,3,7,7]
 * Пояснение:
 * - Разделение числа 13 даёт [1,3].
 * - Разделение числа 25 даёт [2,5].
 * - Разделение числа 83 даёт [8,3].
 * - Разделение числа 77 даёт [7,7].
 * answer = [1,3,2,5,8,3,7,7]. Обратите внимание, что
 * answer содержит разделения в том же порядке.
 */
function separateDigits(nums) {

  // Создаём пустой массив digits, который будет
  // использоваться для накопления всех отдельных цифр из
  // каждого числа входного массива.
  const digits = [];

  // Итерация по каждому элементу входного массива nums
  //
  // Переменная num получает значение текущего элемента.
  //
  // Это локальная переменная цикла, которую можно
  // изменять.
  for (let num of nums) {

    // Для каждого числа создаётся временный массив
    // currDigits, в который будут добавляться цифры
    // текущего числа num.
    //
    // В процессе извлечения цифр они добавляются в
    // обратном порядке (от младших к старшим).
    const currDigits = [];

    // Запускаем цикл while, который выполняется до тех
    // пор, пока значение num не станет равным 0 или
    // другим ложным значением.
    //
    // Это позволяет извлечь все цифры числа по одной.
    while (num) {

      // Вычисляем последнюю (младшую) цифру текущего
      // числа с помощью операции остатка от деления на 10
      // и добавляем эту цифру в конец массива currDigits.
      currDigits.push(num % 10);

      // Удаляем младшую цифру из num: делим на 10 и
      // применяем Math.floor для получения целой части,
      // чтобы подготовить число к следующей итерации
      // извлечения цифры.
      num = Math.floor(num / 10);
    }

    // После завершения извлечения цифр для текущего числа
    // массив currDigits содержит цифры в обратном порядке.
    //
    // Вызываем reverse(), чтобы вернуть их в
    // естественный порядок, затем распаковываем (spread)
    // и добавляем все эти цифры в массив digits
    digits.push(...currDigits.reverse());
  }

  // Возвращаем итоговый массив digits, содержащий
  // последовательность всех отдельных цифр из всех чисел
  // входного массива в том порядке, в котором они
  // встречались в исходном массиве.
  return digits;
}
