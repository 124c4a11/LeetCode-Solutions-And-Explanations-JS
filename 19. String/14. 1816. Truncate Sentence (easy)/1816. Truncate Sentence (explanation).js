/**
 * @param {string} s
 * @param {number} k
 * @return {string}
 * 
 * truncateSentence обрезает входную строку так,
 * чтобы в результате осталось ровно k первых
 * слов, и возвращает эту подстроку.
 * 
 * Параметры:
 * - s — строка (предложение), содержащее слова,
 *   разделённые одиночными пробелами.
 * 
 * - k — целое число - количество первых слов,
 *   которые нужно оставить в результирующей
 *   строке.
 * 
 * 
 * Пример для:
 *   s = "Hello how are you Contestant",
 *   k = 4
 * Результат:
 *   "Hello how are you"
 * Пояснение:
 *   Слова в s:
 *   ["Hello", "how", "are", "you", "Contestant"].
 * 
 *   Первые 4 слова:
 *   ["Hello", "how", "are", "you"].
 * 
 *   Следовательно, функция должна вернуть
 *   "Hello how are you".
 */
function truncateSentence(s, k) {

  // Последовательно проходим по символам строки s.
  //
  // Проход по символам позволяет обнаруживать
  // границы слов по пробелам
  for (let i = 0; i < s.length; i++) {

    // Проверяем, является ли текущий символ
    // пробелом.
    // 
    // Если символ не пробел, значит мы всё ещё
    // внутри слова и ничего не делаем — переходим
    // к следующему символу.
    if (s[i] !== ' ') continue;

    // Этот блок выполняется только при
    // обнаружении пробела
    //
    // Встретили пробел — считаем, что одно слово
    // завершилось перед этим пробелом, поэтому
    // уменьшаем счётчик оставшихся слов k на
    // единицу.
    //
    // Важно:
    // Здесь считается количество пробелов, а не
    // количество слов напрямую.
    k--;

    // Проверяем, стало ли k равным нулю после
    // уменьшения.
    // 
    // Условие if (!k) эквивалентно if (k === 0)
    // 
    // Когда k достигает нуля, это означает, что
    // мы достигли границы после k-го слова.
    // 
    // В этом случае возвращаем подстроку от
    // начала строки до текущего индекса i (не
    // включая завершающий пробел), то есть
    // первые k слов без завершающего пробела.
    //
    // Используется s.slice(0, i), который
    // возвращает новую строку от индекса 0 до
    // i-1.
    if (!k) return s.slice(0, i);
  }

  // Если цикл завершился (дошли до конца строки)
  // и мы не вернули результат внутри цикла, это
  // означает, что в строке было меньше или ровно
  // k слов (в частности, если k > 0), поэтому
  // возвращаем исходную строку целиком.
  return s;
}
