/**
 * @param {string[]} sentences
 * @return {number}
 * 
 * mostWordsFound принимает один параметр — массив
 * предложений sentences, и возвращает максимальное
 * количество слов среди всех предложений в этом
 * массиве.
 * 
 * Пример для:
 *   sentences = [
 *     "alice and bob love leetcode",
 *     "i think so too",
 *     "this is great thanks very much"
 *   ]
 * Результат:
 *   6
 * Объяснение:
 * - Первое предложение
 *   "alice and bob love leetcode" содержит в общей
 *   сложности 5 слов.
 * 
 * - Второе предложение
 *   "i think so too" содержит в общей сложности 4
 *   слова.
 * 
 * - Третье предложение
 *   "this is great thanks very much" содержит в
 *   общей сложности 6 слов.
 * Таким образом, максимальное количество слов в
 * одном предложении достигается в третьем
 * предложении, которое содержит 6 слов.
 */
function mostWordsFound(sentences) {

  // Создаём переменную result и инициализируем её
  // нулём.
  // 
  // Она будет хранить наибольшее найденное
  // количество слов среди обработанных предложений.
  // 
  // Нулевое начальное значение корректно, если
  // массив может быть пустым — функция вернёт 0. 
  let result = 0;

  // Итерация по массиву sentences
  for (const sentence of sentences) {

    // Обновляем result, присваивая ему максимум
    // между текущим значением result и количеством
    // слов в текущем предложении.
    result = Math.max(
      result,

      // Вычисление количества слов в текущем
      // предложении:
      // 
      // 1. sentence.split(' ') разбивает строку по
      //    символу пробела ' ' и возвращает массив
      //    подстрок. 
      // 
      //    Важно: 
      //    split(' ') считает только одиночные
      //    пробелы как разделители, что достаточно
      //    для текущей задачи, так как в текущей
      //    задаче ожидается корректное предложение,
      //    которое не содержит пробелов в начале и
      //    в конце предложения, а также между
      //    словами может быть не более одного
      //    пробела. Но если предложение может
      //    содержать последовательные пробелы, то
      //    это породит пустые строки в массиве.
      //
      //    Например, "a b".split(' ') даст
      //    ["a", "", "b"], и длина будет 3, что
      //    может завысить реальное число слов. 
      // 
      //    Если требуется более точный подсчёт
      //    слов, лучше использовать регулярное
      //    выражение и trim, например
      //    sentence.trim().split(/\s+/). 
      // 
      // 2. .length возвращает количество элементов
      //    в полученном массиве, что
      //    интерпретируется как количество "слов"
      //    в текущем предложении
      sentence.split(' ').length
    );
  }

  // Возвращаем итоговое значение result
  // — максимальное количество элементов,
  // полученных при разбиении предложений по
  // пробелам, среди всех предложений в массиве.
  return result;
}
