/**
 * @param {number} n
 * @return {string}
 * 
 * concatHex36 принимает число n и возвращает строку, состоящую из
 * шестнадцатеричного представления квадрата n, за которым следует
 * представление куба n в системе счисления с основанием 36.
 * 
 * Итог приводится к верхнему регистру.
 * 
 * 
 * Пример для:
 *   n = 13
 * Результат:
 *   "A91P1"
 * Пояснение:
 *   n2 = 13 * 13 = 169. В шестнадцатеричной системе это
 *   (10 * 16) + 9 = 169, что соответствует "A9".
 * 
 *   n3 = 13 * 13 * 13 = 2197. В системе счисления с основанием 36
 *   (36-ричная, hexatrigesimal) это
 *   (1 * 36^2) + (25 * 36) + 1 = 2197, что соответствует "1P1".
 * 
 *   Конкатенация обоих результатов даёт "A9" + "1P1" = "A91P1".
 */
function concatHex36(n) {

  // Возвращается результат сложного выражения:
  // 
  // Внутри круглых скобок выполняются две операции, результаты
  // которых превращаются в строки и объединяются. 
  // 
  // (n ** 2)
  // Оператор ** выполняет возведение в степень.
  // Здесь n возводится во вторую степень, то есть вычисляется
  // квадрат числа n.
  //
  // (n ** 2).toString(16)
  // Метод toString(16) преобразует число в строку, записанную в
  // шестнадцатеричной системе счисления.
  // Основание 16 означает, что используются цифры 0–9 и буквы a–f.
  // 
  // (n ** 3)
  // Здесь n возводится в третью степень, то есть вычисляется куб
  // числа n.
  // 
  // (n ** 3).toString(36)
  // Метод toString(36) преобразует число в строку в 36‑ричной
  // системе счисления.
  // В этой системе используются цифры 0–9 и буквы a–z.
  // 
  // (n ** 2).toString(16) + (n ** 3).toString(36)
  // Оператор + в данном контексте выполняет конкатенацию строк.
  // То есть две строки — шестнадцатеричное представление
  // квадрата и 36‑ричное представление куба — объединяются в одну.
  //
  // ...toUpperCase()
  // Метод toUpperCase() переводит все буквы в итоговой строке в
  // верхний регистр.
  // Это важно, потому что toString(16) и toString(36) возвращают
  // буквы в нижнем регистре.
  return ((n ** 2).toString(16) + (n ** 3).toString(36)).toUpperCase();
}
