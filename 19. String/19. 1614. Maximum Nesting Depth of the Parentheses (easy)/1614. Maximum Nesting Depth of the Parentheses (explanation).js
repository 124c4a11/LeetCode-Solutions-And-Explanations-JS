/**
 * @param {string} s
 * @return {number}
 * 
 * maxDepth вычисляет максимальную глубину вложенности
 * круглых скобок в строке s.
 * 
 * Она принимает один параметр s — строку, и
 * возвращает целое неотрицательное число
 * — максимальное количество одновременно открытых, но
 * ещё не закрытых, скобок в любом префиксе строки.
 * 
 * 
 * Пример для:
 *   s = "(1+(2*3)+((8)/4))+1" 
 * Результат:
 *   3 
 * Пояснение:
 *   Цифра 8 находится внутри трёх вложенных круглых
 *   скобок в строке.
 */
function maxDepth(s) {

  // Инициализируем переменную result значением 0.
  // 
  // Она будет хранить максимальную глубину
  // вложенности, найденную на данный момент.
  let result = 0;

  // Инициализируем переменную depth значением 0.
  // 
  // Она отражает текущую глубину вложенности при
  // обходе строки.
  let depth = 0;

  // Итерация по каждому символу строки s
  for (const char of s) {

    // Проверяем, является ли текущий символ
    // открывающей скобкой '('.
    if (char === '(') {

      // Если да, увеличиваем текущую глубину
      // вложенности на единицу.
      depth++;

      // После увеличения глубины проверяем, не стала
      // ли она больше максимальной.
      // 
      // Math.max сравнивает текущее значение result
      // и новое значение depth.
      // 
      // Если depth больше, то обновляем result.
      result = Math.max(result, depth);
    } else if (char === ')') {

      // Если текущий символ — закрывающая скобка ')'
      //
      // Уменьшаем текущую глубину вложенности на
      // единицу, так как встретили закрывающую
      // скобку.
      //
      // Это соответствует выходу из текущего уровня
      // вложенности на уровень выше.
      depth--;
    }

    // Другие символы (например, буквы или цифры)
    // игнорируются, так как они не влияют на
    // глубину вложенности скобок.
  }

  // После завершения цикла возвращаем значение result.
  //
  // Оно отражает максимальную глубину вложенности
  // скобок в строке
  return result;
}
