/**
 * @param {number} n
 * @param {string[]} commands
 * @return {number}
 * 
 * finalPositionOfSnake вычисляет итоговую позицию
 * «змейки» на квадратном поле n x n, применяя
 * последовательность команд движения и возвращая
 * одномерный индекс конечной клетки в порядке слева
 * направо, сверху вниз.
 * 
 * Параметры:
 * - n — целое число ширины поля
 *
 * - commands — массив строковых команд
 *   'UP', 'DOWN', 'LEFT', 'RIGHT'
 * 
 * 
 * Пример для:
 *   Input: n = 3,
 *   commands = ["DOWN","RIGHT","UP"]
 * Результат:
 *   1
 * Пояснение:
 *    START        DOWN       RIGHT         UP
 *   0* 1  2     0  1  2     0  1  2     0  1* 2
 *   3  4  5     3* 4  5     3  4* 5     3  4  5
 *   6  7  8     6  7  8     6  7  8     6  7  8
 */
function finalPositionOfSnake(n, commands) {

  // Инициализируем вертикальную координату
  // (номер строки).
  // 
  // Нулевая строка соответствует верхней границе
  // поля.
  let row = 0;

  // Инициализируем горизонтальную координату
  // (номер столбца).
  // 
  // Нулевой столбец соответствует левой границе
  // поля.
  let col = 0;

  // Итерация по всем элементам массива commands в
  // порядке их следования.
  for (const command of commands) {

    // Если команда 'DOWN', увеличиваем row на 1,
    // что моделирует движение на одну клетку вниз
    // по вертикали.
    if (command === 'DOWN') row++;

    // Если команда 'UP', уменьшаем row на 1
    // — движение на одну клетку вверх.
    else if (command === 'UP') row--;

    // Если команда 'RIGHT', увеличиваем col на 1
    // — движение вправо на одну клетку.
    else if (command === 'RIGHT') col++;

    // Если команда 'LEFT', уменьшаем col на 1
    // — движение влево на одну клетку.
    else if (command === 'LEFT') col--;
  }

  // Преобразуем двумерные координаты (row, col)
  // в одномерный индекс в строковом порядке и 
  // возвращаем результат.
  return row * n + col;
}
