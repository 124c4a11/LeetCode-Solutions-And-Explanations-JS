/**
 * @param {string[]} words
 * @return {string[]}
 *
 * removeAnagrams удаляет из входного массива words слова,
 * которые являются анаграммами предыдущего слова,
 * возвращая новый массив с сохранённым порядком первых
 * вхождений.
 *
 *
 * Пример для:
 *   words = ["abba","baba","bbaa","cd","cd"]
 * Результат:
 *   ["abba","cd"]
 * Пояснение:
 *   Один из способов получить результирующий массив -
 *   выполнить следующие операции:
 *   - Поскольку words[2] = "bbaa" и words[1] = "baba"
 *     являются анаграммами, выбираем индекс 2 и удаляем
 *     words[2].
 *
 *     Теперь words = ["abba","baba","cd","cd"].
 *
 *   - Поскольку words[1] = "baba" и words[0] = "abba"
 *     являются анаграммами, выбираем индекс 1 и удаляем
 *     words[1].
 *
 *     Теперь words = ["abba","cd","cd"].
 *
 *   - Поскольку words[2] = "cd" и words[1] = "cd"
 *     являются анаграммами, выбираем индекс 2 и удаляем
 *     words[2].
 *
 *     Теперь words = ["abba","cd"].
 *   Больше операций выполнить нельзя, поэтому
 *   ["abba","cd"] - окончательный ответ.
 */
function removeAnagrams(words) {

  // Создаём пустой массив result для накопления итоговых
  // слов.
  const result = [];

  // prevWordHash хранит строковое представление частот
  // букв последнего добавленного в result слова.
  //
  // Инициализируем пустой строкой, чтобы первое слово
  // всегда добавлялось.
  let prevWordHash = '';

  // Итерация по каждому слову из входного массива words в
  // порядке следования.
  for (const word of words) {

    // Создаём Int32Array длиной 26 для подсчёта частот
    // букв латинского алфавита a..z.
    //
    // Индекс соответствует строчной букве латинского
    // алфавита 0 → "a" ... 25 → "z"
    //
    // Значение - частота вхождения текущей буквы в слове
    // word
    //
    // Int32Array даёт компактное представление чисел и
    // быстрее обычного массива для числовых операций.
    const charToFreq = new Int32Array(26);

    // Итерация по каждому слову из входного массива
    // words в порядке следования.
    for (const char of word) {

      // Получаем код символа и вычитаем 97 ('a'), чтобы
      // получить индекс 0..25.
      //
      // Увеличиваем соответствующий счётчик в массиве
      // частот.
      charToFreq[char.codePointAt(0) - 97]++;
    }

    // Преобразуем массив частот в строку без разделителей.
    //
    // Такая строка однозначно представляет
    // мультимножество букв слова (порядок букв не важен).
    //
    // Например, "abb" и "bab" дадут одинаковую строку
    // частот, что позволяет обнаружить анаграммы.
    //
    // Почему используется подсчёт частот, а не
    // сортировка символов:
    // Подсчёт частот даёт линейную по длине слова
    // сложность O(L) для формирования хеша, тогда как
    // сортировка символов - O(LlogL). Это часто
    // предпочтительнее при большом количестве слов.
    const currWordHash = charToFreq.join('');

    // Сравниваем текущий хеш с хешем предыдущего
    // добавленного слова.
    //
    // Если они равны, текущее слово - анаграмма
    // предыдущего, и его нужно пропустить.
    if (currWordHash === prevWordHash) continue;

    // Если хеши различаются, добавляем текущее слово в
    // результирующий массив.
    //
    // Это сохраняет первое вхождение из группы анаграмм
    // и отбрасывает последующие подряд идущие анаграммы.
    result.push(word);

    // Обновляем prevWordHash, чтобы последующие слова
    // сравнивались с текущим добавленным словом.
    prevWordHash = currWordHash;
  }

  // Возвращаем массив result, содержащий исходные слова
  // за исключением последовательных анаграмм.
  return result;
}
