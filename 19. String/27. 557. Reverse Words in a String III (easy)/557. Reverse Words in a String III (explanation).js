/**
 * @param {string} s
 * @return {string}
 * 
 * reverseWords принимает одну строку и возвращает новую
 * строку, в которой каждое слово исходной строки
 * перевёрнуто по символам, тогда как порядок слов
 * остаётся прежним.
 */
function reverseWords(s) {

  // Возвращаем результат последовательных операций над
  // строкой s
  return s

    // Разбиваем строку по символу пробела ' ' на массив
    // подстрок (слов).
    .split(' ')

    // Для каждого элемента массива (каждого "слова")
    // вызываем функцию map.
    // 
    // Внутри map используется стрелочная функция,
    // принимающая параметр word.
    // 
    // Эта функция возвращает новую строку — перевёрнутое
    // слово.
    //
    // Подробно о том, что делает выражение внутри map:
    // 1. word.split('') — разбивает слово на массив
    //    отдельных символов.
    //
    // 2. .reverse() — инвертирует порядок элементов
    //    массива символов на месте и возвращает тот же
    //    массив.
    // 
    // 3. .join('') — собирает массив символов обратно в
    //    строку без разделителей.
    // 
    // В результате получаем массив слов, в котором каждое
    // слово преобразовано в свою версию с символами в
    // обратном порядке.
    .map((word) => word.split('').reverse().join(''))

    // join(' '): объединяет элементы результирующего
    // массива (перевёрнутых слов) в одну строку,
    // вставляя между ними по одному пробелу.
    // 
    // Результат — строка, где каждое слово исходной
    // строки имеет символы в обратном порядке, а порядок
    // самих слов сохранён.
    .join(' ');
}
