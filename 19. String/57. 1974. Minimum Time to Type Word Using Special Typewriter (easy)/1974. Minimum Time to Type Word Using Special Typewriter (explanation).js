/**
 * @param {string} word
 * @return {number}
 *
 * minTimeToType вычисляет минимальное количество секунд, необходимое,
 * чтобы напечатать заданное слово на "специальной печатной машинке",
 * где буквы от a до z расположены по кругу, а указатель изначально
 * указывает на a. Каждый шаг перемещения указателя на соседнюю букву
 * занимает 1 секунду, и на нажатие текущей буквы тоже уходит 1
 * секунда.
 *
 * Каждую секунду можно выполнить одну из следующих операций:
 * - Переместить указатель на один символ против часовой стрелки или
 *   по часовой стрелке.
 *
 * - Набрать символ, на который в данный момент указывает указатель.
 *
 *
 * Пример для:
 *   word = "bza"
 * Вывод:
 *   7
 * Пояснение:
 * Символы печатаются следующим образом:
 * - Переместить указатель по часовой стрелке к 'b' за 1 секунду.
 * - Набрать символ 'b' за 1 секунду.
 * - Переместить указатель против часовой стрелки к 'z' за 2 секунды.
 * - Набрать символ 'z' за 1 секунду.
 * - Переместить указатель по часовой стрелке к 'a' за 1 секунду.
 * - Набрать символ 'a' за 1 секунду.
*/
function minTimeToType(word) {

  // Идея:
  //
  // Поскольку нам нужно вводить каждый символ в слове, на сам набор
  // приходится как минимум 1 секунда на символ. Это даёт базовое
  // время len(word) секунд.
  //
  // Возможность оптимизации заключается в минимизации времени
  // перемещения между символами. При переходе от одной буквы к другой
  // на круговой раскладке у нас есть два варианта: идти по часовой
  // стрелке или против неё.
  //
  // Для любых двух букв, если представить их позиции в виде чисел
  // (где 'a' = 0, 'b' = 1, ..., 'z' = 25), прямое расстояние между
  // ними равно |position1 - position2|. Однако, поскольку буквы
  // расположены по кругу, можно также пойти "в другую сторону",
  // что займёт 26 - прямое_расстояние шагов.
  //
  // Например, переход от 'a' к 'z':
  // - По часовой: 25 шагов ('a' → 'b' → ... → 'z')
  //
  // - Против часовой: 1 шаг ('a' → 'z' напрямую, идя назад)
  //
  // Жадный подход работает здесь, потому что каждый символ должен
  // быть набран в порядке следования, и решение о том, каким путём
  // идти между соседними символами, независимо - выбор кратчайшего
  // пути для каждого перехода всегда даёт оптимальное суммарное время.
  //
  // Поэтому для каждого символа в слове:
  // - вычисляем прямое расстояние от текущей позиции
  //
  // - сравниваем его с переходом в противоположную сторону круга
  //   (26 - прямое_расстояние)
  //
  // - выбираем минимум и добавляем его к общему времени
  //
  // - обновляем текущую позицию и продолжаем
  //
  // Это приводит к формуле: total_time = len(word) + сумма
  // минимальных расстояний между последовательными символами.

  // Инициализация переменной totalTime:
  //
  // Стартовое время равно количеству символов в слове, потому что на
  // нажатие каждой буквы требуется ровно 1 секунда.
  let totalTime = word.length;

  // Установка текущей позиции указателя на кодовую точку символа 'a'.
  //
  // Метод codePointAt(0) возвращает числовое представление символа в
  // Unicode (здесь - ASCII-подобное значение).
  //
  // Используется числовое представление для удобства вычисления
  // расстояний между буквами.
  let currCodePoint = 'a'.codePointAt(0);

  // Итерация по каждому символу строки word в порядке следования.
  for (const char of word) {

    // Для текущего символа вычисляем его кодовую точку - это целое
    // число, соответствующее позиции буквы.
    //
    // Это позволяет сравнивать позиции букв как целые числа и
    // вычислять расстояния между ними.
    const targetCodePoint = char.codePointAt(0);

    // Вычисление прямого расстояния между текущей позицией и целью по
    // числовым кодовым точкам.
    //
    // Math.abs гарантирует неотрицательное значение разницы.
    const directDistance = Math.abs(targetCodePoint - currCodePoint);

    // Так как диск букв цикличен (после 'z' снова 'a'), реальное
    // минимальное расстояние - либо прямой путь directDistance, либо
    // путь в обратную сторону через границу, который равен
    // 26 - directDistance, где 26 - количество букв в английском
    // алфавите.
    //
    // Берём минимум из двух вариантов.
    const minDistance = Math.min(directDistance, 26 - directDistance);

    // Прибавление минимального количества перемещений (в секундах) к
    // общему времени.
    //
    // Таким образом totalTime учитывает и время на нажатия каждой
    // буквы (уже учтено) и время на перемещения между ними.
    totalTime += minDistance;

    // Обновляем текущую позицию указателя на только что напечатанную
    // букву, чтобы следующий расчёт расстояния начинался от этой
    // позиции.
    currCodePoint = targetCodePoint;
  }

  // Возвращаем итоговое время:
  //
  // Сумма времени на нажатия (по 1 секунде на символ) и времени на
  // перемещения между буквами.
  return totalTime;
}
