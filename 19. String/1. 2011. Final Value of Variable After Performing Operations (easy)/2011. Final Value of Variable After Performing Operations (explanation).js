/**
 * @param {string[]} operations
 * @return {number}
 * 
 * finalValueAfterOperations вычисляет итоговое
 * числовое значение переменной X после
 * последовательного применения к ней операций 
 * инкремента/декремента, заданных в массиве
 * строк.
 * 
 * Параметры:
 * - operations — *массив строк*.  
 *   Ожидаемые значения элементов: 
 *   "++X", "X++", "--X", "X--".  
 * 
 * Возвращает:
 * - число - итоговое значение X после всех
 *   операций в operations.
 */
function finalValueAfterOperations(operations) {

  // Инициализация переменной X со значением 0.
  //
  // X служит аккумулятором:
  // Он будет изменяться на +1 или -1 в
  // зависимости от каждой операции.
  let X = 0;

  // Перебираем элементы массива operations по
  // одному.
  //
  // В каждой итерации переменная operation
  // ссылается на текущую строку-операцию.
  for (const operation of operations) {

    // Берётся символ с индексом 1 у строки
    // operation (второй символ).
    //
    // Для операций в формате "++X" или "X++"
    // второй символ — '+',
    // а для "--X" или "X--"
    // второй символ — '-'
    //
    // Если второй символ равен '+'
    // — это означает, что операция является
    // инкрементом (в форматах "++X" или "X++"),
    // поэтому прибавляем 1.
    //
    // В противном случае предполагается, что
    // операция — декремент
    // (форматы "--X" или "X--"), и вычитаем 1.
    //
    // Сравнение operation[1] === '+' проверяет,
    // является ли второй символ плюсом.
    //
    // Тернарный оператор возвращает 1, если
    // второй символ '+', иначе -1.
    //
    // Затем результат прибавляется к X с
    // помощью оператора +=, что эквивалентно
    // X = X + (1 или -1).
    //
    // Такой приём позволяет корректно
    // обработать все четыре допустимых форм
    // записи операции, не анализируя позицию
    // символа X.
    X += operation[1] === '+' ? 1 : -1;
  }

  // Возвращаем накопленное значение X после
  // обработки всех операций.
  //
  // Тип возвращаемого значения — число
  // (integer), равное сумме всех инкрементов
  // и декрементов.
  //
  // Если массив operations пустой, вернётся
  // исходное значение 0.
  return X;
}
