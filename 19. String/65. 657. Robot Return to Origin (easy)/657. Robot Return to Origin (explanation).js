/**
 * @param {string} moves
 * @return {boolean}
 *
 * judgeCircle принимает один параметр moves - итерируемую
 * последовательность символов 'U', 'D', 'L', 'R', в которой
 * каждый символ интерпретируется как один шаг по сетке:
 * U - вверх, D - вниз, R - вправо, L - влево.
 *
 * Функция проверяет, вернётся ли робот в исходную точку
 * после последовательности шагов, заданной строкой moves.
 *
 * Возвращает булево значение (true или false):
 * - true - если робот вернется в исходную точку
 *   (y = 0, x = 0).
 *
 * - false - если робот не вернется в исходную точку.
 *
 *
 * Пример для:
 *   moves = "UD"
 * Вывод:
 *   true
 * Пояснение:
 *   Робот сначала делает шаг вверх (U), затем шаг вниз (D).
 *
 *   Поскольку оба шага имеют одинаковую величину и
 *   противоположные направления, суммарное смещение по
 *   вертикали равно нулю, а по горизонтали смещение
 *   отсутствует.
 *
 *   В результате робот возвращается в исходную точку (0,0),
 *   поэтому функция возвращает true.
 *
 *
 * Пример для:
 *   moves = "LL"
 * Вывод:
 *   false
 * Пояснение:
 *   Робот дважды делает шаг влево (L).
 *
 *   Это даёт суммарное смещение по горизонтали влево на 2
 *   единицы, вертикальное смещение равно нулю.
 *
 *   Поскольку итоговые координаты не совпадают с начальной
 *   точкой, функция возвращает false.
 */
function judgeCircle(moves) {

  // Инициализация вертикальной координаты y в 0.
  //
  // Будем увеличивать при движении вверх и уменьшать при
  // движении вниз.
  //
  // y отражает смещение по оси Y относительно начальной
  // точки.
  let y = 0;

  // Инициализация горизонтальной координаты x в 0.
  //
  // Будем увеличивать при движении вправо и уменьшать при
  // движении влево.
  //
  // x отражает смещение по оси X относительно начальной
  // точки.
  let x = 0;

  // Итерация по каждому символу строки moves
  for (const move of moves) {

    // Если текущий символ равен 'U' (up), считаем это
    // шагом вверх и увеличиваем y на 1.
    //
    // Это эквивалентно смещению на единицу по положительному
    // направлению оси Y.
    if (move === 'U') y++;

    // Если символ равен 'D' (down), считаем это шагом вниз
    // и уменьшаем y на 1.
    //
    // Это эквивалентно смещению на единицу по
    // отрицательному направлению оси Y.
    else if (move === 'D') y--;

    // Если символ равен 'R' (right), считаем это шагом
    // вправо и увеличиваем x на 1.
    //
    // Это эквивалентно смещению на единицу по
    // положительному направлению оси X.
    else if (move === 'R') x++;

    // Если символ равен 'L' (left), считаем это шагом влево
    // и уменьшаем x на 1.
    //
    // Это эквивалентно смещению на единицу по
    // отрицательному направлению оси X.
    else if (move === 'L') x--;
  }

  // Возвращаем логическое значение:
  //
  // true если обе координаты x и y равны нулю (объект
  // вернулся в исходную точку), иначе false.
  return y === 0 && x === 0;
}
