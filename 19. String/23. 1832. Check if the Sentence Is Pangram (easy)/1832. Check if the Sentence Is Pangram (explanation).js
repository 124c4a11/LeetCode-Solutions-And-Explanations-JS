/**
 * @param {string} sentence
 * @return {boolean}
 * 
 * checkIfPangram проверяет, содержит ли переданная строка
 * sentence все буквы английского алфавита от a до z.
 * 
 * Параметры:
 * - sentence — строка, состоящая из строчных английских
 *   букв, которую нужно проверить на наличие всех букв
 *   латинского алфавита.
 * 
 * Возвращает логическое значение:
 *   true, если в строке встречаются все 26 букв, и 
 *   false в противном случае.
 * 
 * 
 * Пример для:
 *   sentence = "thequickbrownfoxjumpsoverthelazydog"
 * Результат:
 *   true
 * Пояснение: 
 *   В sentence содержится по крайней мере по одной букве
 *   каждого символа английского алфавита.
 */
function checkIfPangram(sentence) {

  // Создаём новый массив длиной 26, соответствующий 26
  // буквам латинского алфавита. 
  // 
  // Метод new Array(26) создаёт массив с 26 "пустыми"
  // слотами, а fill(false) заполняет каждый слот
  // значением false.
  // 
  // Идея:
  // Каждый индекс массива соответствует конкретной букве:
  // 0 → 'a', 1 → 'b', ..., 25 → 'z'.
  // 
  // Значение false означает, что соответствующая буква
  // ещё не встречалась в предложении.
  const letterToSeen = new Array(26).fill(false);

  // Проходим по каждому символу строки sentence
  for (const char of sentence) {

    // Для текущего символа вызывается char.codePointAt(0)
    // — возвращает числовой код Unicode для первого
    // (и единственного) code point в char.
    // 
    // Вычитание 97 (код символа 'a' в ASCII/Unicode) даёт
    // индекс в диапазоне 0..25 для строчных латинских
    // букв 'a'..'z'.
    // 
    // Затем по этому индексу в массиве letterToSeen
    // устанавливается true, что означает: буква была
    // обнаружена в строке.
    letterToSeen[char.codePointAt(0) - 97] = true;
  }

  // Метод every проверяет, что для каждого элемента
  // массива letterToSeen предикат возвращает true.
  // 
  // Здесь предикат — стрелочная функция (seen) => seen,
  // которая просто возвращает само булево значение
  // элемента.
  // 
  // Если все элементы равны true (т.е. каждая буква
  // алфавита была помечена как встреченная), every
  // вернёт true.
  // 
  // Если хотя бы один элемент остаётся false
  // (т.е. какая-то буква не встретилась), every вернёт
  // false.
  // 
  // Возвращаем результат проверки как итог работы
  // функции.
  return letterToSeen.every((seen) => seen);
}
