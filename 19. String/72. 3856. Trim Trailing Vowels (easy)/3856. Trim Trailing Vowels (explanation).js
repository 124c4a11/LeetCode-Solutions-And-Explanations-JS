/**
 * @param {string} s
 * @return {string}
 *
 * trimTrailingVowels принимает строку s и удаляет все конечные
 * строчные гласные a, e, i, o, u из строки и возвращает
 * оставшийся префикс.
 *
 *
 * Пример для:
 *   Вход: s = "idea"
 * Результат:
 *   "id"
 * Пояснение:
 *   Удаляя завершающие гласные "ea" из строки "idea", получаем
 *   "id".
 */
function trimTrailingVowels(s) {

  // Создаём переменную i и инициализируем её индексом
  // последнего символа строки s.
  //
  // Если s пустая строка, то s.length === 0 и i станет -1, что
  // корректно обработается в цикле ниже.
  let i = s.length - 1;

  // Если текущий символ (s[i]) равен одной из перечисленных
  // гласных, то это означает, что символ нужно удалить (или,
  // точнее, не включать в результирующую строку), поэтому мы
  // сдвинем индекс влево и продолжим проверку предыдущего
  // символа.
  while (i >= 0) {
    if (
      s[i] === 'a'
      || s[i] === 'e'
      || s[i] === 'i'
      || s[i] === 'o'
      || s[i] === 'u'
    ) i--;

    // Если текущий символ не совпал ни с одной из
    // перечисленных гласных, значит мы достигли границы, после
    // которой все символы слева должны остаться в
    // результирующей строке - прерываем цикл.
    else break;
  }

  // Возвращаем новую строку, полученную методом slice от
  // начала (индекс 0) до i + 1.
  //
  // slice(0, i + 1) включает символ с индексом 0 и исключает
  // символ с индексом i + 1, поэтому последний включённый
  // символ имеет индекс i.
  //
  // Если i === -1 (все символы были удалены), slice(0, 0)
  // вернёт пустую строку.
  return s.slice(0, i + 1);
}
