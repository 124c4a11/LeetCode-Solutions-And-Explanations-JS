/**
 * @param {string} s
 * @return {number}
 * 
 * countKeyChanges принимает строку s и подсчитывает, сколько
 * раз в строке происходит изменение символа при переходе от
 * текущего символа к следующему.
 * 
 * Сравнения выполняются без учёта регистра.
 * 
 * 
 * Пример для:
 *   s = "aAbBcC"
 * Результат:
 *   2
 * Пояснение:
 *   От s[0] = 'a' до s[1] = 'A' изменения клавиши нет, так
 *   как регистр (Caps Lock/Shift) не учитывается.
 * 
 *   От s[1] = 'A' до s[2] = 'b' происходит смена клавиши.
 * 
 *   От s[2] = 'b' до s[3] = 'B' изменения клавиши нет, так
 *   как регистр не учитывается.
 * 
 *   От s[3] = 'B' до s[4] = 'c' происходит смена клавиши.
 * 
 *   От s[4] = 'c' до s[5] = 'C' изменения клавиши нет, так
 *   как регистр не учитывается.
 */
function countKeyChanges(s) {

  // Создаёт константу normalizedStr, содержащую версию
  // входной строки s в нижнем регистре, чтобы сравнения
  // символов были нечувствительны к регистру.
  //
  // 'A' и 'a' будут считаться одинаковыми, что важно для
  // подсчёта смен клавиш/символов.
  const normalizedStr = s.toLowerCase();

  // Инициализация счётчика result значением 0.
  // 
  // Этот счётчик будет увеличиваться на единицу каждый раз,
  // когда обнаруживается переход между двумя соседними
  // символами (т.е. когда соседние символы отличаются).
  let result = 0;

  // Цикл for перебирает индексы строки, начиная с 1 и до
  // длины normalizedStr минус 1 включительно.
  // 
  // Начало с 1 обусловлено тем, что на каждой итерации
  // сравнивается текущий символ с предыдущим (i-1).
  // 
  // Если строка пустая или длина 1, тело цикла не выполнится
  // ни разу, и result останется 0.
  for (let i = 1; i < normalizedStr.length; i++) {

    // Сравниваются два соседних символа: предыдущий
    // normalizedStr[i - 1] и текущий normalizedStr[i].
    // 
    // Оператор !== выполняет строгое сравнение без приведения
    // типов. Для строк это эквивалентно проверке на различие
    // символов.
    // 
    // Если символы различны, это означает "смену клавиши",
    // и счётчик увеличивается на 1.
    if (normalizedStr[i - 1] !== normalizedStr[i]) result++;
  }

  // Возвращается итоговое значение result — количество
  // переходов между различными соседними символами в
  // строке s без учета регистра.
  // 
  // Для пустой строки или строки длины 1 функция вернёт 0.
  return result;
}
