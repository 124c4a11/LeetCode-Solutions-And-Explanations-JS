/**
 * @param {string} word
 * @return {number}
 *
 * numberOfSpecialChars принимает строку word. Буква
 * называется специальной, если она встречается в word
 * и в нижнем, и в верхнем регистре.
 *
 * Возвращает количество специальных букв в word.
 *
 *
 * Пример для:
 *   word = "aaAbcBC"
 * Результат:
 *   3
 * Пояснение:
 *   Специальными буквами в word являются
 *   'a', 'b' и 'c' - каждая из них встречается и в
 *   нижнем, и в верхнем регистре.
 *
 *
 * Пример для:
 *   word = "abc"
 * Результат:
 *   0
 * Пояснение:
 *   Ни одна буква в word не встречается в верхнем
 *   регистре.
 */
function numberOfSpecialChars(word) {

  // Создаётся массив фиксированной длины 58 для
  // хранения булевых флагов встреченных символов.
  //
  // Индексы массива будут соответствовать смещению
  // кодовой точки символа относительно 'A' (65).
  //
  // Индексы 0..25 используются для 'A'..'Z'
  // (код 65..90 → смещение -65).
  //
  // Индексы 32..57 используются для 'a'..'z'
  // (код 97..122 → смещение -65 даёт 32..57).
  //
  // Выбранная длина 58 = 122 - 65 + 1 покрывает
  // диапазон ASCII от 'A' до 'z'.
  //
  // Элементы по умолчанию undefined. Будем записывать
  // true для встреченных символов.
  const charToSeen = new Array(58);

  // Проходим по каждому символу строки word.
  for (const char of word) {

    // Получаем числовой код первого кодового пункта
    // символа (UTF-16/Unicode) с помощью
    // codePointAt(0).
    //
    // Для обычных латинских букв 'A'..'Z' и 'a'..'z'
    // это вернёт 65..90 и 97..122 соответственно.
    //
    // Вычитаем 65, чтобы получить индекс в массиве:
    // 'A' → 0, 'Z' → 25, 'a' → 32, 'z' → 57.
    //
    // Записываем true в соответствующую ячейку,
    // отмечая, что символ встречался.
    charToSeen[char.codePointAt(0) - 65] = true;
  }

  // Инициализируем счётчик букв, которые встречаются
  // и в верхнем, и в нижнем регистре.
  let specialCharsCnt = 0;

  // Проходим по 26 позициям, соответствующим
  // заглавным буквам A..Z (индексы 0..25 в массиве).
  for (let i = 0; i < 26; i++) {

    // Условие проверяет два флага:
    // - charToSeen[i] - встречалась ли заглавная
    //   буква с кодом 65 + i;
    //
    // - charToSeen[i + 32] - встречалась ли
    //   строчная буква с кодом 65 + i + 32
    //
    // Пояснения логики:
    // - При заполнении массива мы записывали true
    //   только для встреченных символов, незаполненные
    //   ячейки - undefined (falsy).
    //
    // - Оператор && вернёт true только если обе
    //   ячейки истинны (т.е. обе буквы встречались).
    //
    // - Мы специально используем смещение +32, так
    //   как в ASCII/Unicode коды заглавной и строчной
    //   буквы латинского алфавита отличаются на 32:
    //   'A' (65) и 'a' (97) → 97 - 65 = 32
    //
    // - Если в word встречалась только заглавная или
    //   только строчная буква, условие ложно.
    //
    // Если оба флага истинны, увеличиваем счётчик
    // букв, которые встречаются и в верхнем, и в
    // нижнем регистре на единицу.
    if (
      charToSeen[i]
      && charToSeen[i + 32]
    ) specialCharsCnt++;
  }

  // Возвращаем итоговое количество букв, для которых
  // в исходной строке присутствуют оба регистра.
  return specialCharsCnt;
}
