/**
 * @param {string} s
 * @return {string}
 *
 * greatestLetter принимает строку английских букв s.
 *
 * Возвращает наибольшую по алфавиту английскую букву в верхнем регистре,
 * которая встречается в s и в нижнем, и в верхнем регистре. Если такой
 * буквы нет, возвращае пустую строку.
 *
 * Английская буква b считается большей, чем буква a, если b идёт после a
 * в английском алфавите.
 *
 *
 * Пример для:
 *   s = "lEeTcOdE"
 *         ^^    ^
 * Результат:
 *   "E"
 * Пояснение:
 *   Буква 'E' - единственная буква, которая встречается и в нижнем, и в
 *   верхнем регистре.
 */
function greatestLetter(s) {

  // Создаём массив asciiToSeen длиной 123 элементов (индексы 0..122)
  // для отметки встреченных символов по их кодам Unicode.
  //
  // Выбран размер 123, чтобы гарантированно покрыть коды ASCII до 122
  // ('z').
  //
  // Элементы по умолчанию - undefined. Будем помечать встреченные коды
  // как true.
  const asciiToSeen = new Array(123);

  // Проходим по каждому символу строки s
  for (const char of s) {

    // Получаем числовой код char и помечаем его как встреченный.
    asciiToSeen[char.codePointAt(0)] = true;
  }

  // Проходим по кодам заглавных латинских букв от 90 ('Z') вниз до 65
  // ('A').
  //
  // Спуск сверху вниз гарантирует, что первая найденная пара будет
  // наибольшей буквой по алфавиту.
  //
  // Цикл выполняет фиксированное число итераций (26), поэтому его вклад
  // в сложность - константа.
  for (let uppercaseAscii = 90; uppercaseAscii >= 65; uppercaseAscii--) {

    // Вычисляем код соответствующей строчной буквы:
    //
    // Для латинского алфавита строчная буква имеет код, равный коду
    // заглавной + 32 (например, 'A' 65 → 'a' 97).
    const lowercaseAscii = uppercaseAscii + 32;

    // Проверяем, встречались ли в исходной строке одновременно заглавная
    // и строчная версии буквы.
    //
    // Поскольку asciiToSeen содержит true для встреченных кодов и
    // undefined для остальных, логическое выражение
    // asciiToSeen[uppercaseAscii] && asciiToSeen[lowercaseAscii]
    // вернёт true только если оба элемента существуют и истинны.
    if (asciiToSeen[uppercaseAscii] && asciiToSeen[lowercaseAscii]) {

      // Если найдена пара (заглавная и строчная), возвращаем заглавную
      // букву, соответствующую коду uppercaseAscii.
      //
      // String.fromCodePoint корректно создаёт строку из числового
      // кода Unicode.
      //
      // Возврат здесь завершает функцию сразу при обнаружении
      // наибольшей подходящей буквы.
      return String.fromCodePoint(uppercaseAscii);
    }
  }

  // Если не найдено ни одной буквы, у которой присутствуют обе формы
  // (заглавная и строчная), функция возвращает пустую строку как
  // индикатор отсутствия результата.
  return '';
}
