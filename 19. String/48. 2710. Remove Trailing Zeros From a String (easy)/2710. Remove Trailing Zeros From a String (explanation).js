/**
 * @param {string} num
 * @return {string}
 * 
 * removeTrailingZeros принимает один параметр — num, который
 * ожидается как строка с цифрами и удаляет все завершающие
 * символы '0' в конце переданной строки и возвращает новую
 * строку без этих нулей.
 * 
 * 
 * Пример для:
 *   num = "51230100"
 * Результат:
 *   "512301"
 * Пояснение:
 *   Число "51230100" имеет 2 завершающих нуля. Мы удаляем их
 *   и возвращаем "512301".
 */
function removeTrailingZeros(num) {

  // Возвращается результат вызова метода replace на строке 
  // num.
  // 
  // Метод replace ищет совпадение с регулярным выражением
  // /0+$/ и заменяет найденную часть на пустую строку ''.
  // 
  // Регулярное выражение подробно:
  // - 0 — символ нуля;
  //
  // - + — квантификатор "один или более", то есть
  //   последовательность из одной или более цифр '0';
  // 
  // - $ — якорь конца строки, гарантирует, что совпадение
  //   будет только в конце строки (т.е. удаляются только
  //   завершающие нули).
  // 
  // Примеры поведения:
  // - "1200" → "12"
  //
  // - "12.3400" → "12.34"
  //
  // - "0" → "" (единичный ноль удаляется)
  // 
  // - "000" → "" (все нули удаляются)
  //
  // - "123" → "123" (если завершающих нулей нет, строка
  //   возвращается без изменений)
  return num.replace(/0+$/, '');
}
