/**
 * @param {string} s
 * @param {number} k
 * @return {string}
 * 
 * reversePrefix берёт строку s и число k, разворачивает 
 * первые k символов этой строки и возвращает новую
 * строку, где перевёрнутый префикс стоит перед
 * неизменённой оставшейся частью строки.
 * 
 * Параметры:
 * - s — исходная строка, 
 * - k — длина префикса для разворота (целое
 *   неотрицательное число)
 * 
 * 
 * Пример для:
 *   s = "abcd",
 *        ^^
 *   k = 2 
 * Результат:
 *   "bacd"
 *    ^^ 
 * Пояснение: 
 *   Первые k = 2 символа "ab"
 *   перевёрнуты в "ba".
 * 
 *   Итоговая строка — "bacd".
 *                      ^^
 */
function reversePrefix(s, k) {

  // Создаём пустой массив prefixArr, в который будем
  // поэтапно помещать символы префикса длиной k.
  // 
  // Использование массива упрощает последующий разворот
  // методом reverse() и объединение методом join().
  const prefixArr = [];

  // Инициализируем счётчик i значением 0.
  // 
  // Он будет использоваться как индекс текущего символа
  // в строке s.
  let i = 0;

  // Цикл while выполняется, пока i < k.
  // 
  // Цель — пройти по индексам 0..k-1 и собрать
  // соответствующие символы.
  while (i < k) {

    // Добавляем в конец массива символ s[i]
    prefixArr.push(s[i]);

    // Увеличиваем i на единицу, чтобы перейти к
    // следующему символу в следующей итерации.
    i++;
  }

  // После завершения цикла:
  // 
  // prefixArr содержит последовательность первых k
  // элементов s.
  // 
  // reverse() разворачивает порядок элементов массива
  // на месте.
  // 
  // join('') объединяет элементы массива в строку без
  // разделителя. 
  // 
  // s.slice(i) возвращает подстроку исходной строки s,
  // начиная с индекса i до конца строки. Если
  // i >= s.length, вернётся пустая строка.
  // 
  // Итоговая строка формируется конкатенацией
  // перевёрнутого префикса и оставшейся части
  // исходной строки.
  return prefixArr.reverse().join('') + s.slice(i);
}
