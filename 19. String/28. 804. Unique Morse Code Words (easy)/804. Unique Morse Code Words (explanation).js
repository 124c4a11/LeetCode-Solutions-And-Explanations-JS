/**
 * @param {string[]} words
 * @return {number}
 * 
 * uniqueMorseRepresentations принимает один параметр — массив
 * строк words и преобразует каждую строку этого массива в строку
 * Морзе и возвращает количество уникальных таких преобразований. 
 * 
 * 
 * Пример для:
 *   words = ["gin","zen","gig","msg"]
 * Результат:
 *   2 
 * Пояснение: 
 *   Преобразование каждого слова: 
 *     "gin" → "--...-."
 *     "zen" → "--...-."
 *     "gig" → "--...--."
 *     "msg" → "--...--."
 * 
 *   Всего 2 различных преобразования:
 *     "--...-." и "--...--."
 */
function uniqueMorseRepresentations(words) {

  // Создаётся константа charToMorse — массив строк, где каждый
  // элемент представляет код Морзе для соответствующей буквы
  // латинского алфавита.
  // 
  // Индекс 0 соответствует букве 'a', индекс 1 — 'b' и так
  // далее до индекса 25 для 'z'.
  const charToMorse = [".-", "-...", "-.-.", "-..", ".", "..-.",
    "--.", "....", "..", ".---", "-.-", ".-..", "--", "-.",
    "---", ".--.", "--.-", ".-.", "...", "-", "..-", "...-",
    ".--", "-..-", "-.--", "--.."];

  // Создаём пустое множество (Set) morseWordSet для хранения
  // уникальных строковых представлений слов в коде Морзе.
  // 
  // Set автоматически исключит дубликаты, поэтому в конце
  // размер множества — количество уникальных представлений.
  const morseWordSet = new Set();

  // Итерация по каждому слову из входного массива words
  for (const word of words) {

    // Для текущего слова создаём временный массив
    // morseWordCodes, в который будем поочерёдно добавлять
    // коды Морзе для каждой буквы.
    // 
    // Массив выбран вместо конкатенации строк, чтобы избежать
    // квадратичной сложности при многократной конкатенации
    // строк в цикле.
    //
    // Многократная конкатенация строк в цикле обычно имеет
    // худшую практическую производительность и может вести к
    // O(n^2) по времени в худшем случае, тогда как сбор в
    // массив с последующим join даёт линейную сложность O(n)
    // и обычно быстрее и экономичнее по памяти при больших
    // объёмах данных.
    const morseWordCodes = [];

    // Итерация по каждому символу (char) в строке word
    for (const char of word) {

      // Получаем числовой код символа через char.codePointAt(0).
      // Для латинских строчных букв 'a'..'z' это 97..122
      //
      // Вычитаем 97, чтобы получить индекс 0..25, и используем
      // его для доступа к массиву charToMorse
      // 
      // Затем добавляем соответствующую строку Морзе в массив
      // morseWordCodes
      morseWordCodes.push(charToMorse[char.codePointAt(0) - 97]);
    }

    // После обработки всех символов слова объединяем элементы
    // morseWordCodes в одну строку без разделителей.
    // 
    // Полученная строка — полное Морзе-представление слова.
    // 
    // Добавляем её в множество morseWordSet.
    morseWordSet.add(morseWordCodes.join(''));
  }

  // Возвращаем размер множества morseWordSet, то есть
  // количество уникальных Морзе-представлений.
  return morseWordSet.size;
}
