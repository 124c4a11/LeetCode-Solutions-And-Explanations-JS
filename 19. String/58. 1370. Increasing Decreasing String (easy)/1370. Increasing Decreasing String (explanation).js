/**
 * @param {string} s
 * @return {string}
 *
 * sortString функция принимает один параметр - строку s и
 * возвращает новую строку, в которой символы исходной строки
 * переставлены по алгоритму "по возрастанию и убыванию" по
 * алфавиту: сначала по возрастанию (a→z), затем по убыванию
 * (z→a), повторяя цикл, пока не будут использованы все
 * символы.
 *
 * Параметр s содержит только строчные латинские буквы a–z.
 *
 *
 * Пример для:
 *   s = "aaaabbbbcccc"
 * Результат:
 *   "abccbaabccba"
 * Пояснение:
 * - После шагов 1, 2 и 3 первой итерации result = "abc"
 *
 * - После шагов 4, 5 и 6 первой итерации result = "abccba"
 *
 * - Первая итерация завершена. Теперь s = "aabbcc" и мы
 *   возвращаемся к шагу 1
 *
 * - После шагов 1, 2 и 3 второй итерации result = "abccbaabc"
 *
 * - После шагов 4, 5 и 6 второй итерации
 *   result = "abccbaabccba"
 */
function sortString(s) {

  // Ключевая идея в том, что этот чередующийся шаблон выбора
  // по возрастанию и убыванию можно упростить до
  // повторяющегося паттерна по алфавиту.
  //
  // При внимательном анализе алгоритма мы замечаем следующее:
  // - В фазе возрастания мы по сути выбираем символы в
  //   алфавитном порядке a, b, c, ... пока они доступны.
  //
  // - В фазе убывания мы выбираем символы в обратном
  //   алфавитном порядке z, y, x, ... пока они доступны.
  //
  // Важное наблюдение: нам не нужно отслеживать "какой
  // символ был добавлен последним", потому что:
  // - При движении вверх, если мы итерируемся от a до z и
  //   берём только доступные символы, условие "больше
  //   последнего добавленного" автоматически выполняется.
  //
  // - При движении вниз, если мы итерируемся от z до a и
  //   берём только доступные символы, условие "меньше
  //   последнего добавленного" автоматически выполняется.
  //
  // Это заставляет по‑другому взглянуть на задачу: вместо
  // сложного отслеживания состояния и сравнений можно
  // просто:
  // - Посчитать частоту каждого символа.
  //
  // - Создать шаблон, который чередует [a‑z] и [z‑a].
  //
  // - Продолжать циклически проходить по этому шаблону и
  //   брать каждый символ, если он ещё доступен (count > 0).
  //
  // Последовательность abc...xyz, а затем zyx...cba
  // естественным образом обеспечивает правила алгоритма.
  // Конкатенируя эти две последовательности и повторно
  // итерируясь по ним, мы чередуем фазы возрастания и
  // убывания, используя все доступные символы.
  //
  // Это превращает, казалось бы, сложную задачу отслеживания
  // состояния в простую задачу подсчёта и итерации:
  // достаточно циклически проходить по строке
  // "abcdefghijklmnopqrstuvwxyzzyxwvutsrqponmlkjihgfedcba"
  // и выбирать доступные символы, пока они не закончатся.


  // Создаём массив фиксированной длины 26 для подсчёта
  // частот букв 'a'..'z'.
  //
  // Все элементы автоматически инициализируются нулём.
  //
  // Индекс 0 соответствует 'a', индекс 25 - 'z'.
  const charToFreq = new Int32Array(26);

  // Итерация по каждому символу входной строки s
  for (const char of s) {

    // Получаем числовой код символа (Unicode code point) и
    // вычитаем 97 - код 'a' в ASCII/Unicode, чтобы получить
    // индекс в диапазоне 0..25 для букв латинского алфавита
    // в нижнем регистре.
    //
    // Затем увеличиваем соответствующую ячейку массива
    // частот на 1.
    charToFreq[char.codePointAt(0) - 97]++;
  }

  // Результат собираем в массив result, потому что push в
  // массив эффективнее, чем конкатенация строк в цикле.
  //
  // В конце массив будет объединён в строку методом join('').
  const result = [];

  // Основной цикл:
  //
  // Повторяем проходы по алфавиту вверх и вниз, пока длина
  // результата не станет равна длине входной строки.
  //
  // Это гарантирует, что мы добавим ровно столько символов,
  // сколько было в исходной строке.
  while (result.length < s.length) {

    // Первый проход:
    //
    // Проходим по алфавиту слева направо
    // (от 'a' к 'z').
    //
    // На каждой итерации пытаемся взять по одному экземпляру
    // каждой буквы, если она ещё осталась.
    for (let i = 0; i < 26; i++) {

      // Если для буквы i нет доступных экземпляров
      // (счётчик равен 0), пропускаем итерацию.
      if (!charToFreq[i]) continue;

      // Преобразуем индекс обратно в символ:
      //
      // i + 97 - код буквы, String.fromCodePoint создаём
      // строку из кода.
      //
      // Добавляем символ в конец массива result.
      result.push(String.fromCodePoint(i + 97));

      // Уменьшаем счётчик для этой буквы, так как мы
      // "использовали" один экземпляр.
      charToFreq[i]--;
    }

    // Второй проход:
    //
    // Проходим по алфавиту справа налево (от 'z' к 'a').
    //
    // Это реализует вторую фазу алгоритма, где берутся
    // оставшиеся буквы в обратном порядке.
    for (let i = 25; i >= 0; i--) {

      // Если для буквы i нет доступных экземпляров
      // (счётчик равен 0), пропускаем итерацию.
      if (!charToFreq[i]) continue;

      // Добавляем символ с кодом i + 97 в результат.
      result.push(String.fromCodePoint(i + 97));

      // Уменьшаем частоту, так как один экземпляр буквы
      // добавлен в результат.
      charToFreq[i]--;
    }
  }

  // После выхода из цикла объединяем массив символов в одну
  // строку без разделителей и возвращаем её.
  return result.join('');
}
