/**
 * @param {string[]} words
 * @return {number}
 *
 * countPrefixSuffixPairs принимает параметр words - массив строк
 * и подсчитывает количество упорядоченных пар индексов (i, j) с
 * i < j, для которых строка words[i] одновременно является
 * префиксом и суффиксом строки words[j]. Возвращает целое число
 * - количество таких пар.
 *
 *
 * Пример для:
 *   words = ["a","aba","ababa","aa"]
 * Результат:
 *   4
 * Пояснение:
 *   В этом примере подсчитаны следующие пары индексов:
 *   - i = 0 и j = 1, потому что isPrefixAndSuffix("a", "aba")
 *     возвращает true.
 *
 *   - i = 0 и j = 2, потому что isPrefixAndSuffix("a", "ababa")
 *     возвращает true.
 *
 *   - i = 0 и j = 3, потому что isPrefixAndSuffix("a", "aa")
 *     возвращает true.
 *
 *   - i = 1 и j = 2, потому что isPrefixAndSuffix("aba", "ababa")
 *     возвращает true.
 *
 *   Следовательно, ответ равен 4.
 */
function countPrefixSuffixPairs(words) {
  const n = words.length;

  // Инициализируем счётчик найденных пар нулём.
  let pairsCnt = 0;

  // Внешний цикл:
  //
  // Перебираем все возможные индексы i для первой строки пары.
  for (let i = 0; i < n; i++) {

    // Извлекаем текущую строку word1 из массива по индексу i.
    //
    // word1 - потенциальный префикс/суффикс.
    const word1 = words[i];

    // Внутренний цикл:
    //
    // Перебираем индексы j для второй строки пары.
    //
    // Начинаем с i + 1, чтобы:
    // 1. не сравнивать элемент сам с собой (i !== j),
    //
    // 2. не дублировать пары (пара (i,j) считается только один
    //    раз, а не и как (j,i)).
    for (let j = i + 1; j < n; j++) {

      // Извлекаем текущую строку word2 из массива по индексу j.
      //
      // word2 - кандидат, у которого мы проверяем, является ли
      // word1 одновременно префиксом и суффиксом.
      const word2 = words[j];

      // Условие проверяет два свойства одновременно:
      // 1. word2.startsWith(word1) - true, если word2 начинается
      //    с последовательности символов word1.
      //
      // 2. word2.endsWith(word1) - true, если word2 заканчивается
      //    на ту же последовательность word1.
      // Оба метода возвращают boolean и выполняют сравнение с
      // учётом регистра символов.
      //
      // Если оба условия выполнены, увеличиваем счётчик пар на
      // единицу.
      if (
        word2.startsWith(word1)
        && word2.endsWith(word1)
      ) pairsCnt++;
    }
  }

  // Возвращаем итоговое количество найденных пар.
  return pairsCnt;
}
