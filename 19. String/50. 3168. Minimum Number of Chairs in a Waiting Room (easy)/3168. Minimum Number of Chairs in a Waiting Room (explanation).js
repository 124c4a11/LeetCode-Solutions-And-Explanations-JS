/**
 * @param {string} s
 * @return {number}
 * 
 * minimumChairs принимает одну строку событий s, которая
 * содержит символы входа 'E' и выхода 'L'. Вычисляет и
 * возвращает минимальное количество стульев, необходимое
 * в любой момент времени, чтобы разместить всех людей.
 * 
 * 
 * Пример для:
 *   s = "ELELEEL"
 *            ^^
 * Результат:
 *   2
 */
function minimumChairs(s) {

  // Инициализация переменной result, которая будет
  // хранить максимальное количество одновременно занятых
  // мест на протяжении обработки строки.
  // 
  // result отражает минимально необходимое число стульев,
  // чтобы вместить всех одновременно присутствующих людей.
  let result = 0;

  // Инициализация переменной currentOccupancy,
  // отслеживающей текущее число занятых мест в любой
  // момент времени при последовательной обработке событий.
  // 
  // currentOccupancy изменяется на +1 при входе и на -1
  // при выходе
  let currentOccupancy = 0;

  // Проходим по символам строки s
  for (const char of s) {

    // Если текущий символ равен 'E' — интерпретируем как
    // событие входа человека в помещение.
    if (char === 'E') {

      // Увеличиваем текущее число занятых мест на 1 при
      // входе человека.
      // 
      // Это отражает моментальное изменение состояния
      // помещения: один человек сел/занял место.
      currentOccupancy++;

      // Обновляем result так, чтобы он всегда был
      // максимумом между прежним и текущим числом
      // занятых мест.
      // 
      // Таким образом result хранит наибольшее
      // зафиксированное одновременное число занятых
      // мест, что и требуется вернуть.
      result = Math.max(result, currentOccupancy);
    } else {

      // Ветка else для символов равных 'L' — выход.
      // 
      // Уменьшаем текущее число занятых мест на 1 при
      // выходе человека. 
      currentOccupancy--;
    }
  }

  // Возвращаем вычисленное минимально необходимое
  // количество стульев (максимальное число одновременно
  // занятых мест), которое было зафиксировано в
  // переменной result в процессе прохода по строке.
  return result;
}
