/**
 * @param {string[]} strs
 * @return {number}
 *
 * minDeletionSize принимает массив строк strs и возвращает
 * количество столбцов, которые нужно удалить, чтобы в
 * каждом оставшемся столбце символы шли в неубывающем
 * порядке сверху вниз.
 *
 *
 * Пример для:
 *   strs = ["cba","daf","ghi"]
 * Результат:
 *   1
 * Пояснение:
 *   Сетка выглядит так:
 *    cba
 *    daf
 *    ghi
 *   Столбцы 0 и 2 отсортированы, а столбец 1 — нет,
 *   поэтому нужно удалить только 1 столбец.
 */
function minDeletionSize(strs) {

  // Определяем количество строк в входном массиве и
  // сохраняем в константу ROWS.
  //
  // Это значение используется для итерации по строкам и
  // для проверки границ.
  const ROWS = strs.length;

  // Определяем количество столбцов как длину первой строки
  // и сохраняем в константу COLS.
  //
  // Предполагается, что все строки имеют одинаковую длину.
  const COLS = strs[0].length;

  // Инициализируем счётчик columnsToDeleteCnt нулём.
  //
  // Этот счётчик будет увеличиваться на 1 для каждого
  // столбца, который необходимо удалить.
  let columnsToDeleteCnt = 0;

  // Внешний цикл проходит по индексам столбцов
  //
  // Для каждого столбца мы будем проверять, упорядочены ли
  // символы в этом столбце по неубыванию сверху вниз.
  for (let c = 0; c < COLS; c++) {

    // Внутренний цикл проходит по строкам, начиная со
    // второй строки (индекс 1), чтобы сравнивать текущую
    // строку с предыдущей в том же столбце.
    //
    // Использование r = 1 позволяет всегда обращаться к
    // strs[r - 1] без выхода за границы.
    for (let r = 1; r < ROWS; r++) {

      // Сравниваем символ в предыдущей строке и текущей
      // строке в столбце c.
      //
      // Если символ выше больше символа ниже, то порядок
      // нарушен (убывание), и этот столбец нужно удалить.
      //
      // Сравнение выполняется как лексикографическое
      // сравнение символов JavaScript.
      if (strs[r - 1][c] > strs[r][c]) {

        // Увеличиваем счётчик удаляемых столбцов, так как
        // найдено хотя бы одно нарушение порядка в текущем
        // столбце.
        columnsToDeleteCnt++;

        // Прерываем внутренний цикл по строкам для
        // текущего столбца, потому что достаточно одного
        // нарушения, чтобы пометить столбец на удаление.
        break;
      }
    }
  }

  // Возвращаем итоговое количество столбцов, которые
  // необходимо удалить, чтобы все оставшиеся столбцы были
  // упорядочены по неубыванию сверху вниз.
  return columnsToDeleteCnt;
}
