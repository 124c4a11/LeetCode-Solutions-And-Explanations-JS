/**
 * @param {string} s
 * @param {number} k
 * @return {number}
 *
 * Принимает бинарную строку s и целое число k.
 *
 * Бинарная строка удовлетворяет k-ограничению, если
 * выполняется хотя бы одно из следующих условий:
 *   - Количество символов '0' в строке не превышает k.
 *
 *   - Количество символов '1' в строке не превышает k.
 *
 * Возвращает целое число, обозначающее количество подстрок
 * строки s, которые удовлетворяют k-ограничению.
 *
 *
 * Пример для:
 *   s = "10101",
 *   k = 1
 * Результат:
 *   12
 * Пояснение:
 *   Каждая подстрока строки s, кроме подстрок "1010",
 *   "10101" и "0101", удовлетворяет k-ограничению.
 */
function countKConstraintSubstrings(s, k) {

  // Ключевая идея состоит в том, что если подстрока
  // удовлетворяет k-ограничению, то все её подстроки тоже
  // будут ему удовлетворять. И наоборот - если подстрока
  // нарушает k-ограничение (содержит больше k нулей И
  // больше k единиц одновременно), то любая подстрока,
  // содержащая её, тоже будет нарушать ограничение.
  //
  // Это свойство подсказывает использование скользящего
  // окна (sliding window). Мы можем поддерживать окно,
  // которое всегда удовлетворяет k-ограничению, и
  // подсчитывать количество подходящих подстрок,
  // заканчивающихся в каждой позиции.
  //
  // Рассмотрим, что происходит, когда мы расширяем окно
  // вправо, добавляя новый символ:
  //  - Если окно всё ещё удовлетворяет k-ограничению -
  //    отлично! Мы нашли больше валидных подстрок.
  //
  //  - Если окно начинает нарушать ограничение (оба
  //    счётчика превышают k), нужно сдвигать левую границу
  //    вправо, пока окно снова не станет валидным.
  //
  // Хитрость подсчёта заключается в том, что для валидного
  // окна [l, r] нам не нужно проверять каждую подстроку
  // отдельно. Мы знаем, что все подстроки, заканчивающиеся
  // в позиции r и начинающиеся в любой позиции от l до r,
  // валидны. Это даёт нам ровно r - l + 1 валидных
  // подстрок, заканчивающихся в r.
  //
  // Почему это работает? Потому что если всё окно [l, r]
  // удовлетворяет k-ограничению
  // (хотя бы один из счётчиков ≤ k), то любая меньшая
  // подстрока внутри него будет иметь ещё меньше нулей и
  // единиц, а значит, тоже будет удовлетворять ограничению.
  //
  // Такой подход позволяет избежать наивного решения за
  // O(n²) или O(n³), где проверяются все возможные
  // подстроки. Поддерживая валидное окно и используя трюк
  // с подсчётом, мы обрабатываем каждый символ ровно один
  // раз, достигая временной сложности O(n).

  // Инициализация переменной для накопления общего
  // количества подстрок, соответствующих условию.
  let substringsCnt = 0;

  // Массив для подсчёта количества символов '0' и '1' в
  // текущем окне.
  //
  // Индекс 0 соответствует символу '0', индекс 1 -
  // символу '1'.
  //
  // Изначально оба счётчика равны нулю.
  const numToCnt = [0, 0];

  // Левый указатель окна (начало текущего скользящего окна).
  //
  // Будет сдвигаться вправо по мере необходимости, чтобы
  // поддерживать инвариант.
  let l = 0;

  // Основной цикл:
  //
  // Правый указатель r проходит по всем индексам строки s.
  //
  // На каждой итерации расширяем окно вправо, включая
  // символ s[r].
  for (let r = 0; r < s.length; r++) {

    // Увеличиваем счётчик для символа s[r].
    //
    // В JavaScript строковый символ '0' или '1' при
    // индексировании массива будет неявно приведён к числу
    // 0 или 1.
    numToCnt[s[r]]++;

    // Пока оба счётчика символов в окне превышают k
    // одновременно, сжимаем окно слева, уменьшая
    // соответствующие счётчики.
    //
    // Условие numToCnt[0] > k && numToCnt[1] > k означает,
    // что в текущем окне и '0', и '1' встречаются более k
    // раз - такое окно недопустимо по условию задачи.
    //
    // Условие использует логическое И (&&): сдвиг будет
    // происходить только если одновременно количество '0'
    // в окне > k и количество '1' в окне > k.
    while (numToCnt[0] > k && numToCnt[1] > k) {

      // Уменьшаем счётчик символа, который исключаем из
      // окна (символ на позиции l).
      //
      // Аналогично, предполагается, что s[l] - '0' или '1'.
      numToCnt[s[l]]--;

      // Сдвигаем левый указатель вправо, фактически
      // исключая s[l] из текущего окна.
      l++;
    }

    // После возможного сужения окна, все подстроки,
    // которые заканчиваются в r и начинаются в любой
    // позиции i, где l <= i <= r, являются допустимыми
    // (в рамках текущего инварианта).
    //
    // Количество таких подстрок равно длине текущего окна:
    // r - l + 1.
    //
    // Добавляем это количество к общему счётчику.
    substringsCnt += r - l + 1;
  }

  // Возвращаем итоговое количество подстрок,
  // удовлетворяющих ограничению по k.
  return substringsCnt;
}
