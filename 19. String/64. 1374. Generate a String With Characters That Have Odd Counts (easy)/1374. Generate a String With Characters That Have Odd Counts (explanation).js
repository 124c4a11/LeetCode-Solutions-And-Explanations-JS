/**
 * @param {number} n
 * @return {string}
 *
 * generateTheString принимает целое число n и возвращает строку
 * из n символов так, чтобы каждый символ в этой строке
 * встречался нечётное число раз.
 *
 * Возвращает строку, содержащую только строчные английские
 * буквы. Если существует несколько допустимых строк, возвращает
 * любую из них.
 *
 *
 * Пример для:
 *   n = 4
 * Вывод:
 *   "pppz"
 * Пояснение:
 *   "pppz" - допустимая строка, поскольку символ 'p'
 *   встречается три раза (нечётно), а символ 'z' встречается
 *   один раз (нечётно).
 *
 *   Обратите внимание, что существуют и другие допустимые
 *   строки, например "ohhh" и "love".
 */
function generateTheString(n) {

  // Ключевая идея: нужно обеспечить, чтобы каждый символ
  // встречался нечётное число раз.
  //
  // Подумайте о самых простых конструкциях:
  // Если использовать один символ, повторённый n раз, это
  // идеально работает, когда n нечётно - символ встречается
  // нечётное число раз.
  //
  // Но что происходит, когда n чётно? Один и тот же символ,
  // повторённый чётное число раз, нарушает требование.
  //
  // Когда n чётно, нужно проявить изобретательность. Нельзя
  // использовать только один символ, поэтому попробуем два
  // разных символа.
  //
  // Как их распределить?
  //
  // Заметьте, что сумма двух нечётных чисел всегда чётна.
  // Значит, если n чётно и мы хотим разбить его на две части,
  // каждая из которых нечётна, можно сделать так:
  // - 1 вхождение одного символа (нечётно)
  //
  // - n-1 вхождений другого символа (нечётно, поскольку n чётно,
  //   n-1 нечётно)
  // Это даёт 1 + (n-1) = n символов, и оба символа встречаются
  // нечётное число раз.
  //
  // Отсюда элегантное решение:
  // - Если n нечётно: используем один и тот же символ n раз
  //   ('a' * n).
  //
  // - Если n чётно: используем один символ n-1 раз и другой
  //   символ один раз ('a' * (n-1) + 'b').
  //
  // Прелесть этого подхода - его простота: нужно обработать
  // только два случая в зависимости от чётности n, и достаточно
  // максимум двух разных символов.


  // Создаётся массив длины n и заполняется символом 'a' во всех
  // позициях.
  //
  // Array(n) создаёт массив с заданной длиной, который
  // изначально содержит "пустые" слоты.
  //
  // Метод fill('a') заполняет все индексы значением 'a' и
  // возвращает тот же массив.
  const chars = Array(n).fill('a');

  // Проверяем чётность n с помощью побитовой операции AND с 1.
  //
  // Выражение (n & 1) извлекает младший бит целочисленного
  // представления n.
  //
  // Для чётных n выражение (n & 1) даёт 0, для нечётных — 1.
  //
  // Если n чётное, заменяем первый элемент массива на 'b' -
  // это меняет количество символов 'a' в строке на нечетное.
  if ((n & 1) === 0) chars[0] = 'b';

  // Объединяем элементы массива в строку без разделителя и
  // возвращаем результат.
  //
  // join('') конкатенирует все элементы массива в одну строку.
  return chars.join('');
}
