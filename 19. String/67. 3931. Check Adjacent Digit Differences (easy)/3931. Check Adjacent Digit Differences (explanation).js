/**
 * @param {string} s
 * @return {boolean}
 *
 * isAdjacentDiffAtMostTwo принимает строку s, состоящую из цифр,
 * и проверяет, что абсолютная разница между каждым соседним
 * элементом не превышает 2. Возвращает true, если это так для
 * всех пар, иначе false
 *
 *
 * Пример для:
 *   s = "132"
 * Результат:
 *   true
 * Пояснение:
 *   Абсолютная разница между цифрами на позициях s[0] и s[1]
 *   равна |1 - 3| = 2.
 *
 *   Абсолютная разница между цифрами на позициях s[1] и s[2]
 *   равна |3 - 2| = 1.
 *
 *   Поскольку обе разницы не превышают 2, ответ — true.
 */
function isAdjacentDiffAtMostTwo(s) {

  // Инициализация цикла for для прохода по элементам коллекции s,
  // начиная со второго элемента (индекс 1).
  //
  // Начало с i = 1 необходимо, чтобы в теле цикла иметь доступ к
  // предыдущему элементу через i - 1 и сравнивать каждый элемент
  // i с предыдущим (i - 1).
  for (let i = 1; i < s.length; i++) {

    // Выражение Math.abs(s[i - 1] - s[i]) вычисляет абсолютную
    // разницу между соседними элементами.
    //
    // Оператор вычитания приводит операнды к числовому типу при
    // необходимости (явное или неявное приведение типов).
    //
    // Сравнение > 2 проверяет, превышает ли абсолютная разница
    // допустимый порог (2).
    //
    // Если обнаружена пара соседних элементов с разницей больше
    // 2, функция немедленно возвращает false.
    //
    // Ранний возврат (short-circuit) экономит время, прекращая
    // дальнейшую проверку при первом нарушении условия.
    if (Math.abs(s[i - 1] - s[i]) > 2) return false;
  }

  // Если цикл завершился без срабатывания раннего возврата,
  // значит для всех соседних пар абсолютная разница не превышает
  // 2, и условие выполнено для всей последовательности.
  //
  // Возвращаем true, подтверждая, что последовательность
  // удовлетворяет требованию.
  return true;
}
