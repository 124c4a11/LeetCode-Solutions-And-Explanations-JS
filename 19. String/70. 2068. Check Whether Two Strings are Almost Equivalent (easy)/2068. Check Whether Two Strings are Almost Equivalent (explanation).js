/**
 * @param {string} word1
 * @param {string} word2
 * @return {boolean}
 *
 * checkAlmostEquivalent принимает две строки одинаковой длины:
 * word1 и word2 и проверяет, являются ли две строки почти
 * эквивалентными - то есть для каждой буквы латинского
 * алфавита разница в количестве её вхождений между word1 и
 * word2 не превышает 3.
 *
 * Возвращает true, если для каждой буквы разница частот в
 * двух строках не превышает 3 по модулю, иначе false.
 *
 *
 * Пример для:
 *   word1 = "aaaa",
 *   word2 = "bccb"
 * Результат:
 *   false
 * Пояснение:
 *   В слове "aaaa" четыре буквы 'a', а в "bccb" - ни одной.
 *   Разница равна 4, что больше допустимого порога 3.
 *
 *
 * Пример для:
 *   word1 = "abcdeef",
 *   word2 = "abaaacc"
 * Результат:
 *   true
 * Пояснение:
 *   Разницы частот каждой буквы в word1 и word2 не превышают 3:
 *   - 'a' встречается 1 раз в word1 и 4 раза в word2.
 *     Разница равна 3.
 *
 *   - 'b' встречается 1 раз в word1 и 1 раз в word2.
 *     Разница равна 0.
 *
 *   - 'c' встречается 1 раз в word1 и 2 раза в word2.
 *     Разница равна 1.
 *
 *   - 'd' встречается 1 раз в word1 и 0 раз в word2.
 *     Разница равна 1.
 *
 *   - 'e' встречается 2 раза в word1 и 0 раз в word2.
 *     Разница равна 2.
 *
 *   - 'f' встречается 1 раз в word1 и 0 раз в word2.
 *     Разница равна 1.
 */
function checkAlmostEquivalent(word1, word2) {

  // Создаём массив фиксированной длины 26 для хранения разницы
  // частот букв латинского алфавита в нижнем регистре.
  //
  // Каждый индекс соответствует одной букве латинского
  // алфавита: 0 → 'a', 25 → 'z'.
  const charToDiff = new Int32Array(26);

  // Одновременно проходим по символам word1 и word2
  //
  // word1 и word2 имеют одинаковую длину
  for (let i = 0; i < word1.length; i++) {

    // Получаем кодовую точку символа word1 на позиции i.
    //
    // Вычитаем 97 (код 'a') чтобы получить индекс в диапазоне
    // 0..25 для букв 'a'..'z'.
    //
    // Увеличиваем соответствующий элемент массива, фиксируя
    // вклад буквы из word1.
    charToDiff[word1.codePointAt(i) - 97]++;

    // Аналогично для символа word2 на той же позиции: получаем
    // индекс буквы и уменьшаем счётчик.
    //
    // Таким образом в массиве хранится разница частот.
    //
    // Положительное значение означает, что буквы встречаются
    // чаще в word1, отрицательное - чаще в word2.
    charToDiff[word2.codePointAt(i) - 97]--;
  }

  // Проходим по всем 26 значениям разницы частот для букв
  // 'a'..'z'.
  for (const diff of charToDiff) {

    // Проверяем, не превышает ли абсолютная разница допустимый
    // порог 3.
    //
    // Если для какой‑то буквы разница больше 3, строки не
    // считаются почти эквивалентными.
    if (Math.abs(diff) > 3) return false;
  }

  // Если ни для одной буквы разница не превышает 3, считаем
  // строки почти эквивалентными и возвращаем true.
  return true;
}
