/**
 * @param {string} moves
 * @return {number}
 *
 * furthestDistanceFromOrigin принимает одну строку moves, где
 * каждый символ - это ход:
 * - символ 'L' означает шаг влево,
 *
 * - символ 'R' означает шаг вправо,
 *
 * - символ '_' (подчёркивание) - шаг "джокер", который можно
 *   направить либо влево, либо вправо по выбору, чтобы
 *   увеличить итоговое смещение.
 *
 * Возвращает максимально возможное расстояние по оси X от
 * начальной точки, которое можно получить, используя
 * имеющиеся ходы и подстановки для символов '_'
 *
 *
 * Пример для:
 *   moves = "_R__LL_"
 * Результат:
 *   5
 * Пояснение:
 *   Самая дальняя точка, до которой можно добраться от начала
 *   0 - это точка -5.
 *
 *   Это достигается при одной из оптимальных
 *   последовательностей замен подчёркиваний, например
 *   "LRLLLLL".
 */
function furthestDistanceFromOrigin(moves) {

  // Объявление переменной lCnt и инициализация нулём - будет
  // хранить количество символов 'L' (ходы влево).
  let lCnt = 0;

  // Объявление переменной rCnt и инициализация нулём - будет
  // хранить количество символов 'R' (ходы вправо).
  let rCnt = 0;

  // Объявление переменной _Cnt и инициализация нулём - будет
  // хранить количество символов '_' (подчёркиваний).
  //
  // Символ '_' трактуется как "джокер" или свободный ход,
  // который можно использовать в любом направлении.
  let _Cnt = 0;

  // Итерация по каждому символу строки moves
  for (const char of moves) {

    // Если текущий символ строго равен строке 'L',
    // увеличиваем счётчик lCnt на единицу.
    if (char === 'L') lCnt++;

    // Если текущий символ строго равен 'R', увеличиваем
    // счётчик rCnt на единицу.
    else if (char === 'R') rCnt++;

    // Если текущий символ строго равен '_' (подчёркивание),
    // увеличиваем счётчик _Cnt на единицу.
    else if (char === '_') _Cnt++;
  }

  // Вычисление и возврат максимально возможного расстояния от
  // начальной точки по оси X.
  //
  // Обоснование:
  // 1. Без учёта '_' текущее смещение равно lCnt - rCnt
  //    (положительное - смещение влево, отрицательное - вправо).
  //
  // 2. Math.abs(lCnt - rCnt) даёт текущее расстояние от нуля.
  //
  // 3. Каждый символ '_' можно направить так, чтобы увеличить
  //    это расстояние на 1 (в ту же сторону, где уже больше
  //    шагов).
  //
  //    Добавление _Cnt моделирует оптимальное использование
  //    всех подчёркиваний в ту сторону, которая увеличит
  //    абсолютную разницу, то есть каждое '_' добавляет
  //    единицу к максимальной дистанции.
  //
  // Поэтому итоговое максимальное расстояние равно абсолютной
  // разнице плюс количество '_'
  return Math.abs(lCnt - rCnt) + _Cnt;
}
