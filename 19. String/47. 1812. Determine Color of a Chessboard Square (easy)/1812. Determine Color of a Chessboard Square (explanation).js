/**
 * @param {string} coordinates
 * @return {boolean}
 * 
 * squareIsWhite определяет цвет шахматной клетки по её
 * координатам в строковой нотации «буква+цифра»
 * (например, "a1"). 
 * 
 * Параметры:
 *   coordinates - строка. Первый символ — латинская
 *   буква столбца в нижнем регистре (a–h), второй
 *   символ — цифра строки (1–8), например "a1", "h8". 
 * 
 * 
 * Пример для:
 *   coordinates = "a1"
 * Результат:
 *   false
 * Пояснение:
 *   На шахматной доске клетка с координатами «a1»
 *   чёрная, поэтому верните false.
 */
function squareIsWhite(coordinates) {

  // Берём второй символ строки coordinates (индекс 1)
  // — это символ цифры, обозначающей номер строки
  // шахматной доски, и приводим его к числу с помощью
  // Number.
  // 
  // Если coordinates равна "a1", то Number("1") даст 1.
  const row = Number(coordinates[1]);

  // Получаем код Unicode (UTF-16) первого символа 
  // строки coordinates с помощью codePointAt(0).
  // 
  // Для строчных латинских букв 'a'..'h' коды идут
  // подряд.
  // 
  // Вычитая 96, мы переводим код буквы в
  // 1‑индексированный номер столбца.
  //
  // Для строчной латинской буквы 'a' codePointAt(0)
  // вернёт 97, поэтому вычитание 96 даёт 1 для 'a',
  // 2 для 'b' и т.д.
  const col = coordinates.codePointAt(0) - 96;

  // Складываем числовые индексы строки и столбца и
  // берём остаток от деления на 2 — это проверка
  // чётности суммы.
  // 
  // Выражение (row + col) % 2 даёт 0 для чётной
  // суммы и 1 для нечётной.
  // 
  // Обёртка Boolean(...) явно приводит числовой
  // результат к булеву: 0 → false, 1 → true.
  // 
  // Если сумма нечётная — поле белое
  // (результат 1 → true).
  // 
  // Если сумма чётная — поле чёрное
  // (результат 0 → false).
  // 
  // Boolean(...) явно приводит 0/1 к false/true
  // соответственно.
  return Boolean((row + col) % 2);
}
