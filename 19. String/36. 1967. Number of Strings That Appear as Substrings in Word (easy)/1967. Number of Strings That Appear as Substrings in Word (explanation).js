/**
 * @param {string[]} patterns
 * @param {string} word
 * @return {number}
 * 
 * numOfStrings подсчитывает, сколько строк из массива
 * patterns встречаются как подстроки в строке word. 
 * 
 * Параметры:
 * - patterns — ожидается массив строк. Каждый элемент
 *   рассматривается как отдельный шаблон (подстрока),
 *   которую нужно искать в word.
 * 
 * - word — ожидается строка, в которой выполняется
 *   поиск подстрок из patterns.
 * 
 * Результат:
 * - число - количество элементов из patterns, которые
 *   встречаются в word хотя бы один раз.
 * 
 * 
 * Пример для:
 *   patterns = ["a","abc","bc","d"],
 *   word = "abc"
 * Результат:
 *   3
 * Пояснение:
 *   - "a" встречается как подстрока в "abc".
 *                                      ^
 * 
 *   - "abc" встречается как подстрока в "abc".
 *                                        ^^^
 * 
 *   - "bc" встречается как подстрока в "abc".
 *                                        ^^
 * 
 *   - "d" не встречается как подстрока в "abc".
 * 
 *   В итоге 3 строки из patterns встречаются как
 *   подстроки в word.
 */
function numOfStrings(patterns, word) {

  // Объявление переменной result с начальным значением
  // 0.
  //
  // Она будет аккумулировать количество шаблонов из
  // patterns, которые встречаются в word.
  let result = 0;

  // Итерация по массиву patterns
  for (const pattern of patterns) {

    // Проверка наличия текущего шаблона pattern в
    // строке word.
    // 
    // Метод String.prototype.includes выполняет поиск
    // подстроки и возвращает true, если подстрока
    // найдена.
    //
    // Если строка word содержит подстроку pattern,
    // увеличиваем счётчик result на единицу.
    if (word.includes(pattern)) result++;
  }

  // Возвращаем итоговое число найденных совпадений
  return result;
}
