/**
 * @param {string} licensePlate
 * @param {string[]} words
 * @return {string}
 *
 * shortestCompletingWord принимает два параметра: licensePlate (строка)
 * и words (массив строк). Она ищет в массиве words самое короткое слово,
 * которое содержит все буквы из строки licensePlate с учётом количества
 * вхождений (игнорируя цифры, пробелы и регистр) и возвращает его.
 *
 *
 * Пример для:
 *   licensePlate = "1s3 PSt",
 *   words = ["step","steps","stripe","stepple"]
 * Результат:
 *   "steps"
 * Пояснение:
 *   licensePlate содержит буквы 's', 'p', 's' (без учёта регистра) и
 *   't'.
 *   - "step" содержит 't' и 'p', но содержит только одну 's'.
 *   - "steps" содержит 't', 'p' и обе буквы 's'.
 *   - "stripe" не содержит вторую 's'.
 *   - "stepple" не содержит вторую 's'.
 *
 *   Поскольку только "steps" содержит все требуемые буквы, это и есть
 *   ответ.
 */
function shortestCompletingWord(licensePlate, words) {

  // Создаём массив фиксированной длины 26 для подсчёта частот букв
  // 'a'..'z' в licensePlate.
  //
  // Каждый индекс соответствует одной букве латинского
  // алфавита: 0 → 'a', 25 → 'z'.
  //
  // Int32Array экономичнее по памяти и быстрее для числовых операций,
  // чем обычный массив.
  //
  // Значения массива инициализируются нулями
  const licenseCharToFreq = new Int32Array(26);

  // Проходим по каждому символу входной строки licensePlate.
  for (const char of licensePlate) {

    // Приводим символ к нижнему регистру и получаем его кодовую точку
    // Unicode.
    //
    // Вычитаем 97 (код 'a') чтобы получить индекс в диапазоне 0..25 для
    // латинских букв.
    //
    // Если символ не латинская буква, результат будет вне диапазона
    // 0..25.
    const charNdx = char.toLowerCase().codePointAt(0) - 97;

    // Если индекс вне диапазона 0..25, значит символ не является
    // латинской буквой a–z.
    //
    // В этом случае пропускаем его - это позволяет игнорировать цифры,
    // пробелы, дефисы и прочие символы.
    if (charNdx < 0 || charNdx > 25) continue;

    // Увеличиваем счётчик для соответствующей буквы.
    //
    // Это учитывает повторяющиеся буквы в licensePlate (например, "Aa"
    // увеличит счётчик 'a' на 2).
    licenseCharToFreq[charNdx]++;
  }

  // Индекс текущего лучшего (самого короткого подходящего) слова в
  // массиве words.
  let shortestCompletingWordNdx = 0;

  // Текущая минимальная длина подходящего слова.
  //
  // Начинаем с Infinity, чтобы любое реальное слово было короче.
  let minLength = Infinity;

  // Проходим по всем словам массива words.
  //
  // Метка внешнего цикла outer используется для управления потоком из
  // вложенного цикла: при обнаружении, что текущее слово не покрывает
  // требуемые буквы, выполняется continue outer, что сразу переходит к
  // следующему слову в массиве words. То есть метка outer позволяет из
  // вложенного цикла немедленно перейти к следующей итерации внешнего
  // цикла.
  outer: for (let i = 0; i < words.length; i++) {

    // Получаем текущее слово по индексу i.
    const word = words[i];

    // Сохраняем длину слова в отдельную переменную для повторного
    // использования и для сравнения с minLength.
    const wordLength = word.length;

    // Если длина текущего слова не меньше уже найденной минимальной,
    // оно не может быть ответом, поэтому пропускаем дальнейшую проверку
    // этого слова (оптимизация по времени).
    if (wordLength >= minLength) continue;

    // Создаём массив частот букв для текущего слова, аналогичный
    // licenseCharToFreq.
    const wordCharToFreq = new Int32Array(26);

    // Подсчитываем частоты букв в слове.
    for (const char of word) {
      wordCharToFreq[char.codePointAt(0) - 97]++;
    }

    // Сравниваем частоты букв, требуемые из licensePlate, с тем, что
    // есть в текущем слове.
    //
    // Проходим по всем 26 буквам алфавита.
    //
    // Если для какой‑то буквы требуется больше экземпляров, чем есть в
    // слове, то текущее слово не покрывает licensePlate и не подходит
    // - мы переходим к следующему слову внешнего цикла.
    for (let j = 0; j < 26; j++) {
      if (licenseCharToFreq[j] > wordCharToFreq[j]) {

        // Используем метку outer, чтобы перейти к следующему слову
        // внешнего цикла, минуя оставшиеся проверки и обновления
        // minLength и shortestCompletingWordNdx
        continue outer;
      }
    }

    // Если мы дошли до этой строки, значит для всех букв
    // wordCharToFreq[j] >= licenseCharToFreq[j], то есть текущее слово
    // покрывает все требуемые буквы и может быть кандидатом на ответ.
    //
    // Обновляем минимальную длину и индекс лучшего слова.
    minLength = wordLength;
    shortestCompletingWordNdx = i;
  }

  // Возвращаем слово из массива words по найденному индексу.
  return words[shortestCompletingWordNdx];
}
