/**
 * @param {string[]} words
 * @return {string[]}
 *
 * findWords принимает массив слов words и возвращает массив
 * слов, которые можно набрать, используя буквы только одной
 * строки QWERTY‑клавиатуры.
 *
 *
 * Пример для:
 *   words = ["Hello","Alaska","Dad","Peace"]
 * Результат:
 *   ["Alaska","Dad"]
 * Пояснение:
 *   Буквы "a" и "A" находятся во 2-й строке американской
 *   раскладки клавиатуры из‑за нечувствительности проверки
 *   к регистру.
 */
function findWords(words) {

  // Создаём строку длиной 26 символов, где каждый символ
  // соответствует одной букве латинского алфавита:
  // позиция 0 - для 'a', 1 - для 'b', ..., 25 - для 'z'.
  //
  // Значение символа в этой строке кодирует "номер" строки
  // клавиатуры, к которой принадлежит соответствующая буква.
  //
  // Например, если на позиции для 'q' стоит '1', это значит,
  // что 'q' относится к первой строке клавиатуры.
  //
  // Такое представление позволяет быстро получить
  // строку клавиатуры для любой буквы по индексу.
  const keyboardRowMapping = '12210111011122000010020202';

  // Инициализируем пустой массив, в который будем добавлять
  // слова, удовлетворяющие условию.
  const validWords = [];

  // Проходим по всем словам массива words.
  //
  // Метка внешнего цикла outer: используется для управления
  // потоком из вложенного цикла.
  //
  // Это позволяет при обнаружении несовпадения строки
  // клавиатуры для какой-либо буквы сразу перейти к
  // следующему слову, минуя оставшиеся проверки текущего
  // слова.
  outer: for (const word of words) {

    // Приводим текущее слово к нижнему регистру, чтобы
    // сравнения были регистронезависимыми.
    //
    // Это упрощает работу с кодами символов и позволяет
    // корректно обрабатывать слова с заглавными буквами.
    const lowercaseWord = word.toLowerCase();

    // Получаем код первого символа слова в диапазоне 0..25.
    //
    // charCodeAt(0) возвращает Unicode-код символа.
    //
    // Вычитание 97 ('a') даёт индекс в алфавите.
    const firstCharNdx = lowercaseWord.charCodeAt(0) - 97;

    // Определяем "целевой" ряд клавиатуры для всего слова
    // по первой букве.
    //
    // Берём символ из keyboardRowMapping по индексу первой
    // буквы - это символ-метка ряда.
    const targetRow = keyboardRowMapping[firstCharNdx];

    // Проходим по остальным символам слова, начиная со
    // второго (индекс 1).
    //
    // Если слово состоит из одной буквы, этот цикл сразу
    // пропускается.
    for (let i = 1; i < lowercaseWord.length; i++) {

      // Для текущего символа также вычисляем индекс в
      // алфавите 0..25.
      //
      // Аналогично, если символ не является латинской
      // буквой, результат может быть вне ожидаемого
      // диапазона.
      const charNdx = lowercaseWord.charCodeAt(i) - 97;

      // Сравниваем метку ряда текущей буквы с меткой ряда
      // первой буквы.
      //
      // Если метки не совпадают, значит слово содержит
      // буквы из разных рядов клавиатуры.
      if (keyboardRowMapping[charNdx] !== targetRow) {

        // При несовпадении используем continue с меткой
        // outer, чтобы немедленно перейти к следующему
        // слову.
        //
        // Это предотвращает дальнейшую проверку оставшихся
        // букв текущего слова и экономит время.
        continue outer;
      }
    }

    // Если цикл завершился без срабатывания continue outer,
    // значит все буквы слова находятся в одном ряду.
    //
    // Добавляем исходное слово в результирующий массив,
    // сохраняя оригинальный регистр и формат слова.
    validWords.push(word);
  }

  // Возвращаем массив слов, которые можно набрать,
  // используя только одну строку клавиатуры.
  //
  // Пустой массив возвращается, если ни одно слово не
  // удовлетворяет условию.
  return validWords;
}
