/**
 * @param {string} s
 * @return {boolean}
 *
 * halvesAreAlike проверяет, имеют ли первая и вторая половины входной строки
 * s одинаковое количество гласных.
 *
 * Функция возвращает булево значение: true, если количество гласных в обеих
 * половинах совпадает, и false в противном случае.
 *
 *
 * Пример для:
 *   s = "book"
 * Результат:
 *   true
 * Пояснение:
 *   a = "bo" и b = "ok". В первой половине 1 гласная, во второй половине 1
 *         ^         ^
 *   гласная. Следовательно, они похожи.
 *
 *
 * Пример для:
 *   s = "textbook"
 * Результат:
 *   false
 * Пояснение:
 *   a = "text" и b = "book". В первой половине 1 гласная, во второй половине
 *         ^            ^^
 *   2 гласные. Следовательно, они не похожи.
 *
 *   Обратите внимание, что гласная 'o' учитывается дважды.
 */
function halvesAreAlike(s) {

  // Вычисление индекса середины строки и сохранение в константу midpoint.
  //
  // Оператор >> 1 выполняет побитовый сдвиг вправо на 1, что эквивалентно
  // целочисленному делению на 2.
  //
  // Использование сдвига гарантирует целочисленный результат и немного
  // быстрее, чем Math.floor(s.length / 2).
  const midpoint = s.length >> 1;

  // Создание множества (Set) гласных символов в обоих регистрах.
  //
  // Set обеспечивает среднее время поиска O(1) для проверки принадлежности
  // символа множеству.
  //
  // Включены как строчные, так и заглавные буквы, чтобы сравнение было
  // регистронезависимым без дополнительной нормализации.
  const vowelSet = new Set(['a', 'e', 'i', 'o', 'u', 'A', 'E', 'I', 'O', 'U']);

  // Инициализация счётчика разницы гласных между первой и второй половинами.
  //
  // Идея:
  // Увеличивать при встрече гласной в первой половине строки s и уменьшать
  // при встрече гласной во второй половине строки.
  //
  // Если количество гласных в обеих половинах одинаково, то после обхода
  // строки переменная vowelBalance будет равна нулю.
  let vowelBalance = 0;

  // Цикл проходит по индексам от 0 до midpoint - 1, то есть по всем символам
  // первой половины.
  //
  // Одновременно в теле цикла проверяется соответствующий символ второй
  // половины по индексу midpoint + i.
  for (let i = 0; i < midpoint; i++) {

    // Eсли текущий символ первой половины строки является гласной
    // (есть в vowelSet), то увеличиваем баланс на 1 - это учитывает вклад
    // первой половины строки.
    if (vowelSet.has(s[i])) vowelBalance++;

    // Если соответствующий символ второй половины строки (индекс midpoint + i)
    // - гласная, то уменьшаем баланс на 1 - это вычитание вклада второй
    // половины строки.
    //
    // В результате, если количество гласных одинаково, все увеличения и
    // уменьшения компенсируют друг друга.
    //
    // Такой подход позволяет сравнить количества гласных без хранения
    // отдельных счётчиков для каждой половины.
    if (vowelSet.has(s[midpoint + i])) vowelBalance--;
  }

  // Возвращаем true, если баланс равен нулю - это означает, что число гласных
  // в первой и второй половинах строки совпадает.
  return vowelBalance === 0;
}
