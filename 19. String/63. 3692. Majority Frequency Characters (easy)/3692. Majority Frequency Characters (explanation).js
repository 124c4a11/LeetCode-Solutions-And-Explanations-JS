/**
 * @param {string} s
 * @return {string}
 *
 * majorityFrequencyGroup принимает строку s (ожидаются строчные
 * латинские буквы) и возвращает группу букв, которые
 * встречаются в строке одинаковое количество раз и образуют
 * самую большую по размеру группу. При равных размерах групп
 * выбирается та, у которой большая частота.
 *
 *
 * Пример для:
 *   s = "aaabbbccdddde"
 * Результат:
 *   "ab"
 * Пояснение:
 *   Обе буквы 'a' и 'b' имеют одинаковую частоту 3 и входят в
 *   группу с наибольшим количеством разных букв. Поэтому
 *   результат - "ab".
 *
 *   Строка "ba" также является корректным ответом.
 */
function majorityFrequencyGroup(s) {

  // Создаём Int32Array длиной 26 для подсчёта частот каждой
  // буквы строки s.
  //
  // Int32Array обеспечивает компактное хранение чисел и
  // инициализируется нулями.
  //
  // Индексы 0..25 соответствуют буквам 'a'..'z'.
  const charToFreq = new Int32Array(26);

  // Итерация по каждому символу входной строки s
  for (const char of s) {

    // Получаем числовой код символа (UTF-16/Unicode), вычитаем
    // 97 ('a') чтобы получить индекс 0..25, и увеличиваем
    // соответствующий счётчик в charToFreq.
    charToFreq[char.codePointAt(0) - 97]++;
  }

  // Создаём массив freqToChars, где индекс - это частота
  // появления символов, а значение - массив символов, имеющих
  // эту частоту.
  //
  // Размер 101, так как максимальная длинна строки s равна 100
  const freqToChars = new Array(101);

  // maxFreqLength хранит текущую максимальную длину (количество
  // букв) среди всех групп букв с одинаковой частотой.
  //
  // Используется для того, чтобы в конце выбрать группу(ы) с
  // наибольшим количеством букв.
  let maxFreqLength = 0;

  // Проходим по всем возможным индексам букв (0..25),
  // соответствующим 'a'..'z'.
  for (let charNdx = 0; charNdx < 26; charNdx++) {

    // Получаем частоту появления буквы с индексом charNdx.
    const freq = charToFreq[charNdx];

    // Если буква не встречалась (частота 0), пропускаем
    // дальнейшую обработку этой буквы.
    if (freq === 0) continue;

    // Если для данной частоты ещё не создан массив букв,
    // инициализируем его пустым массивом.
    //
    // Это отложенная инициализация экономит память, если многие
    // частоты не используются.
    if (!freqToChars[freq]) freqToChars[freq] = [];

    // Преобразуем индекс буквы обратно в символ: charNdx + 'a'.
    //
    // 0 → 'a', 1 → 'b' ... 25 → 'z'.
    const char = String.fromCodePoint(charNdx + 97);

    // Добавляем символ в массив букв для соответствующей
    // частоты.
    //
    // Метод push возвращает новую длину массива, которую мы
    // сохраняем в currFreqLength.
    const currFreqLength = freqToChars[freq].push(char);

    // Если после добавления длина массива для этой частоты не
    // превысила текущий максимум, то нет смысла обновлять
    // maxFreqLength - переходим к следующей букве.
    if (currFreqLength <= maxFreqLength) continue;

    // Обновляем maxFreqLength, так как для этой частоты теперь
    // больше букв, чем было раньше.
    //
    // Это значение будет использоваться при выборе финальной
    // группы букв.
    maxFreqLength = currFreqLength;
  }

  // Проход по возможным значениям частоты в порядке убывания -
  // от 100 до 1.
  //
  // Это обеспечивает приоритет выбора группы с наибольшей
  // частотой в случае, если несколько групп имеют одинаковый
  // размер.
  for (let freq = 100; freq >= 1; freq--) {

    // Получаем массив букв, имеющих текущую частоту freq.
    const chars = freqToChars[freq];

    // Пропускаем итерацию, если:
    // 1. Для этой частоты нет массива (ни одна буква не
    //    встречалась с такой частотой).
    //
    // 2. Длина массива меньше maxFreqLength - значит эта
    //    группа содержит не самое большое количество символов
    //
    // Условие гарантирует, что мы ищем группу с длиной ровно
    // maxFreqLength и с максимальной частотой.
    if (
      !chars
      || chars.length < maxFreqLength
    ) continue;

    // Если найден массив символов с длиной, равной
    // maxFreqLength, возвращаем их как одну строку, объединяя
    // элементы массива без разделителей.
    //
    // join('') объединяет элементы массива без разделителей.
    return chars.join('');
  }

  // Если не найдено ни одной подходящей группы (например,
  // входная строка пустая), возвращаем пустую строку как
  // значение по умолчанию.
  return '';
}
