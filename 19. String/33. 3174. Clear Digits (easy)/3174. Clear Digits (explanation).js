/**
 * @param {string} s
 * @return {string}
 * 
 * clearDigits удаляет из строки все цифры, последовательно
 * удаляя для каждой цифры ближайший слева не‑цифровой
 * символ, и возвращает получившуюся строку.
 * 
 * Параметр:
 *   s — строка состоящая только из строчных букв и цифр.
 * 
 * 
 * Пример для:
 *   s = "cb34"
 * Результат:
 *   ""
 * Пояснение:
 *   Сначала применяем операцию к s[2], и s становится "c4".
 * 
 *   Затем применяем операцию к s[1], и s становится "".
 */
function clearDigits(s) {

  // Создаём пустой массив stack, который будет
  // использоваться как стек (LIFO).
  // 
  // В стек будут помещаться нецифровые символы.
  // 
  // При встрече цифры из стека будет удаляться последний
  // добавленный символ.
  const stack = [];

  // Итерация по каждому символу строки s в порядке
  // следования символов.
  for (const char of s) {

    // Проверяем, является ли текущий символ числом.
    // - isNaN(char) пытается преобразовать char в число.
    //   Если преобразование даёт NaN, значит это не число.
    // 
    // - !isNaN(char) истинно для строк, которые при
    //   приведении к числу дают валидное число
    //   (например, '0'..'9', ' 3').
    //
    // Если символ распознан как цифра/число, удаляем
    // верхний элемент стека.
    // 
    // Если стек пуст, stack.pop() вернёт undefined и
    // ничего не произойдёт — это безопасно.
    if (!isNaN(char)) stack.pop();

    // Если символ не распознан как число, добавляем его в
    // стек.
    // 
    // push помещает элемент в конец массива, который мы
    // используем как вершину стека.
    else stack.push(char);
  }

  // После обработки всех символов объединяем содержимое
  // стека в строку.
  // 
  // join('') соединяет элементы массива без разделителя,
  // сохраняя порядок от нижнего к верхнему (порядок
  // добавления).
  // 
  // Возвращаем полученную строку как результат работы
  // функции.
  return stack.join('');
}
