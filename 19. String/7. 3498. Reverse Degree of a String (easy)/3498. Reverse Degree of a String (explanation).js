/**
 * @param {string} s
 * @return {number}
 * 
 * reverseDegree принимает одну строку, состоящую из
 * строчный латинских букв и возвращает числовую сумму,
 * где каждой строчной латинской букве сопоставляется
 * «обратный» номер в алфавите (a → 26, z → 1),
 * умноженный на позицию буквы в строке (позиция
 * считается с 1).
 * 
 * 
 * Пример для:
 *   s = "abc"
 * Результат:
 *   148
 * Пояснение:
 * 
 * Буква    Индекс         Позиция     Произведение
 *          в обратном     в строке
 *          алфавите  
 * 'a'      26             1           26
 * 'b'      25             2           50
 * 'c'      24             3           72
 * 
 * Обратная степень равна 26 + 50 + 72 = 148.
 */
function reverseDegree(s) {

  // Инициализируется переменная result со значением 0.
  // 
  // В неё будет суммироваться итоговое числовое
  // значение, вычисляемое по символам строки
  let result = 0;

  // Запускаем цикл по индексам символов строки s
  for (let i = 0; i < s.length; i++) {

    // Получаем числовую "обратную позицию" в алфавите
    // для текущего символа:
    // 1. s.codePointAt(i) возвращает числовой код
    //    Unicode для символа в позиции i. Для
    //    ASCII‑строчных латинских букв 'a'..'z' это
    //    97..122. 
    // 
    // 2. Вычитая 97 (код 'a'), мы переводим код в
    //    0‑индексную позицию в алфавите
    //    (a→0, b→1, ..., z→25).
    // 
    // 3. Выражение 26 - (...) инвертирует позицию,
    //    давая "обратный" номер буквы в диапазоне
    //    1..26 (a→26, z→1).
    const reversePosition = 26 - (s.codePointAt(i) - 97);

    // Увеличиваем накопленный результат на значение
    // reversePosition, умноженное на (i + 1).
    // 
    // Множитель (i + 1) придаёт вес символу в
    // зависимости от его позиции в строке: первый
    // символ весит 1, второй — 2 и т.д. 
    // 
    // Таким образом итоговая сумма учитывает и
    // "обратную" позицию буквы, и её порядковый вес
    // в строке.
    result += reversePosition  * (i + 1);
  }

  // Возвращаем накопленное значение result.
  return result;
}
