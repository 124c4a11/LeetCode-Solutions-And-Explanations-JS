/**
 * @param {string} s
 * @param {number[]} indices
 * @return {string}
 * 
 * restoreString переставляет символы входной строки s
 * в новый порядок согласно массиву индексов indices и
 * возвращает восстановленную строку.
 *
 * Параметры:
 * - s — входная строка. Любая последовательность
 *   символов.
 * 
 * - indices — массив целых чисел (индексов). Каждый
 *   элемент indices[i] указывает позицию в итоговой
 *   строке, куда должен быть помещён символ s[i].
 * 
 * Длина s равна длине массива indices.  
 * 
 * 
 * Пример для:
 *   s = "codeleet"
 *   indices = [4,5,6,7,0,2,1,3]
 * Результат:
 *   "leetcode"
 * Пояснение:
 *   Как показано, "codeleet" превращается в "leetcode"
 *   после перемешивания символов по указанным
 *   индексам.
 */
function restoreString(s, indices) {

  // Получаем длину входной строки s и сохраняем её в
  // константу n.
  // 
  // Это значение используется для выделения массива
  // нужного размера и управления циклом.
  const n = s.length;

  // Создаём новый массив result длины n.
  // 
  // Элементы пока не инициализированы (undefined).
  // 
  // Массив служит временным буфером для размещения
  // символов в нужных позициях.
  const result = new Array(n);

  // Запускаем цикл по всем индексам от 0 до n-1
  // включительно.
  // 
  // Переменная i служит индексом в исходной строке s
  // и в массиве indices.
  for (let i = 0; i < n; i++) {

    // Берём символ из исходной строки s на позиции
    // i и помещаем его в массив result в позицию,
    // указанную в массиве indices на той же позиции
    // i.
    // 
    // То есть символ s[i] перемещается в 
    // result[indices[i]].
    // 
    // Предполагается, что indices содержит
    // корректные значения от 0 до n-1 и не содержит
    // дубликатов.
    result[indices[i]] = s[i];
  }

  // Преобразуем массив символов result в строку с
  // помощью join('') и возвращаем её.
  return result.join('');
};
