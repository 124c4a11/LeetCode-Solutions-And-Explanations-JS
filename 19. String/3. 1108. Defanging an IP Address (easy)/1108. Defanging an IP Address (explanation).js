/**
 * @param {string} address
 * @return {string}
 * 
 * defangIPaddr преобразовывает текстовое представление
 * IPv4 адреса.
 * 
 * Её цель — заменить все символы точки '.' на строку
 * [.], что часто используют при публикации адресов в
 * тексте, чтобы избежать автоматического превращения
 * их в ссылки или чтобы усложнить автоматический
 * парсинг адресов.
 * 
 * Параметры:
 * - address - строка содержащая IPv4 адрес в
 *   привычном формате с точками, например
 *   "192.168.0.1".
 * 
 * Возвращает:
 *   Функция возвращает новую строку, в которой все
 *   символы точки . заменены на буквальную
 *   последовательность [.]
 * 
 * 
 * Пример для:
 *   address = "1.1.1.1"
 * Результат:
 *   "1[.]1[.]1[.]1"
 */
function defangIPaddr(address) {

  // Возвращаем результат вызова метода replace у
  // строки address.
  // 
  // Метод replace здесь принимает регулярное
  // выражение в качестве первого аргумента и 
  // строку-замену во втором.
  // 
  // Важно:
  // Метод replace не изменяет исходную строку
  // (strings в JS неизменяемы), он возвращает
  // новую строку.
  // 
  // Регулярное выражение /\./g разбирается так:
  // 1. /.../ — литерал регулярного выражения.
  //
  // 2. \. — экранированная точка. В регулярных
  //    выражениях точка означает "любой символ",
  //    поэтому обратный слэш указывает, что мы
  //    ищем именно символ точки '.'
  // 
  // 3. g — флаг global. Без него replace заменил бы
  //    только первое вхождение точки, с ним
  //    заменяются все вхождения.
  // 
  // Второй аргумент '[.]' — строка-замена.
  // Квадратные скобки здесь — обычные символы
  // строки, они не имеют специального значения в
  // контексте метода replace (в отличие от
  // регулярных выражений).
  // 
  // Результат:
  // превращает "1.2.3.4" в "1[.]2[.]3[.]4".
  //
  // Такое представление часто используется для
  // "дефанга" (defang) IP-адресов, чтобы
  // предотвратить автоматическое создание ссылок
  // или распознавание адреса парсерами.
  return address.replace(/\./g, '[.]');
}
