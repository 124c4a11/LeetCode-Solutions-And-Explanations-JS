/**
 * @param {string} answerKey
 * @param {number} k
 * @return {number}
 *
 * maxConsecutiveAnswers(answerKey, k) вычисляет и возвращает
 * максимальную длину подряд идущих одинаковых ответов
 * ('T' или 'F') в строке answerKey, которую можно получить,
 * выполнив не более k замен символов (заменить 'T' на 'F'
 * или 'F' на 'T').
 *
 * Параметры:
 * - answerKey — строка, состоящая только из символов
 *   'T' и 'F'. Каждый символ представляет ответ на вопрос
 *   (True/False).
 *
 * - k — целое число, задающее максимально допустимое
 *   количество замен символов в answerKey.
 *
 *
 * Пример для:
 *   answerKey = "TTFF",
 *   k = 2
 * Результат:
 *   4
 * Пояснение:
 *   Можно заменить оба символа 'F' на 'T', чтобы получить
 *   answerKey = "TTTT".
 *
 *   В результате получается четыре подряд идущих символа 'T'
 *
 *
 * Пример для:
 *   answerKey = "TFFT",
 *   k = 1
 * Результат:
 *   3
 * Пояснение:
 *   Можно заменить первый 'T' на 'F', получив
 *   answerKey = "FFFT".
 *
 *   Альтернативно, можно заменить второй 'T' на 'F', получив
 *   answerKey = "TFFF".
 *
 *   В обоих случаях получается три подряд идущих символа 'F'
 *
 *
 * Пример для:
 *   answerKey = "TTFTTFTT",
 *   k = 1
 * Результат:
 *   5
 * Пояснение:
 *   Можно заменить первый 'F', получив answerKey = "TTTTTFTT"
 *
 *   Альтернативно, можно заменить второй 'F', получив
 *   answerKey = "TTFTTTTT".
 *
 *   В обоих вариантах получается пять подряд идущих символов
 *   'T'.
 */
function maxConsecutiveAnswers(answerKey, k) {

  // Переменная для хранения максимальной длины допустимого
  // окна (максимального количества подряд идущих одинаковых
  // ответов после не более k замен).
  let maxConsecutive = 0;

  // Счётчик количества символов 'T' в текущем скользящем
  // окне.
  let tCnt = 0;

  // Счётчик количества символов 'F' в текущем скользящем
  // окне.
  let fCnt = 0;

  // Левый индекс окна (указатель на начало текущего окна).
  //
  // Используется вместе с правым указателем r для
  // определения размера окна.
  let l = 0;

  // Основной цикл:
  //
  // Правую границу окна r перемещаем по всей длине строки
  // answerKey.
  //
  // На каждой итерации расширяем окно на один символ справа
  // и обновляем счётчики.
  for (let r = 0; r < answerKey.length; r++) {

    // Если текущий символ равен 'T', увеличиваем tCnt,
    // иначе увеличиваем fCnt.
    answerKey[r] === 'T' ? tCnt++ : fCnt++;

    // Пока оба счётчика превышают k, сдвигаем левую границу
    // окна вправо.
    //
    // Условие tCnt > k && fCnt > k означает, что в текущем
    // окне требуется более k замен как для превращения всех
    // символов в 'T', так и для превращения всех символов в
    // 'F'.
    //
    // Когда оба счётчика превышают k, ни один из вариантов
    // (все 'T' или все 'F') невозможен с допустимым числом
    // замен k, поэтому нужно уменьшать окно до тех пор,
    // пока один из счетчиков не станет <= k.
    while (tCnt > k && fCnt > k) {

      // Уменьшаем счётчик символа, который покидает окно при
      // сдвиге l вправо.
      //
      // Если на позиции l был 'T', уменьшаем tCnt, иначе
      // уменьшаем fCnt.
      //
      // Это корректно поддерживает инвариант, что tCnt и
      // fCnt отражают текущее окно [l, r].
      answerKey[l] === 'T' ? tCnt-- : fCnt--;

      // Сдвигаем левую границу окна на один символ вправо,
      // фактически исключая символ с индексом l.
      l++;
    }

    // Обновляем максимальную длину:
    //
    // Текущая длина окна равна r - l + 1.
    //
    // Так как цикл гарантирует, что в окне выполняется
    // условие (tCnt <= k или fCnt <= k), это окно можно
    // превратить в последовательность одинаковых символов,
    // заменив не более k символов.
    maxConsecutive = Math.max(maxConsecutive, r - l + 1);
  }

  // Возвращаем максимальную найденную длину подряд идущих
  // одинаковых ответов, достижимую при не более k заменах.
  return maxConsecutive;
}
