/**
 * @param {number[]} code
 * @param {number} k
 * @return {number[]}
 *
 * decrypt принимает циклический массив чисел code, целое
 * смещение k, и возвращает новый массив той же длины,
 * где каждый элемент заменён суммой k соседних элементов
 * по правилам задачи:
 * - если k > 0 - сумма следующих k элементов,
 * - если k < 0 - сумма предыдущих |k| элементов,
 * - если k == 0 - все нули.
 *
 *
 * Пример для:
 *   code = [5,7,1,4], k = 3
 * Результат:
 *   [12,10,16,13]
 * Пояснение:
 *   Каждый элемент заменяется суммой следующих 3 чисел.
 *
 *   Расшифрованный код: [7+1+4, 1+4+5, 4+5+7, 5+7+1].
 *
 *   Обратите внимание, что индексы «оборачиваются» по
 *   кругу (циклический массив).
 *
 *
 * Пример для:
 *   code = [1,2,3,4], k = 0
 * Результат:
 *   [0,0,0,0]
 * Пояснение:
 *   Когда k равно нулю, все элементы заменяются на 0.
 *
 *
 * Пример для:
 *   code = [2,4,9,3], k = -2
 * Результат:
 *   [12,5,6,13]
 * Пояснение:
 *   Расшифрованный код: [3+9, 2+3, 4+2, 9+4].
 *
 *   Обратите внимание, что индексы снова оборачиваются
 *   по кругу.
 *
 *   Если k отрицательное, суммируются предыдущие |k|
 *   элементов.
 */
function decrypt(code, k) {
  let n = code.length;

  // Создаём массив decrypted длины n и заполняем его
  // нулями.
  //
  // Это гарантирует, что при k === 0 мы вернём массив
  // нулей, и даёт заранее выделенное место для записи.
  let decrypted = new Array(n).fill(0);

  // Если k равен нулю, по условию задачи каждый элемент
  // результата должен быть 0.
  //
  // В этом случае нет смысла выполнять дальнейшие
  // вычисления - возвращаем заранее подготовленный массив.
  if (k === 0) return decrypted;

  // Объявляем переменные l и r, которые будут обозначать
  // левую и правую границы скользящего окна.
  //
  // Эти границы задаются в относительных индексах (могут
  // выходить за пределы [0, n-1]), но при обращении к
  // массиву используется операция % n для циклической
  // индексации.
  let l;
  let r;

  // Если k положительное, окно для каждого индекса i
  // должно охватывать k следующих элементов справа:
  // это эквивалентно индексам i+1 ... i+k (включительно).
  if (k > 0) {

    // Устанавливаем левую границу окна на 1 (т.е.
    // следующий элемент относительно текущего i).
    l = 1;

    // Правая граница равна k - окно включает элементы с
    // относительными индексами 1..k.
    r = k;
  } else {

    // Если k отрицательное, окно для каждого индекса i
    // должно охватывать |k| предыдущих элементов слева:
    // это эквивалентно индексам i-|k| ... i-1
    // (включительно).
    //
    // Чтобы задать это окно в виде относительных
    // индексов, вычисляем левую границу как n - |k|.
    //
    // Это даёт последовательность индексов, которые при
    // сдвиге будут соответствовать предыдущим элементам.
    l = n - Math.abs(k);

    // Правая граница в этом случае - последний индекс в
    // относительном представлении (n - 1), что
    // соответствует относительным индексам n-|k| ... n-1,
    // которые при модуле n станут индексами,
    // предшествующими текущему i.
    r = n - 1;
  }

  // Инициализируем переменную windowSum, в которой будем
  // хранить сумму элементов текущего окна.
  //
  // Использование скользящей суммы позволяет обновлять
  // значение за O(1) при сдвиге окна, что даёт общую
  // временную сложность O(n) вместо O(n*k).
  let windowSum = 0;

  // Вычисляем начальную сумму для окна, заданного
  // относительными границами l..r включительно.
  //
  // При этом используем i % n для корректной циклической
  // индексации: если l или r >= n, остаток от деления
  // вернёт корректный индекс в диапазоне 0..n-1.
  for (let i = l; i <= r; i++) {
    windowSum += code[i % n];
  }

  // Основной цикл по всем позициям i от 0 до n-1.
  //
  // Для каждой позиции мы записываем текущую windowSum в
  // decrypted[i], затем сдвигаем окно на 1.
  //
  // Благодаря предварительному вычислению начальной
  // суммы и использованию вычитания/добавления крайних
  // элементов, каждый шаг выполняется за константное
  // время.
  for (let i = 0; i < n; i++) {

    // Записываем текущую сумму окна в результирующий
    // массив для позиции i.
    //
    // Это соответствует сумме k соседних элементов
    // (вправо при k>0 или влево при k<0) относительно
    // исходного индекса i.
    decrypted[i] = windowSum;

    // Перед сдвигом окна вычитаем элемент, который
    // выходит из левой границы окна.
    //
    // Используем l % n для корректной циклической
    // индексации, так как l может быть >= n.
    //
    // Это уменьшает windowSum на значение покидающего
    // окна элемента.
    windowSum -= code[l % n];

    // Сдвигаем обе границы окна на единицу вправо в
    // относительном представлении.
    //
    // После этого окно будет соответствовать следующему
    // индексу i+1.
    l++;
    r++;

    // Добавляем в сумму элемент, который вошёл в правую
    // границу после сдвига.
    //
    // Используем r % n для циклической индексации,
    // чтобы корректно обрабатывать случаи, когда r
    // выходит за пределы массива.
    windowSum += code[r % n];
  }

  // Возвращаем полностью заполненный массив decrypted,
  // содержащий для каждой позиции сумму соответствующего
  // окна.
  return decrypted;
}
