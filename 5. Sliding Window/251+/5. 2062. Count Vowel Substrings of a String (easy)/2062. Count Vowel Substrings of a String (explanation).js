/**
 * @param {string} word
 * @return {number}
 *
 * countVowelSubstrings принимает строку word и подсчитывает
 * количество подстрок, которые состоят только из строчных
 * гласных и содержат все пять гласных a, e, i, o, u.
 *
 *
 * Пример для:
 *   word = "aeiouu"
 * Результат:
 *   2
 * Пояснение:
 * Подстроки, состоящие только из гласных, следующие
 * (подчёркнуты):
 * - "aeiouu"
 *    ^^^^^
 * - "aeiouu"
 *     ^^^^^
 *
 *
 * Пример для:
 *   word = "unicornarihan"
 * Результат:
 *   0
 * Пояснение:
 * Не все 5 гласных присутствуют, поэтому подходящих подстрок
 * нет
 */
function countVowelSubstrings(word) {
  const n = word.length;

  // Инициализируем счётчик найденных подстрок, которые
  // состоят только из гласных и содержат все 5 разных
  // гласных.
  let vowelSubstringsCnt = 0;

  // Внешний цикл перебирает возможные позиции левой границы
  // подстроки l.
  //
  // Верхняя граница n - 4 выбрана потому, что минимальная
  // длина подстроки, содержащей все 5 гласных, равна 5,
  // поэтому нет смысла начинать с индекса, после которого
  // остаётся меньше 5 символов.
  for (let l = 0; l < n - 4; l++) {

    // Для каждой новой левой границы создаём пустое
    // множество для отслеживания уникальных гласных,
    // встречающихся в подстроке, начинающейся в l.
    const vowelSet = new Set();

    // Внутренний цикл расширяет правую границу подстроки r
    // от l до конца строки, позволяя проверять все
    // подстроки, начинающиеся в l.
    for (let r = l; r < n; r++) {

      // Получаем текущий символ строки по индексу r и
      // сохраняем в переменную char.
      const char = word[r];

      // Проверяем, является ли текущий символ одной из пяти
      // строчных гласных.
      //
      // Если символ не гласная (включая согласные, цифры,
      // пробелы, заглавные буквы и т.д.), то дальнейшее
      // расширение подстроки бессмысленно, потому что
      // условие "подстрока состоит только из гласных"
      // нарушено.
      //
      // Поэтому прерываем внутренний цикл и переходим к
      // следующей левой границе.
      if (
        char !== 'a'
        && char !== 'e'
        && char !== 'i'
        && char !== 'o'
        && char !== 'u'
      ) break;

      // Если символ - гласная, добавляем её в множество
      // уникальных гласных для текущей подстроки.
      //
      // Set автоматически игнорирует повторные добавления
      // одинаковых символов, поэтому в множестве всегда
      // будут только уникальные гласные, встреченные в
      // подстроке.
      vowelSet.add(char);

      // Если в множестве накопились все пять различных
      // гласных (размер множества равен 5), значит текущая
      // подстрока (от l до r включительно) содержит все
      // гласные и состоит только из гласных.
      //
      // Увеличиваем счётчик.
      //
      // Обратите внимание: даже если дальше расширять
      // подстроку, она останется валидной до тех пор, пока
      // не встретится негласная буква.
      if (vowelSet.size === 5) {
        vowelSubstringsCnt++;
      }
    }
  }

  // После перебора всех возможных левых и правых границ
  // возвращаем итоговый счётчик найденных подстрок.
  return vowelSubstringsCnt;
}
