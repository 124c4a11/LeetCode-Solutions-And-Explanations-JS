/**
 * @param {string} s
 * @return {number}
 *
 * maximumLengthSubstring принимает строку s, состоящую из
 * строчных латинских букв, и возвращает целое число -
 * максимальную длину подстроки, в которой каждый символ
 * встречается не более двух раз.
 *
 *
 * Пример для:
 *   s = "bcbbbcba"
 * Результат:
 *   4
 * Пояснение:
 *   Следующая подстрока имеет длину 4 и содержит не более двух
 *   вхождений каждого символа: "bcbbbcba".
 *                                   ^^^^
 */
function maximumLengthSubstring(s) {

  // Инициализируем переменную для хранения максимальной длины
  // допустимой подстроки, найденной на данный момент.
  //
  // Начинаем с 0, потому что пустая строка даёт длину 0.
  //
  // Будет обновляться по мере обхода строки
  let maxLength = 0;

  // Создаём массив частот для 26 строчных латинских букв (a–z).
  //
  // Int32Array экономит память и быстрее при числовых
  // операциях.
  //
  // Все значения по умолчанию равны 0.
  //
  // Индексы 0..25 соответствуют буквам 'a'..'z'.
  const charToFreq = new Int32Array(26);

  // Левая граница текущего скользящего окна (индекс начала
  // подстроки), инициализируется нулём l будет двигаться
  // вправо при необходимости, чтобы восстановить допустимость
  // окна.
  let l = 0;

  // Проходим по всем индексам строки с помощью правой границы
  // окна r, расширяя окно вправо на каждом шаге.
  //
  // На каждой итерации мы "добавляем" символ s[r] в текущее
  // окно
  for (let r = 0; r < s.length; r++) {

    // Получаем числовой код символа (UTF-16/Unicode), вычитаем
    // 97 ('a') чтобы получить индекс 0..25
    const rightCharNdx = s.codePointAt(r) - 97;

    // Увеличиваем счётчик для символа, который только что
    // добавили в окно
    //
    // Это отражает, что текущая подстрока [l..r] теперь
    // содержит ещё один экземпляр этого символа
    charToFreq[rightCharNdx]++;

    // Если частота символа, только что добавленного в окно,
    // стала больше 2, сдвигаем левый указатель вправо, уменьшая
    // частоты символов, пока частота этого символа не станет
    // <= 2.
    //
    // Это поддерживает инвариант: в окне каждый символ
    // встречается не более двух раз.
    while (charToFreq[rightCharNdx] > 2) {

      // Уменьшаем счётчик символа, находящегося на позиции l,
      // так как он удаляется из окна при сдвиге левого
      // указателя.
      //
      // Здесь снова используется codePointAt и смещение на 97,
      // чтобы получить индекс символа в массиве charToFreq.
      //
      // Важно, что l указывает на текущую левую границу
      charToFreq[s.codePointAt(l) - 97]--;

      // Сдвигаем левый указатель на одну позицию вправо,
      // уменьшая размер окна.
      l++;
    }

    // Вычисляем текущую длину окна как r - l + 1 - это
    // количество символов в подстроке [l..r].
    const currLength = r - l + 1;

    // Обновляем максимальную длину, если текущая длина больше
    // ранее сохранённой.
    //
    // Math.max гарантирует корректное сравнение и присвоение.
    maxLength = Math.max(maxLength, currLength);
  }

  // Возвращаем максимальную длину подстроки, в которой каждый
  // символ встречается не более двух раз.
  return maxLength;
}
