/**
 * @param {number[][]} grid
 * @return {number}
 *
 * deleteGreatestValue принимает на вход двумерный массив
 * чисел (матрицу) и возвращает сумму максимальных
 * значений, которые получаются при поэтапном удалении
 * наибольших элементов из каждой строки - эквивалент
 * суммирования максимумов по столбцам после сортировки
 * строк
 *
 *
 * Пример для:
 *   grid = [[1,2,4],[3,3,1]]
 * Результат:
 *   8
 * Пояснение:
 *   На диаграмме выше показаны значения, которые
 *   удаляются на каждом шаге.
 *   - В первой операции мы удаляем 4 из первой строки и
 *     3 из второй строки (обратите внимание, что есть
 *     две ячейки со значением 3, можно удалить любую из
 *     них). К ответу добавляется 4.
 *
 *   - Во второй операции мы удаляем 2 из первой строки и
 *     3 из второй строки. К ответу добавляется 3.
 *
 *   - В третьей операции мы удаляем 1 из первой строки и
 *     1 из второй строки. К ответу добавляется 1.
 *
 *   Итоговый ответ = 4 + 3 + 1 = 8.
 */
function deleteGreatestValue(grid) {

  // Ключевая идея в том, чтобы понять: нужно многократно
  // находить максимальный элемент в каждой строке и
  // затем брать максимум среди этих удалённых элементов.
  // Поскольку мы будем удалять элементы по столбцам (за
  // одну операцию из каждой строки удаляется по одному
  // элементу), требуется способ организовать данные так,
  // чтобы этот процесс был эффективным.
  //
  // Подумайте, что произойдёт, если отсортировать каждую
  // строку отдельно. После сортировки наибольший элемент
  // каждой строки окажется в крайнем правом положении,
  // второй по величине - во втором справа, и так далее.
  // Такое расположение идеально согласуется с нашим
  // шаблоном удаления - мы удаляем элементы от
  // наибольшего к наименьшему в каждой строке.
  //
  // Когда все строки отсортированы, матрицу можно
  // представить по‑другому. Последний столбец теперь
  // содержит наибольшие элементы каждой строки (они
  // будут удалены в первой операции). Предпоследний
  // столбец содержит вторые по величине элементы каждой
  // строки (удаляются во второй операции), и так далее.
  //
  // Чтобы найти ответ, нужно для каждой операции взять
  // максимальное значение среди удалённых элементов.
  // Поскольку элементы, удаляемые в одной и той же
  // операции, после сортировки оказываются в одном
  // столбце, достаточно найти максимум в каждом столбце
  // и просуммировать их.
  //
  // Например, если у нас есть:
  //   Оригинал: [[5, 1, 3], [2, 9, 6]]
  //
  //   После сортировки: [[1, 3, 5], [2, 6, 9]]
  //
  //   Столбцовые максимумы:
  //     max(1,2)=2, max(3,6)=6, max(5,9)=9
  //
  //   Ответ: 2 + 6 + 9 = 17
  //
  // Это преобразование задачи об удалении по строкам в
  // задачу о столбцовых максимумах после сортировки -
  // основная идея, делающая решение элегантным и
  // эффективным.


  // Определяем константу ROWS как количество строк в
  // grid
  const ROWS = grid.length;

  // Определяем константу COLS как количество столбцов в
  // grid
  const COLS = grid[0].length;

  // Проходим по каждой строке массива grid
  //
  // Сортируем каждую строку в порядке возрастания.
  for (const row of grid) {
    row.sort((a, b) => a - b);
  }

  // Инициализируем аккумулятор для суммы максимальных
  // значений по каждому столбцу.
  let colMaxSum = 0;

  // Внешний цикл по индексам столбцов от 0 до COLS - 1.
  //
  // Для каждого индекса столбца мы найдём максимальный
  // элемент среди всех строк в этом столбце.
  for (let c = 0; c < COLS; c++) {

    // Инициализация переменной для хранения максимума в
    // текущем столбце.
    //
    // Инициализируем нулем, так как диапазон возможных
    // значений от 1 до 100
    let colMax = 0;

    // Проходим по каждой строке и находим максимум в
    // текущем столбце c.
    for (let r = 0; r < ROWS; r++) {
      colMax = Math.max(colMax, grid[r][c]);
    }

    // После прохода по всем строкам добавляем найденный
    // максимум текущего столбца к общей сумме.
    //
    // Это аккумулирует вклад каждого столбца в итоговый
    // результат.
    colMaxSum += colMax;
  }

  // Возвращаем итоговую сумму максимальных значений по
  // всем столбцам.
  return colMaxSum;
}
