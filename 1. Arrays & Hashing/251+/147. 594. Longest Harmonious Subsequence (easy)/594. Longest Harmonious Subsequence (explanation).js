/**
 * @param {number[]} nums
 * @return {number}
 *
 * findLHS принимает массив чисел nums и возвращает длину самой
 * длинной гармоничной подпоследовательности этого массива
 *
 * Гармоничный подпоследовательность - это
 * подпоследовательность, в которой разница между максимальным
 * и минимальным значением равна ровно 1.
 *
 *
 * Пример для:
 *   nums = [1,3,2,2,5,2,3,7]
 * Вывод:
 *   5
 * Пояснение:
 *   Наибольшая гармоничная подпоследовательность: [3,2,2,2,3]
 *
 *
 * Пример для:
 *   nums = [1,1,1,1]
 * Вывод:
 *   0
 * Пояснение:
 *   Гармоничная подпоследовательность отсутствует.
 */
function findLHS(nums) {

  // Создаём объект Map для хранения частот (количества
  // вхождений) каждого уникального числа из nums.
  const numToFreq = new Map();

  // Проходим по каждому элементу входного массива nums.
  for (const num of nums) {

    // Получаем текущую частоту для числа num из Map.
    //
    // Если ключа ещё нет, get вернёт undefined. Оператор
    // nullish coalescing (??) заменяет undefined на 0, затем
    // прибавляем 1.
    //
    // В результате в Map будет храниться обновлённое
    // количество вхождений для этого числа.
    numToFreq.set(num, (numToFreq.get(num) ?? 0) + 1);
  }

  // Инициализируем переменную для хранения максимальной
  // найденной длины гармоничной подпоследовательности.
  //
  // Начальное значение 0 корректно обрабатывает случаи, когда
  // подходящих пар чисел не найдено.
  let maxLength = 0;

  // Итерируем по Map.
  //
  // При итерации Map возвращает пары [ключ, значение].
  //
  // Здесь pair - массив из двух элементов: [число, частота].
  //
  // Для повышения производительности не используем
  // деструктуризацию массива pair в цикле, а обращаемся к его
  // элементам по индексам
  for (const pair of numToFreq) {

    // Извлекаем ключ (текущее число) из пары
    const currNum = pair[0];

    // Извлекаем значение (частоту текущего числа) из пары
    const currNumFreq = pair[1];

    // Вычисляем число, отличающееся на 1, которое вместе с
    // currNum может образовать гармоничную
    // подпоследовательность
    const nextNum = currNum + 1;

    // Получаем частоту соседнего числа из Map.
    //
    // Если такого числа нет в Map, get вернёт undefined.
    const nextNumFreq = numToFreq.get(nextNum);

    // Если nextNumFreq равен undefined (falsy), значит нет
    // элементов nextNum, и текущая пара currNum/nextNum не
    // образует гармоничную подпоследовательность.
    //
    // В этом случае пропускаем оставшуюся часть тела цикла и
    // переходим к следующей паре.
    if (!nextNumFreq) continue;

    // Если nextNum присутствует, длина гармоничной
    // подпоследовательности, составленной из всех вхождений
    // currNum и nextNum, равна сумме их частот.
    const currLength = currNumFreq + nextNumFreq;

    // Обновляем максимальную длину:
    //
    // Сравниваем текущее значение maxLength с currLength и
    // сохраняем большее из них.
    //
    // Math.max возвращает наибольшее значение.
    maxLength = Math.max(maxLength, currLength);
  }

  // Возвращаем найденную максимальную длину гармоничной
  // подпоследовательности.
  //
  // Если подходящих пар не было, вернётся 0.
  return maxLength;
}
