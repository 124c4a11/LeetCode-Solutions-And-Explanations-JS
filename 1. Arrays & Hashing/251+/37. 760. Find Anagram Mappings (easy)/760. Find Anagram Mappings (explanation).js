/**
 * @param {number[]} nums1
 * @param {number[]} nums2
 * @return {number[]}
 * 
 * anagramMappings строит и возвращает массив индексов,
 * показывающий, где каждый элемент nums1 находится в
 * nums2.
 * 
 * Она принимает два массива целых чисел nums1 и nums2,
 * причём nums2 считается анаграммой nums1 (содержит те
 * же элементы в другом порядке).
 * 
 * Пример для:
 *   nums1 = [12,28,46,32,50]
 *   nums2 = [50,12,32,46,28]
 * Результат: 
 *   [1,4,3,2,0]
 */
function anagramMappings(nums1, nums2) {

  // Создаём типизированный массив Int32Array длиной
  // 101 для хранения индексов элементов nums2 по их
  // значениям (значение → индекс).
  //
  // Int32Array — это массив фиксированной длины,
  // заполненный нулями. Он хранит 32‑битные целые
  // числа со знаком.
  //
  // Выбор длины 101 явно предполагает, что значения
  // в nums2 лежат в диапазоне 0..100 включительно.
  const num2ToNdx = new Int32Array(101);

  // Проходим по массиву nums2, чтобы заполнить
  // отображение значение → индекс.
  for (let i = 0; i < nums2.length; i++) {

    // Для текущего значения nums2[i] сохраняем
    // его индекс i в массиве num2ToNdx
    //
    // После этого num2ToNdx[value] будет равен
    // индексу этого value в nums2
    num2ToNdx[nums2[i]] = i;
  }

  // Инициализируем пустой массив result для хранения
  // результатов — индексов соответствий из nums2.
  const result = [];

  // Для каждого числа num из nums1 ищем
  // соответствующий индекс в num2ToNdx и добавляем
  // в result.
  for (const num of nums1) {
    result.push(num2ToNdx[num]);
  }


  // Возвращаем массив result с индексами
  // соответствий для каждого элемента nums1.
  return result;
}
