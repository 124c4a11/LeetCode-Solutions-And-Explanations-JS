/**
 * @param {number[]} nums
 * @param {number} n
 * @return {number[]}
 *
 * shuffle принимает массив nums, состоящий из 2n элементов
 * в виде [x1,x2,...,xn,y1,y2,...,yn].
 *
 * Возвращает массив в виде [x1,y1,x2,y2,...,xn,yn].
 *
 *
 * Пример для:
 *   nums = [2,5,1,3,4,7],
 *   n = 3
 * Результат:
 *   [2,3,5,4,1,7]
 * Пояснение:
 * Поскольку x1 = 2, x2 = 5, x3 = 1, y1 = 3, y2 = 4, y3 = 7,
 * ответ равен [2,3,5,4,1,7].
 */
function shuffle(nums, n) {

  // Создаём новый массив result фиксированной длины n * 2.
  //
  // Это резервирует место для всех элементов и задаёт
  // итоговый размер сразу, что эффективнее, чем
  // динамическое расширение массива.
  const result = new Array(n * 2);

  // Инициализируем счётчик i значением 0.
  //
  // i будет использоваться как текущая позиция для записи
  // в result.
  let i = 0;

  // Цикл перебирает индексы j от 0 до n - 1 включительно
  //
  // j индексирует элементы в первой половине входного
  // массива nums.
  //
  // Каждый проход цикла обрабатывает одну пару элементов:
  // элемент из первой половины nums и соответствующий
  // элемент из второй половины.
  for (let j = 0; j < n; j++) {

    // Записываем элемент из первой половины nums
    // (индекс j) в result.
    //
    // Используется постфиксный инкремент i++, поэтому
    // сначала происходит запись в позицию i, затем i
    // увеличивается на 1.
    //
    // Это сохраняет порядок: сначала элемент из первой
    // половины.
    result[i++] = nums[j];

    // Записываем соответствующий элемент из второй
    // половины nums (индекс n + j) в следующую позицию
    // result
    //
    // Таким образом формируется чередование:
    // nums[0], nums[n], nums[1], nums[n+1], ..
    result[i++] = nums[n + j];
  }

  // Возвращаем заполненный массив result.
  //
  // result содержит 2 * n элементов в порядке
  // [x1, y1, x2, y2, ..., xn, yn], где xi - элементы из
  // первой половины nums, yi - из второй.
  return result;
}
