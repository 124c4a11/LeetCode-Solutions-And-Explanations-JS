/**
 * @param {number[]} nums
 * @param {number} k
 * @return {number}
 *
 * countPairs принимает 0‑индексированный целочисленный массив
 * nums длины n и целое число k.
 *
 * Возвращает количество пар (i, j), где 0 ≤ i < j < n, таких
 * что nums[i] == nums[j] и (i * j) делится на k.
 *
 *
 * Пример для:
 *   nums = [3,1,2,2,2,1,3],
 *   k = 2
 * Результат:
 *   4
 * Пояснение:
 *   Есть 4 пары, удовлетворяющие всем требованиям:
 *   - nums[0] == nums[6], и 0 * 6 == 0, что делится на 2.
 *   - nums[2] == nums[3], и 2 * 3 == 6, что делится на 2.
 *   - nums[2] == nums[4], и 2 * 4 == 8, что делится на 2.
 *   - nums[3] == nums[4], и 3 * 4 == 12, что делится на 2.
 */
function countPairs(nums, k) {
  const n = nums.length;

  // Инициализируем счётчик подходящих пар нулём.
  let pairsCnt = 0;

  // Внешний цикл по правому индексу j.
  //
  // Начинаем с 1, потому что для j = 0 не существует i < j, и
  // поэтому пар с правым индексом 0 быть не может.
  //
  // j пробегает значения от 1 до n-1 включительно.
  for (let j = 1; j < n; j++) {

    // Внутренний цикл по левому индексу i от 0 до j-1, что
    // гарантирует i < j.
    //
    // Для каждой фиксированной j выполняется j итераций
    // внутреннего цикла, поэтому общая временная сложность
    // алгоритма — O(n^2) в худшем случае.
    for (let i = 0; i < j; i++) {

      // Проверка условий для пары (i, j).
      //
      // Оба условия должны быть истинны:
      // 1. значения в массиве на позициях i и j строго равны
      //
      // 2. произведение индексов i * j делится на k без
      //    остатка (остаток равен нулю).
      //
      // Если оба условия истинны, увеличиваем счётчик
      // подходящих пар на 1.
      if (
        nums[i] === nums[j]
        && i * j % k === 0
      ) pairsCnt++;
    }
  }

  // Возвращаем итоговое количество пар, удовлетворяющих
  // обоим условиям.
  return pairsCnt;
}
