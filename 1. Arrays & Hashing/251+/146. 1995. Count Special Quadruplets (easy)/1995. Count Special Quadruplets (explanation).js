/**
 * @param {number[]} nums
 * @return {number}
 *
 * countQuadruplets принимает на вход массив целых чисел и
 * возвращает количество упорядоченных четверок индексов
 * (a, b, c, d) в массиве nums таких, что a < b < c < d и
 * выполняется равенство nums[a] + nums[b] + nums[c] == nums[d].
 *
 *
 * Пример для:
 *   nums = [1, 1, 1, 3, 5]
 * Результат:
 *   4
 * Пояснение:
 *   4 четверки индексов (a, b, c, d) удовлетворяют условию
 *   a < b < c < d и nums[a] + nums[b] + nums[c] == nums[d]:
 *   - (0, 1, 2, 3): 1 + 1 + 1 == 3
 *   - (0, 1, 3, 4): 1 + 1 + 3 == 5
 *   - (0, 2, 3, 4): 1 + 1 + 3 == 5
 *   - (1, 2, 3, 4): 1 + 1 + 3 == 5
 */
function countQuadruplets(nums) {

  // Основная идея алгоритма:
  //
  // Требуется найти количество упорядоченных четверок индексов
  // (a, b, c, d) в массиве nums таких, что a < b < c < d и
  // выполняется равенство nums[a] + nums[b] + nums[c] == nums[d].
  // Это равенство можно переписать как
  // nums[a] + nums[b] == nums[d] - nums[c].
  //
  // Исходя из новой формулы мы можем фиксировать индекс b и для
  // всех пар индексов (c,d) с c > b и d > c подсчитывает частоты
  // значений nums[d] - nums[c] в массиве diffToCnt
  //
  // Затем для всех a < b добавляеть в ответ количество пар
  // (c,d), для которых nums[d] - nums[c] == nums[a] + nums[b].
  // Это эквивалентно проверке
  // nums[a] + nums[b] + nums[c] == nums[d].
  //
  // Такой подход переводит перебор четырёх индексов в сочетание
  // перебора b и подсчётов частот разности nums[d] - nums[c],
  // что упрощает сложность алгоритма до O(n^2) по сравнению с
  // наивным O(n^4).

  const n = nums.length;

  // Инициализируем итоговый счётчик найденных четверок нулём.
  //
  // quadrupletsCnt будет аккумулировать количество всех
  // подходящих комбинаций.
  let quadrupletsCnt = 0;

  // Создаём массив фиксированной длины 201 типа Int32Array для
  // хранения частот разностей nums[d] - nums[c].
  //
  // Int32Array используется для экономии памяти и более быстрой
  // работы с целыми числами по сравнению с обычным массивом.
  //
  // Используется длина 201, так как максимальное значение числа
  // в массиве nums может равняться 100. Следовательно, чтобы
  // безопасно индексировать diffToCnt[nums[a] + nums[b]], нужен
  // массив, покрывающий индексы до 200, то есть длина
  // 200 + 1 = 201.
  const diffToCnt = new Int32Array(201);

  // Внешний цикл по индексу b идёт справа налево от n-1 до 1
  // включительно.
  //
  // Обратный порядок позволяет накапливать информацию о парах
  // (c,d), расположенных правее текущего b, чтобы затем для
  // каждого a < b быстро узнать, сколько пар (c,d) дают нужную
  // разность.
  for (let b = n - 1; b >= 1; b--) {

    // Устанавливаем c как следующий индекс после b - это
    // минимальный c, который удовлетворяет c > b.
    //
    // Важно:
    // c используется только для вычисления начального значения
    // в следующем цикле по d.
    let c = b + 1;

    // Вложенный цикл по d:
    // перебираем все индексы d, которые больше c
    // (то есть d >= b+2).
    //
    // Для каждой пары (c, d) вычисляем разность
    // nums[d] - nums[c] и увеличиваем счётчик этой разности.
    //
    // Таким образом diffToCnt[diff] будет хранить количество
    // пар (c, d) с данной разностью, где c > b.
    for (let d = c + 1; d < n; d++) {

      // Вычисляем разность значений в позициях d и c.
      const diff = nums[d] - nums[c];

      // Увеличиваем счётчик для этой разности, но только если
      // разность неотрицательна.
      //
      // Проверка diff >= 0 предотвращает обращение к
      // отрицательному индексу diffToCnt.
      if (diff >= 0) diffToCnt[diff]++;
    }

    // После того как для текущего b накоплены все пары (c, d) с
    // c > b, перебираем все возможные a < b.
    //
    // Для каждой пары (a, b) нам нужно узнать, сколько пар
    // (c, d) дают разность, равную nums[a] + nums[b].
    //
    // Поэтому мы обращаемся к diffToCnt по индексу
    // nums[a] + nums[b] и добавляем это количество в итог.
    for (let a = 0; a < b; a++) {

      // Индекс в diffToCnt равен сумме nums[a] + nums[b].
      //
      // Это соответствует преобразованию исходного условия
      // nums[a] + nums[b] + nums[c] = nums[d] в
      // nums[a] + nums[b] = nums[d] - nums[c].
      quadrupletsCnt += diffToCnt[nums[a] + nums[b]];
    }
  }

  // Возвращаем итоговое количество найденных четверок.
  return quadrupletsCnt;
}
