/**
 * @param {number[]} apple
 * @param {number[]} capacity
 * @return {number}
 *
 * minimumBoxes вычисляет минимальное количество коробок, которое
 * нужно использовать, чтобы разместить все яблоки в коробках
 *
 * Параметры:
 * - apple - массив чисел. Каждый элемент представляет количество
 *   яблок в одной пачке или куче.
 *
 * - capacity - массив чисел. Каждый элемент - вместимость одной
 *   коробки (сколько яблок она может вместить)
 *
 *
 * Пример для:
 *   apple = [1,3,2],
 *   capacity = [4,3,1,5,2]
 * Результат:
 *   2
 * Пояснение:
 *   Будем использовать коробки вместимостью 4 и 5.
 *
 *   Распределить яблоки можно, так как суммарная вместимость
 *   больше или равна общему количеству яблок.
 */
function minimumBoxes(apple, capacity) {

  // Создаём типизированный массив Int32Array длиной 51 для
  // подсчёта количества коробок каждой вместимости
  // (индексы 0..50).
  //
  // Индекс - вместимость
  // Значение - количество коробок текущей вместимости
  //
  // Используем длину 51, так как вместимости лежат в диапазоне
  // 1..50 включительно
  //
  // Этот подход позволяет избежать явной сортировки массива
  // capacity сложностью O(n log n), и затем жадно брать коробки
  // по убыванию вместимости.
  const capacityToFreq = new Int32Array(51);

  // Проходим по массиву capacity и инкрементируем соответствующий
  // счётчик в capacityToFreq.
  for (const num of capacity) capacityToFreq[num]++;

  // Инициализируем счётчик использованных коробок нулём.
  //
  // Этот счётчик увеличивается каждый раз, когда мы "берём" одну
  // коробку для упаковки яблок.
  let boxesCnt = 0;

  // Вычисляем общее количество яблок, суммируя все элементы
  // массива apple
  let totalApples = apple.reduce((a, b) => a + b, 0);

  // Проходим по возможным вместимостям в порядке убывания - от
  // самых больших коробок к самым маленьким.
  //
  // Это жадный подход: сначала используем коробки с наибольшей
  // вместимостью, чтобы минимизировать их количество.
  for (let currCapacity = 50; currCapacity >= 0; currCapacity--) {

    // Получаем количество доступных коробок текущей вместимости
    // из массива частот.
    let freq = capacityToFreq[currCapacity];

    // Если коробок данной вместимости нет, пропускаем итерацию и
    // переходим к следующей вместимости.
    if (freq === 0) continue;

    // Пока есть коробки данной вместимости, используем их по
    // одной.
    //
    // Оператор freq-- > 0 сначала сравнивает, затем
    // декрементирует, что корректно уменьшает счётчик.
    while (freq-- > 0) {

      // Уменьшаем оставшееся количество яблок на величину
      // текущей вместимости коробки.
      //
      // Это моделирует упаковку одной коробки вместимостью
      // currCapacity.
      totalApples -= currCapacity;

      // Увеличиваем счётчик использованных коробок, поскольку мы
      // только что использовали одну.
      boxesCnt++;

      // Если после использования коробки все яблоки упакованы
      // (totalApples ≤ 0), возвращаем текущее количество
      // использованных коробок как ответ.
      //
      // Ранний выход экономит время, не перебирая оставшиеся
      // коробки.
      if (totalApples <= 0) return boxesCnt;
    }
  }
}
