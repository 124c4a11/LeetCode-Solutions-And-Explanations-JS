/**
 * @param {number[]} nums
 * @return {number[]}
 *
 * numberOfPairs принимает один параметр - массив целых чисел
 * nums и подсчитывает, сколько пар одинаковых чисел можно
 * составить из массива и сколько элементов останется не в
 * парах.
 *
 * Возвращает массив из двух чисел:
 *   первый элемент - число сформированных пар,
 *   второй - число оставшихся (непарных) элементов
 *
 * Пример для:
 *   nums = [1,3,2,1,3,2,2]
 * Результат:
 *   [3,1]
 * Пояснение:
 * - Сформировать пару из nums[0] и nums[3] и удалить их из
 *   nums. Теперь nums = [3,2,3,2,2].
 *
 * - Сформировать пару из nums[0] и nums[2] и удалить их из
 *   nums. Теперь nums = [2,2,2].
 *
 * - Сформировать пару из nums[0] и nums[1] и удалить их из
 *   nums. Теперь nums = [2].
 *
 * - Больше пар сформировать нельзя. Всего образовано 3 пары,
 *   и в nums остался 1 элемент.
 */
function numberOfPairs(nums) {

  // Ключевая идея в том, что нужно посчитать, сколько пар
  // можно составить из равных элементов. Представьте это
  // так: если число встречается в массиве несколько раз,
  // пары можно формировать только из этих вхождений.
  //
  // Например, если число 5 встречается 7 раз, мы можем
  // составить Math.floor(7 / 2) = 3 пары, оставив 1
  // неиспользованное вхождение 5. Это потому, что каждая
  // пара использует ровно 2 одинаковых числа.
  //
  // Это естественно приводит к подходу подсчёта частот:
  // - Сначала посчитайте, сколько раз каждое уникальное
  //   число встречается в массиве.
  //
  // - Для каждого уникального числа с частотой v можно
  //  составить ровно Math.floor(v / 2) пар.
  //
  // Общее число пар - это сумма всех индивидуальных
  // количеств пар.
  //
  // Элегантность этого подхода в том, что нам не нужно
  // фактически выполнять операции удаления. Мы можем
  // напрямую вычислить итог математически:
  // - Общее число пар = сумма (Math.floor(frequency / 2))
  //   по всем уникальным числам.
  //
  // - Оставшиеся элементы =
  //   длина исходного массива - (общее число пар × 2).
  //
  // Умножение на 2 в формуле для оставшихся элементов
  // логично, потому что каждая пара удаляет ровно 2 элемента
  // из исходного массива.
  //
  // Это превращает потенциально сложную задачу симуляции в
  // простую задачу подсчёта, которую можно решить за
  // линейное время одним проходом по подсчётам частот.


  // Создаёт статически типизированный массив Int32Array
  // длиной 101 для хранения счётчиков вхождений каждого
  // числа.
  //
  // Int32Array экономит память и быстрее при числовых
  // операциях по сравнению с обычным объектом-словарём.
  //
  // - Индекс массива соответствует значению числа.
  //
  // - Значение в ячейке - количество вхождений текущего
  //   числа в массиве nums, но в этой реализации мы
  //   используем его как счётчик, который обнуляется после
  //   формирования пары.
  //
  // Значения в nums лежат в диапазоне 0..100, поэтому
  // выделяется 101 слот.
  const numToFreq = new Int32Array(101);

  // Инициализируем счётчик найденных пар нулём.
  //
  // Каждая инкрементация pairsCnt соответствует обнаруженной
  // паре из двух одинаковых элементов.
  let pairsCnt = 0;

  // Перебираем все значения из входного массива nums
  for (const num of nums) {

    // Увеличиваем счётчик для текущего значения num на
    // единицу.
    numToFreq[num]++;

    // Проверяем, является ли количество в ячейке нечётным.
    //
    // Операция (x & 1) извлекает младший бит:
    //   1 означает нечётное,
    //   0 - чётное.
    //
    // Побитовая операция & 1 эквивалентна взятию остатка от
    // деления на 2, но обычно быстрее.
    //
    // Если частота нечётная, это означает, что пара ещё не
    // сформирована (встречен первый элемент пары), поэтому
    // мы пропускаем дальнейшие действия и переходим к
    // следующему элементу.
    if ((numToFreq[num] & 1) === 1) continue;

    // Если мы попали сюда, значит частота стала чётной
    // (вторая встреча текущего числа), следовательно
    // сформировалась одна пара - увеличиваем общий счётчик
    // пар.
    pairsCnt++;

    // Сбрасываем счётчик частоты для текущего числа в 0,
    // потому что пара "потребляет" два элемента и дальнейший
    // подсчёт для этого числа начинается заново. Это
    // позволяет корректно обрабатывать случаи, когда более
    // двух одинаковых элементов могут образовывать несколько
    // пар.
    //
    // Например, при последовательности 5,5,5,5: после двух
    // первых 5 образуется пара и счётчик обнуляется, затем
    // следующие два элемента 5 снова образуют пару.
    numToFreq[num] = 0;
  }

  // Возвращаем массив из двух чисел:
  // 1. pairsCnt - количество сформированных пар;
  //
  // 2. nums.length - pairsCnt * 2 - количество оставшихся
  //    одиночных элементов, вычисляемое как общее число
  //    элементов минус число элементов, занятых в парах.
  //
  // Временная сложность алгоритма O(n), где n - длина nums.
  // Дополнительная память O(1) (фиксированный буфер 101).
  return [pairsCnt, nums.length - pairsCnt * 2];
}
