/**
 * @param {number[]} nums
 * @return {number[]}
 *
 * findMissingElements ищет все отсутствующие целые числа между
 * минимальным и максимальным значениями в переданном массиве
 * nums.
 *
 * Возвращает отсортированный массив всех отсутствующих целых
 * чисел в этом диапазоне. Если отсутствующих чисел нет,
 * возвращает пустой список.
 *
 *
 * Пример для:
 *   nums = [1,4,2,5]
 * Результат:
 *   [3]
 * Пояснение:
 *   Наименьшее целое число равно 1, а наибольшее равно 5,
 *   поэтому полный диапазон должен быть [1,2,3,4,5].
 *
 *   Из этого диапазона отсутствует только число 3.
 */
function findMissingElements(nums) {

  // Создаём типизированный массив Int32Array длиной 101,
  // заполненный нулями.
  //
  // Используется как карта встреченных значений:
  // Индекс массива соответствует самому числу, а значение 0
  // или 1 показывает, встречалось ли число:
  // - 0 - "не встречено"
  // - 1 - "встречено".
  //
  // Выбор длины 101 предполагает, что рассматриваемые числа
  // лежат в диапазоне 0..100 или 1..100.
  //
  // Использование Int32Array экономично по памяти и быстрее
  // обычного массива, объекта или Set для булевых отметок при
  // известном ограниченном диапазоне.
  const numToSeen = new Int32Array(101);

  // Инициализируем переменную min значением 100, так как
  // входные числа находятся в диапазоне 1..100.
  //
  // Это начальное значение гарантирует, что любое меньшее
  // число из nums заменит текущий min (100).
  let min = 100;

  // Инициализируем max значением 1 - аналогично, так как
  // входные числа находятся в диапазоне 1..100.
  //
  // Это начальное значение гарантирует, что любое большее
  // число из nums заменит текущий max (1).
  let max = 1;

  // Проходим по каждому элементу входного массива nums
  for (const num of nums) {

    // Помечаем текущее число как встреченное, записывая 1 в
    // позицию с индексом num.
    numToSeen[num] = 1;

    // Обновляем минимальное значение, сравнивая текущее min и
    // текущий элемент num.
    min = Math.min(min, num);

    // Обновляем максимальное значение, сравнивая текущее max и
    // текущий элемент num.
    max = Math.max(max, num);
  }

  // Создаём пустой массив для накопления чисел, которые
  // отсутствуют между найденными min и max.
  //
  // Результат будет содержать все целые числа, которые не
  // были помечены в numToSeen и находятся строго в диапазоне
  // между min и max
  const missingNums = [];

  // Проходим по всем целым числам строго между min и max (не
  // включая сами границы).
  //
  // Цикл начинается с min + 1, потому что сами границы min и
  // max точно существуют.
  for (let num = min + 1; num < max; num++) {

    // Проверяем, помечено ли текущее число как встреченное в
    // numToSeen.
    //
    // Для непомеченных индексов значение будет 0 (false).
    //
    // Оператор ! приводит 0 к true, поэтому условие
    // срабатывает только для непомеченных (отсутствующих)
    // чисел.
    //
    // Если число не встречено, добавляем его в массив
    // пропущенных чисел.
    if (!numToSeen[num]) missingNums.push(num);
  }

  // Возвращаем массив всех найденных пропущенных чисел.
  return missingNums;
}
