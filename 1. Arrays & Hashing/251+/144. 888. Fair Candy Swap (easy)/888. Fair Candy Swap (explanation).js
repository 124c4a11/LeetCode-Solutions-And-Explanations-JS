/**
 * @param {number[]} aliceSizes
 * @param {number[]} bobSizes
 * @return {number[]}
 *
 * fairCandySwap принимает два массива целых чисел — размеры коробок
 * конфет Алисы и Боба и возвращает одну пару конфет, по одной у
 * Алисы и у Боба, которую можно обменять так, чтобы суммы конфет у
 * обоих стали равны.
 *
 *
 * Пример для:
 *   aliceSizes = [1,1],
 *   bobSizes = [2,2]
 * Результат:
 *   [1,2]
 */
function fairCandySwap(aliceSizes, bobSizes) {

  // Если Алиса отдаёт коробку с a конфетами и получает от Боба
  // коробку с b конфетами, то:
  // Новая сумма у Алисы = sum(aliceSizes) - a + b
  // Новая сумма у Боба  = sum(bobSizes) - b + a
  //
  // Чтобы после обмена у них было одинаково конфет, нужно:
  // sum(aliceSizes) - a + b = sum(bobSizes) - b + a
  //
  // Преобразуя это уравнение:
  // sum(aliceSizes) - sum(bobSizes) = 2a - 2b
  // sum(aliceSizes) - sum(bobSizes) = 2(a - b)
  // a - b = (sum(aliceSizes) - sum(bobSizes)) / 2
  //
  // Это означает, что разница между количеством конфет в коробке,
  // которую отдаёт Алиса, и коробке, которую отдаёт Боб, должна
  // быть ровно половиной разницы между их общими количествами
  // конфет. Обозначим это значение как
  // diff = (sum(aliceSizes) - sum(bobSizes)) / 2.
  //
  // Тогда для любой коробки Алисы с a конфетами Боб должен вернуть
  // коробку с b = a - diff конфетами. Это превращает задачу из
  // перебора всех пар O(n*m) в задачу поиска по множеству.
  //
  // Стратегия:
  // 1. Вычислить diff один раз в начале.
  //
  // 2. Поместить все количества конфет Боба в хеш‑множество для
  //    O(1) поиска.
  //
  // 3. Для каждой коробки Алисы с a конфетами проверить, есть ли у
  //    Боба коробка с a - diff конфетами.
  //
  // 4. Если такая коробка есть, то решение найдено: Алиса отдаёт a,
  //    Боб отдаёт a - diff.
  //
  // Такой подход эффективен, потому что мы проходим по коробкам
  // Алисы один раз, и для каждой коробки за константное время
  // проверяем, есть ли у Боба соответствующая коробка, делающая
  // обмен справедливым.


  // Инициализация аккумулятора для суммы всех элементов массива
  // aliceSizes.
  let aliceTotal = 0;

  // Итерация по массиву aliceSizes
  //
  // На каждой итерации текущее значение size прибавляется к
  // aliceTotal.
  for (const size of aliceSizes) aliceTotal += size;

  // Инициализация аккумулятора для суммы всех элементов массива
  // bobSizes.
  let bobTotal = 0;

  // Создание множества (Set) для хранения уникальных размеров
  // конфет Боба.
  //
  // Set обеспечивает среднюю сложность поиска O(1), что ускоряет
  // последующие проверки.
  const bobSizesSet = new Set();

  // Проход по массиву bobSizes:
  //
  // Одновременно накапливаем сумму и заполняем множество.
  //
  // Это экономит один проход по массиву.
  for (const size of bobSizes) {
    bobTotal += size;
    bobSizesSet.add(size);
  }

  // Вычисляем половину разницы сумм aliceTotal - bobTotal.
  //
  // Здесь используется побитовый сдвиг вправо >> 1 как быстрый
  // способ целочисленного деления на 2.
  //
  // Используется побитовый сдвиг вправо для деления на 2 - это
  // эквивалент Math.floor для положительных чисел, с приведенеим к
  // к 32‑битному целому
  const halfDiff = (aliceTotal - bobTotal) >> 1;

  // Перебираем размеры каждой коробки конфет у Алисы в поисках
  // подходящей пары для обмена.
  //
  // Для каждого aliceSize вычисляем, какой размер конфеты у Боба
  // (targetSize) нужен, чтобы после обмена суммы сравнялись.
  for (const aliceSize of aliceSizes) {

    // Вычисляем требуемый размер коробки конфет у Боба, который
    // уравняет суммы при обмене с текущей коробкой конфет Алисы.
    const targetSize = aliceSize - halfDiff;

    // Проверяем, присутствует ли targetSize в множестве размеров
    // Боба.
    //
    // Если нет — пропускаем текущую итерацию и продолжаем поиск.
    //
    // Использование continue делает код явным и избегает вложенных
    // условных блоков.
    if (!bobSizesSet.has(targetSize)) continue;

    // Если у Боба найдена коробка с подходящим количеством конфет,
    // возвращаем пару [aliceSize, targetSize].
    return [aliceSize, targetSize];
  }

  // Если цикл завершился без нахождения подходящей пары, возвращаем
  // пустой массив.
  //
  // В контексте задачи это означает, что корректного обмена не
  // существует при текущих входных данных.
  //
  // Возвращаемое значение [] служит индикатором отсутствия решения.
  return [];
}
