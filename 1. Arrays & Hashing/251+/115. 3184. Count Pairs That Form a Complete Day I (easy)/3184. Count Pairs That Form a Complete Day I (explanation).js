/**
 * @param {number[]} hours
 * @return {number}
 *
 * Функция считает количество упорядоченных пар индексов i < j
 * в массиве hours, сумма значений которых кратна кратное 24
 * (то есть образует полные сутки)
 *
 *
 * Пример для:
 *   hours = [12, 12, 30, 24, 24]
 * Результат:
 *   2
 * Пояснение:
 *   Пары индексов, которые образуют полный день, это (0, 1) и
 *   (3, 4).
 *
 *   Для пары (0, 1): hours[0] + hours[1] = 12 + 12 = 24, что
 *   равно 1 дню (кратное 24).
 *
 *   Для пары (3, 4): hours[3] + hours[4] = 24 + 24 = 48, что
 *   равно 2 дням (кратное 24).
 */
function countCompleteDayPairs(hours) {

  // Когда нужно найти пары, сумма которых кратна 24, можно
  // рассматривать задачу через остатки. Если два числа в сумме
  // дают число, делящееся на 24, что можно сказать об их
  // остатках при делении на 24?
  //
  // Рассмотрим два числа a и b. Если a + b делится на 24, то
  // (a % 24) + (b % 24) должно быть равно либо 0, либо 24. Это
  // потому что:
  // - Если оба остатка равны 0, их сумма равна 0.
  //
  // - Если остатки в сумме дают 24, они "дополняют" ещё один
  //   полный цикл из 24.
  //
  // - Любая сумма больше 24 означала бы ещё один полный цикл
  //   плюс некоторый остаток.
  //
  // Значит, для любого значения x с остатком r = x % 24 нам
  // нужно найти значения с остатком 24 - r, чтобы получить
  // полный день. Но есть особый случай: когда r = 0, нужен ещё
  // один элемент с остатком 0 (а не 24). Поэтому мы используем
  // формулу (24 - (x % 24)) % 24 - внешний модуль обрабатывает
  // случай, когда x % 24 = 0.
  //
  // Естественный подход - проверять каждую пару, но это было бы
  // O(n²). Вместо этого можно действовать умнее: проходя по
  // массиву, мы ведём учёт того, сколько раз встречался каждый
  // возможный остаток (от 0 до 23). Когда встречаем новое
  // значение, мы сразу знаем, с каким количеством предыдущих
  // значений оно может образовать пару, посмотрев на счётчик
  // комплементарного остатка.
  //
  // Это превращает задачу из "найти все пары" в "для каждого
  // элемента посчитать, с каким количеством предыдущих элементов
  // он может образовать пару" - классический приём, который
  // снижает временную сложность с O(n²) до O(n), обменяв время
  // на память с помощью счётчика частот.


  // Инициализируем переменную для накопления общего количества
  // пар элементов, сумма часов в которых кратна 24.
  let pairsCnt = 0;

  // Создаём типизированный массив Int32Array длиной 24 для
  // хранения счётчиков остатков 0..23.
  //
  // Все элементы инициализируются нулём автоматически.
  //
  // Индекс соответствует остатку от деления на 24, а значение -
  // сколько раз такой остаток уже встречался среди предыдущих
  // элементов.
  const remainderToCnt = new Int32Array(24);

  // Итерация по каждому элементу входного массива hours
  for (const hour of hours) {

    // Вычисляем комплементарный остаток neededRemainder, такой
    // что (hour % 24) + neededRemainder ≡ 0 (mod 24).
    //
    // Внутренний (hour % 24) даёт остаток с тем же знаком, что и
    // hour (в JS остаток может быть отрицательным для
    // отрицательных hour).
    //
    // Выражение (24 - (hour % 24)) даёт "дополнение" до 24, а
    // внешний % 24 нормализует результат в диапазон 0..23.
    //
    // Примеры:
    //  - hour = 5
    //    → hour % 24 = 5
    //    → neededRemainder = (24 - 5) % 24 = 19
    //
    //  - hour = 24
    //    → hour % 24 = 0
    //    → neededRemainder = (24 - 0) % 24 = 0
    //
    //  - hour = -1
    //    → hour % 24 = -1
    //    → neededRemainder = (24 - (-1)) % 24 = 25 % 24 = 1
    //
    // Таким образом формула корректно обрабатывает и нулевые, и
    // отрицательные значения остатка.
    const neededRemainder = (24 - (hour % 24)) % 24;

    // Увеличиваем счётчик пар на количество ранее встреченных
    // элементов с остатком neededRemainder.
    //
    // Это учитывает все пары, где предыдущий элемент имел нужный
    // остаток и вместе с текущим даёт полный день.
    //
    // Поскольку мы считаем только предыдущие элементы, каждая
    // пара учитывается ровно один раз (порядок обхода
    // предотвращает двойной подсчёт).
    pairsCnt += remainderToCnt[neededRemainder];

    // Увеличиваем счётчик для остатка текущего часа, чтобы этот
    // элемент мог образовать пару с последующими элементами.
    //
    // Используем hour % 24 как индекс в массиве remainderToCnt
    // - это безопасно, так как индекс в диапазоне 0..23.
    remainderToCnt[hour % 24]++;
  }

  // Возвращаем итоговое количество пар, сумма которых кратна 24.
  return pairsCnt;
}
