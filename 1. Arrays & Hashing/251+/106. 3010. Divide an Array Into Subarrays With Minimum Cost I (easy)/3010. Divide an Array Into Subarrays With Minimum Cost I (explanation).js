/**
 * @param {number[]} nums
 * @return {number}
 *
 * minimumCost массив целых чисел nums и вычисляет минимально
 * возможную суммарную стоимость при разбиении входного
 * массива nums на три непрерывных подмассива, при условии
 * что стоимость каждого подмассива равна его первому
 * элементу. Функция берёт фиксированный вклад первого
 * подмассива nums[0] и  добавляет к нему два наименьших
 * значения из оставшейся части массива nums[1:], тем самым
 * возвращая минимально возможную сумму первых элементов трёх
 * подмассивов.
 *
 *
 * Пример для:
 *   nums = [10, 3, 1, 1]
 * Результат:
 *   12
 * Пояснение:
 *   Лучший способ сформировать 3 подмассива:
 *   [10,3], [1] и [1] с общей стоимостью 10 + 1 + 1 = 12.
 *
 *   Можно показать, что 12 - минимально достижимая
 *   стоимость.
 */
function minimumCost(nums) {

  // Ключевая идея в том, что первый подмассив всегда должен
  // начинаться с nums[0], поскольку мы разбиваем исходный
  // массив на непрерывные части. Это означает, что nums[0]
  // всегда будет вносить вклад в нашу общую стоимость,
  // независимо от того, как мы разобьём массив.
  //
  // Итак, где нам сделать два разреза? Каждый разрез
  // создаёт новый подмассив, и каждый новый подмассив
  // добавляет в общую стоимость свой первый элемент. Таким
  // образом, после двух разрезов:
  // - Первый подмассив даёт nums[0] (фиксировано)
  //
  // - Второй подмассив даёт свой первый элемент (зависит от
  //   места разреза)
  //
  // - Третий подмассив даёт свой первый элемент (зависит от
  //   места разреза)
  //
  // Вот важное наблюдение: первыми элементами второго и
  // третьего подмассивов могут быть любые элементы из
  // nums[1:] (поскольку мы можем разместить разрезы в любом
  // месте после позиции 0).
  //
  // Чтобы минимизировать общую стоимость, мы хотим, чтобы
  // первые элементы второго и третьего подмассивов были как
  // можно меньше. Это означает, что нам следует:
  // - Найти наименьший элемент в nums[1:] - он станет первым
  //   элементом одного из подмассивов
  //
  // - Найти второй наименьший элемент в nums[1:] - он станет
  //   первым элементом другого подмассива
  //
  // Минимально возможная сумма, следовательно, равна:
  // nums[0] + (наименьший элемент после индекса 0)
  // + (второй наименьший элемент после индекса 0)
  //
  // Это объясняет, почему решение просто находит два
  // наименьших значения среди nums[1:] и добавляет их к
  // nums[0]. Мы всегда можем расположить разрезы так, чтобы
  // эти два элемента стали начальными элементами второго и
  // третьего подмассивов.


  // Инициализация переменной minA значением 50.
  //
  // Будет хранить наименьший найденный элемент в nums[1:]
  //
  // Переменная инициализируется максимально возможным
  // значением диапазона 1 <= nums[i] <= 50, чтобы любое
  // встреченное значение, меньшее 50, стало новым минимумом.
  let minA = 50;

  // Объявление переменной minB и установка её в 50.
  //
  // Будет хранить второй по величине минимум среди элементов
  // nums[1:]
  let minB = 50;

  // Цикл по индексам массива, начиная с 1, потому что
  // nums[0] всегда является первым элементом первого
  // подмассива и не участвует в поиске двух минимальных
  // стартовых элементов оставшихся подмассивов
  for (let i = 1; i < nums.length; i++) {

    // Сохраняем текущий элемент в константу num для удобства
    // и читаемости.
    const num = nums[i];

    // Если текущий элемент меньше текущего minA, значит
    // найден новый абсолютный минимум среди просмотренных.
    //
    // В этом случае прежний minA становится кандидатом на
    // второе место (minB), а minA обновляется до текущего
    // значения num.
    //
    // Такой порядок присвоений гарантирует, что minB всегда
    // хранит второе наименьшее значение из уже просмотренных.
    if (num < minA) {
      minB = minA;
      minA = num;
    } else if (num < minB) {

      // Если текущий элемент не меньше minA, но меньше minB,
      // то он занимает место второго наименьшего.
      //
      // Здесь важно условие else if: оно предотвращает
      // перезапись minB значением, которое на самом деле
      // меньше minA, потому что такие случаи уже обработаны
      // в предыдущем блоке.
      minB = num;
    }
  }

  // Возврат итоговой минимальной стоимости:
  //
  // Фиксированный вклад nums[0] плюс два наименьших элемента,
  // найденных в nums[1:]
  return nums[0] + minA + minB;
}
