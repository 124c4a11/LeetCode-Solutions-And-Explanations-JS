/**
 * @param {number[]} nums
 * @return {number}
 *
 * minimumOperations принимает массив целых чисел и вычисляет
 * минимальное количество операций, необходимых для того,
 * чтобы по правилу задачи все элементы массива стали нулём.
 * Алгоритм считает количество различных ненулевых значений в
 * массиве - каждое такое значение требует одной операции
 *
 *
 * Пример для:
 *   nums = [1,5,0,3,5]
 * Результат:
 *   3
 * Пояснение:
 * В первой операции выбираем x = 1. Теперь nums = [0,4,0,2,4].
 * Во второй операции выбираем x = 2. Теперь nums = [0,2,0,0,2].
 * В третьей операции выбираем x = 2. Теперь nums = [0,0,0,0,0].
 */
function minimumOperations(nums) {

  // Давайте подумаем, что происходит при каждой операции.
  // Когда мы выбираем значение x и вычитаем его из всех
  // положительных элементов, мы по сути "сдвигаем вниз" все
  // положительные значения на одну и ту же величину.
  //
  // Ключевое наблюдение: если всегда выбирать x равным
  // наименьшему ненулевому элементу массива, мы добиваемся
  // важного эффекта - это значение полностью исчезает из
  // массива (вместе со всеми его дубликатами). Почему? Потому
  // что вычитание наименьшего ненулевого значения из самого
  // себя даёт 0, а из любого его дубликата тоже даёт 0.
  //
  // Рассмотрим по шагам:
  // - В первой операции мы устраняем наименьшее ненулевое
  //   значение.
  //
  // - Во второй операции устраняется новое наименьшее ненулевое
  //   значение.
  //
  // - Так продолжается, пока все значения не станут 0.
  //
  // Поскольку в каждой операции можно устранить одно уникальное
  // значение (вместе со всеми его дубликатами), а чтобы
  // обнулить весь массив нужно устранить все уникальные
  // ненулевые значения, минимальное число операций равно
  // количеству различных ненулевых значений.
  //
  // Таким образом задача сводится к вопросу "сколько уникальных
  // ненулевых значений в массиве?". Дубликаты не важны, потому
  // что они устраняются одновременно.


  // Инициализация счётчика операций нулём.
  //
  // Этот счётчик будет увеличиваться ровно тогда, когда мы
  // впервые встречаем новое ненулевое значение.
  let operationsCnt = 0;

  // Создаём массив длины 101 для отслеживания, какие значения
  // уже встречались.
  //
  // Индекс массива соответствует значению числа.
  // Значение в ячейке служит флагом "встречено/не встречено".
  //
  // Использование массива вместо Set/объекта даёт
  // предсказуемую память, быстрый доступ по индексу и ускоряет
  // исполнение кода
  //
  // Используем длину 101, так как входные значения находятся в
  // диапазоне от 0 до 100
  const numToSeen = new Array(101);

  // Проходим по каждому элементу входного массива nums.
  for (const num of nums) {

    // Пропускаем текущую итерацию, если:
    // 1. num === 0 - ноль не требует операции по условию задачи
    //    (удалять ноль не нужно).
    //
    // 2. numToSeen[num] истинно - это число уже встречалось
    //    ранее, и мы уже учли операцию для него.
    if (num === 0 || numToSeen[num]) continue;

    // Если мы попали сюда, значит число ненулевое и ещё не
    // встречалось.
    //
    // Увеличиваем счётчик операций - для удаления всех
    // вхождений этого конкретного числа потребуется одна
    // операция.
    operationsCnt++;

    // Помечаем текущее число как встреченное, чтобы последующие
    // вхождения этого же числа не увеличивали счётчик.
    //
    // Присваивание true делает проверку numToSeen[num] в
    // будущем истинной.
    numToSeen[num] = true;
  }

  // После обработки всех элементов возвращаем накопленное
  // количество операций.
  //
  // По логике алгоритма это количество равно числу уникальных
  // ненулевых значений в массиве.
  return operationsCnt;
}
