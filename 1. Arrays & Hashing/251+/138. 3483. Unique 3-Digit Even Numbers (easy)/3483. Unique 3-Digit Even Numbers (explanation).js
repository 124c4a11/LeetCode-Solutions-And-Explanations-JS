/**
 * @param {number[]} digits
 * @return {number}
 *
 * totalNumbers принимает массив цифр digits и возвращает количество уникальных
 * трёхзначных чётных чисел, которые можно составить из этих цифр, не используя
 * одну и ту же позицию массива более одного раза и не допуская ведущего нуля.
 *
 *
 * Пример для:
 *   digits = [1,2,3,4]
 * Результат:
 *   12
 * Пояснение:
 *   12 различных трёхзначных чётных чисел, которые можно составить:
 *   124, 132, 134, 142, 214, 234, 312, 314, 324, 342, 412 и 432.
 *
 *   Обратите внимание, что 222 нельзя составить, потому что в массиве только
 *   одна цифра 2.
 */
function totalNumbers(digits) {

  // Чтобы составить корректное трёхзначное чётное число, нужно учесть
  // ограничения для каждой позиции:
  // - Разряд единиц (самая правая цифра):
  //   должен быть чётным (0, 2, 4, 6 или 8), чтобы всё число было чётным.
  //
  // - Разряд сотен (самая левая цифра):
  //   не может быть 0 (иначе это не будет трёхзначным числом).
  //
  // - Разряд десятков (средняя цифра):
  //   может быть любой цифрой от 0 до 9.
  //
  // Поскольку нам нужно посчитать различные числа и каждую позицию массива
  // можно использовать только один раз, перед нами стоит задача выбора. По
  // сути мы выбираем 3 позиции из массива для формирования числа с учётом
  // указанных ограничений на значения в этих позициях.
  //
  // Естественный подход - перебрать все допустимые комбинации. Сначала
  // фиксируем цифру единиц (проверяем, чётная ли она), затем пробуем все
  // возможные цифры десятков (избегая уже использованной позиции), и в конце
  // пробуем все возможные цифры сотен (избегая обеих ранее использованных
  // позиций и значения 0).
  //
  // Зачем использовать хеш‑множество (set)? При генерации чисел одно и то же
  // трёхзначное число может получиться из разных комбинаций позиций массива.
  // Например, для массива [1, 2, 1, 2] число 212 можно получить из позиций
  // (0, 1, 3) или (2, 1, 3). Множество автоматически убирает такие дубликаты.
  //
  // Главное понимание в том, что мы смотрим не только на значения в массиве,
  // но и на позиции этих значений. Поэтому в решении используются индексы
  // i, j, k, чтобы гарантировать, что одна и та же позиция не будет
  // использована дважды, даже если одинаковое значение встречается в массиве
  // несколько раз.


  const n = digits.length;

  // Создаём пустое множество для хранения уникальных трёхзначных чисел, чтобы
  // автоматически исключать дубликаты, полученные из разных комбинаций
  // индексов.
  const uniqueNums = new Set();

  // Цикл по индексам, которые будут использоваться как индекс разряда единиц.
  for (let onesNdx = 0; onesNdx < n; onesNdx++) {

    // Проверяем, является ли цифра в позиции onesNdx чётной.
    //
    // Оператор & 1 вернет 1 для нечётных чисел и 0 для чётных.
    //
    // Если цифра нечётная - пропускаем.
    if ((digits[onesNdx] & 1) === 1) continue;

    // Цикл по индексам для разряда десятков.
    for (let tensNdx = 0; tensNdx < n; tensNdx++) {

      // Нельзя использовать ту же позицию массива дважды.
      //
      // Если индекс десятков совпадает с единицами - пропускаем
      if (tensNdx === onesNdx) continue;

      // Цикл по индексам для разряда сотен.
      for (let hundredsNdx = 0; hundredsNdx < n; hundredsNdx++) {

        // Проверяем три условия, при которых текущая комбинация недопустима:
        // 1. digits[hundredsNdx] === 0 - если цифра сотен равна 0, число
        //    перестаёт быть трёхзначным;
        //
        // 2. hundredsNdx === onesNdx - нельзя повторно использовать индекс
        //    единиц;
        //
        // 3. hundredsNdx === tensNdx - нельзя повторно использовать индекс
        //    десятков.
        // Если любое из условий истинно - пропускаем эту итерацию.
        if (
          digits[hundredsNdx] === 0
          || hundredsNdx === onesNdx
          || hundredsNdx === tensNdx
        ) continue;

        // Если все проверки пройдены, формируем трёхзначное число:
        //
        // Выражение
        // digits[hundredsNdx] * 100 + digits[tensNdx] * 10 + digits[onesNdx]
        // корректно собирает число из трёх цифр в соответствующих разрядах.
        //
        // Добавляем полученное число в множество uniqueNums.
        //
        // Если такое значение уже присутствует в множестве, Set автоматически
        // предотвратит дублирование.
        uniqueNums.add(digits[hundredsNdx] * 100 + digits[tensNdx] * 10 + digits[onesNdx]);
      }
    }
  }

  // После перебора всех допустимых комбинаций возвращаем размер множества, то
  // есть количество различных трёхзначных чётных чисел, которые можно
  // составить, соблюдая ограничение "каждая позиция массива может быть
  // использована не более одного раза".
  return uniqueNums.size;
}
