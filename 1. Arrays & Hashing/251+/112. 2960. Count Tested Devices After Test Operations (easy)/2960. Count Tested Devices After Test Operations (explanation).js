/**
 * @param {number[]} batteryPercentages
 * @return {number}
 *
 * countTestedDevices принимет целочисленный массив
 * batteryPercentages длины n, обозначающий проценты заряда n
 * устройств с индексами от 0 до n - 1.
 *
 * Задача - протестировать каждое устройство i
 * последовательно, от 0 до n - 1, выполняя следующие
 * операции тестирования:
 * - Если batteryPercentages[i] больше 0:
 *   - Увеличить счётчик протестированных устройств на 1.
 *
 *   - Уменьшить процент заряда всех устройств с индексами j
 *     в диапазоне [i + 1, n - 1] на 1, при этом их заряд не
 *     должен становиться отрицательным, то есть
 *     batteryPercentages[j] =
 *      Math.max(0, batteryPercentages[j] - 1).
 *
 *   - Перейти к следующему устройству.
 *
 * - В противном случае перейти к следующему устройству безi
 *   выполнения теста.
 *
 * Возвращает целое число, обозначающее количество устройств,
 * которые будут протестированы после выполнения операций
 * тестирования в указанном порядке.
 *
 *
 * Пример для:
 *   batteryPercentages = [1,1,2,1,3]
 * Результат:
 *   3
 * Пояснение:
 * Выполняем операции тестирования последовательно, начиная с
 * устройства 0:
 * - На устройстве 0 значение batteryPercentages[0] > 0,
 *   поэтому теперь 1 протестированное устройство, и
 *   batteryPercentages становится [1,0,1,0,2].
 *
 * - На устройстве 1 значение batteryPercentages[1] == 0,
 *   поэтому переходим к следующему устройству без тестирования.
 *
 * - На устройстве 2 значение batteryPercentages[2] > 0,
 *   поэтому теперь 2 протестированных устройства, и
 *   batteryPercentages становится [1,0,1,0,1].
 *
 * - На устройстве 3 значение batteryPercentages[3] == 0,
 *   поэтому переходим к следующему устройству без тестирования.
 *
 * - На устройстве 4 значение batteryPercentages[4] > 0,
 *   поэтому теперь 3 протестированных устройства, и
 *   batteryPercentages остаётся без изменений.
 *
 * Таким образом, ответ равен 3.
 */
function countTestedDevices(batteryPercentages) {

  // Проследим, что происходит, когда мы тестируем устройства.
  // Когда мы тестируем первое устройство
  // (если его батарея > 0), все последующие устройства теряют
  // 1% заряда. Когда мы тестируем второе устройство, все
  // устройства после него теряют ещё 1% заряда.
  //
  // Здесь видно закономерность.
  //
  // К тому моменту, когда мы доходим до устройства i, сколько
  // заряда оно потеряло? Оно потеряло 1% за каждое устройство,
  // протестированное до него.
  //
  // Если до сих пор протестировано ans устройств, то
  // устройство i потеряло ровно ans процентов заряда. Поэтому
  // при проверке, можно ли протестировать устройство i, нужно
  // проверить, положителен ли его оставшийся заряд:
  // - Исходный заряд: batteryPercentages[i]
  //
  // - Потеря заряда: ans (количество ранее протестированных
  //   устройств)
  //
  // - Оставшийся заряд: batteryPercentages[i] - ans
  //
  // Устройство i можно протестировать, если
  // batteryPercentages[i] - ans > 0, что упрощается до
  // batteryPercentages[i] > ans.
  //
  // Это наблюдение превращает задачу из симуляции уменьшения
  // заряда (что потребовало бы обновления всех будущих
  // устройств при каждом тесте) в простое сравнение. Нам нужно
  // лишь отслеживать, сколько устройств мы уже протестировали
  // (ans) и для каждого устройства проверять, превышает ли его
  // исходный процент заряда это число.
  //
  // Преимущество такого подхода в том, что нам не нужно
  // фактически изменять массив или отслеживать текущие уровни
  // заряда. Исходный процент батареи в сочетании с количеством
  // ранее протестированных устройств даёт всю необходимую
  // информацию.


  // Инициализация счётчика протестированных устройств
  // testedCnt значением 0.
  //
  // testedCnt хранит количество устройств, которые уже были
  // успешно протестированы на текущем шаге перебора.
  //
  // Это значение также интерпретируется как количество
  // процентов, которые потеряли все последующие устройства.
  let testedCnt = 0;

  // Проходим по каждому элементу входного массива
  // batteryPercentages.
  for (const percentage of batteryPercentages) {

    // Условие проверяет, можно ли протестировать текущее
    // устройство с учётом уже протестированных ранее.
    //
    // Логика:
    // К моменту проверки i-го устройства оно потеряло
    // testedCnt процентов заряда из-за предыдущих тестов.
    // Поэтому оставшийся заряд равен percentage - testedCnt.
    // Устройство можно протестировать только если это
    // значение > 0.
    //
    // Эквивалентное и более простое сравнение:
    // percentage > testedCnt.
    //
    // Если условие истинно, увеличиваем счётчик testedCnt на 1,
    // фиксируя, что это устройство протестировано.
    if (percentage > testedCnt) testedCnt++;
  }

  // После завершения перебора всех устройств возвращаем
  // итоговое количество протестированных устройств.
  return testedCnt;
}
