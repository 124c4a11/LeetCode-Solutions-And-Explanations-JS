/**
 * @param {number[]} target
 * @param {number[]} arr
 * @return {boolean}
 *
 * canBeEqual принимает два параметра: target и arr, оба - массивы
 * целых чисел и проверяет, можно ли перестановкой элементов
 * массива arr получить массив target.
 *
 *
 * Пример для:
 *   target = [1,2,3,4],
 *   arr = [2,4,1,3]
 * Результат:
 *   true
 * Пояснение:
 * Можно выполнить следующие шаги, чтобы преобразовать arr в
 * target:
 *   1. Развернуть подмассив [2,4,1], arr становится [1,4,2,3]
 *   2. Развернуть подмассив [4,2], arr становится [1,2,4,3]
 *   3. Развернуть подмассив [4,3], arr становится [1,2,3,4]
 *
 * Существует несколько способов преобразовать arr в target, это
 * не единственный.
 */
function canBeEqual(target, arr) {

  // Создаём статически типизированный массив Int32Array длиной
  // 1001 для хранения частоты вхождения чисел из target.
  //
  // Используем размер 1001, так как значения находятся в
  // диапазоне 1..1000 включительно
  const targetNumToFreq = new Int32Array(1001);

  // Проходим по всем элементам массива target и для каждого числа
  // увеличиваем соответствующий счётчик частоты.
  for (const num of target) targetNumToFreq[num]++;

  // Итерация по каждому элементу массива arr, чтобы проверить,
  // можно ли "покрыть" все элементы arr элементами target с
  // учётом их частот.
  //
  // Идея:
  // Если для какого-то значения в arr нет доступной частоты в
  // targetNumToFreq, то arr содержит лишний элемент и
  // соответствие невозможно.
  for (const num of arr) {

    // Проверяем, остались ли ещё неиспользованные в target
    // элементы со значением num.
    //
    // Если значение равно 0, значит либо такого числа не было в
    // target, либо все его экземпляры уже "использованы"
    // предыдущими элементами arr.
    //
    // В этом случае можно немедленно вернуть false - ранний
    // выход экономит время.
    if (targetNumToFreq[num] === 0) return false;

    // Уменьшаем счётчик для текущего числа, тем самым "используя"
    // одну его копию из target.
    //
    // После обработки всех элементов arr, если ни один счётчик
    // не ушёл в отрицательное значение, это означает, что arr не
    // содержит значений, которых нет в target, и не превышает их
    // частоты.
    targetNumToFreq[num]--;
  }

  // Если мы прошли весь массив arr, значит для каждого элемента
  // arr нашёлся соответствующий элемент в target.
  //
  // Возвращаем true - массив arr можно переставить так, чтобы он
  // стал равен target (мульти-множества совпадают).
  return true;
}
