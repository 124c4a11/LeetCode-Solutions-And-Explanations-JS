/**
 * @param {string} s
 * @return {string}
 *
 * longestNiceSubstring ищет и возвращает самую длинную подстроку
 * входной строки s, которая является хорошей - то есть для
 * каждой буквы в подстроке присутствуют оба регистра (и строчный,
 * и заглавный).
 *
 *
 * Пример для:
 *   s = "YazaAay"
 * Результат:
 *   "aAa"
 * Пояснение:
 *   "aAa" - это "хорошая" строка, потому что буква 'A'/'a' -
 *   единственная буква алфавита, встречающаяся в s, и обе её
 *   формы ('A' и 'a') присутствуют.
 *
 *   "aAa" - самая длинная подстрока, удовлетворяющая условию
 *   "хорошая".
 */
function longestNiceSubstring(s) {
  const n = s.length;

  // Инициализируем переменную bestStart - индекс левой границы
  // лучшей (самой длинной найденной) "хорошей" подстроки
  let bestStart = 0;

  // Инициализируем переменную bestEnd - индекс правой границы
  // лучшей подстроки (включительно)
  let bestEnd = 0;

  // Инициализируем bestSubstrLength нулём - текущая длина лучшей
  // найденной подстроки.
  let bestSubstrLength = 0;

  // Внешний цикл перебирает все возможные начальные позиции
  // подстрок l от 0 до n-1.
  //
  // Это часть классического квадратичного перебора всех подстрок:
  // для каждой l мы расширяем r.
  for (let l = 0; l < n; l++) {

    // Маска для строчных букв в текущем окне [l..r].
    //
    // Каждый бит соответствует одной букве 'a'..'z'
    // (бит 0 - 'a', бит 25 - 'z').
    //
    // Используется целое число, где 1 означает, что буква
    // встретилась хотя бы один раз в нижнем регистре.
    let lowerMask = 0;

    // Аналогичная маска для заглавных букв 'A'..'Z'.
    //
    // Биты соответствуют тем же буквам по позиции, но отражают
    // наличие заглавных символов.
    let upperMask = 0;

    // Внутренний цикл расширяет правую границу r от l до конца
    // строки, формируя все подстроки, начинающиеся в l.
    for (let r = l; r < n; r++) {

      // Получаем кодовую точку символа в позиции r.
      const codePoint = s.codePointAt(r);

      // Если кодовая точка больше 96, это строчная латинская
      // буква ('a' = 97).
      if (codePoint > 96) {

        // Вычисляем смещение относительно 'a' и устанавливаем
        // соответствующий бит в lowerMask.
        //
        // Операция |= сохраняет уже установленные биты и
        // добавляет новый, если буква встретилась.
        lowerMask |= 1 << (codePoint - 97);
      } else {

        // В противном случае считаем символ заглавной латинской
        // буквой ('A' = 65).
        //
        // Устанавливаем соответствующий бит в upperMask по
        // смещению относительно 'A'.
        upperMask |= 1 << (codePoint - 65);
      }

      // Вычисляем длину текущей подстроки от l до r включительно.
      const currSubstrLength = r - l + 1;

      // Условие "хорошая":
      // Для каждой буквы, которая встречается в подстроке, должны
      // присутствовать оба регистра.
      //
      // Это достигается, когда множества строчных и заглавных
      // букв совпадают, т.е. lowerMask === upperMask.
      //
      // Если маски равны и текущая подстрока длиннее ранее
      // найденной, обновляем результат.
      //
      // Равенство масок означает, что для каждой позиции бита
      // либо оба бита установлены, либо оба не установлены.
      if (
        lowerMask === upperMask
        && currSubstrLength > bestSubstrLength
      ) {

        // Обновляем индекс левой границы лучшей подстроки на
        // текущую l
        bestStart = l;

        // Обновляем индекс правой границы лучшей подстроки на
        // текущую r
        bestEnd = r;

        // Обновляем длину лучшей подстроки на текущую длину
        bestSubstrLength = currSubstrLength;
      }
    }
  }

  // Если ни одной "nice" подстроки не найдено (bestSubstrLength
  // остаётся 0), возвращаем пустую строку.
  //
  // Иначе возвращаем срез исходной строки от bestStart до bestEnd
  // включительно: s.slice(bestL, bestR + 1).
  return bestSubstrLength === 0 ? "" : s.slice(bestStart, bestEnd + 1);
}
