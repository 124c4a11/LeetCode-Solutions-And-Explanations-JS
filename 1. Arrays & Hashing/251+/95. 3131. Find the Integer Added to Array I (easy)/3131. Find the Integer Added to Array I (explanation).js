/**
 * @param {number[]} nums1
 * @param {number[]} nums2
 * @return {number}
 *
 * addedInteger принимает два массива одинаковой длины,
 * nums1 и nums2.
 *
 * Каждый элемент в nums1 был увеличен (или уменьшен,
 * если x отрицательно) на целое число, обозначенное
 * переменной x.
 *
 * В результате nums1 стал равен nums2. Два массива
 * считаются равными, если они содержат одни и те же
 * целые числа с одинаковыми частотами.
 *
 * Возвращает целое число x.
 *
 *
 * Пример для:
 *   nums1 = [2, 6, 4],
 *   nums2 = [9, 7, 5]
 * Результат:
 *   3
 * Пояснение:
 *   Целое число, прибавленное к каждому элементу nums1,
 *   равно 3.
 */
function addedInteger(nums1, nums2) {

  // Если посмотреть на любой конкретный элемент nums1
  // и соответствующий ему элемент в nums2 (после
  // сортировки массивов), разность между ними всегда
  // будет равна x.
  //
  // Например, если nums1 = [2, 5, 8] и мы прибавляем
  // x = 3, то получаем [5, 8, 11].
  //
  // Обратите внимание:
  //   5 - 2 = 3
  //   8 - 5 = 3
  //   11 - 8 = 3
  //
  // Каждая соответствующая пара имеет одинаковую
  // разность 3.
  //
  // Ключевая мысль в том, что мы можем взять любую
  // соответствующую пару элементов, чтобы найти x.
  // Самый простой выбор - использовать минимальные
  // элементы обоих массивов.
  //
  // Вычисление x как разности минимумов позволяет
  // избежать сортировки обоих массивов и снизить
  // временную сложность поиска x с O(n log n) до O(n).
  //
  // Следовательно, x = min(nums2) - min(nums1) даёт
  // точное значение, которое было прибавлено, чтобы
  // преобразовать nums1 в nums2.

  // Инициализация переменной nums1Min значением
  // Infinity, чтобы при поиске минимума любое
  // реальное число в массиве nums1 было меньше
  // начального значения.
  //
  // Будет хранить минимальный найденный элемент в
  // массиве nums1
  let nums1Min = Infinity;

  // Инициализация переменной nums2Min значением
  // Infinity, чтобы при поиске минимума любое
  // реальное число в массиве nums2 было меньше
  // начального значения.
  // 
  // Будет хранить минимальный найденный элемент в
  // массиве nums2
  let nums2Min = Infinity;

  // Цикл одновременно проходит по всем элементам
  // массивов nums1 и nums2 и находит минимальные
  // значения в этих массивах. Это возможно, так как
  // длины этих массивов одинаковы.
  for (let i = 0; i < nums1.length; i++) {
    nums1Min = Math.min(nums1Min, nums1[i]);
    nums2Min = Math.min(nums2Min, nums2[i]);
  }

  // Возвращаем разницу между минимальным элементом
  // второго массива и минимальным элементом первого
  // массива, что даёт точное значение, которое было
  // прибавлено, чтобы преобразовать nums1 в nums2.
  return nums2Min - nums1Min;
}
