/**
 * @param {number[]} nums
 * @return {number[]}
 *
 * buildArray принимает перестановку nums (индексация с нуля)
 * и возвращает массив result той же длины, где
 * result[i] = nums[nums[i]] для каждого 0 <= i < nums.length
 *
 * Реализация делает это in‑place, используя кодирование двух
 * значений в одном числе для экономии памяти. Дополнительная
 * память - O(1)
 *
 *
 * Пример для:
 *   nums = [0,2,1,5,3,4]
 * Результат:
 *   [0,1,2,4,5,3]
 * Пояснение:
 * Массив result строится следующим образом:
 *   result = [
 *     nums[nums[0]], nums[nums[1]], nums[nums[2]],
 *     nums[nums[3]], nums[nums[4]], nums[nums[5]]
 *   ]
 *   = [nums[0], nums[2], nums[1], nums[5], nums[3], nums[4]]
 *   = [0,1,2,4,5,3]
 */
function buildArray(nums) {

  // Идея трюка:
  // Упаковать два числа в одно
  //
  // Мы хотим построить массив:
  //
  // result[i] = nums[nums[i]]
  //
  // Но без дополнительного массива.
  //
  // Проблема:
  // Если мы начнём перезаписывать nums[i], мы потеряем
  // исходные значения, которые ещё нужны.
  //
  // Решение:
  // В каждую ячейку временно записывается два числа
  // одновременно:
  //
  // – старое значение
  // – новое значение
  //
  // И всё это — в одном числе.
  //
  // --------------------------------------------------------
  //
  // Как упаковываются два числа в одно
  //
  // Мы используем формулу:
  //
  // nums[i] = old + n * new
  //
  // где:
  //
  // – old = nums[i] (исходное значение)
  // – new = nums[nums[i]] % n (будущее значение)
  // – n — размер массива
  //
  // Почему это работает:
  //
  // 1. Все значения в массиве от 0 до n-1.
  // 2. Значит old < n.
  // 3. Значит n * new — это число, кратное n.
  // 4. Поэтому:
  //
  // nums[i] = old + n * new
  //
  // содержит:
  //
  // – остаток от деления на n → старое значение
  // – целую часть от деления на n → новое значение
  //
  // --------------------------------------------------------
  //
  // Как потом извлекаются значения
  //
  // Старое значение:
  //   nums[i] % n
  //
  // Новое значение:
  //   Math.floor(nums[i] / n)
  //
  // Именно это и делает второй цикл:
  //   nums[i] = Math.floor(nums[i] / n);
  //
  // --------------------------------------------------------
  //
  // Почему мы не портим данные при первом проходе
  //
  // Когда мы делаем:
  //
  // nums[i] += n * (nums[nums[i]] % n);
  //
  // мы берём nums[nums[i]] % n.
  //
  // Если nums[nums[i]] уже было изменено, оно имеет вид:
  //
  // old + n * new
  //
  // Тогда:
  //
  // (nums[nums[i]] % n) → old
  //
  // То есть мы всегда получаем исходное значение, даже если
  // элемент уже был модифицирован.
  //
  // --------------------------------------------------------
  //
  // Итог
  //
  // Трюк работает, потому что:
  //
  // – все числа меньше n
  // – мы кодируем два числа в одном с помощью арифметики
  // – старое значение восстанавливается через % n
  // – новое — через / n
  // – порядок операций гарантирует, что мы никогда не
  //   потеряем исходные данные
  //
  // Это очень элегантный способ сделать in‑place
  // перестановку массива.

  const n = nums.length;

  // Первый проход по массиву:
  //
  // В каждом элементе мы закодируем одновременно старое и
  // новое значение.
  for (let i = 0; i < n; i++) {

    // Обновляем nums[i], добавляя в него "новое" значение,
    // сдвинутое в старшие разряды умножением на n.
    //
    // Подробно:
    // 1. nums[nums[i]] — это значение, которое мы хотим
    //    поместить в позицию i в итоговом массиве.
    //
    // 2. Однако nums[nums[i]] могло уже быть изменено на
    //    предыдущих шагах первого цикла, поэтому берём
    //    (nums[nums[i]] % n), чтобы получить исходное
    //    (не закодированное) значение в диапазоне [0, n-1].
    //
    // 3. Умножение на n сдвигает это новое значение в
    //    "старшие разряды" числа, оставляя исходное nums[i]
    //    в младших разрядах.
    //
    // 4. Операция += сохраняет оба значения в одном числе:
    //    Старшие разряды = новое * n,
    //    младшие = старое значение.
    //
    // Значение старшего разряда мы можем получить при помощи
    // взятия целой части от деления на n. Младшего при
    // помощи остатка по модулю n (остаток от деления на n).
    //
    // Пример:
    //   Если n = 5,
    //   nums[i] = 2 (старое),
    //   nums[nums[i]] → nums[2] (новое) = 4,
    //   то мы делаем nums[i] += 5 * (4 % 5) → nums[i] += 20.
    //
    // После этого nums[i] содержит (2 (старое) + 20) === 22,
    // где целая часть при делении на 5 даст 4 (новое):
    //   Math.floor(22 / 5) → Math.floor(4.4) → 4 (новое)
    // остаток по модулю 5:
    //   22 % 5 → 2 (старое)
    nums[i] += n * (nums[nums[i]] % n);
  }

  // Второй проход по массиву nums:
  //
  // Извлекаем новое значение из каждого закодированного
  // элемента.
  for (let i = 0; i < n; i++) {

    // Деление на n и взятие целой части (Math.floor)
    // возвращает ранее закодированное новое значение, потому
    // что мы его поместили в старшие разряды умножением на n.
    //
    // После этой операции nums[i] становится равным
    // nums[nums[i]] из исходного массива.
    nums[i] = Math.floor(nums[i] / n);
  }

  // Возвращаем модифицированный массив.
  //
  // В результате каждый элемент nums[i] теперь равен
  // исходному nums[nums[i]].
  //
  // Важные характеристики алгоритма:
  // - Временная сложность O(n) — два прохода по массиву.
  //
  // - Дополнительная память O(1) — преобразование
  //   выполняется на месте без выделения вспомогательного
  //   массива.
  return nums;
}
