/**
 * @param {string} s
 * @return {number}
 *
 * residuePrefixes подсчитывает количество префиксов входной строки s,
 * для которых выполняется условие: остаток от деления длины префикса
 * на 3 равен числу уникальных символов в этом префиксе.
 *
 * Входная строка содержит только строчные латинские буквы.
 *
 *
 * Пример для:
 *   s = "abc"
 * Результат:
 *   2
 * Пояснение:
 *   Префикс "a" содержит 1 уникальный символ, длина префикса равна 1,
 *   1 % 3 = 1, поэтому он удовлетворяет условию.
 *
 *   Префикс "ab" содержит 2 уникальных символа, длина префикса равна
 *   2, 2 % 3 = 2, поэтому он удовлетворяет условию.
 *
 *   Префикс "abc" содержит 3 уникальных символа, длина префикса равна
 *   3, 3 % 3 = 0, что не равно 3, поэтому он не удовлетворяет условию.
 *
 *   Таким образом, количество подходящих префиксов равно 2.
*/
function residuePrefixes(s) {

  // Инициализируем счётчик подходящих префиксов нулём.
  //
  // Этот счётчик будет увеличиваться каждый раз, когда текущий
  // префикс удовлетворяет условию: длина префикса % 3 === число
  // уникальных символов.
  let residuePrefixesCnt = 0;

  // Инициализация счётчика уникальных символов в текущем префиксе.
  //
  // Этот счётчик увеличивается только при первом появлении каждого
  // символа в префиксе.
  let prefixUniqueCharsCnt = 0;

  // Создаётся массив длины 26 для отслеживания, встречался ли символ
  // 'a'..'z'.
  //
  // Индекс 0 соответствует 'a', 25 - 'z'.
  //
  // Элементы по умолчанию undefined - используется как булев флаг
  // (undefined/false = не встречался).
  //
  // Такой подход позволяет избежать использования Set, что экономит
  // память и ускоряет работу функции
  const charToSeen = new Array(26);

  // Итерация по индексам строки s
  //
  // На каждой итерации рассматриваем префикс строки, заканчивающийся
  // на текущем символе.
  for (let i = 0; i < s.length; i++) {

    // Получаем числовой индекс текущего символа относительно 'a'.
    //
    // codePointAt возвращает кодовую точку символа. Вычитаем 97
    // чтобы получить индекс в диапазоне 0..25 для 'a'..'z'.
    const charNdx = s.codePointAt(i) - 97;

    // Проверяем, встречался ли этот символ ранее в текущем префиксе.
    //
    // Если элемент массива undefined или false, значит символ ещё не
    // встречался.
    if (!charToSeen[charNdx]) {

      // Помечаем символ как встреченный, записывая true в
      // соответствующую ячейку массива.
      //
      // Это предотвращает повторное увеличение счётчика уникальных
      // символов при последующих вхождениях той же буквы.
      charToSeen[charNdx] = true;

      // Увеличиваем счётчик уникальных символов, так как обнаружен
      // новый символ для текущего префикса.
      prefixUniqueCharsCnt++;
    }

    // Вычисляем длину текущего префикса.
    //
    // Так как i начинается с нуля, длина равна i + 1.
    const prefixLength = i + 1;

    // Сравниваем остаток от деления длины префикса на 3 с количеством
    // уникальных символов в префиксе.
    //
    // Если они не равны, пропускаем дальнейшие действия для этого
    // префикса и переходим к следующей итерации.
    if (prefixLength % 3 !== prefixUniqueCharsCnt) continue;

    // Если остаток от деления длины префикса на 3 равно количеству
    // уникальных символов в префиксе, увеличиваем общий счётчик
    // подходящих префиксов.
    residuePrefixesCnt++;
  }

  // Возвращаем итоговое количество префиксов, удовлетворяющих условию
  return residuePrefixesCnt;
}
