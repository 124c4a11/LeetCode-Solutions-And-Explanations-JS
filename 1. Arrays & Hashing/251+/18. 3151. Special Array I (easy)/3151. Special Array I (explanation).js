/**
 * @param {number[]} nums
 * @return {boolean}
 * 
 * isArraySpecial проверяет, чередуются ли
 * в переданном массиве числа по чётности
 * — то есть у каждой пары соседних
 * элементов один должен быть чётным,
 * а другой нечётным. 
 * 
 * Если найдётся хотя бы одна пара
 * соседних элементов одинаковойчётности,
 * функция возвращает false.
 * 
 * Если все соседние элементы имеют разную
 * чётность, возвращает true.
 * 
 * Параметры:
 * - nums — массив чисел 
 * 
 * Возвращаемое значение:
 * - булево: true или false.
 * 
 * Дополнительные детали:
 * Для пустого массива или массива с одним
 * элементом функция вернёт true,
 * поскольку нет ни одной пары соседних
 * элементов, нарушающей условие.
 */
function isArraySpecial(nums) {

  // Цикл по элементам массива, начиная
  // с индекса 1
  //
  // Цикл начинается с индекса 1, так как
  // для проверки пары соседних элементов
  // мы на каждой итерации работаем с 
  // предыдущим элементом nums[i - 1]
  // и с текущим nums[i].
  for (let i = 1; i < nums.length; i++) {

    // Выражение nums[i - 1] % 2 вычисляет
    // остаток от деления предыдущего
    // элемента на 2, nums[i] % 2 — остаток
    // для текущего элемента.
    //
    // Для целых чисел остаток 0 означает
    // чётное, 1 (или -1 в некоторых
    // реализациях с отрицательными числами)
    // — нечётное.
    // 
    // Здесь подразумеваются обычные
    // неотрицательные целые или целые в
    // привычном смысле.
    //
    // Оператор === строго сравнивает тип
    // и значение остатков (обычно 0 или 1).
    //
    // Если остатки равны, значит оба
    // элемента имеют одинаковую чётность
    // (оба чётные или оба нечётные).
    //
    // При обнаружении двух соседних
    // элементов одинаковой чётности
    // чередование нарушено, поэтому
    // функция мгновенно возвращает false
    // — дальнейшие проверки не нужны.
    //
    // Этот ранний выход экономит время
    // на больших массивах
    // (алгоритм ранней остановки).
    if (nums[i - 1] % 2 === nums[i] % 2) {
      return false;
    }
  }

  // Пример работы цикла для
  // nums = [2, 1, 4]:
  //
  // i = 1
  //   - Сравниваем nums[0] % 2 и nums[1] % 2:
  //     - nums[0] = 2 → 2 % 2 = 0 (чётное).
  //     - nums[1] = 1 → 1 % 2 = 1 (нечётное).
  //     - 0 === 1 → false,
  //       условие if не выполняется,
  //       возврат не происходит.
  //
  // i = 2
  //   - Сравниваем nums[1] % 2 и nums[2] % 2:
  //     - nums[1] = 1 → 1 % 2 = 1 (нечётное).
  //     - nums[2] = 4 → 4 % 2 = 0 (чётное).
  //     - 1 === 0 → false,
  //       условие if не выполняется,
  //       возврат не происходит.
  //
  // - После цикла выполняется return true,
  //   так как все соседние элементы имеют
  //   разную чётность.

  // Если цикл прошёл по всем парам
  // соседних элементов и не нашёл
  // нарушений, значит для каждой пары
  // i-1 и i чётность различалась
  // — массив удовлетворяет условию.
  //
  // Возвращаем true, сигнализируя,
  // что массив "специальный".
  return true;
}
