/**
 * @param {number[][]} items1
 * @param {number[][]} items2
 * @return {number[][]}
 *
 * mergeSimilarItems принимает два массива items1 и items2, каждый
 * элемент которых - пара целых чисел [value, weight].
 *
 * Функция объединяет два набора предметов, представленных как
 * массивы пар [value, weight], суммируя веса (weight) для
 * одинаковых value и возвращая список пар в порядке возрастания
 * value
 *
 *
 * Пример для:
 *   items1 = [[1,1],[4,5],[3,8]],
 *   items2 = [[3,1],[1,5]]
 * Output:
 *   [[1,6],[3,9],[4,5]]
 * Пояснение:
 * - Предмет со значением value = 1 встречается в items1 с весом 1
 *   и в items2 с весом 5, суммарный вес = 1 + 5 = 6.
 *
 * - Предмет со значением value = 3 встречается в items1 с весом 8
 *   и в items2 с весом 1, суммарный вес = 8 + 1 = 9.
 *
 * - Предмет со значением value = 4 встречается только в items1 с
 *   весом 5, суммарный вес = 5.
 * В результате возвращается [[1,6],[3,9],[4,5]].
 */
function mergeSimilarItems(items1, items2) {

  // Создаём типизированный массив Int32Array длиной 1001 для
  // хранения суммарных весов по индексу value.
  //
  // Индекс соответствует значению value
  // Значение соответствует weight
  //
  // Int32Array инициализируется нулями.
  //
  // Используем длину 1001, так как допустимые значения value
  // лежат в диапазоне 0..1000 включительно.
  //
  // Используем массив вместо object и Map, чтобы ускорить работу
  // функции и в дальнейшем избежать возможной сортировки
  const valueToWeight = new Int32Array(1001);

  // Итерация по первому входному массиву items1.
  //
  // Для каждой пары pair ожидается формат [value, weight].
  //
  // Это записывает начальные значения weight в valueToWeight по
  // индексу, равному value.
  //
  // Для ускорения работы кода не используем деструкруризацию
  // pair в цикле, вмето этого обращаемя к значениям pair по
  // индексам
  for (const pair of items1) valueToWeight[pair[0]] = pair[1];

  // Итерация по второму входному массиву items2.
  //
  // Для каждой пары добавляем вес к уже существующему значению в
  // valueToWeight.
  //
  // Оператор += корректно работает, даже если в ячейке уже 0
  // (инициализация Int32Array).
  for (const pair of items2) valueToWeight[pair[0]] += pair[1];

  // Создаём пустой массив для накопления результирующих пар
  // [value, totalWeight].
  const mergedSimilarItems = [];

  // Проходим по всем возможным значениям value от 1 до 1000
  // включительно.
  //
  // Итерация в порядке возрастания гарантирует, что
  // результирующий массив будет отсортирован по value.
  for (let value = 1; value < 1001; value++) {

    // Считываем накопленный вес для текущего значения value из
    // типизированного массива.
    const weight = valueToWeight[value];

    // Если суммарный вес равен нулю - это означает, что value не
    // встречался ни в одном из входных массивов.
    //
    // Пропускаем текущую итерацию.
    if (weight === 0) continue;

    // Добавляем пару [value, weight] в результирующий массив.
    mergedSimilarItems.push([value, weight]);
  }

  // Возвращаем сформированный массив пар [value, totalWeight].
  return mergedSimilarItems;
}
