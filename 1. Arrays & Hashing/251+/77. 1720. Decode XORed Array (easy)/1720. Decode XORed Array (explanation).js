/**
 * @param {number[]} encoded
 * @param {number} first
 * @return {number[]}
 *
 * decode принимает закодированный массив целых чисел
 * encoded длины n - 1, в котором значения закодированы
 * так, что encoded[i] = original[i] XOR original[i + 1].
 *
 * Например, если original = [1,0,2,1], то
 * encoded = [1,2,3].
 *
 * Также принимает целое число first - первый элемент
 * arr, то есть original[0].
 *
 * Возвращает исходный массив original.
 *
 * Можно доказать, что решение существует и единственно.
 *
 *
 * Пример для:
 *   encoded = [1,2,3],
 *   first = 1
 * Результат:
 *   [1,0,2,1]
 * Пояснение:
 *   Если original = [1,0,2,1], и first = 1, то
 *   encoded = [1 XOR 0, 0 XOR 2, 2 XOR 1] = [1,2,3]
 *
 *   Здесь XOR — побитовое исключающее ИЛИ (операция ^).
 */
function decode(encoded, first) {
  const n = encoded.length;

  // Создаёт новый массив original длиной n + 1 для
  // хранения восстановленного исходного массива.
  //
  // Выделение фиксированной длины повышает
  // производительность по сравнению с динамическим push
  const original = new Array(n + 1);

  // Устанавливает нулевой элемент original равным
  // переданному значению first, то есть задаёт известный
  // первый элемент декодируемой последовательности
  original[0] = first;

  // Запускаем цикл по индексам входного массива encoded
  // от 0 до n-1.
  //
  // Каждый шаг цикла восстанавливает следующий элемент
  // исходного (декодированного) массива.
  for (let i = 0; i < n; i++) {

    // Восстанавливаем следующий элемент
    // original[i + 1] с помощью побитовой операции XOR.
    //
    // Предполагается, что
    // encoded[i] = original[i] ^ original[i + 1],
    // поэтому обратная операция даёт:
    // original[i + 1] = encoded[i] ^ original[i].
    original[i + 1] = encoded[i] ^ original[i];
  }

  // Возвращаем полностью восстановленный массив original
  // длиной n + 1, где original[0] == first и для всех i
  // от 0 до n-1 выполнено
  // encoded[i] == original[i] ^ original[i + 1].
  return original;
}
