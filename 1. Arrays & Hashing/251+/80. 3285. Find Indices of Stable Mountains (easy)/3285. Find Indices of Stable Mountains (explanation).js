/**
 * @param {number[]} height
 * @param {number} threshold
 * @return {number[]}
 *
 * stableMountains проходит по массиву высот гор height и
 * находит индексы тех гор, которые считаются
 * "стабильными" по простому правилу: гора на позиции i
 * считается стабильной, если существующая предыдущая
 * гора height[i - 1] имеет высоту строго больше
 * заданного порога threshold. Гора с индексом 0 не
 * считается стабильной, потому что у неё нет предыдущей
 * горы.
 *
 *
 * Пример для:
 *   height = [1,2,3,4,5],
 *   threshold = 2
 * Вывод:
 *   [3,4]
 * Пояснение:
 *   Гора с индексом 3 считается стабильной, потому что
 *   height[2] == 3 больше, чем threshold == 2.
 *
 *   Гора с индексом 4 считается стабильной, потому что
 *   height[3] == 4 больше, чем threshold == 2.
 */
function stableMountains(height, threshold) {

  // Создаём константу stableIndices и инициализируем её
  // пустым массивом.
  //
  // В этот массив будут добавляться индексы (целые
  // числа) тех элементов массива height, для которых
  // выполняется условие: предыдущий элемент больше
  // threshold.
  const stableIndices = [];

  // Цикл for перебирает индексы массива height, начиная
  // с 1 и до последнего индекса (height.length - 1).
  //
  // Начало с i = 1 выбрано, чтобы при каждой итерации
  // иметь доступ к предыдущему элементу через i - 1.
  for (let i = 1; i < height.length; i++) {

    // Условие проверяет, больше ли значение предыдущего
    // элемента (height[i - 1]) заданного порога
    // threshold.
    //
    // Если да, то текущий индекс i считается "стабильным"
    // по определённому правилу и добавляется в результат.
    if (height[i - 1] > threshold) stableIndices.push(i);
  }

  // Возвращаем массив stableIndices как результат
  // выполнения функции.
  //
  // Массив содержит индексы тех элементов, для которых
  // предыдущая высота превышала threshold.
  return stableIndices;
}
