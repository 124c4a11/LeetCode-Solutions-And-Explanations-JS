/**
 * @param {string} s
 * @return {number}
 * 
 * minOperations вычисляет минимальное число
 * замен символов в бинарной строке, чтобы
 * она стала чередующейся (два соседних
 * символа не равны). 
 * 
 * Параметры:
 * - s — строка содержащая только символы
 *   '0' и '1'. Длина строки может быть любой
 *   положительной целой 
 * 
 * Возвращаемое значение
 * - Число — минимальное количество операций
 *   (замен символов) для превращения s в
 *   чередующуюся строку. 
 * 
 * Пример для:
 *   s = "0100"
 * Вывод: 
 *   1
 * Пояснение:
 *   Если изменить последний символ на «1»,
 *   s будет равно «0101», что является
 *   чередованием.
 */
function minOperations(s) {
  const n = s.length;

  // Инициализируем счётчик cnt нулём.
  //
  // cnt будет считать количество позиций,
  // которые уже соответствуют шаблону
  // "0,1,0,1..."
  // (т.е. чётные индексы = '0',
  // нечётные = '1').
  let cnt = 0;

  // Запускаем цикл по всем индексам строки
  // от 0 до n-1 включительно.
  //
  // Используем классический for, чтобы
  // иметь доступ к индексу i для проверки
  // чётности позиции.
  //
  // Цикл обеспечивает проверку каждой
  // позиции и накопление совпадений с
  // выбранным шаблоном.
  for (let i = 0; i < n; i++) {

    // Проверяем, является ли текущий
    // индекс i нечётным.
    //
    // Выражение i % 2 даёт 0 для чётных
    // и 1 для нечётных индексов. В
    // условии 0 трактуется как false,
    // 1 как true.
    if (i % 2) {

      // Если индекс нечётный, ожидаем
      // символ '1' для шаблона
      // "0,1,0,1...".
      //
      // Сравниваем текущий символ
      // s[i] с '1'. Если совпадает,
      // увеличиваем cnt.
      //
      // Это означает, что на этой
      // позиции уже не требуется
      // замена для приведения к
      // шаблону "0,1,0,1...".
      if (s[i] === '1') cnt++;
    } else {

      // Иначе (индекс чётный):
      // Для шаблона "0,1,0,1..." на
      // чётных позициях ожидаем '0'.
      //
      // Если s[i] === '0',
      // увеличиваем cnt, фиксируя,
      // что эта позиция уже
      // соответствует шаблону.
      if (s[i] === '0') cnt++;
    }
  }

  // После прохода cnt — это количество
  // позиций, которые уже совпадают с
  // шаблоном "0 на чётных, 1 на нечётных"
  // (0,1,0,1,...).
  //
  // Чтобы сделать строку чередующейся,
  // есть два возможных целевых шаблона:
  //  A: 0,1,0,1,...  
  //     (совпадений с ним = cnt;
  //     несовпадений = n - cnt)
  //
  //  B: 1,0,1,0,...
  //     (совпадений с ним = n - cnt;
  //      несовпадений = cnt)
  //
  // Число операций (замен символов) для
  // приведения строки к шаблону равно
  // числу несовпадений с этим шаблоном.
  //
  // Поэтому минимальное число операций
  // — это минимум между несовпадениями
  // для A и для B, то есть минимум
  // между (n - cnt) и cnt. Возвращаем
  // этот минимум.
  return Math.min(cnt, n - cnt);


  // Пример для s = "0100"
  //
  // ---
  //
  // Начальные значения:
  //   n = 4
  //   cnt = 0
  //
  // ---
  //
  // Идея: 
  //
  // Мы считаем cnt — количество позиций,
  // которые уже соответствуют шаблону
  // чётные индексы = 0, нечётные = 1
  // (шаблон A = "0 1 0 1 ..."). Затем
  // n - cnt — это количество позиций,
  // соответствующих противоположному
  // шаблону (шаблон B = "1 0 1 0 ...").
  // Минимум из них — ответ.
  // 
  // ---
  //
  // Цикл
  // 
  // i = 0  
  //   - индекс чётный
  //     → ожидаем '0' по шаблону A.  
  //
  //   - s[0] = '0'
  //     → совпадение → cnt = 1
  //
  // 
  // i = 1
  //   - индекс нечётный
  //     → ожидаем '1' по шаблону A.  
  //
  //   - s[1] = '1'
  //     → совпадение → cnt = 2
  //
  // 
  // i = 2
  //   - индекс чётный
  //     → ожидаем '0' по шаблону A.  
  //
  //   - s[2] = '0'
  //     → совпадение → cnt = 3
  // 
  //
  // i = 3
  //   - индекс нечётный 
  //     → ожидаем '1' по шаблону A.  
  //
  //   - s[3] = '0'
  //     → не совпадает
  //     → cnt остаётся 3.
  //
  // ---
  // 
  // Итоговые значения
  // 
  // - cnt = 3 — число позиций, уже
  //   соответствующих шаблону A ("0,1,0,1").
  //
  // - n - cnt = 4 - 3 = 1 — число позиций,
  //   соответствующих шаблону B ("1,0,1,0"),
  //   или эквивалентно числу замен, чтобы
  //   получить шаблон A.
  //
  // - Math.min(cnt, n - cnt)
  //   = Math.min(3, 1) = 1
  //
  // 
  // Вывод функции для s = "0100":
  // 1 — требуется одна замена, чтобы
  //     получить чередующуюся строку
  //     (в данном случае заменить последний
  //     символ на '1', получив "0101").
}
