/**
 * @param {number[]} nums
 * @return {number}
 *
 * minimumDistance принимает массив чисел nums, и возвращает
 * минимальную суммарную попарную дистанцию между тремя
 * индексами, в которых встречается одно и то же значение.
 * Если таких троек нет, возвращает -1.
 *
 *
 * Пример для:
 *   nums = [1,2,1,1,3]
 * Результат:
 *   6
 * Объяснение:
 *   Минимальное расстояние достигается хорошей тройкой
 *   (0, 2, 3).
 *
 *   (0, 2, 3) - хорошая тройка, потому что
 *   nums[0] == nums[2] == nums[3] == 1.
 *
 *   Её расстояние равно
 *   abs(0 - 2) + abs(2 - 3) + abs(3 - 0) = 2 + 1 + 3 = 6.
 */
function minimumDistance(nums) {

  // Создаём массив фиксированной длины 101 для сопоставления
  // значения числа (индекс) → список индексов его появления
  // в nums.
  //
  // Элементы по умолчанию undefined. Мы будем
  // инициализировать только те слоты, которые встречаются в
  // nums.
  //
  // Используем длину 101, так как значения в nums лежат в
  // диапазоне 1..100. Элемент 0 остаётся неиспользуемым.
  //
  // Использование массива вместо Map ускоряет работу кода и
  // экономит память, при условии, что числа находятся в
  // малом диапазоне значений
  const numToSeenIndices = new Array(101);

  // Цикл по индексам входного массива nums
  for (let i = 0; i < nums.length; i++) {

    // Получаем значение элемента массива в позиции i
    const num = nums[i];

    // Если для данного значения ещё не создан массив индексов
    // (undefined или falsy), создаём новый пустой массив.
    //
    // Это отложенное создание списков экономит память для
    // значений, которые не встречаются. И позволяет избежать
    // дополнительного прохода по массиву numToSeenIndices для
    // заполнения его значений пустыми массивами
    if (!numToSeenIndices[num]) {
      numToSeenIndices[num] = [];
    }

    // Добавляем текущий индекс i в массив индексов для числа
    // num.
    numToSeenIndices[num].push(i);
  }

  // Инициализируем minDistance бесконечностью, чтобы
  // корректно сравнивать с первыми найденными значениями.
  //
  // Infinity служит маркером "ничего не найдено" до тех пор,
  // пока не встретится первая тройка индексов.
  let minDistance = Infinity;

  // Перебираем возможные значения чисел от 1 до 100
  // включительно.
  for (let num = 1; num < 101; num++) {

    // Получаем массив индексов, где встречается текущее
    // значение num.
    //
    // Если значение не встречалось, indices будет undefined.
    const indices = numToSeenIndices[num];

    // Пропускаем значение, если массив отсутствует (значение
    // не встречалось) или в нём меньше трёх элементов
    if (
      !indices
      || indices.length < 3
    ) continue;

    // Если мы здесь - для текущего значения есть по крайней
    // мере три вхождения.
    //
    // Перебираем все возможные тройки подряд идущих
    // вхождений в списке indices.
    //
    // Используем indices.length - 2, чтобы i+2 оставался в
    // пределах массива indices.
    for (let i = 0; i < indices.length - 2; i++) {

      // Первый индекс тройки (левый).
      const a = indices[i];

      // Второй индекс тройки (средний).
      const b = indices[i + 1];

      // Третий индекс тройки (правый).
      const c = indices[i + 2];

      // Вычисляем суммарную "дистанцию" между тремя индексами
      // как сумму абсолютных разниц между каждой парой.
      //
      // Формула: |a-b| + |b-c| + |c-a|.
      //
      // Для трёх точек на числовой прямой эта сумма равна
      // 2 * (c - a), но здесь явно вычисляются абсолютные
      // разности для ясности.
      const currDistance =
        Math.abs(a - b)
        + Math.abs(b - c)
        + Math.abs(c - a);

      // Обновляем глобальный минимум, если текущая сумма
      // меньше.
      minDistance = Math.min(minDistance, currDistance);
    }
  }

  // Если minDistance остался Infinity, значит не найдено ни
  // одной тройки одинаковых значений - возвращаем -1.
  //
  // Иначе возвращаем найденную минимальную суммарную
  // попарную дистанцию.
  return minDistance === Infinity ? -1 : minDistance;
}
