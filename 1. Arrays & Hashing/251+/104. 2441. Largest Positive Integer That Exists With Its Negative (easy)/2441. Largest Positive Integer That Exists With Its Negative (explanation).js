/**
 * @param {number[]} nums
 * @return {number}
 *
 * findMaxK принимает один параметр - массив чисел nums.
 * Ищет максимальное число k в массиве nums, для которого
 * одновременно присутствуют k и -k, и возвращает это
 * число или -1, если таких пар нет.
 *
 *
 * Пример для:
 *   nums = [-1,2,-3,3]
 * Результат:
 *   3
 * Пояснение:
 *   3 - единственное значение k, для которого в массиве
 *   присутствуют и k, и -k.
 */
function findMaxK(nums) {

  // Создаём Set из элементов массива для быстрого поиска
  // наличия противоположного числа.
  //
  // Set удаляет дубликаты и обеспечивает среднее время
  // поиска O(1)
  const numsSet = new Set(nums);

  // Инициализируем переменную max значением -1.
  //
  // -1 здесь используется как специальное значение,
  // означающее "пока ничего не найдено".
  let max = -1;

  // Проходим по каждому элементу исходного массива в
  // порядке их следования.
  for (const num of nums) {

    // Проверяем, существует ли в множестве
    // противоположное число -num.
    //
    // Если противоположного числа нет, пропускаем
    // текущую итерацию и переходим к следующему
    // элементу.
    if (!numsSet.has(-num)) continue;

    // Если противоположное число найдено, обновляем max:
    // выбираем наибольшее из текущего max и num.
    max = Math.max(max, num);
  }

  // Возвращаем найденный максимум.
  //
  // Если ни одной пары k и -k не найдено, вернётся
  // исходное значение -1.
  return max;
}
