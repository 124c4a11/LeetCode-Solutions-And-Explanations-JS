/**
 * @param {number[]} nums
 * @return {number}
 *
 * firstUniqueEven массив целых чисел nums и возвращает первый
 * по порядку чётный элемент массива, который встречается
 * ровно один раз. Если такого элемента нет - возвращает -1.
 *
 *
 * Пример для:
 *   nums = [3,4,2,5,4,6]
 * Результат:
 *   2
 * Пояснение:
 *   Числа 2 и 6 - чётные и встречаются ровно один раз.
 *   Поскольку 2 встречается раньше в массиве, ответ равен 2.
 */
function firstUniqueEven(nums) {

  // Создаём массив фиксированной длины 101 типа Int32Array
  // для подсчёта частот значений.
  //
  // Int32Array экономит память и даёт быстрый доступ по
  // индексу.
  //
  // Выбран размер 101, так как все значения nums находятся в
  // диапазоне 0..100 включительно.
  const numToFreq = new Int32Array(101);

  // Проходим по каждому значению из входного массива nums.
  //
  // Для каждого значения увеличиваем соответствующий счётчик
  // в numToFreq.
  for (const num of nums) numToFreq[num]++;

  // Второй проход по входному массиву:
  //
  // Ищем первое число, которое одновременно чётное и
  // уникально (встречается ровно один раз).
  //
  // Повторный проход нужен, чтобы сохранить порядок появления
  // элементов в исходном массиве.
  for (const num of nums) {

    // Проверяем два условия одновременно:
    // 1. (num & 1) === 0 - битовая операция проверяет, чётное
    //    ли число (младший бит равен 0). (num & 1) даёт 0 для
    //    чётных и 1 для нечётных.
    //
    // 2. numToFreq[num] === 1 - число встречается ровно один
    //    раз в массиве nums.
    //
    // Если оба условия истинны, возвращаем это число как
    // результат.
    if (
      (num & 1) === 0
      && numToFreq[num] === 1
    ) return num;
  }

  // Если ни один элемент не прошёл обе проверки (чётность и
  // уникальность), возвращаем -1.
  return -1;
}
