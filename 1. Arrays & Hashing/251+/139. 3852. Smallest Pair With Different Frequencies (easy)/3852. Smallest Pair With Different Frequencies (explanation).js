/**
 * @param {number[]} nums
 * @return {number[]}
 *
 * minDistinctFreqPair принимает массив целых чисел nums и ищет
 * первую в порядке возрастания пару различных чисел, у которых
 * частоты встречаемости отличаются. Функция возвращает первую
 * найденную такую пару в виде массива
 * [первоеЧисло, второеЧисло]. Если подходящей пары нет,
 * возвращается [-1, -1]
 *
 *
 * Пример для:
 *   nums = [1,1,2,2,3,4]
 * Результат:
 *   [1,3]
 * Пояснение:
 *   Наименьшее значение равно 1, его частота появления в
 *   массиве - 2.
 *
 *   Следующее наименьшее значение, большее чем 1, у которого
 *   частота отличается от частоты 1 - это 3, его частота
 *   равна 1.
 *
 *   Следовательно, ответ - [1, 3].
 */
function minDistinctFreqPair(nums) {

  // Создаётся типизированный массив Int32Array длиной 101 для
  // хранения счётчиков частот вхождения чисел в nums.
  //
  // Каждый элемент инициализируется нулём автоматически.
  //
  // Индекс массива соответствует значению числа.
  // Значение - частота вхождения этого числа в nums.
  //
  // Используется длина 101, так как значения nums лежат в
  // диапазоне 1..100.
  const numToFreq = new Int32Array(101);

  // Проход по всем элементам входного массива nums
  //
  // Для каждого значения num увеличиваем соответствующий
  // счётчик в массиве numToFreq.
  for (const num of nums) numToFreq[num]++;

  // Переменная x используется для хранения первого найденного
  // числа с ненулевой частотой.
  //
  // Изначально x = 0 служит маркером "пока не найдено", так
  // как возможное минимальное значение в nums равно 1
  let x = 0;

  // Проход по всем возможным числам от 1 до 100 включительно.
  for (let num = 1; num < 101; num++) {

    // Если частота текущего числа равна нулю, значит это число
    // не встречается в входном массиве - пропускаем его.
    if (numToFreq[num] === 0) continue;

    // Если ещё не найдено первое число (x === 0), сохраняем
    // текущее число как первое найденное.
    //
    // Таким образом x хранит минимальное (по значению) число
    // из nums, которое встречается хотя бы один раз, потому
    // что мы идём по возрастанию num.
    if (x === 0) {
      x = num;

      // Если x уже установлен, сравниваем частоты текущего
      // числа и x.
      //
      // Если частоты различаются, возвращаем пару [x, num].
      //
      // Возвращаем именно первую найденную пару в порядке
      // обхода по возрастанию значений чисел:
      // первое - минимальное найденное число,
      // второе - текущее число, у которого частота отличается.
    } else if (numToFreq[x] !== numToFreq[num]) {
      return [x, num];
    }

    // Если частоты равны, продолжаем поиск дальше, не меняя x.
    //
    // Это означает, что x остаётся первым найденным числом, и
    // мы ищем следующее число с отличной частотой.
  }

  // Если цикл завершился без нахождения двух чисел с разными
  // частотами, возвращаем [-1, -1].
  return [-1, -1];
}
