/**
 * @param {number[]} arr
 * @return {number[]}
 *
 * arrayRankTransform преобразует каждый элемент входного массива
 * в его порядковый ранг среди уникальных значений (наименьшему — 1,
 * следующему уникальному — 2 и т.д.).
 *
 * Возвращает новый массив рангов в том же порядке, что и исходный
 * массив.
 *
 *
 * Пример для:
 *   arr = [40,10,20,30]
 * Результат:
 *   [4,1,2,3]
 * Пояснение:
 *   40 — наибольший элемент;
 *   10 — наименьший;
 *   20 — второй по возрастанию (второй наименьший);
 *   30 — третий по возрастанию (третий наименьший).
 */
function arrayRankTransform(arr) {
  const n = arr.length;

  // Создаём Set из arr, чтобы удалить все повторяющиеся значения
  //
  // Затем с помощью оператора spread разворачиваем Set обратно в
  // массив.
  //
  // После этого вызываем метод sort с числовым компаратором
  // (a - b), чтобы отсортировать уникальные значения по
  // возрастанию.
  const sortedUniqueNums = [...new Set(arr)].sort((a, b) => a - b);

  // Создаём Map для сопоставления каждого уникального числа с его
  // рангом.
  const numToRank = new Map();

  // Инициализируем счётчик ранга значением 1, чтобы первый
  // (наименьший) уникальный элемент получил ранг 1.
  let currRank = 1;

  // Проходим по каждому элементу отсортированного массива
  // уникальных значений в порядке возрастания.
  //
  // Порядок важен: ранги присваиваются на основе позиции в
  // отсортированном списке уникальных значений.
  for (const num of sortedUniqueNums) {

    // Для текущего уникального числа num записываем в Map
    // соответствие num → currRank.
    numToRank.set(num, currRank);

    // Увеличиваем счётчик ранга для следующего уникального числа.
    currRank++;
  }

  // Создаём пустой массив, в который будем помещать ранги в том же
  // порядке, что и элементы исходного arr.
  //
  // Это гарантирует, что возвращаемый массив имеет ту же длину и
  // порядок, что и arr.
  const ranks = new Array(n);

  // Проходим по каждому элементу исходного массива arr в исходном
  // порядке.
  //
  // Для каждого элемента получаем его ранг из Map и добавляем в
  // результирующий массив.
  for (let i = 0; i < n; i++) {
    ranks[i] = numToRank.get(arr[i]);
  }

  // Возвращаем массив рангов, соответствующий каждому элементу
  // исходного массива arr.
  return ranks;
}
