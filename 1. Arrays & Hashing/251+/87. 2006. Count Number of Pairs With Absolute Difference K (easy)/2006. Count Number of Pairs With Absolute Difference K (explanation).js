/**
 * @param {number[]} nums
 * @param {number} k
 * @return {number}
 *
 * countKDifference принимает целочисленный массив nums и целое число k.
 *
 * Возвращает количество пар (i, j) таких, что
 *i < j и |nums[i] - nums[j]| === k.
 *
 * Значение |x| определяется следующим образом:
 *  x, если x >= 0.
 * -x, если x < 0.
 *
 *
 * Пример для:
 *   nums = [1,2,2,1], k = 1
 * Результат:
 *   4
 * Пояснение: Пары с абсолютной разницей 1:
 *   - [1,2,2,1]
 *      ^ ^
 *
 *   - [1,2,2,1]
 *      ^   ^
 *
 *   - [1,2,2,1]
 *        ^   ^
 *
 *   - [1,2,2,1]
 *          ^ ^
 */
function countKDifference(nums, k) {

  // Инициализация счётчика найденных пар.
  //
  // Переменная хранит итоговое количество пар, удовлетворяющих условию
  // |nums[i] - nums[j]| = k.
  let pairsCnt = 0;

  // Создание Map для хранения частот ранее встреченных значений из массива.
  //
  // Ключ — значение из массива,
  // Значение — сколько раз это значение встретилось ранее.
  const numToFreq = new Map();

  // Итерация по каждому элементу массива nums в порядке обхода
  // (слева направо).
  for (const num of nums) {

    // Для текущего элемента num мы хотим посчитать, сколько ранее
    // встреченных элементов дают разницу ровно k с текущим:
    // это числа num - k и num + k.
    //
    // numToFreq.get(num - k) возвращает количество ранее встреченных
    // элементов со значением num - k, либо undefined, если таких не было.
    // Оператор ?? 0 заменяет undefined на 0.
    //
    // Аналогично для num + k. Сумма этих двух частот - число новых пар, где
    // текущий элемент выступает вторым элементом пары (пары считаются один
    // раз при появлении второго элемента).
    //
    // Это корректно для подсчёта упорядоченных пар i < j, потому что мы
    // учитываем только ранее встреченные элементы.
    //
    // Мы проверяем num - k и num + k, потому что условие
    // |nums[i] - nums[j]| = k эквивалентно двум уравнениям
    // (nums[i] - nums[j] = +- k), то есть для текущего элемента num
    // подходящие ранее встреченные значения равны num - k или num + k. Это
    // даёт все пары с i < j, если при подсчёте учитываются только элементы,
    // встреченные до текущего индекса.
    pairsCnt += (numToFreq.get(num - k) ?? 0) + (numToFreq.get(num + k) ?? 0);

    // Увеличиваем счётчик вхождений текущего числа num на 1.
    //
    // Если ключа ещё не было, numToFreq.get(num) вернёт undefined, оператор
    // ?? 0 заменит на 0, затем прибавим 1.
    //
    // После set текущее число будет учитываться при последующих итерациях
    // как "ранее встреченное".
    numToFreq.set(num, (numToFreq.get(num) ?? 0) + 1);
  }

  // Возвращаем итоговое количество найденных пар.
  return pairsCnt;
}
