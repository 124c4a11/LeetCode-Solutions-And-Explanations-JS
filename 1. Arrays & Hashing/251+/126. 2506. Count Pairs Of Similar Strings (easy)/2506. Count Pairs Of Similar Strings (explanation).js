/**
 * @param {string[]} words
 * @return {number}
 *
 * similarPairs принимает массив строк words и возвращает
 * количество пар слов в массиве, которые имеют одинаковое
 * множество уникальных букв. Две строки считаются похожими,
 * если набор их уникальных символов совпадает независимо
 * от порядка и повторов букв.
 *
 *
 * Пример для:
 *   words = ["aba","aabb","abcd","bac","aabc"]
 * Результат:
 *   2
 * Пояснение:
 * Есть 2 пары, удовлетворяющие условию:
 * - i = 0 и j = 1 : слова words[0] и words[1] содержат
 *   только символы 'a' и 'b'.
 *
 * - i = 3 и j = 4 : слова words[3] и words[4] содержат
 *   только символы 'a', 'b' и 'c'.
 */
function similarPairs(words) {

  // Ключевая идея - понять, что две строки считаются
  // похожими, если у них одинаковое множество уникальных
  // символов. Это означает, что нам нужен способ
  // представить "набор символов" каждой строки в форме,
  // удобной для сравнения.
  //
  // Сначала можно подумать о том, чтобы для каждого слова
  // создать отсортированную строку уникальных символов
  // (например, преобразовать "abca" в "abc"). Однако это
  // требует операций со строками и сортировки, что может
  // быть неэффективно.
  //
  // Более элегантный подход возникает, когда мы замечаем,
  // что:
  // - существует всего 26 строчных букв;
  //
  // - для определения похожести важны только факты
  //   присутствия букв, а не их количество или порядок;
  //
  // - по сути, это задача проверки принадлежности к
  //   множеству.
  //
  // Это естественно приводит к идее битовой манипуляции.
  // Можно использовать 26-битное целое, где каждый бит
  // обозначает наличие конкретной буквы в строке. Например:
  // - бит 0 соответствует 'a';
  // - бит 1 соответствует 'b';
  // - бит 25 соответствует 'z'.
  //
  // Тогда строка "abc" будет представлена как ...0000111
  // (установлены три крайних бита), и "cab" получит ту же
  // маску.
  //
  // Имея такое представление, подсчёт похожих пар
  // становится простым. Обрабатывая каждую строку:
  // - преобразовать её в битовую маску;
  //
  // - проверить, сколько ранее обработанных строк имеют ту
  //   же маску (они образуют пары с текущей строкой);
  //
  // - добавить это число к ответу;
  //
  // - обновить счётчик для этой маски.
  //
  // Такой подход эффективен, потому что:
  // - преобразование строки в битовую маску занимает
  //   O(длина строки});
  //
  // - сравнение двух битовых масок - это сравнение двух
  //   целых чисел, O(1);
  //
  // - требуется всего один проход по массиву.


  // Инициализация счётчика найденных похожих пар.
  let pairsCnt = 0;

  // Создаём Map для хранения соответствия между битовой
  // маской множества букв и числом слов с этой маской.
  //
  // Ключ - целое число (битовая маска),
  // Значение - количество встреченных слов с такой маской.
  const bitmaskToFreq = new Map();

  // Проходим по каждому слову из входного массива words.
  for (const word of words) {

    // Для текущего слова инициализируем битовую маску нулём.
    //
    // В этой маске каждый бит будет означать присутствие
    // соответствующей буквы (a..z).
    let wordBitmask = 0;

    // Проходим по каждому символу (букве) в текущем слове.
    for (const char of word) {

      // Получаем числовой код символа и вычитаем 97
      // ('a' в ASCII/UTF-16) чтобы получить индекс от 0 до
      // 25.
      //
      // Затем сдвигаем единицу влево на этот индекс,
      // получая маску для конкретной буквы.
      //
      // Операция |= объединяет эту маску с уже накопленной,
      // гарантируя, что каждый символ устанавливает
      // соответствующий бит.
      //
      // Повторные буквы не меняют маску, потому что
      // установка одного и того же бита несколько раз даёт
      // тот же результат.
      wordBitmask |= 1 << (char.charCodeAt(0) - 97);
    }

    // Получаем количество ранее встреченных слов с такой же
    // битовой маской.
    //
    // Если в Map нет записи для этой маски, оператор
    // nullish coalescing (??) возвращает 0.
    const prevCnt = bitmaskToFreq.get(wordBitmask) ?? 0;

    // Все ранее встреченные слова с той же маской образуют
    // пары с текущим словом.
    //
    // Добавляем это количество к общему счётчику пар.
    //
    // Таким образом, если уже было N слов с такой маской,
    // текущее слово добавит N новых пар.
    pairsCnt += prevCnt;

    // Обновляем Map: увеличиваем частоту для этой маски на
    // 1, фиксируя, что текущее слово теперь учтено.
    bitmaskToFreq.set(wordBitmask, prevCnt + 1);
  }

  // Возвращаем итоговое количество похожих пар слов.
  return pairsCnt;
}
