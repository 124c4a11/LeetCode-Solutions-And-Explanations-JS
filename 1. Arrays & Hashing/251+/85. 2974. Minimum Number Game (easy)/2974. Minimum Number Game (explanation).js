/**
 * @param {number[]} nums
 * @return {number[]}
 *
 * numberGame принимает целочисленный массив nums с
 * нулевой индексацией и чётной длиной.
 *
 * Алиса и Боб решили сыграть в игру, в которой в
 * каждом раунде Алиса и Боб выполняют по одному ходу.
 *
 * Правила игры следующие:
 * - В каждом раунде сначала Алиса удаляет минимальный
 *   элемент из nums, затем Боб делает то же самое.
 *
 * - Далее сначала Боб добавляет удалённый элемент в
 *   массив arr, затем Алиса делает то же самое.
 *
 * -  Игра продолжается до тех пор, пока nums не
 *    станет пустым.
 *
 * Возвращает получившийся массив arr.
 *
 * numberGame сортирует переданный массив чисел по
 * возрастанию, затем попарно меняет местами соседние
 * элементы (0↔1, 2↔3 и т.д.), возвращая тот же
 * (мутированный) массив.
 *
 *
 * Пример для:
 *   nums = [5,4,2,3]
 * Результат:
 *   [3,2,5,4]
 * Пояснение:
 *   В первом раунде сначала Алиса удаляет 2, затем
 *   Боб удаляет 3.
 *
 *   Далее в массив arr сначала Боб добавляет 3, затем
 *   Алиса добавляет 2.
 *
 *   В результате arr = [3,2].
 *
 *   В начале второго раунда nums = [5,4].
 *
 *   Теперь сначала Алиса удаляет 4, затем Боб удаляет
 *   5.
 *
 *   Оба добавляют свои элементы в arr, и он
 *   становится [3,2,5,4].
 */
function numberGame(nums) {

  // Сортирует массив nums по возрастанию с числовым
  // компаратором (a - b).
  //
  // После сортировки элементы упорядочены от
  // наименьшего к наибольшему.
  //
  // Это моделирует последовательность удаления
  // минимальных элементов: если мы возьмём пары
  // соседних элементов в отсортированном массиве, то
  // в каждой паре первый - меньший, второй - больший.
  //
  // По правилам игры в одной итерации сначала Алиса
  // удаляет минимальный (меньший), затем Боб удаляет
  // следующий минимальный (больший из двух), а в
  // массив arr сначала добавляет Боб (больший), затем
  // Алиса (меньший). Поэтому перестановка соседних
  // элементов в отсортированном массиве
  // (меняя местами меньший и больший в каждой паре)
  // даёт искомый порядок arr.
  nums.sort((a, b) => a - b);

  // Инициализация индекса i нулём.
  //
  // Он будет указывать на первый элемент текущей пары
  // в отсортированном массиве.
  //
  // Перебор будет идти по непересекающимся парам:
  // (0,1), (2,3), ...
  let i = 0;

  // Цикл выполняется, пока существует правый сосед
  // для текущего индекса i.
  //
  // Условие i < nums.length - 1 гарантирует, что
  // обращение к nums[i + 1] безопасно.
  //
  // Поскольку длина nums чётная, цикл пройдёт по
  // всем элементам, обрабатывая их парами.
  while (i < nums.length - 1) {

    // Сохраняет значение текущего (левого) элемента
    // пары во временную переменную tmp.
    //
    // В отсортированном массиве это меньший элемент
    // в паре.
    const tmp = nums[i];

    // На место левого элемента записывается значение
    // правого элемента пары.
    //
    // То есть меньший элемент заменяется большим -
    // это первая часть обмена, в результате чего в
    // позиции i окажется элемент, соответствующий
    // ходу Боба (больший).
    nums[i] = nums[i + 1];

    // В правую позицию записывается сохранённый ранее
    // меньший элемент.
    //
    // Это завершает обмен местами двух соседних
    // элементов: теперь пара (меньший, больший) стала
    // (больший, меньший), что соответствует порядку
    // добавления в arr: сначала Боб, затем Алиса.
    nums[i + 1] = tmp;

    // Переход к следующей паре:
    //
    // Увеличиваем i на 2, чтобы не затрагивать уже
    // обработанные элементы.
    //
    // Это гарантирует, что каждая пара элементов
    // будет обменена ровно один раз.
    i += 2;
  }

  // Возвращает модифицированный массив nums.
  //
  // Поскольку преобразования выполнены in-place,
  // возвращаемый массив уже соответствует искомому
  // arr: в нём элементы упорядочены в том порядке, в
  // котором они были бы добавлены в arr при симуляции
  // игры (для каждой пары: Боб, затем Алиса).
  return nums;
}
