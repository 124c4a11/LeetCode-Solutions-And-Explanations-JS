/**
 * @param {number[][]} points
 * @return {number}
 *
 * maxWidthOfVerticalArea вычисляет максимальную ширину
 * вертикальной области между любыми двумя точками на
 * плоскости. Для этого она сначала сортирует точки по их
 * x‑координате, а затем находит наибольшую разницу между
 * соседними x‑координатами в отсортированном массиве.
 *
 * Идея:
 * Максимальная "пустая" вертикальная полоса между
 * точками будет между двумя соседними по x точками после
 * сортировки.
 */
function maxWidthOfVerticalArea(points) {

  // Сортируем массив points по возрастанию координаты x.
  points.sort((a, b) => a[0] - b[0]);

  // Инициализация переменной для хранения текущего
  // максимального расстояния по оси x между соседними
  // точками.
  //
  // Начальное значение 0 корректно для случаев, когда
  // точек меньше двух - в этом случае цикл ниже не
  // выполнится.
  let maxVerticalArea = 0;

  // Проходим по отсортированному массиву, начиная с
  // индекса 1, чтобы сравнивать каждую точку с
  // предыдущей.
  //
  // Цикл гарантированно рассматривает только пары
  // соседних по x точек, что достаточно после сортировки.
  for (let i = 1; i < points.length; i++) {

    // Обновляем maxVerticalArea, выбирая максимум между
    // текущим значением и разницей x соседних точек.
    maxVerticalArea = Math.max(
      maxVerticalArea,
      points[i][0] - points[i - 1][0]
    );
  }

  // Возвращаем вычисленную максимальную ширину
  // вертикальной области.
  return maxVerticalArea;
}
