/**
 * @param {number[]} nums
 * @return {number}
 *
 * unequalTriplets принимает массив чисел и возвращает количество
 * троек индексов (i < j < k) в массиве, у которых значения
 * попарно различны.
 *
 *
 * Пример для:
 *   nums = [4,4,2,4,3]
 * Результат:
 *   3
 * Пояснение:
 *   Следующие тройки удовлетворяют условиям:
 *   - (0, 2, 4) потому что 4 != 2 != 3
 *   - (1, 2, 4) потому что 4 != 2 != 3
 *   - (2, 3, 4) потому что 2 != 4 != 3
 *
 *   Так как таких троек 3, возвращается 3.
 *
 *   Обратите внимание, что (2, 0, 4) не является допустимой
 *   тройкой, потому что 2 > 0 (индексы должны удовлетворять
 *   i < j < k).
 */
function unequalTriplets(nums) {

  // Создаём объект Map для хранения частоты (количества
  // вхождений) каждого уникального значения из входного массива.
  const numToFreq = new Map();

  // Итерация по каждому элементу входного массива nums.
  for (const num of nums) {

    // Для текущего значения num получаем уже накопленную частоту
    // из Map.
    //
    // Если значение отсутствует, выражение numToFreq.get(num)
    // вернёт undefined, оператор ?? заменит его на 0.
    //
    // Затем к полученному числу прибавляем 1 и записываем
    // обратно в Map.
    //
    // Таким образом на выходе получаем корректную частоту
    // каждого уникального числа.
    numToFreq.set(num, (numToFreq.get(num) ?? 0) + 1);
  }

  // Инициализируем переменную tripletsCnt нулём — в неё будем
  // аккумулировать итоговое количество троек.
  let tripletsCnt = 0;

  // leftElementsCnt будет хранить суммарное количество
  // элементов, относящихся к уже обработанным уникальным
  // значениям.
  //
  // Изначально слева ничего нет, поэтому 0.
  let leftElementsCnt = 0;

  // rightElementsCnt хранит количество элементов, которые ещё не
  // обработаны.
  //
  // Изначально это весь массив, поэтому присваиваем длину nums.
  let rightElementsCnt = nums.length;

  // Перебираем пары [ключ, значение] из Map numToFreq.
  //
  // pair — массив из двух элементов: [num, freq].
  //
  // Каждая пара соответствует одному уникальному значению из
  // массива и его частоте.
  //
  // Порядок итерации по Map соответствует порядку вставки
  // уникальных ключей.
  //
  // Для ускорения работы кода не используем деструктуризацию
  // массива pair в цикле, а обращаемся к его элементам по
  // индексам
  for (const pair of numToFreq) {

    // Извлекаем частоту текущего значения num
    //
    // pair[0] — само значение num,
    // pair[1] — его частота.
    const freq = pair[1];

    // Перед расчётом комбинаций уменьшаем rightElementsCnt на
    // freq.
    //
    // Это отражает перенос текущего уникального значения из
    // правой (необработанной) части в "текущий" блок, чтобы при
    // вычислении комбинаций rightElementsCnt соответствовал
    // количеству элементов строго правее текущего уникального
    // значения.
    rightElementsCnt -= freq;

    // Добавляем к tripletsCnt количество троек, в которых:
    // - один элемент берётся из уже обработанных уникальных
    //   значений (leftElementsCnt),
    //
    // - один элемент берётся из текущего уникального значения
    //   (freq),
    //
    // - один элемент берётся из ещё необработанных уникальных
    //   значений (rightElementsCnt).
    //
    // Произведение leftElementsCnt * freq * rightElementsCnt
    // даёт число троек с тремя разными значениями, где
    // центральную роль в расчёте играет текущее уникальное
    // значение.
    //
    // Такой подход учитывает все возможные перестановки индексов
    // i < j < k, при условии что мы рассматриваем комбинации по
    // группам одинаковых значений, а не по отдельным индексам.
    tripletsCnt += leftElementsCnt * freq * rightElementsCnt;

    // После учёта комбинаций переносим все элементы текущего
    // уникального значения в левую часть, увеличивая
    // leftElementsCnt на freq.
    //
    // Это подготовит корректные значения для следующей итерации.
    leftElementsCnt += freq;
  }

  // Возвращаем итоговое количество троек с попарно различными
  // значениями.
  return tripletsCnt;
}
