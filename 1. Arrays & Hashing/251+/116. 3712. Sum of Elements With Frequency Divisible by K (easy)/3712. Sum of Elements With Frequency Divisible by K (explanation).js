/**
 * @param {number[]} nums
 * @param {number} k
 * @return {number}
 *
 * sumDivisibleByK массив целых чисел nums и целое число k.
 *
 * Возвращает целое число, равное сумме всех элементов массива
 * nums, частота которых делится на k без остатка, или 0, если
 * таких элементов нет.
 *
 * Примечание:
 * Элемент включается в сумму ровно столько раз, сколько он
 * встречается в массиве, если его общая частота делится на k.
 *
 *
 * Премер для:
 *   nums = [1,2,2,3,3,3,3,4],
 *   k = 2
 * Результат:
 *   16
 * Пояснение:
 * - Число 1 встречается 1 раз (нечётная частота).
 * - Число 2 встречается 2 раза (чётная частота).
 * - Число 3 встречается 4 раза (чётная частота).
 * - Число 4 встречается 1 раз (нечётная частота).
 * Поэтому суммарная сумма равна 2 + 2 + 3 + 3 + 3 + 3 = 16.
 */
function sumDivisibleByK(nums, k) {

  // Создаём статически типизированный массив Int32Array
  // длиной 101 для хранения частот встречаемости значений
  // 0..100.
  //
  // Индекс - значению числа
  // Значение - количество его вхождений
  //
  // Использование Int32Array экономит память и ускоряет
  // операции с целыми числами
  //
  // Используем длину 101, так как значения в nums находятся в
  // диапазоне от 1 до 100
  const numToFreq = new Int32Array(101);

  // Итерация по входному массиву nums
  //
  // Для каждого значения num увеличиваем соответствующий
  // счётчик в numToFreq.
  for (const num of nums) numToFreq[num]++;

  // Инициализируем аккумулятор итоговой суммы.
  //
  // В него будем добавлять вклад только тех значений, частота
  // которых делится на k
  let sumForDivisibleByK = 0;

  // Перебираем все возможные значения индексов от 1 до 100
  // включительно, чтобы проверить частоту каждого возможного
  // числа
  //
  // Начинаем с num = 1, так как возможные значения num
  // находятся в диапазоне от 1 до 100
  for (let num = 1; num < 101; num++) {

    // Считывает частоту (количество вхождений) текущего числа
    // num из массива частот.
    const freq = numToFreq[num];

    // Пропускаем текущую итерацию, если:
    // 1. freq равна 0 - число не встречалось в nums, или
    //
    // 2. freq % k !== 0 - частота не кратна k (остаток от
    //    деления ненулевой).
    //
    // Оператор !freq эквивалентен freq === 0 для целых чисел.
    //
    // Проверка freq % k использует остаток от деления.
    //
    // Это условие экономит дальнейшие вычисления и исключает
    // добавление некорректного вклада в сумму.
    if (!freq || freq % k) continue;

    // Если частота ненулевая и кратна k, добавляет в
    // аккумулятор произведение значения числа на его частоту.
    //
    // Умножение num * freq соответствует суммированию всех
    // экземпляров числа num
    sumForDivisibleByK += num * freq;
  }

  // Возвращаем накопленную сумму.
  //
  // Если ни одно значение не удовлетворяло условию, будет
  // возвращён 0.
  //
  // Временная сложность функции - O(n + 101) по входному
  // массиву nums, где n - длина nums. память - O(101) для
  // массива частот.
  return sumForDivisibleByK;
}
