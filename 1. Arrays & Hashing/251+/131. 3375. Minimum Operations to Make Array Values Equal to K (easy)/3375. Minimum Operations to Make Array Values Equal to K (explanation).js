/**
 * @param {number[]} nums
 * @param {number} k
 * @return {number}
 *
 * minOperations получает на вход целочисленный массив nums
 * и целое число k.
 *
 * Параметры:
 * - nums - массив целых чисел;
 * - k - целое число, к которому нужно привести все
 *   элементы массива.
 *
 * Функция возвращает минимальное количество операций,
 * необходимых, чтобы все элементы nums стали равны k.
 * Если это невозможно, функция возвращает -1.
 *
 * Условие задачи:
 * Целое число h называется допустимым, если все значения в
 * массиве, строго большие h, одинаковы.
 *
 * Например, для nums = [10, 8, 10, 8] допустимым является
 * h = 9, потому что все nums[i] > 9 равны 10, а 5 не
 * является допустимым.
 *
 * Разрешается выполнять следующую операцию над nums:
 * 1. Выбрать целое число h, которое допустимо для текущих
 *    значений в nums.
 *
 * 2. Для каждого индекса i, где nums[i] > h, присвоить
 *    nums[i] значение h.
 *
 *
 * Пример для:
 *   nums = [5,2,5,4,5],
 *   k = 2
 * Выход:
 *   2
 * Пояснение:
 * Операции можно выполнить в следующем порядке: сначала
 * выбрать допустимое целое h = 4 и применить операцию,
 * затем выбрать допустимое целое h = 2 и применить
 * операцию - в результате все элементы станут равны k = 2.
 *
 *
 * Пример для:
 *   nums = [2,1,2],
 *   k = 2
 * Результат:
 *   -1
 * Пояснение:
 * Невозможно сделать все значения равными 2, поскольку
 * присутствует элемент, меньший чем k, что делает задачу
 * невыполнимой.
 */
function minOperations(nums, k) {

  // Когда мы выбираем допустимое значение h и применяем
  // операцию, все значения, строго большие h, становятся
  // равными h. Это означает, что мы фактически
  // "выравниваем" массив сверху вниз.
  //
  // Рассмотрим массив [10, 8, 10, 8]. Если выбрать h = 9,
  // все значения больше 9 (то есть 10) превратятся в 9, в
  // результате получится [9, 8, 9, 8]. Теперь у нас новый
  // массив с на одну уникальную величину выше целевого
  // меньше.
  //
  // Ключевое наблюдение: какое допустимое h наиболее
  // эффективно выбирать в каждой операции?
  //
  // Если у нас есть различные значения, например
  // [12, 10, 8, 5], и мы хотим получить k = 5, мы могли
  // бы делать так:
  // - Выбрать h = 11, чтобы 12 → 11, получив [11, 10, 8, 5]
  //
  // - Затем выбрать h = 10, чтобы 11 → 10, получив
  //   [10, 10, 8, 5]
  //
  // - И так далее...
  // Но это неэффективно.
  //
  // Вместо этого следует в первой операции выбрать h
  // равным второму по величине значению (второму максимуму).
  // Если выбрать h = 10 сразу, 12 → 10, и мы мгновенно
  // устраняем одну уникальную величину.
  //
  // Отсюда вывод: в каждой операции мы можем устранить
  // ровно одно уникальное значение, строго большее k,
  // выбирая в качестве h второе по величине значение.
  // Наибольшее значение "схлопывается" до второго по
  // величине.
  //
  // Следовательно, минимальное число операций равно
  // количеству уникальных значений, строго больших k.
  // Для отслеживания уникальных значений удобно
  // использовать Set; если k уже присутствует в исходном
  // массиве (то есть k равняется минимальному значению),
  // мы вычитаем 1 из счёта, поскольку k менять не нужно.
  //
  // Если встречается любое значение меньше k, возвращаем
  // -1 немедленно, потому что операции только уменьшают
  // значения, а не увеличивают их.


  // Создаём Set и инициализируем его единственным
  // элементом k.
  //
  // Set хранит только уникальные значения. Добавление
  // одинаковых значений не увеличит размер.
  //
  // Предварительное добавление k позволяет затем вычесть
  // его из итогового счёта, не проверяя отдельно наличие k
  // в nums
  const uniqueNums = new Set([k]);

  // Перебираем все элементы массива nums в порядке их
  // следования.
  for (const num of nums) {

    // Если встречается значение строго меньше k, функция
    // немедленно завершает выполнение и возвращает -1.
    //
    // Это ранний выход: он сигнализирует о том, что
    // входные данные не удовлетворяют требованию
    // "все элементы >= k".
    //
    // Возврат происходит до каких-либо дальнейших
    // действий, поэтому последующие элементы не
    // обрабатываются.
    if (num < k) return -1;

    // Добавляем текущее значение num в Set уникальных
    // значений.
    //
    // Если num уже присутствует в Set, метод add не
    // изменит коллекцию. В противном случае размер Set
    // увеличится на 1.
    uniqueNums.add(num);
  }

  // Возвращаем количество уникальных элементов в Set за
  // вычетом 1.
  //
  // Вычитание 1 компенсирует заранее добавленный элемент k,
  // поэтому результат соответствует числу уникальных
  // значений из nums, которые >= k и отличны от k.
  //
  // Свойство size возвращает целое число - количество
  // уникальных элементов в Set.
  return uniqueNums.size - 1;
}
