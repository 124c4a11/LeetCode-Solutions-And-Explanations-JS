/**
 * @param {number[]} nums
 * @return {number}
 *
 * minimumOperations принимает целочисленный массив nums
 * и возвращает минимальное суммарное количество единичных
 * операций (увеличение или уменьшение на 1) необходимых,
 * чтобы каждый элемент входного массива стал кратным 3.
 *
 *
 * Пример для:
 *   nums = [1,2,3,4]
 * Результат:
 *   3
 * Пояснение:
 * Все элементы массива можно сделать кратными 3 за 3
 * операции:
 * - Вычесть 1 из 1.
 * - Прибавить 1 к 2.
 * - Вычесть 1 из 4.
 */
function minimumOperations(nums) {

  // Идея:
  //
  // Когда мы хотим сделать число кратным 3, нужно
  // учитывать, какой остаток оно даёт при делении на 3.
  // Любое целое число при делении на 3 может иметь
  // только три возможных остатка: 0, 1 или 2.
  //
  // Если остаток равен 0, число уже кратно 3 и
  // дополнительных операций не требуется.
  //
  // Если остаток равен 1, есть два варианта:
  // - Вычесть 1, чтобы получить кратное 3 (1 операция).
  //
  // - Прибавить 2, чтобы достичь следующего кратного 3
  //   (2 операции).
  //
  // Если остаток равен 2, также есть два варианта:
  // - Вычесть 2, чтобы получить кратное 3 (2 операции).
  //
  // - Прибавить 1, чтобы достичь следующего кратного 3
  //   (1 операция).
  //
  // Ключевая идея:
  // Для числа с остатком r (где r равно 1 или 2) можно
  // либо:
  // - Сдвинуться назад, вычитая r, чтобы получить
  //   ближайшее кратное 3;
  //
  // - Сдвинуться вперёд, прибавив 3 − r, чтобы получить
  //   следующее кратное 3.
  //
  // Поскольку мы хотим минимизировать число операций,
  // всегда выбираем меньшее из r и 3 − r. Это даёт
  // формулу min(r, 3 − r) для каждого элемента.
  //
  // Поскольку каждый элемент обрабатывается независимо
  // (операции над одним элементом не влияют на другие),
  // достаточно пройти по всем элементам и
  // просуммировать минимальные операции для каждого.


  // Инициализация переменной operationsCnt значением 0.
  //
  // В него будем аккумулировать аккумулировать
  // суммарное минимальное количество операций,
  // необходимых, чтобы сделать каждое число массива
  // кратным 3.
  let operationsCnt = 0;

  // Итерация по каждому элементу входного массива nums
  for (const num of nums) {

    // Вычисляем остаток от деления текущего числа на 3.
    //
    // Возможные значения remainder:
    // 0 (уже кратно 3), 1 или 2 (требуется
    // корректировка).
    const remainder = num % 3;

    // Если остаток равен 0, число уже кратно 3 и не
    // требует операций.
    //
    // Пропускаем дальнейшие вычисления для этого
    // элемента.
    if (remainder === 0) continue;

    // Для remainder === 1 можем либо вычесть 1,
    // либо прибавить 2.
    //
    // Для remainder === 2 можем либо прибавить 1,
    // либо вычесть 2.
    //
    // Выражение Math.min(remainder, 3 - remainder)
    // выбирает наименьшее количество единичных шагов
    // между уменьшением и увеличением до ближайшего
    // кратного 3.
    //
    // Добавляем это минимальное число шагов к общему
    // счётчику operationsCnt.
    //
    // Пример:
    //   Если remainder = 1,
    //   Math.min(1, 2) = 1 - выгоднее уменьшить на 1.
    //
    // Пример:
    //   Если remainder = 2,
    //   Math.min(2, 1) = 1 - выгоднее увеличить на 1.
    operationsCnt += Math.min(remainder, 3 - remainder);
  }


  // Возврат накопленного значения operationsCnt
  // - минимального суммарного количества операций
  // (прибавлений или вычитаний единицы), необходимых,
  // чтобы все числа стали кратны 3
  return operationsCnt;
}
