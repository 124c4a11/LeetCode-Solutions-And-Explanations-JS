/**
 * @param {number[]} nums
 * @return {number[]}
 *
 * decompressRLElist разворачивает список, закодированный
 * методом run‑length encoding (RLE).
 *
 * Она принимает массив чисел nums в формате
 * [freq, val, freq, val, ...]. и возвращает массив, где
 * каждое значение val повторено freq раз.
 *
 *
 * Пример для:
 *   nums = [1,2,3,4]
 * Результат:
 *   [2,4,4,4]
 * Пояснение:
 *   - первая пара [1,2] означает, что freq = 1 и val = 2,
 *     поэтому генерируем массив [2].
 *
 *   - вторая пара [3,4] означает, что freq = 3 и val = 4,
 *     поэтому генерируем массив [4,4,4].
 *
 *   В конце конкатенация [2] + [4,4,4] даёт итоговый
 *   массив [2,4,4,4].
 */
function decompressRLElist(nums) {

  // Инициализация пустого массива result, в который будут
  // поочерёдно добавляться все распакованные элементы.
  const result = [];

  // Цикл for проходит по массиву nums с шагом 2, потому
  // что элементы идут парами: на чётных индексах -
  // частоты, на нечётных - значения.
  for (let i = 0; i < nums.length; i += 2) {

    // Извлечение значения (value) из позиции i + 1 - это
    // элемент, который нужно будет повторить freq раз.
    const val = nums[i + 1];

    // Считываем частоту (frequency) из текущей позиции i.
    let freq = nums[i];

    // Цикл while с условием freq-- использует постфиксный
    // декремент:
    // - Сначала проверяется текущее значение freq. Если
    //   оно > 0, тело выполняется.
    //
    // - После проверки значение freq уменьшается на 1.
    //
    // Таким образом, тело цикла выполнится ровно nums[i]
    // раз при положительной частоте.
    //
    // Если freq равно 0 или отрицательно, тело цикла не
    // выполнится ни разу.
    //
    // Внутри цикла добавляем в конец массива result копию
    // val с помощью push.
    while (freq--) result.push(val);
  }

  // После обработки всех пар возвращаем массив result,
  // содержащий распакованные элементы в порядке,
  // соответствующем входным парам.
  //
  // Временная сложность функции - O(n + m), где
  // n = nums.length, m = суммарное количество
  // добавленных элементов (сумма всех частот).
  //
  // Память дополнительно используется под result - O(m).
  return result;
}
