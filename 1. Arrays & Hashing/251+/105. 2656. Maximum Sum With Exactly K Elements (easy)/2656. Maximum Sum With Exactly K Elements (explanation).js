/**
 * @param {number[]} nums
 * @param {number} k
 * @return {number}
 *
 * maximizeSum возвращает сумму k последовательных чисел,
 * начиная с текущего максимума массива nums и увеличивая
 * это значение на 1 в каждой итерации.
 *
 * Параметры:
 *   nums - массив чисел,
 *   k - целое количество итераций/добавлений.
 *
 *
 * Пример для:
 *   nums = [1,2,3,4,5],
 *   k = 3
 * Результат:
 *   18
 * Пояснение:
 *   Нужно выбрать ровно 3 элемента из nums, чтобы
 *   максимизировать сумму.
 *
 *   В первой итерации выбираем 5.
 *   Тогда сумма равна 5, а nums = [1,2,3,4,6].
 *
 *   Во второй итерации выбираем 6.
 *   Тогда сумма равна 5 + 6, а nums = [1,2,3,4,7].
 *
 *   В третьей итерации выбираем 7.
 *   Тогда сумма равна 5 + 6 + 7 = 18,
 *   а nums = [1,2,3,4,8].
 *
 *   Итак, функция вернёт 18.
 *
 *   Можно доказать, что 18 - это максимально возможный
 *   ответ.
 */
function maximizeSum(nums, k) {

  // Инициализация аккумулятора maxSum значением 0.
  //
  // maxSum будет хранить суммарный результат - сумму k
  // последовательно увеличивающихся значений, начиная с
  // текущего максимума массива.
  let maxSum = 0;

  // Находим максимальный элемент массива nums.
  let max = Math.max(...nums);

  // Цикл выполняется ровно k раз при положительном
  // целочисленном k.
  //
  // Выражение k-- возвращает текущее значение k, затем
  // уменьшает k на 1.
  //
  // Таким образом, цикл выполняется, пока исходное
  // значение k > 0
  while (k--) {

    // Прибавляем текущее значение max к накопленной
    // сумме maxSum.
    maxSum += max;

    // Увеличиваем значение max на 1.
    //
    // Операция изменяет локальную переменную max.
    //
    // Это моделирует сценарий, где после каждого
    // добавления максимальный элемент увеличивается на 1,
    // поэтому на следующей итерации мы добавляем уже
    // увеличенное значение.
    max++;
  }

  // Возвращаем итоговую сумму после выполнения k
  // итераций
  return maxSum;
}
