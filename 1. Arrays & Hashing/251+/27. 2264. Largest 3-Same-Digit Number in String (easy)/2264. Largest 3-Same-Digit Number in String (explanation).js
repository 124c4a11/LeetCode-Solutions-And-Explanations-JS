/**
 * @param {string} num
 * @return {string}
 * 
 * largestGoodInteger ищет в строке из цифр так
 * называемый «good integer» — последовательность
 * из трёх одинаковых подряд идущих цифр — и
 * возвращает максимальный из найденных
 * «good integer» в виде строки из трёх символов.
 * Если таких троек нет, возвращает пустую строку.
 * 
 * Параметры:
 * - num — строка, представляющая
 *   последовательность цифр
 * 
 * Пример для:
 *   num = "6777133339"
 * Результат:
 *   "777"
 * Пояснение:
 *   Есть две разные «хорошие» тройки: "777" и "333".
 *   "777" — наибольшая, поэтому возвращаем "777".
 */
function largestGoodInteger(num) {

  // Если длина строки меньше 3, то в num не может
  // быть трех одинаковых подряд идущих символов,
  // дальнейшие операции не имеют смысла
  // — немедленно возвращаем пустую строку.
  if (num.length < 3) return '';

  // Объявление переменной result и присвоение ей
  // начального значения -1.
  //
  // Используется как флаг "не найдено" и как
  // хранилище для максимального значения тройки
  // одинаковых символов.
  //
  // -1 используется как флаг "не найдено", так как
  // наименьшим значением, которое мы можем
  // встретить в num является 0
  let result = -1;

  // Запуск цикла for для перебора индексов строки
  // num.
  //
  // Условие i < num.length - 2 гарантирует, что
  // при обращении к num[i + 1] и num[i + 2] не
  // произойдёт выхода за границы строки, так как
  // нам нужно читать три символа подряд (текущий
  // и ещё два следующих).
  for (let i = 0; i < num.length - 2; i++) {

    // Условие проверяет, являются ли три
    // последовательных символа одинаковыми:
    // сравниваем символ в позиции i с символом
    // в i + 1 и с символом в i + 2.
    //
    // Здесь сравнения выполняются как строковые
    // сравнения символов (например, '7' === '7').
    if (
      num[i] === num[i + 1]
      && num[i] === num[i + 2]
    ) {

      // Найдено три одинаковых подряд идущих
      // символа.
      //
      // Number(num[i]) преобразует символьное
      // представление цифры (напр., '8') в
      // числовое значение 8.
      //
      // Math.max(result, Number(num[i]))
      // гарантирует, что result хранит
      // максимальную найденную цифру среди всех
      // троек.
      //
      // Если это первая найденная тройка, result
      // был -1, поэтому Math.max(-1, x) вернёт x.
      result = Math.max(result, Number(num[i]));

      // После нахождения тройки одинаковых
      // символов мы увеличиваем индекс i на 2
      // дополнительно.
      //
      // Это делает шаг цикла эквивалентным
      // пропуску элементов, входящих в только что
      // найденную тройку, так как последующая
      // итерация цикла выполнит i++ в заголовке
      // цикла, и итоговый шаг будет +3 от текущей
      // позиции.
      //
      // Такой подход предотвращает повторную
      // проверку перекрывающихся троек, которые
      // полностью включают уже найденную тройку.
      i += 2;
    }
  }

  // После завершения цикла проверяем значение result.
  //
  // Если result остался -1, значит ни одной тройки
  // одинаковых символов не найдено, и функция должна
  // вернуть пустую строку ''.
  //
  // Если result содержит число от 0 до 9, то
  // возвращается строка, состоящая из этого числа,
  // повторённого 3 раза.
  //
  // `${result}` преобразует число обратно в строку,
  // а .repeat(3) создаёт строку, содержащую этот
  // символ трижды.
  //
  // Возвращаемое значение соответствует формату
  // наибольшего «good integer» из задачи:
  // трёхзначная или пустая строка.
  return result === -1 ? '' : `${result}`.repeat(3);
}
