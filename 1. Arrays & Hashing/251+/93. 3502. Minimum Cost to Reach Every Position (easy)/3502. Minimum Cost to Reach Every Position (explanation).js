/**
 * @param {number[]} cost
 * @return {number[]}
 *
 * minCosts преобразует входной массив cost так, чтобы каждый
 * элемент стал равен минимальному значению на префиксе от
 * начала до текущей позиции (то есть вычисляет префиксные
 * минимумы).
 *
 * Возвращает преобразованный массив cost
 *
 *
 * Пример для:
 *   cost = [5,3,4,1,3,2]
 * Результат:
 *   [5,3,3,1,1,1]
 */
function minCosts(cost) {

  // Проходим по элементам массива cost
  //
  // Начинаем с 1, чтобы при каждой итерации иметь доступ к
  // предыдущему элементу cost[i - 1].
  for (let i = 1; i < cost.length; i++) {

    // Если предыдущий элемент меньше текущего, то текущему
    // элементу присваивается значение предыдущего.
    //
    // Это приводит к тому, что последовательность становится
    // неубывающей относительно предыдущего элемента -
    // текущий элемент уменьшается до предыдущего, если он
    // был больше.
    if (cost[i - 1] < cost[i]) {
      cost[i] = cost[i - 1];
    }
  }

  // После завершения цикла возвращается тот же массив cost,
  // но уже модифицированный.
  return cost;
}
