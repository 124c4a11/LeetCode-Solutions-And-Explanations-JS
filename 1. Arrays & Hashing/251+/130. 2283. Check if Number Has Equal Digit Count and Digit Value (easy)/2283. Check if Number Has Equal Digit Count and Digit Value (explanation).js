/**
 * @param {string} num
 * @return {boolean}
 *
 * digitCount проверяет, соответствует ли для каждого индекса
 * i количество вхождений цифры i в строке num числу,
 * записанному в num[i].
 *
 * Параметры:
 * - num - строка, содержащая только символы 0–9.
 *
 *
 * Пример для:
 *   num = "1210"
 * Результат:
 *   true
 * Пояснение:
 * - num[0] = '1'. Цифра 0 встречается в num один раз.
 * - num[1] = '2'. Цифра 1 встречается в num два раза.
 * - num[2] = '1'. Цифра 2 встречается в num один раз.
 * - num[3] = '0'. Цифра 3 встречается в num ноль раз.
 * Условие выполняется для каждого индекса в "1210", поэтому
 * возвращается true.
 */
function digitCount(num) {

  // Создаём массив фиксированной длины 10 для подсчёта
  // частот цифр 0–9.
  //
  // Int32Array обеспечивает компактное представление целых
  // чисел и инициализируется нулями.
  //
  // Индекс массива соответствует самой цифре (0..9).
  // Значение - сколько раз эта цифра встретилась.
  const digitToFreq = new Int32Array(10);

  // Проходим по каждому символу строки num.
  //
  // В JavaScript при итерации по строке переменная digit
  // будет строкой длины 1, например '3'.
  //
  // При использовании digitToFreq[digit] происходит неявное
  // приведение '3' к числу 3.
  //
  // Увеличиваем счётчик для соответствующей цифры.
  for (const digit of num) digitToFreq[digit]++;

  // Перебираем индексы от 0 до num.length - 1.
  //
  // Здесь мы рассматриваем только те цифры, индексы которых
  // присутствуют в строке (т.е. 0..num.length-1).
  //
  // Если длина строки меньше 10, цифры с индексами
  // >= num.length не проверяются.
  for (let digit = 0; digit < num.length; digit++) {

    // Получаем фактическое количество вхождений цифры
    // (числовой индекс) в строке.
    const digitFreq = digitToFreq[digit];

    // Берём символ из строки num в позиции digit и явно
    // преобразуем его в число с помощью Number.
    //
    // Это ожидаемая частота появления цифры, указанная в
    // самой строке.
    const targetFreq = Number(num[digit]);

    // Сравниваем фактическую частоту и ожидаемую.
    //
    // Если они не равны, функция немедленно возвращает
    // false
    if (digitFreq !== targetFreq) return false;
  }

  // Если все индексы от 0 до num.length-1 прошли проверку,
  // возвращаем true.
  //
  // Это означает, что для каждого индекса i количество цифр
  // i в строке совпадает с числом num[i].
  return true;
}
