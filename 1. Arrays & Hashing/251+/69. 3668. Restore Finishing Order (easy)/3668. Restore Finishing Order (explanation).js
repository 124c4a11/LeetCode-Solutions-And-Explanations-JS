/**
 * @param {number[]} order
 * @param {number[]} friends
 * @return {number[]}
 *
 * recoverOrder принимает целочисленный массив order
 * длины n и целочисленный массив friends.
 *
 * - order содержит каждое целое число от 1 до n ровно
 *   один раз. Эти числа представляют идентификаторы
 *   участников гонки в порядке их финиша.
 *
 * - friends содержит идентификаторы ваших друзей в
 *   гонке, отсортированные в строго возрастающем
 *   порядке. Гарантируется, что каждый идентификатор
 *   из friends присутствует в массиве order.
 *
 * Возвращает массив, содержащий идентификаторы ваших
 * друзей в порядке их финиша.
 *
 *
 * Пример для:
 *   order = [3,1,2,5,4],
 *   friends = [1,3,4]
 * Результат:
 *   [3,1,4]
 * Пояснение:
 *   Порядок финиша: [3, 1, 2, 5, 4].
 *
 *   Следовательно, порядок финиша ваших друзей:
 *   [3, 1, 4].
*/
function recoverOrder(order, friends) {

  // Создаём объект Set на основе массива friends.
  // Set обеспечивает быстрые операции проверки
  // принадлежности элемента из массива order к
  // массиву friends (в среднем O(1)), что выгодно
  // при многократных проверках внутри цикла.
  const friendsSet = new Set(friends);

  // Инициализируем пустой массив result, в который
  // будем последовательно добавлять тех участников
  // из order, которые присутствуют в friendsSet.
  //
  // Это позволит сформировать массив из элементов
  // friends в порядке их появления в order
  const result = [];

  // Проходим по каждому элементу массива order в
  // исходном порядке.
  for (const participant of order) {

    // Проверяем, содержится ли текущий participant в
    // множестве friendsSet.
    //
    // Если friendsSet не содержит participant, то
    // этот участник не является другом и пропускается.
    if (!friendsSet.has(participant)) continue;

    // Если participant найден в friendsSet,
    // добавляем его в конец массива result.
    //
    // Это позволяет сформировать массив из элементов
    // friends в порядке их появления в order
    result.push(participant);
  }

  // Возвращаем сформированный массив result,
  // содержащий только тех участников из order,
  // которые также присутствуют в friends.
  //
  // Порядок элементов соответствует порядку в order.
  //
  // Временная сложность алгоритма:
  // O(n + m), где n — длина order, m — длина friends
  // (для построения Set).
  //
  // Пространственная сложность:
  // O(m + k), где m — размер Set, k — количество
  // отобранных друзей в result.
  return result;
}
