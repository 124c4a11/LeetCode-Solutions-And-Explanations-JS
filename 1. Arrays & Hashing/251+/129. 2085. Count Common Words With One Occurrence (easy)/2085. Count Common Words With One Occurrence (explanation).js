/**
 * @param {string[]} words1
 * @param {string[]} words2
 * @return {number}
 *
 * countWords принимает два массива строк words1 и words2.
 *
 * Возвращает количество строк, которые встречаются ровно
 * один раз в каждом из двух массивов.
 *
 *
 * Пример для:
 *   words1 = ["leetcode","is","amazing","as","is"],
 *   words2 = ["amazing","leetcode","is"]
 * Результат:
 *   2
 * Пояснение:
 * - "leetcode" встречается ровно один раз в каждом из
 *   массивов. Считаем это слово.
 *
 * - "amazing" встречается ровно один раз в каждом из
 *   массивов. Считаем это слово.
 *
 * - "is" встречается в обоих массивах, но в words1 оно
 *   встречается 2 раза. Не считаем это слово.
 *
 * - "as" встречается один раз в words1, но не
 *   встречается в words2. Не считаем это слово.
 *
 * Таким образом, есть 2 строки, которые встречаются ровно
 * один раз в каждом из двух массивов.
 */
function countWords(words1, words2) {

  // Создаём Map для хранения статуса каждого слова,
  // встречающегося в words1.
  //
  // Ключ: слово.
  // Значение: статус - 0, 1 или 2.
  //
  // Возможные значения статуса:
  // 0 - слово либо повторялось в words1, либо уже признано
  //     невалидным;
  //
  // 1 - слово встречалось ровно один раз в words1 и ещё не
  //     встречалось в words2;
  //
  // 2 - слово встречалось ровно один раз в words1 и уже
  //     встретилось один раз в words2.
  const wordToStatus = new Map();

  // Проходим по массиву words1
  for (const word of words1) {

    // Если слово уже присутствует в Map, это означает,
    // что оно встречается в words1 как минимум дважды.
    //
    // В этом случае помечаем его статусом 0 - слово не
    // уникально в words1 и не может быть валидным.
    if (wordToStatus.has(word)) wordToStatus.set(word, 0);

    // Если слово встречается впервые в words1, помечаем
    // статусом 1 - потенциально уникальное в words1.
    else wordToStatus.set(word, 1);
  }

  // Инициализируем счётчик валидных слов.
  //
  // validWordsCnt отслеживает количество слов, которые
  // на данный момент удовлетворяют условию: "встречается
  // ровно один раз в words1 и ровно один раз в пройденной
  // части words2".
  let validWordsCnt = 0;

  // Проходим по всем словам из массива words2, чтобы
  // определить, какие из них встречаются ровно один раз в
  // words2 и ровно один раз в words1.
  for (const word of words2) {

    // Если слово отсутствует в Map, значит оно не
    // встречалось в words1, и его можно игнорировать -
    // переходим к следующему слову.
    if (!wordToStatus.has(word)) continue;

    // Получаем текущий числовой статус слова из Map.
    // Возможные значения статуса:
    // 0 - слово либо повторялось в words1, либо уже
    //     признано невалидным;
    //
    // 1 - слово встречалось ровно один раз в words1 и ещё
    //     не встречалось в words2;
    //
    // 2 - слово встречалось ровно один раз в words1 и уже
    //     встретилось один раз в words2.
    const status = wordToStatus.get(word);

    // Если статус === 1, это означает:
    // - слово встречалось ровно один раз в words1,
    // - и это первая встреча этого слова в words2.
    //
    // Следовательно, слово теперь встречается ровно один
    // раз в обоих массивах (по состоянию на текущую
    // итерацию).
    if (status === 1) {

      // Увеличиваем счётчик валидных слов, фиксируя, что
      // это слово удовлетворяет условию.
      validWordsCnt++;

      // Обновляем статус на 2, чтобы отметить, что слово
      // уже встретилось в words2 один раз.
      //
      // Это предотвращает повторный учёт при следующей
      // встрече в words2.
      wordToStatus.set(word, 2);

      // Если статус === 2, это означает, что слово уже
      // было учтено как валидное (одна встреча в words1 и
      // words2), но теперь встречается второй и более раз
      // в words2, поэтому оно перестаёт удовлетворять
      // условию "ровно один раз в words2".
    } else if (status === 2) {

      // Поскольку слово встретилось во второй раз в
      // words2, оно перестаёт быть уникальным в words2.
      //
      // Поэтому уменьшаем счётчик валидных слов, отменяя
      // ранее сделанное увеличение.
      validWordsCnt--;

      // Обновляем статус на 0, чтобы пометить слово как
      // окончательно невалидное для дальнейших проверок.
      //
      // Это также предотвращает повторное уменьшение
      // счётчика при последующих встречах в words2.
      wordToStatus.set(word, 0);
    }
  }

  // Возвращаем итоговое количество слов, которые
  // встретились ровно один раз в words1 и ровно один раз
  // в words2.
  return validWordsCnt;
}
