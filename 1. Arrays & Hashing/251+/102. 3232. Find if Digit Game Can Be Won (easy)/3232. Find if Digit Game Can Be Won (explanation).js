/**
 * @param {number[]} nums
 * @return {boolean}
 *
 * canAliceWin суммирует все однозначные числа и все двузначные
 * числа в переданном массиве nums и возвращает true, если эти
 * суммы различаются (тогда Алиса может выбрать большую сумму и
 * выиграть).
 *
 * У Алисы есть два варианта выбора - взять все однозначные
 * числа (1–9) или все двузначные числа (10–99) из массива nums.
 * Оставшиеся числа получает Боб. Если сумма выбранной Алисой
 * группы строго больше суммы оставшихся чисел, Алиса
 * выигрывает. Функция вычисляет обе суммы и проверяет,
 * различаются ли они - при различии Алиса всегда может выбрать
 * большую группу и выиграть.
 *
 *
 * Пример для:
 *   nums = [1,2,3,4,10]
 * Результат:
 *    false
 * Пояснение:
 * Алиса не может выиграть, выбрав либо все однозначные числа,
 * либо все двузначные числа.
 *
 *
 * Пример для:
 *   nums = [1,2,3,4,5,14]
 * Результат:
 *   true
 * Пояснение:
 * Алиса может выиграть, выбрав однозначные числа, сумма
 * которых равна 15.
 */
function canAliceWin(nums) {

  // Инициализация аккумулятора singleDigitSum значением 0.
  //
  // Будет накапливать сумму всех элементов массива, которые
  // считаются "однозначными" (меньше 10).
  let singleDigitSum = 0;

  // Инициализация аккумулятора doubleDigitSum значением 0.
  //
  // Будет накапливать сумму всех элементов массива, которые
  // считаются "многозначными" (>= 10).
  let doubleDigitSum = 0;

  // Перебираем каждый элемент входного массива nums по
  // значению.
  for (const num of nums) {

    // Условие проверяет, меньше ли текущее значение num чем 10.
    //
    // Если да, число считается "однозначным" и добавляется к
    // singleDigitSum.
    if (num < 10) singleDigitSum += num;

    // В противном случае (num >= 10 или условие не выполнено)
    // число считается "двузначным" - добавляем его к
    // doubleDigitSum.
    else doubleDigitSum += num;
  }

  // Возвращается логическое значение: true, если суммы двух
  // групп различаются, иначе false.
  //
  // То есть функция сигнализирует о "победе Алисы", когда
  // суммы не равны.
  return singleDigitSum !== doubleDigitSum;
}
