/**
 * @param {number[]} seats
 * @param {number[]} students
 * @return {number}
 *
 * minMovesToSeat принимает массив seats длины n, где
 * seats[i] - позиция i-го места.
 *
 * Также функция принимает массив students длины n, где
 * students[j] — позиция j-го студента.
 *
 * В комнате есть n доступных мест и n студентов.
 *
 * Можно выполнять следующий шагов любое количество раз:
 * - За один шаг можно увеличить или уменьшить позицию
 *   i-го студента на 1 (т.е. переместить i-го студента
 *   с позиции x на x + 1 или x - 1).
 *
 * Функция возвращает минимальное число шагов, необходимых,
 * чтобы посадить каждого студента на место так, чтобы
 * никакие два студента не занимали одно и то же место.
 * То есть каждый студент перемещается к ближайшему стулу,
 * чтобы сделать минимальное количество шагов.
 *
 * Обратите внимание, что в начале может быть несколько
 * мест или студентов в одной и той же позиции.
 *
 *
 * Пример для:
 *   seats = [3,1,5],
 *   students = [2,7,4]
 * Результат:
 *   4
 * Пояснение:
 * Студенты перемещаются следующим образом:
 *  - Первый студент перемещается с позиции 2 на позицию 1
 *    за 1 ход.
 *
 *  - Второй студент перемещается с позиции 7 на позицию 5
 *    за 2 хода.
 *
 *  - Третий студент перемещается с позиции 4 на позицию 3
 *    за 1 ход.
 * В сумме использовано 1 + 2 + 1 = 4 хода.
 */
function minMovesToSeat(seats, students) {

  // С учетом ограниченного диапазона позиций (0 - 101), в
  // решении используется counting sort‑подход, чтобы
  // избежать сравнительной сортировки O(nlogn).
  //
  // Алгоритм позволяет решить задачу за  O(n + R) где
  // n - число студентов/мест, R - размер диапазона позиций
  // (здесь R = 101). При фиксированном небольшом R это
  // близко к линейной сложности O(n).

  const n = seats.length;

  // Создаём фиксированный массив целых (Int32Array) длиной
  // 101 для подсчёта количества стульев на каждой позиции
  // от 0 до 100 включительно.
  //
  // Все элементы массива по умолчанию заполнены нулями.
  //
  // Размер 101 выбран потому что в задаче позиции
  // ограничены 1..100.
  //
  // Индекс 0 остаётся доступным, но не используется.
  const posToSeatsCnt = new Int32Array(101);

  // Аналогично создаём массив счётчиков для студентов по
  // позициям 0..100.
  //
  // posToStudentsCnt[pos] будет хранить, сколько студентов
  // в данный момент находятся на позиции pos.
  const posToStudentsCnt = new Int32Array(101);

  // Проходим по каждому значению в входных массивах seats
  // и students.
  for (let i = 0; i < n; i++) {
    
    // Заполняем массив частот для стульев:
    //
    // Для каждой позиции seats[i] увеличиваем
    // соответствующий счётчик в posToSeatsCnt.
    //
    // Это реализует идею подсчёта (counting sort):
    // Вместо сортировки массивов мы агрегируем частоты по
    // позициям.
    posToSeatsCnt[seats[i]]++;
    
    // Заполняем массив частот для студентов:
    //
    // Для каждой позиции students[i] в students
    // увеличиваем соответствующий счётчик.
    //
    // После этого у нас есть два гистограммных
    // представления распределения мест и студентов.
    posToStudentsCnt[students[i]]++;
  }

  // Инициализируем аккумулятор результата - суммарное
  // число перемещений.
  let result = 0;

  // Указатель i будет пробегать индексы позиций для мест
  // (индекс в posToSeatsCnt).
  //
  // Он всегда движется в неубывающем порядке от 0 к 100,
  // обеспечивая жадное сопоставление слева направо.
  let i = 0;

  // Указатель j будет пробегать индексы позиций для
  // студентов (индекс в posToStudentsCnt).
  //
  // Аналогично движется в неубывающем порядке.
  //
  // Идея:
  // Сопоставлять ближайшие по позиции элементы
  // "студент - стул".
  let j = 0;

  // remainder хранит, сколько ещё пар "студент - стул"
  // нужно сопоставить.
  //
  // Изначально равно числу мест (или студентов), так как
  // их количество одинаково.
  let remainder = n;

  // Основной цикл:
  //
  // Продолжаем, пока не распределим все стулья
  // (remainder > 0).
  //
  // Алгоритм последовательно сопоставляет ближайшие по
  // позиции доступные стулья и студентов, что эквивалентно
  // сортировке обоих массивов и парному суммированию
  // расстояний.
  while (remainder > 0) {

    // Если на текущей позиции i нет ни одного свободного
    // стула (счётчик равен нулю), увеличиваем i, чтобы
    // перейти к следующей позиции стульев.
    if (posToSeatsCnt[i] === 0) {
      i++;

      // Так как на текущей позиции нет ни одного
      // свободного стула, дальнейшие вычисления
      // бессмысленны - пропускает оставшуюся часть тела
      // цикла и начинает следующую итерацию
      continue;
    }

    // Если на текущей позиции j нет студентов, увеличиваем
    // j, чтобы перейти к следующей позиции студентов.
    if (posToStudentsCnt[j] === 0) {
      j++;

      // Так как на текущей позиции нет ни одного студента,
      // дальнейшие вычисления бессмысленны - пропускает
      // оставшуюся часть тела цикла и начинает следующую
      // итерацию
      continue;
    }

    // Когда и на позиции i есть свободный стул, и на
    // позиции j есть студент, добавляем к результату
    // расстояние между ними.
    //
    // Math.abs(i - j) - количество шагов, которое
    // потребуется студенту, чтобы добраться до стула в
    // позиции i из позиции j.
    result += Math.abs(i - j);

    // Уменьшаем счётчик мест на позиции i, так как одно
    // место в этой позиции теперь занято текущим студентом.
    posToSeatsCnt[i]--;

    // Уменьшаем счётчик студентов на позиции j, так как
    // текущий студент теперь перемещён и занял место в
    // позиции i
    posToStudentsCnt[j]--;

    // Уменьшаем количество оставшихся пар на единицу - мы
    // обработали одну пару "студент - стул".
    remainder--;
  }

  // Возвращаем накопленное минимальное суммарное
  // количество перемещений.
  return result;
}
