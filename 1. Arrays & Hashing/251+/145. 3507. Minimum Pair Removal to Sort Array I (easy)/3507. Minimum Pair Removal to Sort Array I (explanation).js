/**
 * @param {number[]} nums
 * @return {number}
 *
 * minimumPairRemoval принимает один параметр - массив чисел nums
 * и последовательно объединяет соседние пары с минимальной
 * суммой, заменяя пару их суммой и удаляя второй элемент, пока
 * массив не станет неубывающим.
 *
 *
 * Пример для:
 *   nums = [5, 2, 3, 1]
 * Результат:
 *   2
 * Пояснение:
 *   Пара (3, 1) имеет минимальную сумму 4. После замены массив
 *   становится nums = [5, 2, 4].
 *
 *   Пара (2, 4) имеет минимальную сумму 6. После замены массив
 *   становится nums = [5, 6].
 *
 *   Массив nums стал неубывающим после двух операций.
 */
function minimumPairRemoval(nums) {
  let removalsCnt = 0;

  // Внешний цикл повторяется до тех пор, пока в массиве есть
  // хотя бы одна пара соседних элементов.
  //
  // Условие nums.length > 1 гарантирует, что внутри есть пара
  // i и i+1 для проверки.
  while (nums.length > 1) {

    // Переменная для хранения минимальной суммы соседней пары,
    // найденной на текущей итерации.
    //
    // Инициализируем Infinity, чтобы любая реальная сумма была
    // меньше и могла её заменить.
    let minSum = Infinity;

    // Флаг, показывающий, является ли текущий массив неубывающим
    // (каждый элемент <= следующего).
    //
    // Если после прохода по массиву флаг останется true, это
    // означает, что дальнейшие объединения не нужны.
    let isAscending = true;

    // Индекс левого элемента пары, у которой обнаружена
    // минимальная сумма на текущей итерации.
    //
    // Значение -1 означает, что пара не выбрана. После прохода
    // оно должно быть >= 0, если массив не пуст.
    let targetNdx = -1;

    // Проход по всем индексам, где существует пара (i и i+1).
    //
    // Цикл идёт до nums.length - 1, чтобы i+1 всегда было
    // валидным индексом.
    for (let i = 0; i < nums.length - 1; i++) {

      // Вычисление суммы текущей соседней пары для сравнения с
      // текущим минимумом.
      const currSum = nums[i] + nums[i + 1];

      // Проверка монотонности:
      // Если текущий элемент больше следующего, это означает спад.
      //
      // Наличие хотя бы одного спада означает, что массив не
      // является неубывающим, и тогда возможны дальнейшие
      // объединения.
      //
      // Сбрасываем флаг упорядоченности - это сигнал для внешней
      // логики, что ещё есть смысл продолжать удаления.
      if (nums[i] > nums[i + 1]) {
        isAscending = false;
      }

      // Сравниваем сумму текущей пары с минимальной найденной
      // суммой.
      //
      // Если текущая сумма меньше, обновляем minSum и
      // запоминаем индекс левого элемента этой пары.
      if (currSum < minSum) {
        minSum = currSum;
        targetNdx = i;
      }
    }

    // Если после полного прохода по массиву флаг isAscending
    // остался true, это означает, что массив уже упорядочен по
    // неубыванию и дальнейшие объединения не улучшат порядок.
    //
    // В таком случае алгоритм завершает работу и выходит из
    // цикла.
    if (isAscending) break;

    // Увеличиваем счётчик операций, потому что собираемся
    // выполнить объединение выбранной пары.
    removalsCnt++;

    // Заменяем левый элемент выбранной пары на их сумму
    // (минимальную сумму, найденную выше).
    //
    // Это изменение сохраняется в исходном массиве nums
    // (мутируемый in-place подход).
    nums[targetNdx] = minSum;

    // Удаляем правый элемент пары (тот, что был на позиции
    // targetNdx + 1), тем самым уменьшая длину массива на 1 и
    // фактически "объединяя" пару в один элемент.
    //
    // splice изменяет массив на месте и сдвигает все последующие
    // элементы влево.
    nums.splice(targetNdx + 1, 1);
  }

  // После завершения цикла возвращаем общее количество
  // выполненных объединений/удалений пар.
  //
  // Это число показывает, сколько раз мы заменяли пару суммой и
  // удаляли правый элемент.
  return removalsCnt;
}
