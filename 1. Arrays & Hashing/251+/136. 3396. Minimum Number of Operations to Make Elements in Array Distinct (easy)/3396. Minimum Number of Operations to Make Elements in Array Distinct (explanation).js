/**
 * @param {number[]} nums
 * @return {number}
 *
 * minimumOperations принимает массив чисел nums и возвращает
 * минимальное число операций удаления эелементов с начала
 * массива, чтобы в оставшемся суффиксе все элементы были
 * уникальны
 *
 *
 * Пример для:
 *   nums = [1,2,3,4,2,3,3,5,7]
 * Результат:
 *   2
 * Пояснение:
 *   В первой операции удаляются первые 3 элемента, в
 *   результате массив становится [4, 2, 3, 3, 5, 7].
 *
 *   Во второй операции удаляются следующие 3 элемента, в
 *   результате массив становится [3, 5, 7], в котором все
 *   элементы различны.
 *
 *   Следовательно, ответ равен 2.
 */
function minimumOperations(nums) {

  // Ключевая идея в том, что нужно сохранить как можно больше
  // элементов, при этом все оставшиеся элементы должны быть
  // различны. Поскольку мы можем удалять элементы только с
  // начала массива, нужно думать о том, какие элементы можно
  // оставить в конце.
  //
  // Если смотреть на массив справа налево, можно найти самую
  // длинную суффиксную часть массива, в которой все элементы
  // различны. Этот суффикс мы хотим сохранить, а всё, что
  // находится перед ним, нужно удалить.
  //
  // Почему идём справа налево:
  // - Удалять можно только с начала, поэтому мы хотим
  //   сохранить элементы в конце.
  //
  // - При обходе справа налево можно отслеживать уже
  //   встреченные элементы с помощью множества (set).
  //
  // - Как только встречаем дубликат (элемент уже есть в
  //   множестве), становится ясно, что этот элемент и всё
  //   перед ним нужно удалить.
  //
  // Пример для массива [1, 2, 3, 4, 2]:
  // - Начинаем справа: 2 - новый, добавляем в множество.
  //
  // - 4 - новый, добавляем в множество.
  //
  // - 3 - новый, добавляем в множество.
  //
  // - 2 уже есть в множестве! Это значит, что нужно удалить
  //   всё от индекса 0 до 1 (первое вхождение 2).
  //
  // Как только мы знаем, что нужно удалить все элементы до
  // индекса i, число операций вычисляется как
  // Math.floor(i/3) + 1, потому что:
  // - За одну операцию удаляем 3 элемента.
  //
  // - Если нужно удалить i+1 элементов, требуется
  //   Math.floor(i/3) полных операций по 3 элемента.
  //
  // - И ещё одна операция для оставшихся 1 или 2 элементов.
  //
  // Этот жадный подход - сохранять максимально возможный
  // корректный суффикс - гарантирует минимальное число
  // операций.


  // Создаётся массив numToSeen длиной 101 для отметки
  // встреченных значений по индексу равному самому числу
  //
  // Используется длина 101, так как значения в nums находятся в
  // диапазоне от 0 до 100 включительно
  //
  // Индекс массива соответствует значению элемента (числу) из
  // nums
  //
  // Элементы массива по умолчанию равны undefined что означает
  // "не встречалось". Встреченные элементы мы будет помечать
  // значением true.
  //
  // Использование массива вместо Set даёт более быстрый доступ
  // по индексу O(1) и экономит память при небольшом диапазоне
  // значений.
  const numToSeen = new Array(101);

  // Проходим по массиву справа налево: начинаем с последнего
  // индекса и идём к нулю.
  //
  // Это позволяет найти максимальный суффикс с уникальными
  // элементами.
  for (let i = nums.length - 1; i >= 0; i--) {

    // Сохраняем текущий элемент в переменную
    const num = nums[i];

    // Если текущее значение уже отмечено в numToSeen, значит
    // мы встретили дубликат, и все элементы до этого индекса
    // (включая текущий) нужно удалить.
    //
    // Возвращаем минимальное число операций:
    // Math.floor(i / 3) + 1 вычисляет, сколько операций по
    // удалению до 3 элементов с начала потребуется, чтобы
    // удалить i+1 элементов.
    if (numToSeen[num]) return Math.floor(i / 3) + 1;

    // Отмечаем текущее значение как встреченное, чтобы
    // последующие проверки обнаруживали дубликаты.
    numToSeen[num] = true;
  }

  // Если цикл завершился без раннего возврата, значит все
  // элементы в массиве уникальны, удалять ничего не нужно -
  // возвращаем 0.
  return 0;
}
