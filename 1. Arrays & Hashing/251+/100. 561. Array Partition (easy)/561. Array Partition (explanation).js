/**
 * @param {number[]} nums
 * @return {number}
 *
 * arrayPairSum принимает массив чисел и возвращает максимально
 * возможную сумму минимальных элементов в n парах, полученных
 * из этого массива. Для этого она сортирует массив и суммирует
 * элементы на чётных индексах (0, 2, 4, ...).
 *
 *
 * Пример для:
 *   nums = [1,4,3,2]
 * Результат:
 *   4
 * Пояснение:
 * Все возможные разбиения на пары (порядок элементов внутри
 * пары не важен):
 * 1. (1, 4), (2, 3) → min(1, 4) + min(2, 3) = 1 + 2 = 3
 * 2. (1, 3), (2, 4) → min(1, 3) + min(2, 4) = 1 + 2 = 3
 * 3. (1, 2), (3, 4) → min(1, 2) + min(3, 4) = 1 + 3 = 4
 */
function arrayPairSum(nums) {

  // Чтобы понять, почему после сортировки нужно объединять
  // соседние элементы в пары, давайте разберёмся, что
  // происходит при формировании пар.
  //
  // Когда мы выбираем два числа, чтобы составить пару, в сумму
  // добавляется только меньшее из них — большее фактически
  // "теряется". Значит, ключевая идея в том, чтобы
  // минимизировать эти потери.
  //
  // Рассмотрим числа [1, 2, 5, 6]. Если составить пары (1, 6)
  // и (2, 5), получим:
  // min(1, 6) + min(2, 5) = 1 + 2 = 3.
  // Здесь мы «теряем» 6 − 1 = 5 в первой паре и 5 − 2 = 3 во
  // второй, всего потери = 8.
  //
  // Но если составить пары (1, 2) и (5, 6), получим:
  // min(1, 2) + min(5, 6) = 1 + 5 = 6.
  // Теперь потери составляют 2 − 1 = 1 в первой паре и
  // 6 − 5 = 1 во второй, всего потери = 2.
  //
  // Отсюда видно закономерность: чтобы минимизировать потери,
  // нужно объединять числа, близкие по значению. А самые
  // близкие числа — это соседние элементы в отсортированном
  // массиве.
  //
  // После сортировки мы берём каждый второй элемент, начиная с
  // индекса 0 (то есть элементы с позициями 0, 2, 4, ...). Это
  // и есть меньшие элементы из каждой пары соседей, и их сумма
  // даёт максимально возможный результат.


  // Сортировка массива nums по возрастанию.
  nums.sort((a, b) => a - b);

  // Объявляем переменную maxSum и инициализируем её нулём.
  // В эту переменную будем аккумулировать сумму выбранных
  // элементов.
  let maxSum = 0;

  // Проходим по индексам массива с шагом 2, начиная с 0.
  //
  // Так как массив отсортирован по возрастанию, меньшим в
  // каждой паре будет элемент с чётным индексом.
  //
  // Шаг i += 2 гарантирует, что мы берём только элементы с
  // чётными индексами
  for (let i = 0; i < nums.length; i += 2) {

    // Добавляем в maxSum элемент с чётным индексом i.
    maxSum += nums[i];
  }

  // возвращает максимально возможную сумму минимальных
  // элементов всех парах
  return maxSum;
}
