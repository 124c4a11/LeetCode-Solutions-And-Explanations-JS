/**
 * @param {number} n
 *
 * Поток получает n пар (idKey, value) в произвольном порядке,
 * где idKey - целое число от 1 до n, а value - строка.
 * Не существует двух пар с одинаковым id.
 *
 * Поток возвращает значения в порядке возрастания их ID,
 * выдавая после каждой вставки максимально возможный "кусок"
 * (список) последовательных значений.
 * Конкатенация всех таких кусков должна давать полностью
 * отсортированный список.
 *
 * - OrderedStream(int n)
 *     Создаёт поток, рассчитанный на n значений.
 *
 * - String[] insert(int idKey, String value)
 *     Вставляет пару (idKey, value) в поток и возвращает
 *     самый большой возможный список подряд идущих
 *     значений, которые теперь могут быть выведены по
 *     порядку.
 *
 *
 * Пример для:
 *   [
 *     "OrderedStream", "insert", "insert",
 *     "insert", "insert", "insert"
 *   ]
 *   [
 *     [5], [3, "ccccc"], [1, "aaaaa"],
 *     [2, "bbbbb"], [5, "eeeee"], [4, "ddddd"]
 *   ]
 *
 * Output:
 *   [
 *     null, [], ["aaaaa"],
 *     ["bbbbb", "ccccc"], [], ["ddddd", "eeeee"]
 *   ]
 *
 * Пояснение:
 *   Значения, отсортированные по ID:
 *   ["aaaaa", "bbbbb", "ccccc", "ddddd", "eeeee"].
 *
 *   OrderedStream os = new OrderedStream(5);
 *
 *   os.insert(3, "ccccc"); Вставляет (3, "ccccc"),
 *   возвращает [].
 *
 *   os.insert(1, "aaaaa"); Вставляет (1, "aaaaa"),
 *   возвращает ["aaaaa"].
 *
 *   os.insert(2, "bbbbb"); Вставляет (2, "bbbbb"),
 *   возвращает ["bbbbb", "ccccc"].
 *
 *   os.insert(5, "eeeee"); Вставляет (5, "eeeee"),
 *   возвращает [].
 *
 *   os.insert(4, "ddddd"); Вставляет (4, "ddddd"),
 *   возвращает ["ddddd", "eeeee"].
 *
 *   Конкатенация всех возвращённых частей:
 *   [] + ["aaaaa"] + ["bbbbb", "ccccc"] + []
 *   + ["ddddd", "eeeee"]
 *   = ["aaaaa", "bbbbb", "ccccc", "ddddd", "eeeee"]
 *
 *   Итоговый порядок совпадает с ожидаемым.
 */
class OrderedStream {

  // Ключевая идея в том, что нам нужно выводить значения в
  // отсортированном порядке, но мы можем вывести очередное
  // значение только тогда, когда все предыдущие уже вставлены
  // и выведены.
  //
  // Представь это как сборку пазла, где детали нужно
  // выкладывать строго по номерам. Даже если у тебя есть
  // деталь №3, ты не можешь положить её, пока не стоят
  // №1 и №2. Но как только №2 поставлена, если №3 уже была у
  // тебя, ты можешь сразу выложить обе.
  //
  // Отсюда возникает идея использовать указатель, который
  // отслеживает текущую ожидаемую позицию - по сути отвечает
  // на вопрос: "Какой ID мы ждём следующим?".
  // При вставке значения:
  // - Если его позиция не совпадает с указателем - сохраняем
  //   его на потом.
  //
  // - Если оно закрывает позицию указателя - начинаем
  //   выводить значения.
  //
  // Хитрость в том, что после закрытия позиции указателя
  // можно вывести сразу несколько подряд идущих значений,
  // потому что некоторые могли быть вставлены заранее.
  // Поэтому мы двигаем указатель вперёд, пока не встречаем
  // незаполненную позицию.
  //
  // Использование массива идеально, потому что:
  // - ID идут от 1 до n - это естественно отображается на
  //   индексы массива.
  //
  // - Проверка заполненности позиции занимает O(1).
  //
  // - Указатель просто двигается вперёд.
  //
  // Указатель поддерживает важный инвариант:
  // все позиции до него уже выведены, а он указывает на
  // первую невыведенную. Это гарантирует правильный порядок
  // вывода и позволяет возвращать максимально возможный
  // "пакет" значений при каждой вставке.

  // Конструктор принимает n - ожидаемое количество элементов
  // в потоке (максимальный id).
  constructor(n) {

    // Создаём массив длиной n + 1, чтобы использовать удобную
    // 1‑индексацию: индексы от 1 до n включительно.
    //
    // Это упрощает соответствие idKey (который начинается с 1)
    // индексам массива и избегает смещения на -1.
    //
    // Элементы по умолчанию undefined.
    //
    // Память выделяется под n+1 ссылок.
    this.data = new Array(n + 1);

    // Указатель текущей позиции i - индекс первого ещё не
    // возвращённого элемента.
    //
    // Изначально равен 1, потому что поток ожидает элемент с
    // idKey = 1 первым.
    //
    // После каждого успешного чтения непрерывного блока i
    // сдвигается вперёд.
    this.i = 1;
  }

  /**
   * @param {number} idKey
   * @param {string} value
   * @return {string[]}
   *
   * Метод insert вставляет значение value в позицию idKey и
   * возвращает массив подряд идущих значений, начиная с
   * текущей позиции this.i. Если по this.i нет значения,
   * возвращается пустой массив.
  */
  insert(idKey, value) {

    // Записываем value в массив по индексу idKey.
    //
    // Если idKey уже был заполнен ранее, значение
    // перезапишется - поведение соответствует простому
    // присваиванию.
    //
    // idKey находится в диапазоне [1, n]
    this.data[idKey] = value;

    // Инициализируем массив для накопления самого большого
    // непрерывного чанка, начинающегося с текущей позиции
    // this.i.
    //
    // Этот массив будет возвращён в конце метода.
    const largestChunk = [];

    // Пока на позиции, на которую указывает this.i, есть
    // значение (т.е. позиция заполнена), добавляем это
    // значение в результат и сдвигаем указатель вправо.
    //
    // Это позволяет вернуть сразу несколько подряд идущих
    // элементов, если они уже были вставлены ранее.
    while (this.data[this.i]) {
      largestChunk.push(this.data[this.i]);
      this.i++;
    }

    // Возвращаем собранный массив подряд идущих значений.
    //
    // Если текущая вставка не закрыла позицию указателя,
    // массив будет пустым.
    return largestChunk;
  }
}


