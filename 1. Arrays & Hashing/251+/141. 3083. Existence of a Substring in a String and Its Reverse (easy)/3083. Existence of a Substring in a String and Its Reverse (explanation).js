/**
 * @param {string} s
 * @return {boolean}
 *
 * isSubstringPresent принимает строку s и проверяет, есть ли в ней
 * подстрока длины 2, которая также встречается в перевёрнутой
 * строке s.
 *
 * Возвращает true, если такая подстрока существует, иначе
 * возвращает false.
 *
 *
 * Пример для:
 *   s = "leetcode"
 * Результат:
 *   true
 * Пояснение:
 *   Подстрока "ee" длиной 2 также присутствует в
 *   reverse(s) == "edocteel".
 */
function isSubstringPresent(s) {
  const n = s.length;

  // Создаём пустой Set для хранения уникальных двухсимвольных
  // подстрок в порядке "левый затем правый".
  //
  // Set выбран из‑за быстрой проверки наличия элемента
  // (амортизированное O(1)).
  const substringSet = new Set();

  // Проходим по строке с i = 1 до i = n-1 включительно.
  //
  // На каждой итерации формируем двухсимвольную подстроку из
  // символов s[i-1] и s[i] - это все подряд идущие пары в прямом
  // порядке.
  //
  // Начинаем цикл с i = 1, так как на каждой итерации цикла нам
  // нужен доступ к пердыдущему cимволу строки для формирования
  // пары
  for (let i = 1; i < n; i++) {

    // Конкатенируем символы s[i-1] и s[i] в строку длины 2 и
    // добавляем в множество.
    //
    // Пример для s = "abca": добавятся "ab", "bc", "ca".
    substringSet.add(s[i - 1] + s[i]);
  }

  // Второй цикл идёт в обратном порядке: от n-2 до 0 включительно.
  //
  // На каждой итерации формируется пара символов в обратном
  // порядке относительно прямого обхода: s[i+1] + s[i].
  //
  // Цель: для каждой пары соседних символов в обратном порядке
  // (правый затем левый) проверить, встречалась ли когда-либо в
  // прямом порядке соответствующая пара.
  //
  // Начинаем с n-2, так как на каждой итерации цикла нам нужен
  // доступ к следующему символу строки для формирования пары
  //
  // Такой подход позволяет избежать операции реверса строки
  for (let i = n - 2; i >= 0; i--) {

    // Формируем обратную пару и проверяем, содержится ли она в
    // множестве прямых пар.
    //
    // Если да, значит существует индекс j такой, что
    // s[j] + s[j+1] == s[i+1] + s[i], то есть пара в прямом
    // порядке совпадает с некоторой парой в обратном порядке.
    //
    // В этом случае функция немедленно возвращает true - ранний
    // выход для эффективности.
    if (substringSet.has(s[i + 1] + s[i])) return true;
  }

  // Если ни одна обратная пара не была найдена в множестве прямых
  // пар, возвращаем false - таких "перекрёстных" подстрок нет.
  return false;
}
