/**
 * @param {string} s
 * @param {number} k
 * @return {number}
 *
 * minDeletion вычисляет минимальное число удалений символов
 * из строки s, чтобы в оставшейся строке было не более k
 * различных строчных латинских букв.
 *
 * Параметры:
 * - s - входная строка, ожидается состоящей из строчных
 *   английских букв a–z.
 *
 * - k - целое число, максимальное допустимое количество
 *   различных символов в результирующей строке.
 *
 *
 * Пример для:
 *   s = "yyyzz",
 *   k = 1
 * Результат:
 *   2
 * Пояснение:
 *   В строке s присутствуют два различных символа
 *   ('y' и 'z') с частотами 3 и 2 соответственно.
 *
 *   Поскольку допускается не более k = 1 различных символов,
 *   необходимо полностью удалить все вхождения одного из
 *   символов.
 *
 *   Удаление всех 'z' (2 символа) даёт строку, содержащую
 *   только 'y', то есть не более k различных символов.
 *
 *   Следовательно, минимальное число удалений равно 2.
 */
function minDeletion(s, k) {

  // Чтобы минимизировать число удалений при сохранении не
  // более k различных символов, нужно решить, какие символы
  // оставить, а какие удалить полностью.
  //
  // Ключевая идея: если мы собираемся удалить символ,
  // следует удалить все его вхождения. Почему? Потому что
  // сохранение хотя бы одного вхождения всё равно
  // считается как один различный символ, поэтому частичное
  // удаление не помогает уменьшить число различных символов.
  //
  // Учитывая это, задача сводится к выбору символов,
  // которые нужно удалить полностью, чтобы минимизировать
  // общее число удалений.
  //
  // Жадный подход: удалять сначала символы с наименьшей
  // частотой. Это интуитивно понятно - если приходится
  // удалять целые символы, выгоднее удалить те, которые
  // встречаются реже.
  //
  // Например, при частотах: a:10, b:8, c:2, d:1 и
  // необходимости оставить только 2 различных символа,
  // следует удалить 'd' (1 удаление) и 'c' (2 удаления),
  // всего 3 удаления, вместо удаления 'a' или 'b', что
  // потребовало бы гораздо больше удалений.
  //
  // Стратегия решения:
  // 1. Посчитать частоту каждого символа
  //
  // 2. Отсортировать эти частоты по возрастанию
  //
  // 3. Удалять символы, начиная с наименьшей частоты, пока
  //    не останется не более k различных символов
  //
  // 4. Сумма частот удалённых символов - ответ
  //
  // Выражение sorted(Counter(s).values())[:-k] элегантно
  // это отражает - оно берёт все значения частот, кроме k
  // наибольших (которые мы сохраняем), и суммирует
  // остальные (которые удаляем).


  // Инициализация счётчика уникальных символов, найденных в
  // строке.
  //
  // Этот счётчик увеличивается только при первом появлении
  // каждого символа.
  let uniqueCnt = 0;

  // Создание типизированного массива Int32Array длиной 26
  // для хранения частот каждой буквы.
  //
  // Индекс 0 соответствует 'a', индекс 25 - 'z'.
  //
  // Используется Int32Array для экономии памяти и более
  // быстрой работы с числовыми операциями по сравнению с
  // обычным массивом.
  //
  // Все элементы массива инициализируются нулем
  const charToFreq = new Int32Array(26);

  // Проходим по каждому символу строки s.
  for (const char of s) {

    // Получаем числовой индекс буквы в диапазоне 0..25:
    // код символа минус код 'a' (97).
    const charNdx = char.codePointAt(0) - 97;

    // Если частота данного символа равна нулю, значит он
    // встречается впервые, поэтому увеличиваем счётчик
    // уникальных символов.
    //
    // Это позволяет в дальнейшем быстро понять, сколько
    // разных букв присутствует в строке s.
    if (charToFreq[charNdx] === 0) uniqueCnt++;

    // Увеличиваем счётчик в массиве частот для текущего
    // символа на единицу.
    charToFreq[charNdx]++;
  }

  // Если количество уникальных символов уже не превышает k,
  // то удалять ничего не нужно.
  //
  // Возвращаем 0 как минимальное число удалений.
  //
  // Ранний выход экономит время на сортировке и
  // суммировании
  if (uniqueCnt <= k) return 0;

  // Сортируем массив частот по возрастанию, чтобы
  // наименьшие частоты оказались в начале.
  //
  // После сортировки первые элементы - это буквы, удаление
  // которых требует минимального числа удалений.
  charToFreq.sort((a, b) => a - b);

  // Инициализация счётчика суммарных удалений.
  //
  // Будем накапливать количество символов, которые нужно
  // удалить.
  let deletionsCnt = 0;

  // Проходим по первым (26 - k) элементам отсортированного
  // массива частот и суммируем их.
  //
  // Нам нужно удалить все буквы, кроме k наиболее частых,
  // поэтому суммируем частоты самых редких букв.
  //
  // Поскольку массив отсортирован по возрастанию, первые
  // (26 - k) элементов - это частоты букв, которые
  // выгоднее удалить полностью.
  for (let i = 0; i < 26 - k; i++) {

    // Добавляем частоту i-го по возрастанию элемента к
    // общему числу удалений.
    //
    // Если среди первых элементов есть нули (буквы, которые
    // не встречались), они не повлияют на сумму.
    deletionsCnt += charToFreq[i];
  }

  // Возвращаем минимальное количество удалений, необходимое
  // для того, чтобы в строке осталось не более k уникальных
  // символов.
  return deletionsCnt;
}
