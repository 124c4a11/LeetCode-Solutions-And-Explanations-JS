/**
 * @param {number[]} nums
 * @return {number}
 *
 * maxFrequencyElements принимает массив чисел nums, подсчитывает
 * частоту появления каждого его элемента и возвращает сумму всех
 * вхождений тех элементов, которые встречаются максимально часто.
 *
 *
 * Пример для:
 *   nums = [1,2,2,3,1,4]
 * Результат:
 *   4
 * Пояснение:
 *   Элементы 1 и 2 встречаются по 2 раза - это максимальная
 *   частота в массиве.
 *
 *   Следовательно суммарная частота элементов с максимальной
 *   частотой равна 4.
 */
function maxFrequencyElements(nums) {

  // Инициализация переменной maxFreq значением 0.
  //
  // Будет хранить наибольшее количество вхождений какого-либо
  // числа, найденное на текущем шаге.
  let maxFreq = 0;

  // Инициализация переменной maxFreqCnt значением 0.
  //
  // Будет хранить количество различных чисел, частота которых
  // равна maxFreq.
  let maxFreqCnt = 0;

  // Создание Map numToFreq для сопоставления каждого уникального
  // числа из nums с количеством его вхождений в массиве nums.
  const numToFreq = new Map();

  // Проходим по всем элементам входного массива nums
  for (const num of nums) {

    // Получение текущей частоты для числа num из Map.
    //
    // Если числа ещё нет в Map, get вернёт undefined, оператор
    // nullish coalescing (??) заменит undefined на 0, затем
    // прибавляем 1 - получаем обновлённую частоту.
    const currFreq = (numToFreq.get(num) ?? 0) + 1;

    // Сохраняем обновлённую частоту currFreq в Map для ключа num,
    // перезаписывая прежнее значение или добавляя новый num,
    // если его нет в Map.
    numToFreq.set(num, currFreq);

    // Если новая частота currFreq строго больше текущей
    // известной максимальной частоты maxFreq, значит найдено
    // новое значение с более высокой частотой, и нужно обновить
    // соответствующие счётчики.
    if (currFreq > maxFreq) {

      // Обновляем maxFreq до новой, более высокой частоты
      // currFreq.
      maxFreq = currFreq;

      // Сбрасываем счётчик maxFreqCnt в 1, потому что прежние
      // значения, которые соответствовали старому maxFreq,
      // больше не релевантны - теперь у нас новая максимальная
      // частота.
      //
      // Присваиваем maxFreqCnt 1, чтобы учесть текущее число
      // num как первое обнаруженное число с новой максимальной
      // частотой.
      maxFreqCnt = 1;

      // Если новая частота currFreq равна уже известной
      // максимальной частоте maxFreq, это означает, что ещё одно
      // (возможно ранее встречавшееся) значение теперь имеет
      // частоту, равную максимуму, поэтому нужно увеличить
      // счётчик таких значений.
    } else if (currFreq === maxFreq) {

      // Увеличиваем счётчик чисел, частота которых равна
      // текущему максимуму.
      maxFreqCnt++;
    }
  }

  // Возвращаем произведение maxFreq на maxFreqCnt.
  //
  // Это значение эквивалентно суммарному количеству элементов в
  // массиве, которые принадлежат всем значениям, имеющим
  // максимальную частоту (например, если два разных числа
  // встречаются по 3 раза, вернётся 6).
  return maxFreq * maxFreqCnt;
}
