/**
 * @param {string[]} arr
 * @param {number} k
 * @return {string}
 * 
 * kthDistinct ищет k-ю по порядку строку в массиве,
 * которая является уникальной (встретилась ровно
 * один раз) и возвращает ее.
 *
 * Если таких уникальных строк меньше k, функция
 * возвращает пустую строку.
 * 
 * Параметры:
 * - arr — массив строк 
 * - k — целое число, означающее позицию нужной
 *   уникальной строки в порядке первого появления
 * 
 * Пример:
 *   Для:
 *     arr = ["d","b","c","b","c","a"]
 *     k = 2
 * 
 *   Результат:
 *     "a"
 * 
 *   Объяснение:
 *     Единственные уникальные (distinct) строки
 *     в arr — это "d" и "a".
 * 
 *     "d" появляется первой, поэтому это 1-я
 *     уникальная строка.
 * 
 *     "a" появляется второй, поэтому это 2-я
 *     уникальная строка.
 * 
 *     Поскольку k == 2, возвращается "a".
 */
function kthDistinct(arr, k) {

  // Создаём новый объект Map для хранения
  // соответствия "строка → частота появления".
  const strToFreq = new Map();

  // Проходим по каждому элементу массива arr
  // в порядке от начала к концу.
  //
  // Цель этого прохода — подсчитать, сколько
  // раз каждая строка встречается в массиве
  // и сохранить это значение в strToFreq
  for (const str of arr) {

    // Получаем текущую зарегистрированную
    // частоту для строки str из Map.
    //
    // Если записи ещё нет, strToFreq.get(str)
    // вернёт undefined.
    //
    // Оператор ?? заменяет undefined на 0,
    // затем добавляем 1 — таким образом
    // выполняем инкремент счётчика для
    // данной строки.
    //
    // После вычисления новой частоты
    // записываем её обратно в Map по ключу
    // str.
    strToFreq.set(str, (strToFreq.get(str) ?? 0) + 1);
  }

  // Второй проход по массиву в том же порядке,
  // чтобы определить k-ю по порядку уникальную
  // строку.
  for (const str of arr) {

    // Проверяем в Map, сколько раз встречается
    // текущая строка.
    //
    // Если значение больше 1, значит строка
    // неуникальна (повторяется) — пропускаем
    // её и переходим к следующему элементу.
    if (strToFreq.get(str) > 1) continue;

    // Если строка уникальна (частота === 1),
    // уменьшаем счётчик k — мы нашли одну из
    // уникальных строк в порядке их появления.
    k--;

    // Проверяем, стало ли k равным 0.
    //
    // Условие if (!k) эквивалентно
    // if (k === 0).
    //
    // Если k достиг нуля, значит это искомая
    // k-я уникальная строка.
    //
    // В этом случае возвращаем строку и
    // завершаем выполнение функции.
    if (!k) return str;
  }

  // Если мы прошли весь массив и не вернули
  // значение, значит уникальных строк
  // оказалось меньше, чем исходное k.
  //
  // Функция возвращает пустую строку ''
  // как индикатор отсутствия k-й уникальной
  // строки.
  return '';
};
