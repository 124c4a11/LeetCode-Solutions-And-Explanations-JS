/**
 * @param {number[]} nums1
 * @param {number[]} nums2
 * @return {number[]}
 *
 * findIntersectionValues принимает два массива целых чисел
 * nums1 и nums2 размеров n и m соответственно.
 *
 * Вычисляет следующие значения:
 * - answer1 - количество индексов i таких, что nums1[i]
 *   присутствует в nums2.
 *
 * - answer2 - количество индексов i таких, что nums2[i]
 *   присутствует в nums1.
 *
 * Возвращает [answer1, answer2].
 *
 *
 * Пример для:
 *   nums1 = [4,3,2,3,1],
 *   nums2 = [2,2,5,2,3,6]
 * Результат:
 *   [3,4]
 * Пояснение:
 *   Элементы с индексами 1, 2 и 3 в nums1 также
 *   присутствуют в nums2. Поэтому answer1 = 3.
 *
 *   Элементы с индексами 0, 1, 3 и 4 в nums2 также
 *   присутствуют в nums1. Поэтому answer2 = 4.
 */
function findIntersectionValues(nums1, nums2) {

  // Создаём типизированный массив Int32Array длиной 101
  // для отметки присутствия значений из nums1.
  //
  // Индексы этого массива соответствуют возможным
  // значениям элементов (0..100).
  //
  // Все элементы инициализируются нулём.
  //
  // Значение 0 означает "не встречалось",
  // 1 - "встречалось".
  //
  // Используем числовые значения вместо булевых для
  // удобства подсчёта общих значений в обоих массивах без
  // дополнительных проверок.
  const num1ToSeen = new Int32Array(101);

  // Проходим по каждому элементу nums1 и помечаем
  // соответствующий индекс в num1ToSeen как 1.
  //
  // Это превращает массив в булевую карту присутствия:
  // индекс = значение числа,
  // значение = 1 (присутствует) или 0 (отсутствует).
  for (const num of nums1) num1ToSeen[num] = 1;

  // Создаём типизированный массив Int32Array длиной 101
  // для отметки присутствия значений из nums2.
  //
  // Индексы этого массива соответствуют возможным
  // значениям элементов (0..100).
  //
  // Все элементы инициализируются нулём.
  //
  // Значение 0 означает "не встречалось",
  // 1 - "встречалось".
  //
  // Используем числовые значения вместо булевых для
  // удобства подсчёта общих значений в обоих массивах без
  // дополнительных проверок.
  const num2ToSeen = new Int32Array(101);

  // Проходим по каждому элементу nums2 и помечаем
  // соответствующий индекс в num2ToSeen как 1.
  //
  // Это превращает массив в булевую карту присутствия:
  // индекс = значение числа,
  // значение = 1 (присутствует) или 0 (отсутствует).
  for (const num of nums2) num2ToSeen[num] = 1;

  // Создаём Int32Array длиной 2 для хранения результатов
  // подсчёта.
  //
  // result[0] будет накапливать количество элементов из
  // nums1, которые присутствуют в nums2.
  //
  // result[1] будет накапливать количество элементов из
  // nums2, которые присутствуют в nums1.
  const result = new Int32Array(2);

  // Проходим по каждому элементу nums1 и добавляем к
  // result[0] значение флага num2ToSeen[num].
  //
  // Поскольку num2ToSeen[num] равен либо 0, либо 1, это
  // фактически считает количество элементов nums1, для
  // которых существует соответствующее значение в nums2.
  //
  // Если nums1 содержит дубликаты, каждый дубликат также
  // будет учитываться.
  for (const num of nums1) {
    result[0] += num2ToSeen[num];
  }

  // Проходим по каждому элементу nums2 и добавляем к
  // result[1] значение флага num1ToSeen[num].
  //
  // Аналогично предыдущему циклу, это считает количество
  // элементов nums2, которые также встречаются в nums1.
  //
  // Дубликаты в nums2 также будут учитываться.
  for (const num of nums2) {
    result[1] += num1ToSeen[num];
  }

  // Возвращаем массив result где:
  //
  // result[0] - число элементов из nums1, найденных в
  // nums2 (с учётом повторов);
  //
  // result[1] - число элементов из nums2, найденных в
  // nums1 (с учётом повторов).
  return result;
}
