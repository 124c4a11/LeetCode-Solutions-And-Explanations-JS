/**
 * @param {number[]} nums
 * @return {number}
 *
 * maxProduct находит два наибольших элемента в переданном
 * массиве чисел и возвращает произведение
 * (maxA - 1) * (maxB - 1)
 *
 *
 * Пример для:
 *   nums = [3,4,5,2]
 * Результат:
 *   12
 * Пояснение:
 *   Если выбрать индексы i = 1 и j = 2 (индексация с 0),
 *   получится максимальное значение:
 *     (nums[1] - 1) * (nums[2] - 1)
 *     = (4 - 1) * (5 - 1)
 *     = 3 * 4 = 12
 */
function maxProduct(nums) {

  // Инициализируем переменную maxA значением
  // отрицательной бесконечности.
  //
  // Это гарантирует, что любой числовой элемент массива
  // будет больше начального значения.
  //
  // maxA будет хранить наибольший найденный элемент по
  // ходу обхода.
  let maxA = -Infinity;

  // Инициализируем переменную maxB значением
  // отрицательной бесконечности.
  //
  // maxB будет хранить второй по величине элемент.
  let maxB = -Infinity;

  // Итерация по всем элементам входного массива nums.
  for (const num of nums) {

    // Сравниваем текущий элемент num с текущим максимумом
    // maxA.
    //
    // Если num строго больше maxA, значит найден новый
    // максимум.
    if (num > maxA) {

      // Перед обновлением maxA сдвигаем прежний максимум
      // в maxB.
      //
      // Это сохраняет второй по величине элемент без
      // дополнительного прохода по массиву.
      maxB = maxA;

      // Обновляем maxA до текущего элемента num - теперь
      // это новый наибольший элемент, найденный до сих
      // пор.
      maxA = num;

      // Если текущий элемент не превысил maxA, проверяем,
      // может ли он стать вторым по величине (maxB).
    } else if (num > maxB) {

      // Если num больше текущего maxB (но не больше maxA),
      // то он становится новым вторым по величине
      // элементом.
      //
      // Обратите внимание:
      // При равенстве с maxA (num === maxA) это условие
      // выполнится, если num > maxB, что позволяет
      // корректно обрабатывать случаи с двумя одинаковыми
      // максимальными значениями.
      maxB = num;
    }
  }

  // После прохода по массиву maxA и maxB содержат два
  // наибольших значения.
  //
  // Возвращаем результат в виде произведения
  // (maxA - 1) * (maxB - 1).
  return (maxA - 1) * (maxB - 1);
}
