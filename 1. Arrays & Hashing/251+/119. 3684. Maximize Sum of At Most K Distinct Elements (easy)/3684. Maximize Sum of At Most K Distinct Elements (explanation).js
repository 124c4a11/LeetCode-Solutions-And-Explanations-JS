/**
 * @param {number[]} nums
 * @param {number} k
 * @return {number[]}
 *
 * maxKDistinct функция возвращает не более k уникальных
 * наибольших чисел из массива nums, упорядоченных по убыванию.
 *
 * Параметры:
 * - nums - массив чисел
 *
 * - k - целое число, сколько уникальных максимальных значений
 *   нужно вернуть.
 *
 *
 * Пример для:
 *   nums = [1,1,1,2,2,2],
 *   k = 6
 * Результат:
 *   [2,1]
 * Пояснение:
 *   Максимальная сумма равна 3, которая достигается выбором
 *   1 и 2.
 *
 *   Мы переставляем их в строго убывающем порядке как [2, 1].
 */
function maxKDistinct(nums, k) {

  // Сортируем массив nums по убыванию (от большего к меньшему)
  //
  // sort изменяет исходный массив на месте
  nums.sort((a, b) => b - a);

  // Создаём пустой массив для накопления найденных различных
  // максимальных значений.
  //
  // Этот массив будет содержать уникальные элементы в порядке
  // убывания (первый элемент - наибольший).
  const maxKDistinctNums = [];

  // Проходим по каждому элементу отсортированного массива nums.
  for (const num of nums) {

    // Проверяем, равен ли текущий элемент последнему
    // добавленному в maxKDistinctNums.
    //
    // Метод at(-1) возвращает последний элемент массива или
    // undefined, если массив пуст.
    //
    // Это позволяет пропускать подряд идущие дубликаты в
    // отсортированном массиве.
    if (maxKDistinctNums.at(-1) === num) continue;

    // Если текущий элемент отличается от последнего
    // добавленного, добавляем его в результирующий массив.
    //
    // Это гарантирует, что в maxKDistinctNums будут только
    // уникальные значения (в контексте последовательности).
    //
    // Поскольку мы итерируемся по убыванию, добавляемые
    // элементы будут упорядочены от наибольшего к меньшему.
    maxKDistinctNums.push(num);

    // Уменьшаем счётчик k на единицу и проверяем, достигли ли
    // мы требуемого количества уникальных значений.
    //
    // Оператор --k сначала декрементирует значение k, затем
    // возвращает новое значение для сравнения.
    //
    // Если после декремента k стало 0, значит мы собрали
    // ровно требуемое количество уникальных максимальных
    // чисел, и можно прервать цикл досрочно, чтобы не
    // выполнять лишнюю работу.
    if (--k === 0) break;
  }

  // Возвращаем массив найденных уникальных максимальных
  // значений.
  //
  // Если уникальных значений в nums было меньше, чем исходное
  // k, массив будет короче.
  return maxKDistinctNums;
}
