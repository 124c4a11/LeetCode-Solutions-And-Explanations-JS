/**
 * @param {number[]} nums
 * @param {number} k
 * @param {number} multiplier
 * @return {number[]}
 *
 * getFinalState принимает массив целых чисел nums, целое
 * число k и целое число multiplier.
 *
 * Выполняет k операций над nums. В каждой операции:
 * - Находт минимальное значение x в nums. Если
 *   минимальных значений несколько, выбрает то, которое
 *   встречается первым.
 *
 * - Заменяет выбранное минимальное значение x на
 *   x * multiplier.
 *
 * Возвращает массив целых чисел, обозначающий итоговое
 * состояние nums после выполнения всех k операций.
 *
 *
 * Пример для:
 *   nums = [2,1,3,5,6],
 *   k = 5,
 *   multiplier = 2
 * Результат:
 *   [8,4,6,5,6]
 * Пояснение:
 *   После операции 1  [2, 2, 3, 5, 6]
 *   После операции 2  [4, 2, 3, 5, 6]
 *   После операции 3  [4, 4, 3, 5, 6]
 *   После операции 4  [4, 4, 6, 5, 6]
 *   После операции 5  [8, 4, 6, 5, 6]
 */
function getFinalState(nums, k, multiplier) {

  // Создаётся экземпляр приоритетной очереди
  // (минимальная куча).
  //
  // В очередь будут помещаться индексы элементов массива
  // nums, а не сами значения, чтобы при изменении nums
  // можно было обновлять приоритеты, переиспользуя
  // индексы.
  const minHeap = new PriorityQueue((a, b) => {

    // Компаратор получает два индекса a и b.
    //
    // Сначала сравниваются значения массива nums по
    // этим индексам.
    //
    // Если значения равны, используется tie-breaker по
    // индексам.
    //
    // Это обеспечивает детерминированный и стабильный
    // порядок при равных значениях: элемент с меньшим
    // индексом будет считаться "меньше" и извлекаться
    // раньше.
    if (nums[a] === nums[b]) return a - b;

    // Если значения различаются, меньший по значению
    // элемент считается "меньше" и будет извлечён раньше
    // (минимальная куча).
    return nums[a] - nums[b];
  });

  // Заполняем кучу индексами всех элементов массива nums.
  //
  // Это обеспечивает, что на старте в куче присутствуют
  // все позиции для выбора.
  for (let i = 0; i < nums.length; i++) {
    minHeap.enqueue(i);
  }

  // Цикл выполняется ровно k раз.
  //
  // Конструкция while (k--) использует постфиксный
  // декремент: условие проверяет текущее значение k > 0,
  // затем уменьшает k на 1.
  while (k--) {

    // Извлекаем из мин-кучи индекс i, соответствующий
    // минимальному значению nums[i] (при равенстве
    // значений - меньший индекс)
    //
    // dequeue удаляет и возвращает этот индекс из кучи.
    const i = minHeap.dequeue();

    // Модифицируем значение в массиве:
    //
    // Умножаем nums[i] на multiplier и сохраняем
    // результат обратно в nums[i].
    //
    // Операция изменяет исходный массив nums in-place.
    nums[i] *= multiplier;

    // После изменения значения заново помещаем тот же
    // индекс в мин-кучу, чтобы его приоритет был
    // пересчитан и он мог снова участвовать в
    // последующих извлечениях.
    //
    // Без этого шага очередь не отразит обновлённое
    // значение.
    minHeap.enqueue(i);
  }

  // Возвращаем модифицированный массив nums как итоговое
  // состояние после k итераций.
  return nums;
}
