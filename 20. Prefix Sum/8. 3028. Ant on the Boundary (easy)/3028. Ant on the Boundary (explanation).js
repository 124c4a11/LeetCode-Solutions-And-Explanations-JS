/**
 * @param {number[]} nums
 * @return {number}
 *
 * returnToBoundaryCount подсчитывает, сколько раз
 * накопительная (префиксная) сумма элементов
 * входного массива становится равной нулю при
 * последовательном проходе по элементам. Это
 * моделирует задачу, где муравей начинает в
 * позиции 0 и после каждого шага (элемента массива)
 * перемещается влево или вправо на величину,
 * заданную элементом. Функция считает, сколько раз
 * муравей оказывается точно на границе (позиции 0)
 * после хода.
 *
 * Параметры:
 *   nums - массив ненулевых чисел. Положительное
 *   значение означает движение вправо,
 *   отрицательное — влево.
 *
 *
 * Пример для:
 *   nums = [2,3,-5]
 * Результат:
 *   1
 * Пояснение:
 *   После первого шага муравей находится в 2 шагах
 *   вправо от границы.
 *
 *   После второго шага муравей находится в 5 шагах
 *   вправо от границы.
 *
 *   После третьего шага муравей оказывается на
 *   границе.
 *
 *   Следовательно, ответ равен 1.
 */
function returnToBoundaryCount(nums) {

  // Инициализация счётчика result значением 0.
  //
  // result будет увеличиваться на единицу каждый
  // раз, когда накопленная сумма delta станет
  // ровно нулём.
  let result = 0;

  // Инициализация переменной delta значением 0.
  //
  // delta служит для накопления текущей суммы
  // (смещения) при последовательном обходе
  // элементов массива.
  //
  // Начальное значение 0 означает, что до
  // обработки элементов муравей находится на
  // границе
  let delta = 0;

  // Перебираем каждый элемент коллекции nums по
  // порядку.
  for (const num of nums) {

    // К текущему накопленному значению delta
    // прибавляется значение текущего элемента num.
    //
    // Это позволяет отслеживать, как суммарное
    // смещение меняется после каждого шага.
    delta += num;

    // Условие проверяет, равна ли обновлённая delta
    // нулю
    //
    // Если условие истинно, это означает, что после
    // добавления текущего элемента муравей
    // "вернулся" к нулевой границе, поэтому
    // увеличиваем счётчик result на единицу.
    if (delta === 0) result++;
  }

  // Возвращаем итоговое значение result — количество
  // моментов, когда delta была равна нулю, то есть
  // муравей вернулся на границу
  return result;
}
