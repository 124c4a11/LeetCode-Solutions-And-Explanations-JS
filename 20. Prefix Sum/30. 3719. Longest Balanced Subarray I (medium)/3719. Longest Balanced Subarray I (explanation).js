/**
 * @param {number[]} nums
 * @return {number}
 *
 * longestBalanced принимает на вход массив целых чисел nums
 * и возвращает длину самой длинной подстроки (подмассива),
 * в которой количество различных чётных чисел равно
 * количеству различных нечётных чисел.
 *
 *
 * Пример для:
 *   nums = [2,5,4,3]
 * Результат:
 *   4
 * Пояснение:
 * Самый длинный сбалансированный подмассив - [2, 5, 4, 3].
 * В нём 2 различных чётных числа [2, 4] и 2 различных
 * нечётных числа [5, 3].
 * Следовательно, ответ равен 4.
 *
 *
 * Пример для:
 *   nums = [3,2,2,5,4]
 * Результат:
 *   5
 * Пояснение:
 * Самый длинный сбалансированный подмассив
 * - [3, 2, 2, 5, 4].
 * В нём 2 различных чётных числа [2, 4] и 2 различных
 * нечётных числа [3, 5].
 * Следовательно, ответ равен 5.
 *
 *
 * Пример для:
 *   nums = [1,2,3,2]
 * Результат:
 *   3
 * Пояснение:
 * Самый длинный сбалансированный подмассив — [2, 3, 2].
 * В нём 1 различное чётное число [2] и 1 различное нечётное
 * число [3].
 * Следовательно, ответ равен 3.
 */
function longestBalanced(nums) {
  const n = nums.length;

  // Инициализируем переменную для хранения максимальной
  // длины найденного сбалансированного подмассива.
  let maxSubarrayLen = 0;

  // Создаём Int32Array для меток посещения значений из nums.
  //
  // Размер массива равен (максимальное значение в nums + 1),
  // чтобы индекс соответствовал самому числу.
  //
  // Элемент numToSeenAt[x] будет хранить метку (i+1),
  // указывающую, в какой итерации внешнего цикла, с началом
  // i, это число x уже встречалось.
  //
  // Почему используем в качестве метки i + 1:
  // Int32Array по умолчанию заполнен нулями. Если помечать
  // посещённые значения просто i, то при i === 0 метка
  // совпадёт с начальным значением массива и число будет
  // считаться уже "увиденным" до первой проверки. Запись
  // i + 1 даёт ненулевую метку для первой итерации и
  // позволяет однозначно отличать "не видели" (0) от
  // "видели при текущем i" (i+1)
  const numToSeenAt = new Int32Array(Math.max(...nums) + 1);

  // Внешний цикл перебирает все возможные начальные
  // индексы подмассива i.
  //
  // Для каждого i мы будем расширять правую границу j и
  // отслеживать уникальные числа с учетом чётности.
  for (let i = 0; i < n; i++) {

    // Оптимизация:
    //
    // Если оставшаяся длина массива (n - i) уже меньше или
    // равна текущему максимуму, то дальнейшие подмассивы,
    // начинающиеся с i или позже, не смогут превзойти
    // maxSubarrayLen.
    //
    // В этом случае можно прервать внешний цикл и вернуть
    // текущий максимум.
    if (maxSubarrayLen >= n - i) break;

    // Счётчик баланса уникальных чисел с учетом чётности для
    // текущего начала i.
    //
    // При первом появлении уникального нечётного числа
    // увеличиваем на 1, при первом появлении уникального
    // чётного — уменьшаем на 1.
    //
    // Баланс равный нулю означает, что количество уникальных
    // нечётных равно количеству уникальных чётных.
    let uniqueParityBalance = 0;

    // Внутренний цикл расширяет правую границу подмассива от
    // i до j включительно.
    for (let j = i; j < n; j++) {

      // Получаем текущее число из массива
      const num = nums[j];

      // Проверяем, встречалось ли это конкретное значение
      // num в текущем подмассиве, начинающемся с i.
      //
      // Используется приём с метками:
      // Если numToSeenAt[num] !== i+1, значит число ещё не
      // встречалось в текущем подмассиве
      if (numToSeenAt[num] != i + 1) {

        // Если число ещё не учитывалось в текущем подмассиве,
        // обновляем баланс с учетом чётности:
        //
        // Выражение (num & 1) даёт 1 для нечётных и 0 для
        // чётных.
        //
        // Если нечётное — добавляем 1 к балансу
        // Если чётное — вычитаем 1
        uniqueParityBalance += (num & 1) ? 1 : -1;

        // Помечаем число как встреченное в этой итерации
        // внешнего цикла, записывая i+1.
        //
        // Использование i+1 вместо i позволяет отличать
        // метку от начального нулевого значения массива.
        numToSeenAt[num] = i + 1;
      }

      // Если после возможного обновления баланс уникальных
      // чисел с учетом чётности стал равен нулю, значит в
      // подмассиве nums[i..j] количество уникальных
      // нечётных равно количеству уникальных чётных.
      if (uniqueParityBalance === 0) {

        // Обновляем максимальную длину:
        //
        // Сравниваем текущую максимальную длину с длиной
        // текущего подмассива (j - i + 1).
        //
        // Math.max гарантирует, что maxSubarrayLen всегда
        // хранит наибольшую найденную длину.
        maxSubarrayLen = Math.max(maxSubarrayLen, j - i + 1);
      }
    }
  }

  // Возвращаем длину наибольшего сбалансированного
  // подмассива, найденного в массиве nums.
  return maxSubarrayLen;
}
