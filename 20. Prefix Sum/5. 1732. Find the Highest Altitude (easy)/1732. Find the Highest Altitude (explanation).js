/**
 * @param {number[]} gain
 * @return {number}
 *
 * largestAltitude вычисляет наибольшую высоту, которую
 * достигает путешественник (или велосипедист), если он
 * стартует с высоты 0 и последовательно применяет
 * приращения высоты из массива gain.
 *
 * На каждом шаге функция накапливает текущее изменение
 * высоты и отслеживает максимум среди всех
 * промежуточных высот.
 *
 * Параметры:
 * - gain — массив целых чисел (Array), где каждый
 *   элемент gain[i] представляет собой изменение
 *   высоты при переходе от точки i к точке i+1.
 *
 *   Положительное значение означает подъём,
 *   отрицательное — спуск.
 *
 *
 * Пример для:
 *   gain = [-5,1,5,0,-7]
 * Результат:
 *   1
 * Пояснение:
 *   Высоты по шагам: [0, -5, -4, 1, 1, -6].
 *   Максимальная высота — 1.
 */
function largestAltitude(gain) {

  // Инициализация переменной maxAltitude значением 0.
  //
  // Эта переменная хранит наибольшую высоту,
  // достигнутую в процессе суммирования приращений.
  //
  // Начальное значение 0 соответствует условию задачи,
  // что начальная высота равна нулю.
  let maxAltitude = 0;

  // Инициализация переменной currAltitude значением 0.
  //
  // currAltitude отслеживает текущую высоту на каждом
  // шаге после применения очередного приращения.
  //
  // Используется для накопления пройденной высоты по
  // мере итерации по массиву gain.
  let currAltitude = 0;

  // Последовательно перебирает все элементы массива
  // gain
  for (const altitude of gain) {

    // К текущей высоте currAltitude прибавляется
    // текущее приращение altitude.
    //
    // Это обновляет состояние высоты после очередного
    // шага (восхождение или спуск).
    currAltitude += altitude;

    // Обновляем maxAltitude:
    //
    // Сравниваем предыдущее максимальное значение и
    // новую текущую высоту.
    //
    // Math.max возвращает большее из двух значений.
    // Таким образом maxAltitude всегда хранит
    // наибольшую встреченную высоту.
    maxAltitude = Math.max(maxAltitude, currAltitude);
  }

  // После обработки всех приращений возвращаем
  // найденную максимальную высоту.
  //
  // Если все приращения отрицательны или массив пуст,
  // вернётся 0 (стартовая высота).
  return maxAltitude;
}
