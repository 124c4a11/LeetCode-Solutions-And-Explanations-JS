/**
 * @param {number} n
 * @return {number}
 *
 * pivotInteger принимает число n и находит pivot integer
 * - целое число x в диапазоне [1, n], для которого сумма
 * чисел от 1 до x равна сумме чисел от x до n.
 *
 * Если такое число x существует функция, возвращает его
 *
 * Если нет — возвращает -1
 *
 *
 * Пример для:
 *   n = 8
 * Результат:
 *   6
 * Пояснение:
 *   6 — это pivot integer, потому что:
 *   1 + 2 + 3 + 4 + 5 + 6 = 21
 *   6 + 7 + 8 = 21
 */
function pivotInteger(n) {

  // Вычисляем сумму первых n натуральных чисел по
  // классической формуле арифметической прогрессии:
  //
  // sum = 1 + 2 + ... + n = n * (n + 1) / 2.
  const sum = n * (n + 1) / 2;

  // Берём квадратный корень из получённой суммы.
  //
  // Идея:
  // Если sum — полный квадрат, то sqrt(sum) даст целое
  // число pivot, которое одновременно является суммой
  // от 1 до pivot и от pivot до n при определённых
  // условиях задачи.
  //
  // Результат Math.sqrt также возвращается как Number
  // и может быть не точно целым из‑за представления с
  // плавающей точкой.
  const pivot = Math.sqrt(sum);

  // Проверям, является ли pivot целым числом:
  //
  // Выражение pivot % 1 даёт дробную часть числа
  // (например, 3.5 % 1 === 0.5; 4 % 1 === 0), для целого
  // числа дробная часть ровна 0.
  //
  // Если дробная часть ненулевая (истинна), возвращается
  // -1 — сигнал, что целого "pivot" не существует.
  //
  // В противном случае возвращается само значение pivot.
  return pivot % 1 ? -1 : pivot;
}
