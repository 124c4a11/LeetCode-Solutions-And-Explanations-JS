/**
 * @param {number[]} nums
 * @param {number} k
 * @return {boolean}
 *
 * checkSubarraySum принимает массив целых чисел nums и
 * целое число k.
 *
 * Возвращает true, если в nums существует "хороший"
 * подмассив, иначе возвращает false.
 *
 * "Хороший" подмассив - это подмассив, у которого:
 * - длина не меньше двух,
 *
 * - сумма элементов подмассива кратна k.
 *
 * Примечания:
 * - Подмассив - это непрерывная часть массива.
 *
 * - Целое число x является кратным k, если существует целое
 *   n такое, что x = n * k.
 *   Ноль всегда кратен k.
 *
 *
 * Пример для:
 *   nums = [23, 2, 4, 6, 7],
 *   k = 6
 * Результат:
 *   true
 * Пояснение:
 *   nums = [23, 2, 4, 6, 7],
 *               ^  ^
 *   [2, 4] - это непрерывный подмассив длины 2, сумма
 *   элементов которого кратна 6.
 */
function checkSubarraySum(nums, k) {

  // Ключевая идея вытекает из понимания того, как работает
  // арифметика по модулю в сочетании с префиксными суммами.
  // Если мы хотим узнать, есть ли подмассив с суммой,
  // кратной k, можно воспользоваться фактом: если две
  // префиксные суммы дают одинаковый остаток при делении
  // на k, то сумма подмассива между ними кратна k.
  //
  // Разберём по шагам:
  // - Если префиксная сумма до индекса i равна sum_i, а до
  //   индекса j - sum_j (где j < i), то сумма подмассива от
  //   j+1 до i равна sum_i - sum_j.
  //
  // - Чтобы эта сумма делилась на k, нужно, чтобы
  //   (sum_i - sum_j) % k = 0.
  //
  // - Это происходит тогда, когда sum_i % k = sum_j % k.
  //   Иными словами, обе префиксные суммы дают одинаковый
  //   остаток при делении на k.
  //
  // Вместо того чтобы проверять все возможные подмассивы
  // (что неэффективно), можно отслеживать остатки
  // префиксных сумм по мере прохода по массиву. Если мы
  // видим один и тот же остаток в двух разных позициях,
  // которые находятся как минимум на двух индексах друг от
  // друга, значит между ними есть подходящий подмассив.
  //
  // Зачем нужна дистанция не менее двух индексов? Потому
  // что подмассив должен содержать как минимум два элемента.
  // Если одинаковые остатки найдены в позициях j и i при
  // условии i - j > 1, то подмассив от j+1 до i содержит
  // i - (j+1) + 1 = i - j элементов, то есть как минимум 2.
  //
  // Подход с хеш-таблицей естественно вытекает из этой идеи
  // - мы сохраняем каждый остаток с позицией его первого
  // появления, а при повторном встречании остатка проверяем,
  // прошло ли достаточно индексов, чтобы образовался
  // валидный подмассив.

  // Создаём Map для хранения первого индекса, где
  // встречается конкретный остаток префиксной суммы по
  // модулю k.
  //
  // Инициализируем запись для остатка 0 значением -1, чтобы
  // корректно обрабатывать подмассивы, начинающиеся с
  // индекса 0.
  //
  // Хранение первого индекса важно: при повторном появлении
  // того же остатка между двумя такими индексами сумма
  // элементов кратна k.
  const prefixRemainderToNdx = new Map([[0, -1]]);

  // Переменная для накопления текущего остатка префиксной
  // суммы по модулю k.
  //
  // Начинаем с 0 - это остаток суммы пустой префиксной
  // части.
  let prefixRemainder = 0;

  // Проходим по всем элементам массива
  for (let i = 0; i < nums.length; i++) {

    // Обновляем префиксный остаток:
    //
    // Добавляем текущий элемент nums[i] к накопленной сумме
    // и берём остаток от деления на k.
    prefixRemainder = (prefixRemainder + nums[i]) % k;

    // Если для текущего остатка ещё не сохранён индекс в
    // Map, сохраняем текущий индекс как первый встреченный.
    //
    // Сохранение именно первого индекса важно: при проверке
    // длины подмассива мы хотим максимально ранний левый
    // край, чтобы иметь наибольшую вероятность получить
    // длину >= 2.
    if (!prefixRemainderToNdx.has(prefixRemainder)) {
      prefixRemainderToNdx.set(prefixRemainder, i);

      // Если остаток уже встречался ранее, это означает,
      // что сумма элементов между индексом первого появления
      // и текущим индексом кратна k.
      //
      // Проверяем, что длина такого подмассива не менее 2:
      // текущее i минус сохранённый индекс должен быть
      // больше 1.
    } else if (i - prefixRemainderToNdx.get(prefixRemainder) > 1) {

      // Если расстояние между текущим индексом и индексом
      // первого появления остатка больше 1, то существует
      // подмассив длины >= 2 (индексы (firstIndex+1) .. i),
      // сумма которого делится на k.
      //
      // Объяснение математической идеи:
      // Если prefixSum[i] % k === prefixSum[j] % k и i > j,
      // то (prefixSum[i] - prefixSum[j]) % k === 0, то есть
      // сумма элементов между j+1 и i кратна k.
      return true;
    }
  }

  // Если мы прошли весь массив и не нашли подходящего
  // подмассива, возвращаем false.
  //
  // Это означает, что не существует пары префиксных сумм с
  // одинаковым остатком, расположенных на расстоянии не
  // менее 2 индексов, дающих подмассив длиной >= 2 с
  // суммой, кратной k.
  return false;
}
