/**
 * @param {number[]} nums
 * @return {number[]}
 *
 * getSumAbsoluteDifferences принимает на вход массив чисел nums,
 * отсортированный в неубывающем порядке и возвращает массив той
 * же длины, где для каждого индекса i хранится сумма абсолютных
 * разниц между этим элементом и всеми остальными элементами
 * массива nums
 *
 *
 * Пример для:
 *   nums = [2,3,5]
 * Результат:
 *   [4,3,5]
 * Пояснение:
 *   Предполагая, что индексация массива начинается с нуля,
 *   result[0] = |2-2| + |2-3| + |2-5| = 0 + 1 + 3 = 4,
 *   result[1] = |3-2| + |3-3| + |3-5| = 1 + 0 + 2 = 3,
 *   result[2] = |5-2| + |5-3| + |5-5| = 3 + 2 + 0 = 5.
 */
function getSumAbsoluteDifferences(nums) {

  // Ключевая идея заключается в том, что массив отсортирован
  // в неубывающем порядке.
  //
  // Это свойство позволяет нам не вычислять абсолютные значения
  // с условием, а разложить сумму на две части.
  //
  // Для любого элемента nums[i] в отсортированном массиве:
  // - Все элементы перед ним (индексы 0..i-1) меньше или равны,
  //   поэтому |nums[i] - nums[j]| = nums[i] - nums[j]
  //
  // - Все элементы после него (индексы i+1..n-1) больше или
  //   равны, поэтому |nums[i] - nums[j]| = nums[j] - nums[i]
  //
  // Чтобы вычислить сумму абсолютных разниц для nums[i]:
  //
  // Вклад левой части:
  // - Нам нужно (nums[i] - nums[0]) + (nums[i] - nums[1]) +
  //   ... + (nums[i] - nums[i-1])
  //
  // - Это упрощается до
  //   nums[i] * i - (nums[0] + nums[1] + ... + nums[i-1])
  //
  // - Что равно nums[i] * i - t, где t — сумма всех элементов
  //   до индекса i (не включая i)
  //
  // Вклад правой части:
  // - Нам нужно (nums[i+1] - nums[i]) + (nums[i+2] - nums[i]) +
  //   ... + (nums[n-1] - nums[i])
  //
  // - Это упрощается до (nums[i+1] + nums[i+2] +
  //   ... + nums[n-1]) - nums[i] * (n - i - 1)
  //
  // - Сумма элементов после i равна s - t - nums[i],
  //   где s — общая сумма массива
  //
  // - Поэтому вклад становится
  //   (s - t - nums[i]) - nums[i] * (n - i - 1)
  //
  // - Что упрощается до s - t - nums[i] * (n - i)
  //
  // Объединяя оба вклада:
  // result[i] = nums[i] * i - t + s - t - nums[i] * (n - i)
  //
  // Поддерживая текущую сумму t при проходе по массиву, мы
  // можем вычислять каждое result[i] за константное время, что
  // даёт общую линейную временную сложность O(n).

  const n = nums.length;

  // Вычисляет общую сумму всех элементов массива nums
  const totalSum = nums.reduce((a, b) => a + b, 0);

  // Создаёт массив result длины n для хранения итоговых
  // значений
  const result = new Array(n);

  // Инициализируем префиксную сумму prefixSum нулём — она
  // будет хранить сумму элементов слева от текущего индекса.
  let prefixSum = 0;

  // Запускаем цикл по всем индексам массива от 0 до n-1
  for (let i = 0; i < n; i++) {

    // Вычисляет вклад левой части:
    //
    // Сумма разниц между nums[i] и всеми элементами слева
    const leftPart = nums[i] * i - prefixSum;

    // Вычисляет вклад правой части:
    //
    // Сумма разниц между элементами справа и nums[i]
    const rightPart = totalSum - prefixSum - nums[i] * (n - i);

    // Суммирует левую и правую части и сохраняет результат
    // для текущего индекса
    result[i] = leftPart + rightPart;

    // Обновляет prefixSum, добавляя текущий элемент, чтобы
    // он отражал сумму элементов слева для следующей итерации
    prefixSum += nums[i];
  }

  // Возвращает массив result с суммами абсолютных разниц для
  // каждого индекса
  return result;
}
