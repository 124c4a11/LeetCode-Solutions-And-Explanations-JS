/**
 * @param {number[]} nums
 * @param {number} target
 * @return {number}
 *
 * countMajoritySubarrays принимает целочисленный массив
 * nums, целое число target и возвращает количество
 * подмассивов массива nums, в которых target является
 * мажоритарным.
 *
 * Мажоритарный элемент подмассива — это элемент,
 * который встречается строго более чем в половине
 * элементов этого подмассива.
 *
 *
 * Пример для:
 *   nums = [1,2,2,3],
 *   target = 2
 * Результат:
 *   5
 * Пояснение:
 * Подмассивы, в которых target = 2 является элементом
 * большинства:
 * - nums[1..1] = [2]       // один элемент 2 → он же
 *                             большинство
 *
 * - nums[2..2] = [2]       // аналогично
 *
 * - nums[1..2] = [2,2]     // два элемента 2
 *                             → 2 встречается 2 раза
 *                               из 2
 *
 * - nums[0..2] = [1,2,2]   // 2 встречается 2 раза из 3
 *                             → строго больше половины
 *
 * - nums[1..3] = [2,2,3]   // 2 встречается 2 раза из 3
 *                             → строго больше половины
 * Итого таких подмассивов: 5.
 */
function countMajoritySubarrays(nums, target) {

  // Инициализируем итоговый счётчик результата -
  // суммарное количество подмассивов, где target
  // является мажоритарным.
  let result = 0;

  // Счётчик подмассивов, которые заканчиваются на
  // текущей позиции и в которых target уже является
  // мажоритарным.
  //
  // Этот счётчик обновляется инкрементально при проходе
  // по массиву, чтобы не пересчитывать все подмассивы
  // заново.
  let endingSubarraysCnt = 0;

  // Текущий префиксный баланс относительно target.
  //
  // Баланс увеличивается на 1 при встрече target и
  // уменьшается на 1 при встрече любого другого числа.
  //
  // Баланс позволяет свести условие "target > половины"
  // к сравнению префиксных сумм.
  let targetBalance = 0;

  // Map: для каждого значения баланса хранится, сколько
  // префиксов с таким балансом уже встречалось.
  //
  // Инициализация [0,1] означает, что пустой префикс
  // (до начала массива) имеет баланс 0 и встречается
  // один раз.
  //
  // Это важно для корректного учёта подмассивов,
  // начинающихся с индекса 0.
  const targetBalanceToFreq = new Map([[0, 1]]);

  // Проходим по каждому элементу массива nums
  for (const num of nums) {

    // Если текущий элемент равен target, то он
    // увеличивает префиксный баланс на 1.
    if (num === target) {

      // Если текущий элемент - target, то все префиксы
      // с текущим значением targetBalance дают новые
      // подмассивы, заканчивающиеся в этой позиции, в
      // которых target становится мажоритарным.
      //
      // Добавляем их количество к endingSubarraysCnt.
      //
      // get(targetBalance) возвращает число префиксов с
      // таким балансом. Это значение всегда определено
      // для уже встреченных балансов.
      endingSubarraysCnt +=
        targetBalanceToFreq.get(targetBalance);

      // Увеличиваем баланс, фиксируя, что в текущем
      // префиксе теперь на один target больше.
      targetBalance++;
    } else {

      // Если текущий элемент не равен target, он
      // уменьшает относительное преимущество target в
      // префиксе.
      //
      // При уменьшении баланса некоторые подмассивы,
      // которые ранее удовлетворяли условие
      // мажоритарности target, перестают удовлетворять
      // это условие - их количество равно числу
      // префиксов с балансом (targetBalance - 1).
      //
      // Вычитаем это количество из endingSubarraysCnt.
      // Если таких префиксов не было, вычитаем 0.
      //
      // Почему именно targetBalance - 1:
      // - до обработки текущего не-target баланс был
      //   targetBalance (старое значение);
      //
      // - после уменьшения он станет targetBalance - 1;
      //
      // - подмассивы, которые "держались" на том, что
      //   их префиксный баланс был ровно на 1 больше,
      //   теперь теряют преимущество target ровно на 1;
      //
      // - поэтому нужно вычесть из endingSubarraysCnt
      //   количество префиксов со старым балансом
      //   targetBalance - 1 (относительно нового
      //   состояния), то есть тех, для которых
      //   добавление ещё одного "минуса" делает разницу
      //   не строго положительной.
      //
      // targetBalance - 1 - это "граница", на которой
      // подмассив перестаёт иметь строгий перевес
      // target. Мы вычитаем все случаи, где баланс
      // опустился именно до этой границы, потому что
      // они перестали удовлетворять условию
      // большинства.
      endingSubarraysCnt -=
        targetBalanceToFreq.get(targetBalance - 1) ?? 0;

      // Уменьшаем баланс, фиксируя, что встретился
      // не-target элемент.
      targetBalance--;
    }


    // Добавляем к общему результату число подмассивов,
    // которые заканчиваются на текущем индексе и в
    // которых target является мажоритарным.
    //
    // Это аккумулирует вклад каждого индекса в
    // итоговое число подходящих подмассивов.
    result += endingSubarraysCnt;

    // Обновляем Map частот префиксных балансов:
    //
    // Фиксируем, что текущий префиксный баланс
    // встретился ещё на один раз.
    //
    // Если для этого баланса ещё не было записи, get
    // вернёт undefined, нулевое слияние заменит
    // undefined на 0, и мы запишем 1.
    targetBalanceToFreq.set(
      targetBalance,
      (targetBalanceToFreq.get(targetBalance) ?? 0) + 1
    );
  }

  // Возвращаем итоговое количество подмассивов, где
  // target является мажоритарным элементом.
  return result;
}
