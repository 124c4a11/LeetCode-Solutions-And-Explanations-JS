/**
 * @param {number[]} nums
 * @return {number[]}
 *
 * smallerNumbersThanCurrent принимает массив чисел и возвращает
 * новый массив той же длины, где на позиции i находится
 * количество элементов исходного массива, которые строго меньше,
 * чем текущее число nums[i]
 *
 *
 * Пример для:
 *   nums = [8,1,2,2,3]
 * Результат:
 *   [4,0,1,1,3]
 * Пояснение:
 *   - Для nums[0] = 8 существует четыре числа, меньших его
 *     (1, 2, 2 и 3).
 *
 *   - Для nums[1] = 1 не существует ни одного числа, меньшего
 *     его.
 *
 *   - Для nums[2] = 2 существует одно число, меньшее его (1).
 *
 *   - Для nums[3] = 2 существует одно число, меньшее его (1).
 *
 *   - Для nums[4] = 3 существует три числа, меньших его
 *     (1, 2 и 2).
 */
function smallerNumbersThanCurrent(nums) {

  // Сохраняет длину входного массива в константу n для
  // многократного использования
  const n = nums.length;

  // Находим максимальное значение в массиве nums и прибавляем 1.
  //
  // Это нужно, чтобы определить размер вспомогательного массива
  // для подсчёта частот значений от 0 до max.
  //
  // Нужен +1, потому что значения используются как индексы и нам
  // нужен индекс max(nums) включительно).
  const m = Math.max(...nums) + 1;

  // Создаём типизированный массив Int32Array длины m,
  // заполненный нулями, где по индексу i хранится количество
  // элементов <= i. Это позволяет за O(1) получить число
  // элементов, строго меньших заданного значения.
  //
  // Int32Array экономичнее по памяти и быстрее для числовых
  // операций по сравнению с обычным массивом
  //
  // Индекс массива соответствует значению num, а значение в
  // ячейке равно количеству элементов в nums, меньших или
  // равных этому num (то есть "количество чисел меньше этого
  // числа" плюс "количество чисел, равных этому числу").
  const numToPrefixSum = new Int32Array(m);

  // Проходим по каждому числу в nums и увеличиваем
  // соответствующий счётчик.
  //
  // После этого numToPrefixSum[num] будет содержать количество
  // вхождений числа num в исходном массиве.
  for (const num of nums) numToPrefixSum[num]++;

  // Преобразуем массив частот в массив префиксных сумм.
  //
  // После этого на позиции i будет храниться количество
  // элементов исходного массива, значение которых <= i.
  //
  // Это позволяет в O(1) получить количество элементов строго
  // меньше заданного значения, взяв префиксную сумму для
  // (num - 1).
  for (let i = 1; i < m; i++) {
    numToPrefixSum[i] += numToPrefixSum[i - 1];
  }

  // Создаём результирующий массив длины n для хранения ответов.
  //
  // Используем new Array(n), чтобы заранее выделить память под
  // все элементы.
  const result = new Array(n);

  // Проходим по каждому элементу исходного массива nums по
  // индексу, чтобы заполнить соответствующую позицию в result.
  for (let i = 0; i < n; i++) {
    const num = nums[i];

    // Если число равно 0, то элементов строго меньше него нет
    // - записываем 0.
    //
    // Иначе берём значение numToPrefixSum[num - 1], которое
    // содержит количество элементов <= num - 1, то есть строго
    // меньше num.
    //
    // Используем num - 1 потому, что numToPrefixSum[num] после
    // предыдущего шага содержит количество элементов <= num.
    result[i] = num === 0 ? 0 : numToPrefixSum[num - 1];
  }

  // Возвращаем массив result, где на каждой позиции i находится
  // количество элементов исходного массива, строго меньших
  // nums[i].
  return result;
}
