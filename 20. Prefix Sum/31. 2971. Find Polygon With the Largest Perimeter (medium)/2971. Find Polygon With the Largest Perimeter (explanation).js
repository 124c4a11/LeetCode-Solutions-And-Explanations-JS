/**
 * @param {number[]} nums
 * @return {number}
 *
 * largestPerimeter(nums) принимает массив положительных
 * целых чисел nums и ищет среди элементов массива такой
 * набор сторон (минимум 3 стороны), при котором
 * выполняется условие многоугольника — самая длинная
 * сторона строго меньше суммы остальных — и возвращает
 * максимальный возможный периметр (сумму выбранных
 * сторон). Если ни один набор из массива не
 * удовлетворяет условию, функция возвращает -1.
 *
 *
 * Пример для:
 *   nums = [1,12,1,2,5,50,3]
 * Результат:
 *   12
 * Пояснение:
 *   Многоугольник с наибольшим периметром, который
 *   можно составить из nums, имеет 5 сторон:
 *   1, 1, 2, 3 и 5.
 *
 *   Периметр равен 1 + 1 + 2 + 3 + 5 = 12.
 *
 *   Нельзя составить многоугольник, где самой длинной
 *   стороной была бы 12 или 50, потому что невозможно
 *   подобрать 2 или более меньших сторон, сумма которых
 *   была бы больше любой из них.
 *
 *   Можно показать, что наибольший возможный периметр
 *   равен 12.
 *
 *
 * Пример для:
 *   nums = [5,5,50]
 * Вывод:
 *   -1
 * Пояснение:
 *   Невозможно составить многоугольник из nums, так как
 *   многоугольник должен иметь как минимум 3 стороны, и
 *   50 > 5 + 5.
 */
function largestPerimeter(nums) {
  const n = nums.length;

  // Сортируем массив по возрастанию
  //
  // Сортировка существенно упрощает проверку условия
  // "самая длинная сторона строго меньше суммы
  // остальных", так как после сортировки самая длинная
  // сторона будет в конце массива, и достаточно
  // сравнить её с суммой всех предыдущих элементов,
  // чтобы проверить возможность построить многоугольник
  nums.sort((a, b) => a - b);

  // Инициализируем переменную для хранения
  // максимального периметра.
  //
  // -1 означает, что пока подходящего многоугольника не
  // найдено.
  let maxPerimeter = -1;

  // Инициализируем префиксную сумму первых двух
  // элементов массива.
  //
  // prefixSum будет хранить сумму всех рассмотренных до
  // текущего шага сторон, кроме текущего nums[i].
  //
  // Начинаем с nums[0] + nums[1], так как при
  // отсортированном массиве первые два элемента -
  // минимальные стороны, и для первой проверки (первого
  // кандидата на "самую длинную" сторону) нам нужна
  // именно их сумма.
  let prefixSum = nums[0] + nums[1];

  // Проходим по массиву, начиная с третьего элемента
  // (индекс 2), рассматривая каждый элемент как
  // кандидата на самую длинную сторону.
  for (let i = 2; i < n; i++) {

    // Сохраняем текущий элемент в локальную константу
    const num = nums[i];

    // Проверяем условие существования многоугольника
    // для набора сторон, где текущая сторона — самая
    // длинная:
    //
    // Сумма всех предыдущих сторон (prefixSum) должна
    // быть строго больше текущей (самой длинной)
    // стороны (num).
    if (prefixSum > num) {

      // Если условие выполняется, то можно построить
      // многоугольник из всех рассмотренных сторон, и
      // его периметр равен сумме предыдущих сторон
      // (prefixSum) плюс текущая сторона (num).
      //
      // Обновляем maxPerimeter, так как мы идём по
      // возрастанию nums, предыдущие i дают меньший,
      // либо равный периметр, поэтому перезапись
      // корректна.
      maxPerimeter = prefixSum + num;
    }

    // Добавляем текущий элемент к префиксной сумме,
    // чтобы на следующей итерации prefixSum
    // представляла сумму всех элементов слева от
    // следующего элемента.
    prefixSum += num;
  }

  // Возвращаем найденный максимальный периметр или -1,
  // если ни для одной позиции не было выполнено условие
  // существования многоугольника.
  return maxPerimeter;
}
