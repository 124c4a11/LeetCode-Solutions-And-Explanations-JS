/**
 * @param {number[][]} bookings
 * @param {number} n
 * @return {number[]}
 *
 * corpFlightBookings принимает массив бронирований bookings
 * и целое n — число рейсов. Вычисляет итоговое количество
 * забронированных мест для каждого из n рейсов, применяя
 * технику разностного массива (difference array) для
 * эффективного суммирования диапазонов.
 *
 * Параметры:
 * - bookings — массив бронирований. Каждый элемент имеет
 *   вид [first, last, seats], где:
 *   - first — номер первого рейса в диапазоне
 *     (1‑индексированный),
 *
 *   - last — номер последнего рейса в диапазоне
 *     (включительно),
 *
 *   - seats — количество мест, добавляемых ко всем рейсам
 *     в этом диапазоне.
 *
 * - n — целое число, общее количество рейсов. Длина
 *   результирующего массива должна быть равна n.
 *
 * Возвращает:
 *   Массив длины n, где каждый элемент — итоговое количество
 *   забронированных мест для соответствующего рейса (элемент
 *   с индексом i соответствует рейсу номеру i+1, так как
 *   входные номера рейсов 1‑индексированы)
 *
 *
 * Пример для:
 *   bookings = [[1,2,10],[2,3,20],[2,5,25]],
 *   n = 5
 * Результат:
 *   [10,55,45,25,25]
 * Пояснение:
 *   Метки рейсов:        1   2   3   4   5
 *   Бронирование 1:     10  10
 *   Бронирование 2:         20  20
 *   Бронирование 3:         25  25  25  25
 *   Всего мест:         10  55  45  25  25
 *   Следовательно, ответ = [10,55,45,25,25]
 */
function corpFlightBookings(bookings, n) {

  // Создаём Int32Array длины n, заполненный нулями.
  //
  // Используется как разностный массив и затем как итоговый
  // массив.
  const result = new Int32Array(n);

  // Итерация по каждому элементу входного массива bookings.
  //
  // Каждый элемент описывает одно диапазонное обновление
  // [first, last, seats].
  //
  // Для ускорения работы цилкла не используем
  // деструктуризацию массива booking, а обращаемся
  // к значениям booking по индексу
  for (const booking of bookings) {

    // Извлекаем номер первого рейса в диапазоне (индексация
    // в данных 1‑based).
    const first = booking[0];

    // Извлекаем номер последнего рейса в диапазоне
    // (включительно, 1‑based).
    const last = booking[1];

    // Извлекаем количество мест, которое нужно добавить ко
    // всем рейсам в диапазоне.
    const seats = booking[2];

    // Применяем разностную технику:
    //
    // Прибавляем seats в позиции first-1 (0‑based).
    //
    // Это помечает начало интервала прибавления. Не трогаем
    // все элементы внутри диапазона.
    result[first - 1] += seats;

    // Если last < n, то в позиции last (то есть сразу после
    // конца диапазона в 0‑based) вычитаем seats — это
    // помечает конец интервала.
    //
    // При последующем префиксном суммировании эффект
    // прибавления будет действовать только внутри
    // [first-1, last-1].
    //
    // Если last == n, то вычитание не нужно, потому что за
    // пределами массива ничего не накапливается.
    if (last < n) result[last] -= seats

    // Пример работый цикла для:
    //   bookings = [[1,2,10],[2,3,20],[2,5,25]],
    //   n = 5
    //
    // Изначально:
    //   result = [0, 0, 0, 0, 0]
    //
    // Шаг 1:
    //   booking = [1,2,10]
    //   result[0] += 10 → [10, 0, 0, 0, 0]
    //   result[2] -= 10 → [10, 0, -10, 0, 0]
    //
    // Шаг 2:
    //   booking = [2,3,20]
    //   result[1] += 20 → [10, 20, -10, 0, 0]
    //   result[3] -= 20 → [10, 20, -10, -20, 0]
    //
    // Шаг 3:
    //   booking = [2,5,25]
    //   result[1] += 25 → [10, 45, -10, -20, 0]
    //   last = 5, last < n? нет, вычитание не выполняется
    //
    // Итог после выполнения цикла:
    //   result =  [10, 45, -10, -20, 0]
  }

  // После применения всех пометок (границ диапазонов)
  // выполняем префиксную сумму, чтобы восстановить
  // фактические значения для каждого рейса:
  //
  // Каждый элемент становится суммой всех предыдущих
  // разностей.
  //
  // Начинаем с let i = 1 потому, что при вычислении
  // префиксных сумм мы берём текущее значение и добавляем к
  // нему значение предыдущей позиции. Первая позиция
  // (result[0]) уже корректна и не требует накопления,
  // поэтому цикл начинается со второй позиции — индекса 1
  for (let i = 1; i < n; i++) {

    // Накопление:
    //
    // Текущий элемент увеличивается на значение предыдущего,
    // превращая разности в окончательные суммы.
    result[i] += result[i - 1];

    // Пример работый цикла для:
    //   bookings = [[1,2,10],[2,3,20],[2,5,25]],
    //   n = 5
    //
    // result после предыдущего цикла:
    //   result = [10, 45, -10, -20, 0]
    //
    // Шаг i = 1:
    //   result[1] += result[0] → 45 + 10 = 55
    //   result = [10, 55, -10, -20, 0]
    //
    // Шаг i = 2:
    //   result[2] += result[1] → -10 + 55 = 45
    //   result = [10, 55, 45, -20, 0]
    //
    // Шаг i = 3:
    //   result[3] += result[2] → -20 + 45 = 25
    //   result = [10, 55, 45, 25, 0]
    //
    // Шаг i = 4:
    //   result[4] += result[3] → 0 + 25 = 25
    //   result = [10, 55, 45, 25, 25]
    //
    // Итог после выполнения цикла:
    //   result = [10, 55, 45, 25, 25]
  }

  // Возвращаем массив result с итоговым количеством
  // забронированных мест для каждого рейса
  // (индексы 0..n-1 соответствуют рейсам 1..n)
  return result;
}
