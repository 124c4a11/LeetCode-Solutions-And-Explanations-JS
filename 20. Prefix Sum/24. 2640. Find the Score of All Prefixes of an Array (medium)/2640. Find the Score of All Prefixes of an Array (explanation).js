/**
 * @param {number[]} nums
 * @return {number[]}
 *
 * Функция findPrefixScore принимает на вход массив
 * чисел nums и возвращает массив той же длины, где
 * каждый элемент result[i] — это сумма значений
 * conversion‑массива для префикса nums[0..i], то есть
 * накопленный "score" для первых i+1 элементов.
 *
 * Для любого массива arr conversion определяют так:
 * conversion[i] = arr[i] + max(arr[0..i]).
 *
 * Score массива — это сумма всех элементов conversion.
 * Для префикса nums[0..i] функция возвращает сумму
 * conversion[0] + conversion[1] + ... + conversion[i].
 *
 *
 * Пример для:
 *   nums = [2,3,7,5,10]
 * Выход:
 *   [4,10,24,36,56]
 * Пояснение:
 * - Для префикса [2] массив преобразований равен [4],
 *   поэтому score = 4
 *
 * - Для префикса [2, 3] массив преобразований равен
 *   [4, 6], поэтому score = 10
 *
 * - Для префикса [2, 3, 7] массив преобразований равен
 *   [4, 6, 14], поэтому score = 24
 *
 * - Для префикса [2, 3, 7, 5] массив преобразований
 *   равен [4, 6, 14, 12], поэтому score = 36
 *
 * - Для префикса [2, 3, 7, 5, 10] массив
 *   преобразований равен [4, 6, 14, 12, 20], поэтому
 *   score = 56
 */
function findPrefixScore(nums) {
  const n = nums.length;

  // Создаём массив result длины n заранее, чтобы
  // выделить память один раз и затем записывать
  // значения по индексам.
  //
  // Это эффективнее, чем динамическое push.
  const result = new Array(n);

  // Инициализируем переменную max нулём.
  //
  // В процессе итерации она будет хранить максимальное
  // значение среди элементов текущего префикса.
  let max = 0;

  // Переменная score аккумулирует суммарный "score"
  // для текущего префикса.
  //
  // На каждом шаге мы добавляем к ней (nums[i] + max),
  // где max — максимальный элемент текущего префикса.
  let score = 0;

  // Итерация по всем индексам массива от 0 до n-1
  // включительно.
  for (let i = 0; i < n; i++) {

    // Обновляем максимальный элемент префикса
    max = Math.max(max, nums[i]);

    // Увеличиваем накопленный score на сумму текущего
    // элемента и текущего максимума.
    score += nums[i] + max;

    // Сохраняем текущее значение score в
    // результирующий массив на позиции i.
    //
    // После завершения цикла result[i] содержит
    // итоговый префиксный score для префикса длины
    // i+1.
    result[i] = score;
  }

  // Возвращаем массив result с префиксными "score" для
  // каждого индекса.
  return result;
}
