/**
 * @param {number[]} nums
 * @return {number}
 *
 * waysToMakeFair подсчитывает, сколько индексов в переданном массиве
 * nums можно удалить так, чтобы после удаления массив стал fair - то
 * есть сумма элементов на чётных индексах стала равна сумме
 * элементов на нечётных индексах.
 *
 * Учитывается 0‑индексация.
 *
 * При удалении элемента все элементы справа сдвигаются влево, и их
 * индексная парность меняется - четные индексы становятся нечетными,
 * нечетные становятся четными.
 *
 * Параметры:
 * - nums — массив целых чисел
 *
 * Возвращает:
 * - Целое число - количество индексов, удаление которых делает
 *   массив fair.
 *
 *
 * Пример для:
 *   nums = [2,1,6,4]
 * Вывод:
 *   1
 * Пояснение:
 * - Удаление индекса 0: [1,6,4]
 *     Сумма на чётных индексах: 1 + 4 = 5.
 *     Сумма на нечётных индексах: 6.
 *     Не сбалансирован.
 *
 * - Удаление индекса 1: [2,6,4]
 *     Сумма на чётных индексах: 2 + 4 = 6.
 *     Сумма на нечётных индексах: 6.
 *     Сбалансирован.
 *
 * - Удаление индекса 2: [2,1,4]
 *     Сумма на чётных индексах: 2 + 4 = 6.
 *     Сумма на нечётных индексах: 1.
 *     Не сбалансирован.
 *
 * - Удаление индекса 3: [2,1,6]
 *     Сумма на чётных индексах: 2 + 6 = 8.
 *     Сумма на нечётных индексах: 1.
 *     Не сбалансирован.
 * Всего 1 индекс, удаление которого делает массив nums
 * сбалансированным.
 *
 *
 * Пример для:
 *   nums = [1,1,1]
 * Результат:
 *   3
 * Пояснение:
 *   Вы можете удалить любой индекс, и оставшийся массив будет
 *   сбалансированным (fair).
 *
 *
 * Пример для:
 *   nums = [1,2,3]
 * Результат:
 *   0
 * Пояснение:
 *   Нельзя сделать массив сбалансированным (fair) удалением любого
 *   одного индекса.
 */
function waysToMakeFair(nums) {
  const n = nums.length;

  // Инициализация переменной rightEvenSum, которая будет хранить
  // сумму элементов, расположенных на чётных индексах в "правой"
  // (необработанной) части массива.
  let rightEvenSum = 0;

  // Инициализация переменной rightOddSum, которая будет хранить
  // сумму элементов, расположенных на нечётных индексах в "правой"
  // (необработанной) части массива.
  let rightOddSum = 0;

  // Цикл проходит по всем индексам массива и распределяет элементы
  // по rightEvenSum и rightOddSum.
  //
  // После этого шага "правая" часть содержит суммы всех элементов
  // массива, разделённые по парности индекса.
  //
  // Эти суммы представляют собой начальное состояние "правой"
  // части до того, как мы начнём последовательно рассматривать
  // удаление каждого элемента
  for (let i = 0; i < n; ++i) {

    // Проверяем, является ли индекс i чётным (остаток от деления
    // на 2 равен 0).
    //
    // Если да, добавляем текущий элемент в rightEvenSum.
    if (i % 2 === 0) rightEvenSum += nums[i];

    // В противном случае индекс нечётный — добавляем элемент в
    // rightOddSum.
    else rightOddSum += nums[i];
  }

  // fairCnt будет считать количество индексов i, при удалении
  // которых оставшийся массив становится "fair" (равные суммы на
  // чётных и нечётных позициях).
  let fairCnt = 0;

  // Инициализация leftEvenSum — сумма элементов на чётных
  // индексах слева от текущего рассматриваемого индекса.
  let leftEvenSum = 0;

  // Инициализация leftOddSum — сумма элементов на нечётных
  // индексах слева от текущего рассматриваемого индекса.
  let leftOddSum = 0;

  // Второй проход по массиву:
  //
  // На каждой итерации рассматриваем удаление элемента с индексом i.
  //
  // Поддерживаем инварианты:
  // - leftEvenSum/leftOddSum — суммы слева от i (не включая i),
  //
  // - rightEvenSum/rightOddSum — суммы справа от i (включая i до
  //   вычитания на текущей итерации).
  //
  // Важный инвариант:
  // Перед проверкой мы удаляем текущий элемент из правых сумм,
  // затем проверяем условие равенства сумм после удаления и
  // сдвига правой части.
  for (let i = 0; i < n; ++i) {

    // Перед проверкой "удаляем" текущий элемент из правых сумм,
    // потому что при реальном удалении он перестанет принадлежать
    // правой части.
    //
    // Если i чётный, текущий элемент был учтён в rightEvenSum,
    // поэтому вычитаем его из rightEvenSum.
    if (i % 2 === 0) rightEvenSum -= nums[i];

    // Если i нечётный, текущий элемент был учтён в rightOddSum,
    // поэтому вычитаем его из rightOddSum.
    else rightOddSum -= nums[i];

    // Ключевая логика:
    //
    // После удаления элемента все элементы справа от i сдвинутся
    // на одну позицию влево, поэтому их индексная парность
    // изменится: прежние чётные индексы станут нечётными и
    // наоборот.
    //
    // Новая сумма элементов на чётных индексах после удаления
    // равна:
    //   leftEvenSum (чётные элементы слева остаются на тех же
    //   парных позициях)
    //   + rightOddSum (правые элементы, которые были на нечётных
    //     позициях, после сдвига попадут на чётные позиции).
    //
    // Новая сумма элементов на нечётных индексах после удаления
    // равна:
    //   leftOddSum (нечётные элементы слева остаются на тех же
    //   парных позициях)
    //   + rightEvenSum (правые элементы, которые были на чётных
    //     позициях, после сдвига попадут на нечётные позиции).
    //
    // Если эти две суммы равны, то, после удаления nums[i],
    // массив станет "fair" .
    if (leftEvenSum + rightOddSum === leftOddSum + rightEvenSum) {
      // Увеличиваем счётчик подходящих индексов на единицу.
      fairCnt++;
    }

    // После проверки мы добавляем текущий элемент в левую часть,
    // потому что при переходе к следующему индексу этот элемент
    // будет считаться уже обработанным и относиться к "левой"
    // части.
    //
    // Если i чётный, текущий элемент добавляется к leftEvenSum.
    if (i % 2 === 0) leftEvenSum += nums[i];

    // Иначе (i нечётный) добавляем к leftOddSum.
    else leftOddSum += nums[i];
  }

  // Возвращаем общее количество индексов, удаление которых делает
  // массив "fair".
  //
  // Это итоговый результат, накопленный в fairCnt.
  return fairCnt;
}
