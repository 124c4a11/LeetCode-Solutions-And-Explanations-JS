/**
 * @param {string} customers
 * @return {number}
 *
 * bestClosingTime принимает журнал посещений магазина,
 * представленный 0‑индексной строкой customers,
 * состоящей только из символов 'N' и 'Y'.
 *
 * Если i‑й символ равен 'Y', это означает, что в i‑й
 * час приходят клиенты, а 'N' указывает, что в i‑й час
 * клиентов нет.
 *
 * Если магазин закрывается в j‑й час (0 <= j <= n),
 * штраф вычисляется следующим образом:
 * - За каждый час, когда магазин открыт и клиенты не
 *   приходят, штраф увеличивается на 1.
 *
 * - За каждый час, когда магазин закрыт и приходят
 *   клиенты, штраф увеличивается на 1.
 *
 * Возвращает наименьший по времени час, в который
 * магазин должен закрыться, чтобы суммарный штраф
 * был минимальным.
 *
 * Обратите внимание, что если магазин закрывается в
 * j‑й час, это означает, что он закрыт в час j.
 *
 *
 * Пример для:
 *   customers = "YYNY"
 * Результат:
 *   2
 * Пояснение:
 * - Закрытие магазина в 0-й час даёт штраф 1+1+0+1 = 3.
 * - Закрытие магазина в 1-й час даёт штраф 0+1+0+1 = 2.
 * - Закрытие магазина в 2-й час даёт штраф 0+0+0+1 = 1.
 * - Закрытие магазина в 3-й час даёт штраф 0+0+1+1 = 2.
 * - Закрытие магазина в 4-й час даёт штраф 0+0+1+0 = 1.
 * Закрытие в 2-й или 4-й час даёт минимальный штраф.
 * Поскольку 2 раньше, оптимальное время закрытия — 2.
 *
 *
 * Пример для:
 *   customers = "NNNNN"
 * Результат:
 *   0
 * Пояснение:
 * Лучше всего закрыть магазин в 0-й час, так как
 * клиенты не приходят вовсе.
 *
 *
 * Пример для:
 *   customers = "YYYY"
 * Результат:
 *   4
 * Пояснение:
 * Лучше всего закрыть магазин в 4-й час, так как в
 * каждый час приходит клиент.
 */
function bestClosingTime(customers) {

  // Инициализация переменной closingTime значением 0.
  //
  // Переменная хранит оптимальное время закрытия
  // магазина - количество часов от начала работы,
  // после которых закрытие даёт максимальную
  // накопленную прибыль и минимальный штраф.
  //
  // Интерпретация:
  // Если ни одна позиция не улучшает прибыль,
  // оптимально закрыться сразу (до первого часа).
  let closingTime = 0;

  // Инициализация currProfit значением 0.
  //
  // currProfit — накопительный счётчик, отражающий
  // текущую накопительную прибыль, которая выражается
  // как разница разница между выгодой и штрафом при
  // рассмотрении префикса строки customers от начала
  // до текущей позиции:
  // - Каждый 'Y' даёт +1 (прибыль)
  // - Каждый 'N' даёт -1 (штраф).
  let currProfit = 0;

  // Инициализация maxProfit, в которой хранится
  // наилучшее (максимальное) значение currProfit,
  // найденное до текущего шага.
  //
  // Начальное значение 0 соответствует сценарию
  // "закрыться сразу"
  //
  // Используется для принятия решения о лучшем
  // времени закрытия.
  let maxProfit = 0;

  // Цикл for проходит по всем позициям строки
  // customers слева направо.
  //
  // i соответствует текущему часу
  for (let i = 0; i < customers.length; i++) {

    // Проверка символа в позиции i.
    //
    // Если символ равен 'Y', это означает, что в этот
    // час пришёл клиент, что увеличивает текущую
    // прибыль на 1.
    //
    // В противном случае (ожидается 'N') считаем, что
    // отсутствие клиента уменьшает прибыль на 1.
    if (customers[i] === "Y") currProfit++;
    else currProfit--;

    // Сравнение текущей накопленной прибыли с
    // максимальной зафиксированной.
    //
    // Если currProfit превысил maxProfit, это означает,
    // что закрытие после текущего часа даёт лучшую
    // общую прибыль, чем все ранее рассмотренные
    // варианты.
    if (currProfit > maxProfit) {

      // Обновляем maxProfit новым, более высоким
      // значением currProfit.
      //
      // Это фиксирует новый рекорд по прибыли для
      // префикса до i включительно.
      maxProfit = currProfit;

      // Устанавливаем closingTime равным i + 1, потому
      // что предполагаемое закрытие происходит после
      // обработки текущего часа, так как текущий час
      // "i" участвует в формировании текущей
      // максимальной прибыли.
      closingTime = i + 1;
    }
  }

  // Возвращаем найденное оптимальное время закрытия.
  //
  // Если ни одна позиция не увеличила прибыль выше 0,
  // closingTime останется 0, что означает: выгоднее
  // закрыться до начала (не открываться).
  return closingTime;
}
