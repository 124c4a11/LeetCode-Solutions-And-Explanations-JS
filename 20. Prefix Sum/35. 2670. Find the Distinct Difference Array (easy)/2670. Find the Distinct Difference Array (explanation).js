/**
 * @param {number[]} nums
 * @return {number[]}
 *
 * distinctDifferenceArray принимает массив чисел nums и
 * вычисляет для каждого индекса i разность числа уникальных
 * элементов в префиксе nums[0..i] и числа уникальных
 * элементов в суффиксе nums[i+1..n-1] и возвращает массив
 * таких разностей.
 *
 *
 * Пример для:
 *   nums = [1,2,3,4,5]
 * Результат:
 *   [-3,-1,1,3,5]
 * Пояснение:
 * - Для индекса i = 0 в префиксе находится 1 элемент, а в
 *   суффиксе - 4 различных элемента. Следовательно,
 *   diff[0] = 1 - 4 = -3.
 *
 * - Для индекса i = 1 в префиксе 2 различных элемента, а в
 *   суффиксе 3 различных элемента. Следовательно,
 *   diff[1] = 2 - 3 = -1.
 *
 * - Для индекса i = 2 в префиксе 3 различных элемента, а в
 *   суффиксе 2 различных элемента. Следовательно,
 *   diff[2] = 3 - 2 = 1.
 *
 * - Для индекса i = 3 в префиксе 4 различных элемента, а в
 *   суффиксе 1 различный элемент. Следовательно,
 *   diff[3] = 4 - 1 = 3.
 *
 * - Для индекса i = 4 в префиксе 5 различных элементов, а в
 *   суффиксе нет элементов. Следовательно,
 *   diff[4] = 5 - 0 = 5.
 */
function distinctDifferenceArray(nums) {
  const n = nums.length;

  // Создаём типизированный массив Int32Array длины n для
  // хранения количества уникальных элементов в префиксах.
  const prefixUniqueCnt = new Int32Array(n);

  // Создаём пустой Set для отслеживания уникальных значений
  // при проходе слева направо.
  //
  // Set хранит только уникальные элементы и предоставляет
  // свойство size для быстрого получения количества
  // уникальных элементов.
  const uniqueSet = new Set();

  // Проход по массиву слева направо для заполнения
  // prefixUniqueCnt.
  //
  // На каждой итерации множество uniqueSet содержит все
  // уникальные элементы из nums[0..i].
  for (let i = 0; i < n; i++) {

    // Добавляем текущий элемент nums[i] в Set.
    //
    // Если элемент уже присутствует, Set не изменится.
    //
    // Это обеспечивает корректный подсчёт уникальных
    // значений в префиксе.
    uniqueSet.add(nums[i]);

    // Записываем текущее количество уникальных элементов
    // (размер Set) в prefixUniqueCnt на позиции i.
    //
    // Таким образом prefixUniqueCnt[i] - количество
    // различных элементов в подмассиве nums[0..i].
    prefixUniqueCnt[i] = uniqueSet.size;
  }

  // Очищаем Set, чтобы переиспользовать его для подсчёта
  // уникальных элементов в суффиксах при проходе справа
  // налево.
  //
  // Метод clear удаляет все элементы из Set за O(1)
  // амортизированно.
  uniqueSet.clear();

  // Создаём результирующий массив diffArr длины n.
  //
  // В нём будут храниться искомые разности для каждого
  // индекса.
  const diffArr = new Array(n);

  // Запускаем цикл справа налево: от последнего индекса n-1
  // до 0 включительно.
  //
  // Проход справа налево позволяет на каждой итерации иметь
  // в uniqueSet множество уникальных элементов, которые
  // находятся строго правее текущего индекса i (то есть в
  // суффиксе nums[i+1..n-1]).
  for (let i = n - 1; i >= 0; i--) {

    // Вычисляем разность: количество уникальных в префиксе,
    // включающем i (prefixUniqueCnt[i]), минус количество
    // уникальных в суффиксе, расположенном правее i
    // (uniqueSet.size).
    //
    // Важно, что на этой строке uniqueSet ещё не содержит
    // nums[i], поэтому uniqueSet.size соответствует числу
    // уникальных элементов в nums[i+1..n-1], а не в
    // nums[i..n-1].
    diffArr[i] = prefixUniqueCnt[i] - uniqueSet.size;

    // После вычисления разности добавляем текущий элемент
    // nums[i] в uniqueSet.
    //
    // Это нужно для того, чтобы при следующей итерации
    // (i-1) uniqueSet отражал уникальные элементы правее
    // нового i, то есть для корректного подсчёта суффикса
    // относительно следующего индекса.
    uniqueSet.add(nums[i]);
  }

  // Возвращаем заполненный массив diffArr, где для каждого
  // индекса i хранится prefixUniqueCnt[i] - (количество
  // уникальных элементов в nums[i+1..n-1]).
  return diffArr;
}
